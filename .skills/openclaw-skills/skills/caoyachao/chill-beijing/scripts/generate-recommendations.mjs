function getTodayType() {
  const day = new Date().getDay();
  if (day === 0 || day === 6) return 'weekend';
  if (day === 5) return 'friday';
  return 'workday';
}

function getTodayString() {
  return new Date().toLocaleDateString('zh-CN', {
    year: 'numeric', month: 'long', day: 'numeric', weekday: 'long'
  });
}

function getTomorrowString() {
  const date = new Date();
  date.setDate(date.getDate() + 1);
  return date.toLocaleDateString('zh-CN', {
    month: 'long', day: 'numeric', weekday: 'long'
  });
}

function formatWeather(weather) {
  if (!weather?.today) return '';
  
  let output = '\n🌤️ **今日天气**\n';
  output += '• ' + weather.today.condition + ' | ' + weather.today.temp + '°C（体感 ' + weather.today.feelsLike + '°C）\n';
  output += '• 气温范围：' + weather.today.minTemp + '°C ~ ' + weather.today.maxTemp + '°C\n';
  output += '• 湿度：' + weather.today.humidity + '% | 风速：' + weather.today.wind + ' km/h\n';
  
  output += '\n🌤️ **' + getTomorrowString() + '**\n';
  output += '• ' + weather.tomorrow.condition + ' | ' + weather.tomorrow.minTemp + '°C ~ ' + weather.tomorrow.maxTemp + '°C\n';
  if (weather.tomorrow.chanceOfRain > 30) {
    output += '• ⚠️ 降雨概率 ' + weather.tomorrow.chanceOfRain + '%，建议带伞\n';
  }
  
  output += '\n💡 **出行建议**：';
  const temp = parseInt(weather.today.temp);
  if (temp < 10) output += '天气较冷，建议室内活动或保暖出行\n';
  else if (temp > 28) output += '天气炎热，推荐室内观影或有空调的演出场所\n';
  else if (weather.today.condition.includes('雨')) output += '有雨，推荐室内活动\n';
  else output += '天气舒适，适合各类室内外活动\n';
  
  return output;
}

