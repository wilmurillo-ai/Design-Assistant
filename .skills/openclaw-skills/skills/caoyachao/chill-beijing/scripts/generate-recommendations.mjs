export async function generateRecommendations(options = {}) {
  const { forceType = null } = options;
  const todayType = forceType || getTodayType();
  const todayStr = getTodayString();
  
  let output = '🎯 **北京放松指南 - ' + todayStr + '**\n';
  output += '━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n';
  output += '📍 今日类型：' + (todayType === 'workday' ? '工作日' : todayType === 'friday' ? '周五（下班+周末预告）' : '周末') + '\n';
  output += '⏰ 生成时间：' + new Date().toLocaleString('zh-CN') + '\n';
  output += '━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n';
  
  try {
    console.log('获取天气信息...');
    const weather = await fetchWeather();
    output += formatWeather(weather);
    
    if (todayType === 'workday') {
      console.log('生成工作日推荐...');
      const data = await fetchWorkdayData();
      output += '\n━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n';
      output += '🌅 **今晚下班好去处**\n';
      output += formatWorkdayRecommendations(data);
      
    } else if (todayType === 'friday') {
      console.log('生成周五推荐...');
      const [workData, weekendData] = await Promise.all([
        fetchWorkdayData(),
        fetchWeekendData()
      ]);
      
      output += '\n━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n';
      output += '🌅 **今晚下班好去处**\n';
      output += formatWorkdayRecommendations(workData);
      
      output += '\n\n━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n';
      output += '🌟 **周末预告（明后天）**\n';
      output += formatWeekendRecommendations(weekendData);
      
    } else {
      console.log('生成周末推荐...');
      const data = await fetchWeekendData();
      output += formatWeekendRecommendations(data);
    }
    
    output += '\n\n━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n';
    output += '💡 数据来源：猫眼、大麦、豆瓣、小红书、各场馆官网\n';
    output += '💡 提示：热门演出建议提前购票，小众活动通常现场有余票\n';
    
  } catch (error) {
    console.error('生成推荐失败:', error);
    output += '\n❌ 数据获取失败，请稍后重试\n';
  }
  
  return output;
}

