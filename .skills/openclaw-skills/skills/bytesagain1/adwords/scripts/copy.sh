cmd_aida() {
  cat <<'EOF'
## 🎪 AIDA框架文案生成器

请AI用AIDA框架生成完整文案：

### AIDA框架

#### A - Attention (注意力)
**目标**: 在3秒内抓住眼球

技巧：
- 震撼数据: "每天有XX人因为___而___"
- 反常识: "你以为的___其实是错的"
- 直击痛点: "还在为___烦恼？"
- 视觉冲击: [描述画面/场景]

#### I - Interest (兴趣)
**目标**: 让读者继续读下去

技巧：
- 展开痛点: "你是否经历过___？"
- 讲故事: "[客户名]也曾___"
- 提供洞见: "研究发现___"
- 制造悬念: "解决方案比你想的简单..."

#### D - Desire (欲望)
**目标**: 让读者想要你的产品

技巧：
- 好处列表: ✅ [好处1] ✅ [好处2] ✅ [好处3]
- 前后对比: 使用前___ vs 使用后___
- 客户证言: "[真实评价]"
- 数据证明: "[数字]% 的用户[效果]"
- 降低风险: "XX天无理由退款"

#### A - Action (行动)
**目标**: 促使立即行动

技巧：
- 紧迫感: "限时/限量/限额"
- 简化行动: "只需[1步]"
- 额外激励: "现在行动额外获得___"
- 降低门槛: "免费试用/0元体验"

### AIDA文案模板
```
🎯 [Attention标题]

[Interest段落——展开故事/痛点]

你将获得:
✅ [Desire好处1]
✅ [Desire好处2]
✅ [Desire好处3]

[社会证明/数据]

👉 [Action CTA按钮]
[紧迫提示]
```

### 变体：PAS框架
- **P (Problem)**: 描述问题
- **A (Agitate)**: 加深痛感
- **S (Solution)**: 给出方案
EOF
}

