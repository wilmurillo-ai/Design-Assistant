// ────────────────────────────────────────────────────────────

#include <iostream>
using namespace std;

bool isPrime(int n) {
    if (n <= 1) return false;
    for (int i = 2; i * i <= n; i++)
        if (n % i == 0) return false;
    return true;
}

int main() {
    int N, count = 0;
    cin >> N;
    for (int i = 2; i <= N; i++)
        if (isPrime(i))
            count++;
    cout << count;
    return 0;
}


// ────────────────────────────────────────────────────────────
// 【第5关】慢方法 vs 聪明方法对比
// ────────────────────────────────────────────────────────────

// 🐌 慢方法（不推荐）：i 从 2 遍历到 n-1，最多试 n-2 次
bool isPrime_slow(int n) {
    for (int i = 2; i < n; i++)
        if (n % i == 0) return false;
    return true;
}

// 🚀 聪明方法（推荐）：i 只试到 √n，最多试 √n 次
bool isPrime_fast(int n) {
    if (n <= 1) return false;
    for (int i = 2; i * i <= n; i++)
        if (n % i == 0) return false;
    return true;
}

