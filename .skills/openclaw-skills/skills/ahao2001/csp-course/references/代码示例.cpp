// ❌ Bug 1：缺少 n<=1 的判断，1 会被误判为素数
bool isPrime_bug1(int n) {
    for (int i = 2; i * i <= n; i++)
        if (n % i == 0) return false;
    return true; // n=1 时循环不执行，直接返回 true（错！）
}
// ✅ 修正：在开头加上 if(n<=1) return false;

// ❌ Bug 2：主函数从 i=1 开始，会把 1 也计入素数
