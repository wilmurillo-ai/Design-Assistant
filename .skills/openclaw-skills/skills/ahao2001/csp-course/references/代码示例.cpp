// ────────────────────────────────────────────────────────────
// 【第2关】isPrime() 完整实现（侦探版）
//   ① n≤1 直接返回 false
//   ② 从 2 试到 √n（i*i <= n），省力！
//   ③ 能被整除 → 立刻返回 false
//   ④ 全部通过 → 返回 true
// ────────────────────────────────────────────────────────────

bool isPrime(int n) {
    // ① 不是素数：n<=1
    if (n <= 1)
        return false;
    // ②③ 从2试到√n
    for (int i = 2; i * i <= n; i++) {
        // ④ 能整除？不是素数
        if (n % i == 0)
            return false;
    }
    // ⑤ 全部通过！
    return true;
}


