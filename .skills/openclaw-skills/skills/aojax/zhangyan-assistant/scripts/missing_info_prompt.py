#!/usr/bin/env python3
"""
 missing_info_prompt.py

 功能：根据不同门类生成针对性的补图提示
"""

def generate_category_specific_prompt(category):
    """根据藏品门类生成补图提示"""
    prompts = {
        '书画': {
            '必拍': [
                '整体全貌',
                '局部用笔细节',
                '款识特写',
                '印章特写',
                '题跋特写',
                '装裱边缘',
                '接缝或可疑部位',
                '背面'
            ],
            '补充信息': [
                '纸本还是绢本',
                '立轴/手卷/册页/镜心',
                '尺寸',
                '来源',
                '是否重装裱',
                '是否有题签、签条、出版著录'
            ],
            '提醒': '书画最怕：真画假款、后添款、伪印、拼接改装、旧裱新画'
        },
        '瓷器': {
            '必拍': [
                '正面整体图',
                '侧面图',
                '俯视图',
                '口沿图',
                '腹部纹饰图',
                '底足图',
                '款识特写',
                '疑似修补处特写'
            ],
            '补充信息': [
                '高度、口径、足径',
                '重量（如方便）',
                '来源',
                '是否有冲线、补釉、修口',
                '是否有旧配盒或旧标签'
            ],
            '提醒': '瓷器最怕：后写款、仿古做旧、修补遮瑕、老底新身或新底老身'
        },
        '玉器': {
            '必拍': [
                '整体图',
                '侧面图',
                '边缘转折图',
                '孔道图',
                '阴刻线槽图',
                '表面反光图',
                '透光图',
                '可疑沁色区域特写'
            ],
            '补充信息': [
                '尺寸',
                '材质判断（若已知）',
                '来源',
                '是否做过清洗、抛光、处理'
            ],
            '提醒': '玉器最怕：新玉仿老工、老料新工、酸蚀做旧、染色仿沁、机械工冒充手工老工'
        },
        '铜器与杂项': {
            '必拍': [
                '整体图',
                '底部图',
                '侧面图',
                '铭文/款识特写',
                '局部工艺特写',
                '接缝和连接处特写',
                '锈层或皮壳细节图',
                '附件连接部位图'
            ],
            '补充信息': [
                '尺寸',
                '材质',
                '来源',
                '是否有修配、补件、翻修历史'
            ],
            '提醒': '铜器与杂项最怕：新仿古器、新老拼配、后刻铭文、假皮壳假锈、翻修冒充原状'
        }
    }
    
    if category not in prompts:
        # 默认通用提示
        return generate_generic_prompt()
    
    info = prompts[category]
    prompt = f"# {category}补图建议\n\n"
    prompt += "## 必拍\n"
    for item in info['必拍']:
        prompt += f"- {item}\n"
    
    prompt += "\n## 补充信息\n"
    for item in info['补充信息']:
        prompt += f"- {item}\n"
    
    prompt += f"\n## 提醒\n{info['提醒']}"
    
    return prompt

def generate_generic_prompt():
    """生成通用补图提示"""
    prompt = "# 通用补图建议\n\n"
    prompt += "## 必拍\n"
    prompt += "- 整体图\n"
    prompt += "- 局部细节图\n"
    prompt += "- 底部/背面图\n"
    prompt += "- 款识/印章/铭文特写\n"
    prompt += "- 可疑部位特写\n"
    
    prompt += "\n## 补充信息\n"
    prompt += "- 尺寸\n"
    prompt += "- 材质\n"
    prompt += "- 来源\n"
    prompt += "- 是否修复过\n"
    prompt += "- 是否有盒、签条、老标签、出版或著录资料\n"
    
    prompt += "\n## 说明\n"
    prompt += "图不全时，结论只能非常保守。请尽量提供清晰、多角度的图片，以便更准确地分析。"
    
    return prompt

if __name__ == "__main__":
    # 示例用法
    print(generate_category_specific_prompt('瓷器'))