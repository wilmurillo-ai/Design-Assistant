def generate_complete_construction_set(area: str, layout: str, style: str, 
                                       rooms_info: dict, output_dir: str) -> dict:
    """生成完整的施工图纸套装"""
    
    output_path = Path(output_dir)
    output_path.mkdir(parents=True, exist_ok=True)
    
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    
    print(f"🏗️  开始生成施工图纸套装...")
    print(f"   面积：{area}")
    print(f"   户型：{layout}")
    print(f"   风格：{style}")
    print(f"   输出：{output_path}\n")
    
    # 1. 生成电路施工图纸
    print("📐 [1/5] 生成电路施工图纸...")
    electrical_content = generate_electrical_plan(rooms_info)
    electrical_path = output_path / "01_电路施工图纸.md"
    electrical_path.write_text(electrical_content, encoding='utf-8')
    print(f"   ✅ {electrical_path}\n")
    
    # 2. 生成水路施工图纸
    print("📐 [2/5] 生成水路施工图纸...")
    plumbing_content = generate_plumbing_plan(rooms_info)
    plumbing_path = output_path / "02_水路施工图纸.md"
    plumbing_path.write_text(plumbing_content, encoding='utf-8')
    print(f"   ✅ {plumbing_path}\n")
    
    # 3. 生成开关插座点位图
    print("📐 [3/5] 生成开关插座点位图...")
    points_content = generate_switch_socket_layout(rooms_info, style)
    points_path = output_path / "03_开关插座点位图.md"
    points_path.write_text(points_content, encoding='utf-8')
    print(f"   ✅ {points_path}\n")
    
    # 4. 生成材料清单
    print("📐 [4/5] 生成水电材料清单...")
    materials = generate_material_list(electrical_content, plumbing_content)
    materials_path = output_path / "04_水电材料清单.json"
    materials_path.write_text(json.dumps(materials, ensure_ascii=False, indent=2), encoding='utf-8')
    print(f"   ✅ {materials_path}\n")
    
    # 5. 生成施工说明汇总
    print("📐 [5/5] 生成施工说明汇总...")
    summary_content = generate_construction_summary(area, layout, style, rooms_info)
    summary_path = output_path / "05_施工说明汇总.md"
    summary_path.write_text(summary_content, encoding='utf-8')
    print(f"   ✅ {summary_path}\n")
    
    result = {
        "timestamp": timestamp,
        "area": area,
        "layout": layout,
        "style": style,
        "output_dir": str(output_path),
        "files": [
            str(electrical_path),
            str(plumbing_path),
            str(points_path),
            str(materials_path),
            str(summary_path)
        ],
        "file_count": 5
    }
    
    print(f"✅ 施工图纸套装生成完成！")
    print(f"📁 共生成 {result['file_count']} 个文件")
    print(f"📂 输出目录：{output_path}")
    
    return result


