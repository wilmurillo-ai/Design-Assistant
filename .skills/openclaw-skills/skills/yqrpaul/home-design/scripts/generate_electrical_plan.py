            md += "| 位置 | 类型 | 功率 | 控制 |\n"
            md += "|------|------|------|------|\n"
            for item in data["照明"]:
                control = item.get('控制', '开关控制')
                md += f"| {item['位置']} | {item['类型']} | {item['功率']} | {control} |\n"
            md += "\n"
        
