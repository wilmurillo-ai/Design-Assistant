/**
 * Book Summary Skill
 * Generate podcast-style summaries of books
 */

class BookSummarySkill {
  constructor() {
    this.config = {
      key_ideas: 3,
      style: 'podcast',
      narrator: 'single',
      maxLength: null, // null = no limit
    };
  }

  /**
   * Generate podcast-style summary
   */
  generatePodcastSummary(book, options = {}) {
    if (!book.title || !book.author || !book.summary) {
      return { error: 'Missing required fields: title, author, summary' };
    }

    const config = { ...this.config, ...options };
    const keyIdeas = this.extractKeyIdeas(book.summary, config.key_ideas);
    const duration = this.estimateDuration(keyIdeas);

    return {
      title: book.title,
      author: book.author,
      duration: duration,
      script: this.buildScript(book, keyIdeas, config),
      format: 'podcast',
    };
  }

  /**
   * Extract key ideas from summary (no truncation)
   */
  extractKeyIdeas(summary, count = 3) {
    const sentences = summary.split(/[.!?]+/).filter(s => s.trim().length > 0);
    const ideas = [];

    // Distribute sentences evenly
    const step = Math.max(1, Math.floor(sentences.length / count));
    for (let i = 0; i < count && i * step < sentences.length; i++) {
      ideas.push({
        title: `Key Idea ${i + 1}`,
        content: sentences[i * step].trim(),
        takeaway: this.generateTakeaway(sentences[i * step]),
      });
    }

    return ideas.length > 0 ? ideas : [{ 
      title: 'Main Idea', 
      content: summary, 
      takeaway: 'Key insight from the book' 
    }];
  }

  /**
   * Generate actionable takeaway (no truncation)
   */
  generateTakeaway(sentence) {
    return `Apply this insight: ${sentence.trim()}`;
  }

  /**
   * Estimate podcast duration
   */
  estimateDuration(ideas) {
    const totalWords = ideas.reduce((sum, idea) => sum + idea.content.split(/\s+/).length, 0);
    const minutes = Math.ceil(totalWords / 150);
    return `~${Math.max(5, minutes)} minutes`;
  }

  /**
   * Build podcast script (no truncation)
   */
  buildScript(book, ideas, config = {}) {
    let script = `# 📖 ${book.title} — Summary Podcast Script\n\n`;
    script += `**Author**: ${book.author}\n`;
    script += `**Duration**: ${this.estimateDuration(ideas)}\n`;
    script += `**Style**: Single narrator, summary format\n\n`;
    script += `---\n\n`;

    // Hook
    script += `## [HOOK]\n\n`;
    script += `${book.summary}\n\n`;
    script += `[pause]\n\n`;

    // Intro
    script += `## [INTRO]\n\n`;
    script += `"${book.title}" by ${book.author} explores key ideas that can transform how you think.\n\n`;
    script += `[pause]\n\n`;

    // Key Ideas
    ideas.forEach((idea, index) => {
      script += `## [KEY IDEA ${index + 1}]: ${idea.title}\n\n`;
      script += `${idea.content}\n\n`;
      script += `**Takeaway**: ${idea.takeaway}\n\n`;
      script += `[pause]\n\n`;
    });

    // Synthesis
    script += `## [SYNTHESIS]\n\n`;
    script += `These ideas work together to show that ${book.summary}\n\n`;
    script += `[pause]\n\n`;

    // Call to Action
    script += `## [CALL TO ACTION]\n\n`;
    script += `Start with one idea from this book and apply it today.\n\n`;
    script += `---\n\n`;
    script += `*Summary generated by Heardly — turning books into knowledge you can hear.*\n`;

    return script;
  }

  /**
   * Generate text summary (no truncation)
   */
  generateTextSummary(book) {
    if (!book.title || !book.author || !book.summary) {
      return { error: 'Missing required fields' };
    }

    const ideas = this.extractKeyIdeas(book.summary, 3);

    return {
      title: book.title,
      author: book.author,
      summary: book.summary,
      key_ideas: ideas.map(idea => ({
        title: idea.title,
        content: idea.content,
        takeaway: idea.takeaway,
      })),
      format: 'text',
    };
  }

  /**
   * Generate full summary with custom options
   */
  generateFullSummary(book, options = {}) {
    const config = { ...this.config, ...options };
    const keyIdeas = this.extractKeyIdeas(book.summary, config.key_ideas);

    return {
      title: book.title,
      author: book.author,
      summary: book.summary,
      key_ideas: keyIdeas,
      podcast_script: this.buildScript(book, keyIdeas, config),
      text_summary: this.generateTextSummary(book),
      duration: this.estimateDuration(keyIdeas),
    };
  }
}

module.exports = BookSummarySkill;
