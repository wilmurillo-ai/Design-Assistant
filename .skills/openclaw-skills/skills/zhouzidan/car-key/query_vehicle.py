def print_summary(normalized: dict, lang: str):
    summary = build_summary(normalized, lang)
    print("{0}\n".format(t("summary_title", lang)))
    print("=" * 50)
    print_basic_info(normalized, lang)
    print("\n{0}: {1}".format(t("location_info", lang), summary["address"]))
    print("{0}: {1}".format(t("power", lang), summary["power"]))
    print("{0}: {1}".format(t("gear", lang), summary["gear"]))
    print("{0}: {1}".format(t("lock_summary", lang), summary["lock"]))
    print("{0}: {1}".format(t("door_state", lang), summary["door"]))
    print("{0}: {1}".format(t("trunk", lang), summary["trunk"]))
    print("{0}: {1}".format(t("window_state", lang), summary["window"]))
    if summary["air_rows"]:
        temps = " / ".join(row["display"] for row in summary["air_rows"])
        print("{0}: {1}".format(t("air_conditioner_state", lang), temps))
    print("{0}: {1}".format(t("position_update_time", lang), summary["updated_at"]))
