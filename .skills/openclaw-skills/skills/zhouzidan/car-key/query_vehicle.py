    mode_group = parser.add_mutually_exclusive_group()
    mode_group.add_argument(
        "-p",
        "--position",
        action="store_true",
        help="Query vehicle position only / 仅查询车辆位置信息",
    )
    mode_group.add_argument(
        "-c",
        "--condition",
        action="store_true",
        help="Query vehicle condition only / 仅查询车辆车况信息",
    )
    parser.add_argument("--json", action="store_true", help="Output JSON data / 输出 JSON 数据")
    parser.add_argument(
        "--detail",
        action="store_true",
        help="Show full detailed status instead of summary / 输出完整详细状态而不是摘要",
    )
    parser.add_argument(
        "--lang",
        choices=SUPPORTED_LANGS,
        default="zh",
        help="Output language: zh or en / 输出语言：zh 或 en",
    )
    parser.add_argument(
        "--apikey",
        help="Provide the full API key and save it to cache / 提供完整 API Key 并写入缓存",
    )
