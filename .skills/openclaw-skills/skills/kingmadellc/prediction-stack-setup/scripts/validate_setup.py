def load_config():
    """Load OpenClaw config from ~/.openclaw/config.yaml.

    If the config file doesn't exist, generates a template so the user
    has a clear starting point instead of a cryptic error.
    """
    config_path = Path.home() / ".openclaw" / "config.yaml"

    if not config_path.exists():
        # Generate template config on first run
        config_path.parent.mkdir(parents=True, exist_ok=True)
        config_path.write_text(_CONFIG_TEMPLATE)
        logger.info(f"")
        logger.info(f"📋 First run — generated config template at:")
        logger.info(f"   {config_path}")
        logger.info(f"")
        logger.info(f"Next steps:")
        logger.info(f"  1. Open {config_path}")
        logger.info(f"  2. Add your Kalshi API key + private key path")
        logger.info(f"  3. (Optional) Add Anthropic API key, enable Ollama")
        logger.info(f"  4. Run this again to validate")
        logger.info(f"")
        logger.info(f"Get Kalshi API keys at: https://kalshi.com/account/api")
        logger.info(f"")
        # Return the template so validation can still run and show what's missing
        try:
            import yaml
            return yaml.safe_load(_CONFIG_TEMPLATE) or {}
        except ImportError:
            return {}

    try:
        import yaml
        with open(config_path, 'r') as f:
            return yaml.safe_load(f) or {}
    except ImportError:
        logger.error("PyYAML not installed. Install with: pip install pyyaml")
        return None
    except Exception as e:
        logger.error(f"Error loading config: {e}")
        return None


def validate_kalshi(config: dict, verbose: bool = False) -> ValidationResult:
    """Test Kalshi API connectivity and authentication."""
    result = ValidationResult("Kalshi API", required=True)
    start = time.time()

    try:
        kalshi_cfg = config.get("kalshi", {})

        if not kalshi_cfg.get("enabled", False):
            result.passed = False
            result.error = "Kalshi not enabled in config"
            result.duration_ms = int((time.time() - start) * 1000)
            return result

        key_id = kalshi_cfg.get("api_key_id") or os.getenv("KALSHI_KEY_ID")
        key_path = kalshi_cfg.get("private_key_file") or os.getenv("KALSHI_KEY_PATH")

        if not key_id:
            result.error = "KALSHI_KEY_ID not configured"
            result.passed = False
            result.duration_ms = int((time.time() - start) * 1000)
            return result

        if not key_path:
            result.error = "KALSHI_KEY_PATH not configured"
            result.passed = False
            result.duration_ms = int((time.time() - start) * 1000)
            return result

        key_path = _resolve_private_key_path(key_path)

        if not key_path.exists():
            result.error = f"Private key file not found: {key_path}"
            result.passed = False
            result.duration_ms = int((time.time() - start) * 1000)
            return result

        # Test Kalshi connection
        try:
            from kalshi_python_sync import Configuration, KalshiClient
        except ImportError:
            from kalshi_python import Configuration, KalshiClient

        config_obj = Configuration(host="https://api.elections.kalshi.com/trade-api/v2")
        with open(key_path, 'r') as f:
            config_obj.private_key_pem = f.read()
        config_obj.api_key_id = key_id

        client = KalshiClient(config_obj)

        # Test with get_positions() — public method, avoids _portfolio_api internals
        try:
            resp = client.get_positions(limit=1)
            balance = None  # v3 SDK — balance fetched separately if needed
        except (TypeError, AttributeError):
            balance = client._portfolio_api.get_balance()

        if verbose:
            if balance is not None:
                logger.info(f"  → Kalshi balance: ${balance / 100:.2f}")
            else:
                logger.info("  → Kalshi auth OK (v3 SDK)")

        result.passed = True

    except Exception as e:
        result.passed = False
        error_str = str(e).lower()

        if "401" in error_str or "403" in error_str or "auth" in error_str:
            result.error = f"Authentication failed: Check API key and private key file"
        elif "timeout" in error_str or "connection" in error_str:
            result.error = f"Network error: Cannot reach Kalshi API"
        elif "not found" in error_str or "no such file" in error_str:
            result.error = f"File not found: Check private_key_file path in config"
        else:
            result.error = str(e)[:100]

    result.duration_ms = int((time.time() - start) * 1000)
    return result


def validate_anthropic(config: dict, verbose: bool = False) -> ValidationResult:
    """Test Anthropic Claude API connectivity."""
    result = ValidationResult("Anthropic (Claude)", required=True)
    start = time.time()

    try:
        api_key = config.get("anthropic", {}).get("api_key") or os.getenv("ANTHROPIC_API_KEY")

        if not api_key:
            result.error = "ANTHROPIC_API_KEY not configured"
            result.passed = False
            result.duration_ms = int((time.time() - start) * 1000)
            return result

        from anthropic import Anthropic

        client = Anthropic(api_key=api_key)

        # Test with a simple completion
        response = client.messages.create(
            model="claude-3-5-sonnet-20241022",
            max_tokens=10,
            messages=[
                {
                    "role": "user",
                    "content": "Respond with only the word OK"
                }
            ]
        )

        if response.content:
            result.passed = True
            if verbose:
                logger.info(f"  → Response: {response.content[0].text.strip()}")

    except Exception as e:
        result.passed = False
        error_str = str(e).lower()

        if "401" in error_str or "unauthorized" in error_str or "invalid" in error_str:
            result.error = "Invalid or expired API key"
        elif "timeout" in error_str or "connection" in error_str:
            result.error = "Network error: Cannot reach Anthropic API"
        elif "429" in error_str or "rate" in error_str:
            result.error = "Rate limited: Try again in a moment"
        else:
            result.error = str(e)[:100]

    result.duration_ms = int((time.time() - start) * 1000)
    return result


def validate_polygon(config: dict, verbose: bool = False) -> ValidationResult:
    """Test Polygon.io API connectivity (optional, for enhanced edge detection)."""
    result = ValidationResult("Polygon.io API", required=False)
    start = time.time()

    try:
        polygon_cfg = config.get("polygon", {})
        api_key = polygon_cfg.get("api_key") or os.getenv("POLYGON_API_KEY")

        if not api_key:
            result.error = "API key not configured"
            result.passed = False
            result.duration_ms = int((time.time() - start) * 1000)
            return result

        import requests

        # Test with a simple ticker query
        response = requests.get(
            "https://api.polygon.io/v1/marketstatus",
            params={"apikey": api_key},
            timeout=5
        )

        if response.status_code == 200:
            result.passed = True
            if verbose:
                data = response.json()
                logger.info(f"  → Market status: {data.get('status', 'unknown')}")
        elif response.status_code == 401:
            result.error = "Invalid or expired API key"
        else:
            result.error = f"HTTP {response.status_code}"

    except Exception as e:
        result.passed = False
        error_str = str(e).lower()

        if "timeout" in error_str or "connection" in error_str:
            result.error = "Network error: Cannot reach Polygon.io API"
        else:
            result.error = str(e)[:100]

    result.duration_ms = int((time.time() - start) * 1000)
    return result


def validate_ollama(config: dict, verbose: bool = False) -> ValidationResult:
    """Test Ollama model availability (optional, for Xpulse + Kalshalyst fallback)."""
    result = ValidationResult("Ollama (Qwen)", required=False)
    start = time.time()

    try:
        ollama_cfg = config.get("ollama", {})

        if not ollama_cfg.get("enabled", False):
            result.error = "Ollama not enabled in config"
            result.passed = False
            result.duration_ms = int((time.time() - start) * 1000)
            return result

        model_name = ollama_cfg.get("model", "qwen3:latest")
        timeout = ollama_cfg.get("timeout_seconds", 60)

        import requests

        # Test 1: Check if Ollama server is running
        response = requests.get(
            "http://localhost:11434/api/tags",
            timeout=3
        )

        if response.status_code != 200:
            result.error = f"Ollama server not responding (HTTP {response.status_code})"
            result.passed = False
            result.duration_ms = int((time.time() - start) * 1000)
            return result

        # Test 2: Check if model is available
        available_models = response.json().get("models", [])
        model_names = [m["name"] for m in available_models]

        if not any(model_name in name for name in model_names):
            result.error = f"Model '{model_name}' not found. Available: {', '.join(model_names[:3])}"
            result.passed = False
            result.duration_ms = int((time.time() - start) * 1000)
            return result

        # Test 3: Try a simple inference
        response = requests.post(
            "http://localhost:11434/api/generate",
            json={
                "model": model_name,
                "prompt": "respond with OK",
                "stream": False,
            },
            timeout=timeout
        )

        if response.status_code == 200:
            result.passed = True
            if verbose:
                data = response.json()
                logger.info(f"  → Model generated response in {data.get('eval_duration', 0) / 1e9:.2f}s")
        else:
            result.error = f"Model inference failed (HTTP {response.status_code})"

    except requests.exceptions.ConnectionError:
        result.error = "Ollama not running on localhost:11434"
        result.passed = False
    except requests.exceptions.Timeout:
        result.error = "Ollama request timed out"
        result.passed = False
    except Exception as e:
        result.passed = False
        result.error = str(e)[:100]

    result.duration_ms = int((time.time() - start) * 1000)
    return result


def validate_polymarket(config: dict, verbose: bool = False) -> ValidationResult:
    """Test Polymarket public API (optional, no auth needed)."""
    result = ValidationResult("Polymarket API", required=False)
    start = time.time()

    try:
        import requests

        # Test with public endpoint (no auth needed)
        response = requests.get(
            "https://gamma-api.polymarket.com/markets",
            params={"limit": 1},
            timeout=5
        )

        if response.status_code == 200:
            result.passed = True
            if verbose:
                data = response.json()
                market_count = len(data) if isinstance(data, list) else data.get("count", 0)
                logger.info(f"  → Public API reachable, markets available")
        else:
            result.error = f"HTTP {response.status_code}"

    except Exception as e:
        result.passed = False
        error_str = str(e).lower()

        if "timeout" in error_str or "connection" in error_str:
            result.error = "Network error: Cannot reach Polymarket API"
        else:
            result.error = str(e)[:100]

    result.duration_ms = int((time.time() - start) * 1000)
    return result


def main():
    parser = argparse.ArgumentParser(
        description="Validate Prediction Stack API configuration"
    )
    parser.add_argument("--kalshi-only", action="store_true", help="Test only Kalshi")
    parser.add_argument("--verbose", "-v", action="store_true", help="Detailed output")
    args = parser.parse_args()

    # Load config (generates template on first run, returns None only on pyyaml missing)
    config = load_config()
    if config is None:
        logger.error("Cannot proceed without PyYAML. Install with: pip install pyyaml")
        return 1

    logger.info("━" * 70)
    logger.info("Prediction Stack API Validation")
    logger.info("━" * 70)
    logger.info("")

    results = []

    # Required services
    logger.info("REQUIRED SERVICES (must pass for stack to function)")
    logger.info("─" * 70)

    result = validate_kalshi(config, args.verbose)
    results.append(result)
    logger.info(result)
    if result.error:
        logger.info(f"   Error: {result.error}")

    if not args.kalshi_only:
        result = validate_anthropic(config, args.verbose)
        results.append(result)
        logger.info(result)
        if result.error:
            logger.info(f"   Error: {result.error}")

    logger.info("")

    # Optional services
    if not args.kalshi_only:
        logger.info("OPTIONAL SERVICES (enhance edge detection and resilience)")
        logger.info("─" * 70)

        result = validate_polygon(config, args.verbose)
        results.append(result)
        logger.info(result)
        if result.error:
            logger.info(f"   Error: {result.error}")

        result = validate_ollama(config, args.verbose)
        results.append(result)
        logger.info(result)
        if result.error:
            logger.info(f"   Error: {result.error}")

        result = validate_polymarket(config, args.verbose)
        results.append(result)
        logger.info(result)
        if result.error:
            logger.info(f"   Error: {result.error}")

    logger.info("")
    logger.info("━" * 70)

    # Summary
    required_results = [r for r in results if r.required]
    optional_results = [r for r in results if not r.required]

    required_passed = sum(1 for r in required_results if r.passed)
    optional_passed = sum(1 for r in optional_results if r.passed)

    logger.info(f"Required: {required_passed}/{len(required_results)} passed")
    logger.info(f"Optional: {optional_passed}/{len(optional_results)} passed")
    logger.info("")

    # Recommendation
    if required_passed == len(required_results):
        logger.info("✅ All required services validated. Stack is ready to launch.")
        logger.info("")
        return 0
    else:
        failed = [r for r in required_results if not r.passed]
        logger.error(f"❌ {len(failed)} required service(s) failed validation:")
        for r in failed:
            logger.error(f"   - {r.service_name}: {r.error}")
        logger.error("")
        logger.error("Fix these services before proceeding. Check error messages above.")
        logger.error("")
        return 1


if __name__ == "__main__":
    sys.exit(main())
