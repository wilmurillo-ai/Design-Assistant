#!/usr/bin/env bash
set -euo pipefail

NAME=""
BASE_PATH="apps"
AUTH_MODE="jwt"

while [[ $# -gt 0 ]]; do
  case "$1" in
    --name) NAME="$2"; shift 2;;
    --path) BASE_PATH="$2"; shift 2;;
    --with-auth) AUTH_MODE="$2"; shift 2;;
    *) echo "Unknown arg: $1"; exit 1;;
  esac
done

if [[ -z "$NAME" ]]; then
  echo "Usage: scaffold_neo_app.sh --name <project-name> [--path apps] [--with-auth jwt]"
  exit 1
fi

ROOT="$BASE_PATH/$NAME"
FRONTEND="$ROOT/frontend"
BACKEND="$ROOT/backend"

mkdir -p "$FRONTEND/src" "$FRONTEND/public"
mkdir -p "$BACKEND/src" "$BACKEND/src/config" "$BACKEND/src/models" "$BACKEND/src/controllers" "$BACKEND/src/routes" "$BACKEND/src/middlewares"

