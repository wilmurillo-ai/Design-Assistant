cat > "$BACKEND/src/controllers/authController.js" <<'EOF'
const bcrypt = require('bcryptjs');
const jwt = require('jsonwebtoken');
const User = require('../models/User');

async function register(req, res, next) {
  try {
    const { name, email, password } = req.body;
    const exists = await User.findOne({ email });
    if (exists) return next({ status: 409, message: 'Email already used' });

    const passwordHash = await bcrypt.hash(password, 10);
    const user = await User.create({ name, email, passwordHash });
    res.status(201).json({ id: user._id, email: user.email });
  } catch (e) {
    next(e);
  }
}

async function login(req, res, next) {
  try {
    const { email, password } = req.body;
    const user = await User.findOne({ email });
    if (!user) return next({ status: 401, message: 'Invalid credentials' });

    const ok = await bcrypt.compare(password, user.passwordHash);
    if (!ok) return next({ status: 401, message: 'Invalid credentials' });

    const token = jwt.sign({ sub: user._id, role: user.role }, process.env.JWT_SECRET, {
      expiresIn: process.env.JWT_EXPIRES_IN || '7d'
    });

    res.json({ token });
  } catch (e) {
    next(e);
  }
}

module.exports = { register, login };
EOF

cat > "$BACKEND/src/controllers/itemController.js" <<'EOF'
const Item = require('../models/Item');

async function createItem(req, res, next) {
  try {
    const doc = await Item.create({ ...req.body, createdBy: req.user.sub });
    res.status(201).json(doc);
  } catch (e) { next(e); }
}

async function listItems(_req, res, next) {
  try {
    const docs = await Item.find().sort({ createdAt: -1 });
    res.json(docs);
  } catch (e) { next(e); }
}

async function getItem(req, res, next) {
  try {
    const doc = await Item.findById(req.params.id);
    if (!doc) return next({ status: 404, message: 'Not found' });
    res.json(doc);
  } catch (e) { next(e); }
}

async function updateItem(req, res, next) {
  try {
    const doc = await Item.findByIdAndUpdate(req.params.id, req.body, { new: true });
    if (!doc) return next({ status: 404, message: 'Not found' });
    res.json(doc);
  } catch (e) { next(e); }
}

async function deleteItem(req, res, next) {
  try {
    const doc = await Item.findByIdAndDelete(req.params.id);
    if (!doc) return next({ status: 404, message: 'Not found' });
    res.json({ ok: true });
  } catch (e) { next(e); }
}

module.exports = { createItem, listItems, getItem, updateItem, deleteItem };
EOF

