def gen_standup(content, lang):
    import re
    # Try to parse "昨天做了X,今天计划Y,阻塞Z" pattern
    parts = re.split(r'[,，;；]+', content.strip())
    yesterday = parts[0] if len(parts) > 0 else "(nothing)"
    today_plan = parts[1] if len(parts) > 1 else "(nothing)"
    blocker = parts[2] if len(parts) > 2 else "(none)"

    if lang == "en":
        return """
# Standup — {date} ({weekday})

## ✅ Yesterday
- {yesterday}

## 📌 Today
- {today_plan}

## 🚧 Blockers
- {blocker}

---
_Generated by work-report-writer_
""".format(date=today(), weekday=datetime.date.today().strftime("%A"),
           yesterday=yesterday, today_plan=today_plan, blocker=blocker).strip()
    else:
        return """
# 站会汇报 — {date}（{weekday}）

## ✅ 昨日完成
- {yesterday}

## 📌 今日计划
- {today_plan}

## 🚧 阻塞项
- {blocker}

---
_Generated by work-report-writer_
""".format(date=today(), weekday=weekday_cn(),
           yesterday=yesterday, today_plan=today_plan, blocker=blocker).strip()


