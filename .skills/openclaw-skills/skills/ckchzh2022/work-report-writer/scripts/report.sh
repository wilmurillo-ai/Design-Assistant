#!/usr/bin/env bash
# work-report-writer — 工作汇报生成器
# Usage: report.sh <type> [args...] [--lang cn|en]
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"

# Parse --lang flag from any position
LANG_FLAG="cn"
ARGS=()
while [[ $# -gt 0 ]]; do
  case "$1" in
    --lang)
      LANG_FLAG="${2:-cn}"
      shift 2
      ;;
    *)
      ARGS+=("$1")
      shift
      ;;
  esac
done
set -- "${ARGS[@]+"${ARGS[@]}"}"

TYPE="${1:-help}"

python3 - "$TYPE" "$LANG_FLAG" "${@:2}" << 'PYEOF'
# -*- coding: utf-8 -*-
from __future__ import print_function
import sys
import datetime

def today():
    return datetime.date.today().strftime("%Y-%m-%d")

def this_week():
    d = datetime.date.today()
    start = d - datetime.timedelta(days=d.weekday())
    end = start + datetime.timedelta(days=6)
    return "{} ~ {}".format(start.strftime("%Y-%m-%d"), end.strftime("%Y-%m-%d"))

def this_month():
    d = datetime.date.today()
    return d.strftime("%Y年%m月")

def this_year():
    return str(datetime.date.today().year)

def weekday_cn():
    names = ["周一","周二","周三","周四","周五","周六","周日"]
    return names[datetime.date.today().weekday()]

def split_items(text):
    """Split comma/semicolon separated items into a list."""
    import re
    items = re.split(r'[,，;；、\n]+', text.strip())
    return [i.strip() for i in items if i.strip()]

