def main():
    args = sys.argv[1:]
    if len(args) < 1:
        print(show_help())
        sys.exit(0)

    report_type = args[0]
    lang = args[1] if len(args) > 1 else "cn"
    params = args[2:]

    if report_type == "help":
        print(show_help())
    elif report_type == "daily":
        if not params:
            print("Error: daily requires content. Usage: report.sh daily \"内容\"")
            sys.exit(1)
        print(gen_daily(params[0], lang))
    elif report_type == "weekly":
        if not params:
            print("Error: weekly requires content. Usage: report.sh weekly \"内容\"")
            sys.exit(1)
        print(gen_weekly(params[0], lang))
    elif report_type == "monthly":
        if not params:
            print("Error: monthly requires content. Usage: report.sh monthly \"内容\"")
            sys.exit(1)
        print(gen_monthly(params[0], lang))
    elif report_type == "project":
        if len(params) < 2:
            print("Error: project requires name and content. Usage: report.sh project \"项目名\" \"内容\"")
            sys.exit(1)
        print(gen_project(params[0], params[1], lang))
    elif report_type == "annual":
        if not params:
            print("Error: annual requires keywords. Usage: report.sh annual \"关键词\"")
            sys.exit(1)
        print(gen_annual(params[0], lang))
    elif report_type == "standup":
        if not params:
            print("Error: standup requires content. Usage: report.sh standup \"昨天X,今天Y,阻塞Z\"")
            sys.exit(1)
        print(gen_standup(params[0], lang))
    elif report_type == "okr":
        if len(params) < 2:
            print("Error: okr requires objective and key results. Usage: report.sh okr \"目标\" \"KR1,KR2\"")
            sys.exit(1)
        print(gen_okr(params[0], params[1], lang))
    elif report_type == "review":
        if len(params) < 2:
            print("Error: review requires project name and content. Usage: report.sh review \"项目名\" \"成果1,成果2\"")
            sys.exit(1)
        print(gen_review(params[0], params[1], lang))
    elif report_type == "highlight":
        if not params:
            print("Error: highlight requires content. Usage: report.sh highlight \"工作内容\"")
            sys.exit(1)
        gen_highlight(params[0], lang)
    else:
        print("Unknown command: {}".format(report_type))
        print(show_help())
        sys.exit(1)


if __name__ == "__main__":
    main()
