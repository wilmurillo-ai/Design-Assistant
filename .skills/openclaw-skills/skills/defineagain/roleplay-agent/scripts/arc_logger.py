#!/usr/bin/env python3
"""
arc_logger.py
Logs scene debrief notes to memory/personas/{name}_arc.md
Run: python3 arc_logger.py "Persona Name" "[session notes]"
"""

import sys, os
from datetime import datetime

MEMORY_DIR = "/root/.openclaw/workspace/memory/personas"
OUTFILE = lambda name: f"{MEMORY_DIR}/{name.lower().replace(' ','_')}_arc.md"

def log(name, session_note):
    os.makedirs(MEMORY_DIR, exist_ok=True)
    path = OUTFILE(name)
    date = datetime.utcnow().strftime("%Y-%m-%d")

    entry = f"\n## Session — {date}\n\n**What happened:**\n"
    entry += f"{session_note}\n\n"

    # Append or create
    if os.path.exists(path):
        with open(path) as f:
            existing = f.read()
        # Find last ## Session and insert after
        idx = existing.rfind("## Session — ")
        if idx == -1:
            content = existing + entry
        else:
            content = existing[:idx] + entry + existing[idx:]
    else:
        content = f"# Arc Log — {name}\n\n*Auto-generated by roleplay-agent*\n{entry}"

    with open(path, "w") as f:
        f.write(content.strip() + "\n")

    print(f"Logged → {path}")

def main():
    if len(sys.argv) < 3:
        print("Usage: python3 arc_logger.py \"Persona Name\" \"Session notes...\"")
        sys.exit(1)
    name = sys.argv[1]
    note = sys.argv[2]
    log(name, note)

if __name__ == "__main__":
    main()