#!/usr/bin/env python3
"""
save_persona.py — 接收 LLM 分析结果，写入 persona/$user.json
由 SKILL.md 中的 Agent 在 LLM 调用完成后调用。

用法:
  python3 save_persona.py --analysis-json '<JSON字符串>' --meta-file /tmp/persona_meta.json
  或
  python3 save_persona.py --analysis-file /tmp/persona_analysis_result.json --meta-file /tmp/persona_meta.json
"""

import argparse
import json
import sys
from datetime import datetime
from pathlib import Path


def main():
    parser = argparse.ArgumentParser(description='保存 LLM 分析结果到 persona 文件')
    parser.add_argument('--analysis-json', help='LLM 返回的 JSON 字符串')
    parser.add_argument('--analysis-file', help='LLM 返回结果的文件路径')
    parser.add_argument('--meta-file', default='/tmp/persona_meta.json', help='analyze.py 生成的元数据文件')
    args = parser.parse_args()

    # 读取元数据
    meta_path = Path(args.meta_file)
    if not meta_path.exists():
        print(f"ERROR: 元数据文件 {meta_path} 不存在，请先运行 analyze.py", file=sys.stderr)
        sys.exit(1)

    with open(meta_path, 'r', encoding='utf-8') as f:
        meta = json.load(f)

    # 读取分析结果
    analysis_str = args.analysis_json
    if not analysis_str and args.analysis_file:
        analysis_str = Path(args.analysis_file).read_text(encoding='utf-8')
    if not analysis_str:
        print("ERROR: 请通过 --analysis-json 或 --analysis-file 提供分析结果", file=sys.stderr)
        sys.exit(1)

    # 解析 JSON（尝试提取 markdown 代码块中的 JSON）
    import re
    json_match = re.search(r'```(?:json)?\s*(\{.*?\})\s*```', analysis_str, re.DOTALL)
    if json_match:
        analysis_str = json_match.group(1)
    # 直接查找第一个 { 到最后一个 }
    start = analysis_str.find('{')
    end = analysis_str.rfind('}')
    if start != -1 and end != -1:
        analysis_str = analysis_str[start:end+1]

    try:
        analysis = json.loads(analysis_str)
    except json.JSONDecodeError as e:
        print(f"ERROR: 无法解析 LLM 返回的 JSON: {e}", file=sys.stderr)
        print(f"原始内容: {analysis_str[:500]}", file=sys.stderr)
        sys.exit(1)

    # 构建完整 persona 文件
    persona_dir = Path(meta['persona_dir'])
    persona_dir.mkdir(parents=True, exist_ok=True)

    # 读取模板
    template_path = Path(meta['template_path'])
    template = {}
    if template_path.exists():
        with open(template_path, 'r', encoding='utf-8') as f:
            template = json.load(f)

    now = datetime.utcnow().isoformat() + "Z"
    memory_files = meta.get('memory_files', [])
    import os
    file_names = [os.path.basename(f) for f in memory_files]

    # 提取日期范围
    import re as _re
    dates = [_re.match(r'(\d{4}-\d{2}-\d{2})', os.path.basename(f)).group(1)
             for f in memory_files
             if _re.match(r'\d{4}-\d{2}-\d{2}', os.path.basename(f))]
    date_range = [min(dates), max(dates)] if dates else [None, None]

    user_name = meta['user']
    output = {
        "_comment": "Auto-generated by persona-creator skill. Do not edit manually.",
        "user_id": user_name,
        "display_name": user_name,
        "created_at": now,
        "updated_at": now,
        "version": 1,
        "statistics": {
            "analyzed_messages": len(meta.get('messages', [])),
            "date_range": date_range,
            "memory_files_scanned": file_names
        },
        "persona": analysis
    }

    output_path = persona_dir / f"{user_name}.json"

    # 如果已存在，保留 created_at
    if output_path.exists():
        with open(output_path, 'r', encoding='utf-8') as f:
            existing = json.load(f)
        output['created_at'] = existing.get('created_at', now)
        output['version'] = existing.get('version', 1) + 1

    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(output, f, ensure_ascii=False, indent=2)

    print(f"✅ 风格档案已保存: {output_path}")
    print(f"SAVED_TO:{output_path}")
    print(f"USER:{user_name}")
    print(f"MESSAGES_ANALYZED:{len(meta.get('messages', []))}")

    # 打印摘要
    p = analysis
    print(f"\n📊 风格摘要 for [{user_name}]:")
    print(f"  语气: {p.get('tone', {}).get('overall', 'N/A')}")
    print(f"  常用表情: {', '.join(p.get('emoji_usage', {}).get('favorites', []))}")
    print(f"  口头禅: {', '.join(p.get('catchphrases', [])[:5])}")
    print(f"  话题偏好: {', '.join(p.get('topic_preferences', [])[:5])}")


if __name__ == '__main__':
    main()
