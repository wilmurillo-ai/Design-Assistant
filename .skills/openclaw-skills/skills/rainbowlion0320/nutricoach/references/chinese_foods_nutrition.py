def get_food_count():
    """Return total number of foods in database."""
    return len(CHINESE_FOODS)


def get_foods_by_category(category):
    """Return foods filtered by category."""
    return [f for f in CHINESE_FOODS if f[1] == category]


def search_foods(query):
    """Search foods by name."""
    query = query.lower()
    return [f for f in CHINESE_FOODS if query in f[0].lower()]


