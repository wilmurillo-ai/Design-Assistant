def escape_xml(s):
    """转义 XML 特殊字符"""
    if not isinstance(s, str):
        s = str(s)
    return s.replace('&', '&amp;').replace('<', '&lt;').replace('>', '&gt;').replace('"', '&quot;')


def generate_xmind(test_data, output_file="测试用例.xmind"):
    """
    生成 XMind 文件
    
    Args:
        test_data: 测试用例数据（字典）
        output_file: 输出文件名
    
    Returns:
        dict: 包含生成结果的信息
    """
    topic_id = 0
    
    def get_id():
        nonlocal topic_id
        topic_id += 1
        return f"t{topic_id}"
    
    def make_topic(title, children=""):
        """创建 XMind topic 元素"""
        tid = get_id()
        title_escaped = escape_xml(title)
        if children:
            return f'<topic id="{tid}" timestamp="1773346652017"><title>{title_escaped}</title><children><topics type="attached">{children}</topics></children></topic>'
        else:
            return f'<topic id="{tid}" timestamp="1773346652017"><title>{title_escaped}</title></topic>'
    
    # 构建模块主题
    modules_xml = ""
    total_cases = 0
    total_points = 0
    
    for module_name, test_points in test_data.items():
        points_xml = ""
        for point in test_points:
            total_points += 1
            cases_xml = ""
            for case in point.get("cases", []):
                total_cases += 1
                # 组用例信息文本
                case_info = f"{case.get('用例名称', 'Unnamed')} [{case.get('优先级', '中')}]"
                case_info += f"\n前置条件：{case.get('前置条件', '')}"
                steps = case.get('步骤', [])
                if isinstance(steps, list):
                    steps_str = " ".join([f"{i+1}.{s}" for i, s in enumerate(steps)])
                else:
                    steps_str = str(steps)
                case_info += f"\n步骤：{steps_str}"
                case_info += f"\n预期结果：{case.get('预期结果', '')}"
                cases_xml += make_topic(case_info)
            points_xml += make_topic(point.get("测试点", "Unnamed"), cases_xml)
        modules_xml += make_topic(module_name, points_xml)
    
    # 生成 content.xml
    content_xml = f'''<?xml version="1.0" encoding="UTF-8" standalone="no"?>
<xmap-content xmlns="urn:xmind:xmap:xmlns:content:2.0" xmlns:fo="http://www.w3.org/1999/XSL/Format" xmlns:svg="http://www.w3.org/2000/svg" xmlns:xhtml="http://www.w3.org/1999/xhtml" xmlns:xlink="http://www.w3.org/1999/xlink" timestamp="1773346652017" version="2.0">
  <sheet id="sheet1" timestamp="1773346652017">
    <topic id="root" timestamp="1773346652017">
      <title>测试用例</title>
      <children>
        <topics type="attached">
          {modules_xml}
        </topics>
      </children>
    </topic>
  </sheet>
</xmap-content>'''
    
    styles_xml = '''<?xml version="1.0" encoding="UTF-8" standalone="no"?>
<styles xmlns="urn:xmind:xmap:xmlns:content:2.0" version="2.0"/>'''
    
    comments_xml = '''<?xml version="1.0" encoding="UTF-8" standalone="no"?>
<comments xmlns="urn:xmind:xmap:xmlns:content:2.0" version="2.0"/>'''
    
    manifest_xml = '''<?xml version="1.0" encoding="UTF-8"?>
<manifest:manifest xmlns:manifest="urn:oasis:names:tc:opendocument:xmlns:manifest:1.0">
 <manifest:file-entry manifest:media-type="application/vnd.xmind.workbook+zip" manifest:full-path="/"/>
 <manifest:file-entry manifest:media-type="text/xml" manifest:full-path="content.xml"/>
 <manifest:file-entry manifest:media-type="text/xml" manifest:full-path="styles.xml"/>
 <manifest:file-entry manifest:media-type="text/xml" manifest:full-path="comments.xml"/>
</manifest:manifest>'''
    
    # 创建 ZIP 文件
    with zipfile.ZipFile(output_file, 'w', zipfile.ZIP_DEFLATED) as zf:
        zf.writestr('content.xml', content_xml)
        zf.writestr('styles.xml', styles_xml)
        zf.writestr('comments.xml', comments_xml)
        zf.writestr('META-INF/manifest.xml', manifest_xml)
    
    return {
        "file": output_file,
        "modules": len(test_data),
        "test_points": total_points,
        "test_cases": total_cases
    }


