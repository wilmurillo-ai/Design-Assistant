## 📜 公司制度规范生成

请指定制度类型，生成对应规范文档：

### 可选制度类型
1. **考勤制度** — 工作时间、打卡、迟到早退、旷工处理
2. **休假制度** — 年假、病假、事假、婚假、产假等
3. **报销制度** — 差旅、招待、办公用品、审批流程
4. **行为规范** — 着装、保密、利益冲突、社交媒体
5. **绩效考核** — 考核周期、指标、评分、结果应用
6. **晋升制度** — 职级体系、晋升条件、评审流程
7. **远程办公** — 申请条件、工作要求、设备支持

### 制度文档结构
```
一、目的和适用范围
二、定义和术语
三、具体规定
  3.1 [核心条款]
  3.2 [操作细则]
  3.3 [特殊情况处理]
四、违规处理
五、附则
  - 生效日期
  - 解释权归属
  - 版本记录
```

### 注意事项
- 确保符合当地劳动法规
- 条款清晰无歧义
- 包含员工签收确认
- 定期审查更新 (建议每半年)
