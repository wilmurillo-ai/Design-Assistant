| 轮次 | 面试官 | 时长 | 重点评估 |
|------|--------|------|----------|
| 初筛 | HR | 30min | 基本匹配度、薪资期望 |
| 一面 | 直属上级 | 60min | 专业能力、项目经验 |
| 二面 | 部门负责人 | 45min | 综合素质、文化匹配 |
| 终面 | VP/CEO | 30min | 战略认同、价值观 |

