offboard)
cat <<'PROMPT'
## 👋 离职管理流程

### 1. 离职申请
- 确认离职类型：主动离职 / 协商解除 / 合同到期
- 确认最后工作日 (法定提前30天/试用期3天)
- 书面离职申请签收

### 2. 离职面谈
**面谈要点：**
- 离职原因 (真实原因挖掘)
- 对公司/团队的建议
- 是否有挽留可能
- 竞业限制确认

**常见离职原因分类：**
| 类别 | 具体原因 | 改进建议 |
|------|----------|----------|
| 薪资 | 低于市场/涨幅不满意 | 薪资调研/调薪机制 |
| 发展 | 晋升受阻/技能停滞 | 职业通道/培训 |
| 管理 | 与上级关系/管理风格 | 管理培训/团队调整 |
| 文化 | 加班/氛围/价值观 | 文化建设/制度优化 |
| 个人 | 家庭/健康/搬迁 | 弹性政策/远程 |

### 3. 工作交接清单
- [ ] 编写工作交接文档
- [ ] 项目进度和待办事项移交
- [ ] 客户/供应商关系交接
- [ ] 文档和资料归档
- [ ] 代码/设计稿权限移交
- [ ] 密码和账号交接

### 4. 行政事项
- [ ] 归还办公设备 (电脑/工卡/钥匙)
- [ ] 关闭系统权限和企业邮箱
- [ ] 社保/公积金减员
- [ ] 工资结算 (含未休年假折算)
- [ ] 开具离职证明
- [ ] 人事档案/社保转移

### 5. 离职证明模板
```
离职证明

兹证明[姓名]，身份证号[XXX]，于[入职日期]至[离职日期]
