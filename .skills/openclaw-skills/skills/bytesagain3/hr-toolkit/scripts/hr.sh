- **必备条件** (Must Have):
  - 学历要求
  - 工作经验 (年限+领域)
  - 核心技能 (3-5项)
- **加分项** (Nice to Have):
  - 额外技能
  - 行业经验
  - 证书资质

### 3. 薪资福利
- 薪资范围 (市场参考)
- 福利清单
- 成长空间

