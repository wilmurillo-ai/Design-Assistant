### 对比文案策略

**策略1: 直接对比 (适合有明显优势时)**
> "同等价位唯一支持XX功能的产品"
> "比同类产品轻40%，但续航多50%"

**策略2: 类比对比 (适合新品类)**
> "如果说XX是SUV，那我们就是跑车"
> "XX的品质，XX 1/3 的价格"

**策略3: 用户视角对比**
> "用过3款同类产品后，我最终选了这款，因为..."
> "从XX换到我们产品的用户反馈：..."

**策略4: 场景对比**
| 场景 | 我们 | 竞品 |
|------|------|------|
| 日常通勤 | ✅ 降噪+长续航 | ⚠️ 续航不够 |
| 运动健身 | ✅ 防水IPX5 | ❌ 不防水 |
| 会议通话 | ✅ 双麦降噪 | ⚠️ 通话模糊 |

### 注意事项
⚠️ 不要诋毁竞品，用事实说话
⚠️ 对比维度要对消费者有意义
⚠️ 避免虚假宣传，数据要可验证
⚠️ 突出"为什么选我们"而非"别人多差"
