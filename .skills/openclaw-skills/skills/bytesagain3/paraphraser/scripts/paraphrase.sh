#!/usr/bin/env bash
# paraphraser — 文本改写助手
# Usage: bash paraphrase.sh <command> [text...]
# Powered by BytesAgain

set -euo pipefail

CMD="${1:-help}"
shift 2>/dev/null || true
INPUT="$*"

case "$CMD" in
  rewrite)
    cat <<'PROMPT'
## 文本改写/降重

请对以下文本进行改写，要求：
1. **保持原意**：核心信息和观点不变
2. **充分改写**：替换同义词、调整句式结构、改变语序
3. **自然流畅**：改写后的文本要通顺自然，不能生硬
4. **降重目标**：确保与原文的文字重复率低于 20%

### 改写策略
- 同义词替换：用近义词替代原文用词
- 句式变换：主动↔被动、长句↔短句、陈述↔反问
- 语序调整：调整分句顺序、调整修饰语位置
- 概括与展开：适当概括冗长表达，或展开简略表达

### 待改写文本
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    echo "### 输出格式"
    echo "1. **改写结果**：完整的改写文本"
    echo "2. **改写说明**：列出主要改写手法（3-5条）"
    echo "3. **预估降重率**：估算与原文的差异程度"
    ;;

  academic)
    cat <<'PROMPT'
## 学术风格改写

请将以下文本改写为学术风格，要求：
1. **学术用语**：使用专业术语和学术表达
2. **严谨表述**：添加限定词、避免绝对化表达
3. **被动语态**：适当使用被动语态和无人称表达
4. **逻辑严密**：添加因果、转折等逻辑连接词
5. **规范格式**：符合学术写作规范

### 学术改写要点
- 口语 → 书面语（"很多" → "大量"、"觉得" → "认为"）
- 模糊 → 精确（"最近" → "近年来"、"一些" → "若干"）
- 主观 → 客观（"我认为" → "研究表明"、"显然" → "据分析"）
- 简单 → 专业（使用领域术语替代通俗表达）

### 待改写文本
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    echo "### 输出格式"
    echo "1. **学术版本**：改写后的学术风格文本"
    echo "2. **术语标注**：标注使用的专业术语及含义"
    echo "3. **改写对照**：关键改写点的原文→学术版对照"
    ;;

  simplify)
    cat <<'PROMPT'
## 简化表达

请将以下文本简化为通俗易懂的表达，要求：
1. **去专业化**：用日常用语替代专业术语
2. **短句优先**：长句拆分为短句，每句一个意思
3. **具象化**：抽象概念用具体例子或比喻说明
4. **口语化**：适当使用口语化表达，让读者感到亲切
5. **目标读者**：初中生也能看懂

### 简化策略
- 专业术语 → 日常用语（附括号解释如必要）
- 长难句 → 多个短句
- 抽象概念 → 生活化比喻或例子
- 复杂逻辑 → 分步骤说明（第一、第二……）
- 删除不必要的修饰和限定

### 待简化文本
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    echo "### 输出格式"
    echo "1. **简化版本**：通俗易懂的改写"
    echo "2. **术语解释**：原文中专业术语的白话解释"
    echo "3. **一句话总结**：用一句话概括核心意思"
    ;;

  formal)
    cat <<'PROMPT'
## 正式化改写

请将以下文本改写为正式、规范的表达，适用于公文/商务/正式场合：
1. **书面语**：去除所有口语、网络用语、俚语
2. **规范用词**：使用标准书面用语
3. **严肃语气**：保持专业、严肃、得体的语气
4. **结构清晰**：逻辑分明、层次清楚
5. **敬语得体**：适当使用敬语和谦辞

### 正式化要点
- "搞定" → "完成"、"弄" → "处理/实施"
- "挺好的" → "较为理想"、"没问题" → "可以接受"
- "咱们" → "我们"、"你" → "您/贵方"
- 去掉语气词（"吧"、"呢"、"啊"）
- 去掉网络用语（"yyds"、"绝绝子"、"666"）

### 待改写文本
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    echo "### 输出格式"
    echo "1. **正式版本**：正式化改写结果"
    echo "2. **修改标注**：标注口语→正式的关键替换"
    echo "3. **适用场景**：建议适用的场景（公文/邮件/汇报等）"
    ;;

  expand)
    cat <<'PROMPT'
## 文本扩写

请对以下文本进行扩写，丰富内容和论述：
1. **增加论据**：补充支持观点的事实、数据或案例
2. **详细阐述**：对关键概念进行深入解释
3. **添加过渡**：增加段落间的过渡和衔接
4. **丰富维度**：从多个角度分析（原因、影响、对策等）
5. **扩写比例**：扩展到原文的 2-3 倍篇幅

### 扩写策略
- **What**：是什么？定义和解释核心概念
- **Why**：为什么？分析原因和背景
- **How**：怎么做？具体方法和步骤
- **Example**：举例子，用实际案例支撑
- **Impact**：影响是什么？短期和长期影响
- **Comparison**：对比分析，正反两面

### 待扩写文本
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    echo "### 输出格式"
    echo "1. **扩写结果**：完整的扩写文本"
    echo "2. **扩展要点**：列出新增的内容要点"
    echo "3. **字数对比**：原文 vs 扩写后字数"
    ;;

  compare)
    cat <<'PROMPT'
## 改写对比分析

请对以下原文和改写版本进行详细对比分析：
1. **差异标注**：标出所有改动之处
2. **改写手法**：分析使用了哪些改写技巧
3. **语义保持**：评估是否保持了原意
4. **质量评分**：从多个维度评分

### 对比维度
- **用词变化**：哪些词被替换，替换是否恰当
- **句式变化**：句式结构有什么改变
- **语序变化**：语序是否调整，效果如何
- **信息保持**：是否有信息丢失或增加
- **流畅度**：改写后是否更流畅/自然

### 待对比文本
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    echo "### 输出格式"
    cat <<'FORMAT'
1. **逐句对比表**：
   | 原文 | 改写 | 改写手法 |
   |------|------|----------|
   | ...  | ...  | ...      |

2. **综合评分**（满分10分）：
   - 降重效果：_/10
   - 语义保持：_/10
   - 流畅度：_/10
   - 总评：_/10

3. **改进建议**：可以进一步优化的地方
FORMAT
    ;;

  help|--help|-h|"")
    cat <<'HELP'
╔══════════════════════════════════════════════╗
║         📝 Paraphraser — 文本改写助手        ║
╠══════════════════════════════════════════════╣
║                                              ║
║  Commands:                                   ║
║    rewrite   改写文本/降重                    ║
║    academic  学术风格改写                     ║
║    simplify  简化表达，通俗易懂               ║
║    formal    正式化改写                       ║
║    expand    扩写文本，丰富内容               ║
║    compare   对比原文和改写版本               ║
║    help      显示此帮助菜单                   ║
║                                              ║
