#!/usr/bin/env bash
# poem.sh — 诗歌生成助手
# Usage: bash poem.sh <command> [args...]

set -euo pipefail

CMD="${1:-help}"
shift 2>/dev/null || true

show_help() {
  cat <<'HELP'
Poem Generator 🎭 — 诗歌创作助手

用法: bash poem.sh <command> [args...]

命令:
  write        根据主题/情感自由创作诗歌
  haiku        生成俳句(5-7-5)
  couplet      对联生成(春联/婚联/趣联)
  modern       现代诗创作
  acrostic     藏头诗生成
  translate    古诗白话文/英文翻译

示例:
  bash poem.sh write
  bash poem.sh haiku
  bash poem.sh couplet

  Powered by BytesAgain | bytesagain.com | hello@bytesagain.com
HELP
}

cmd_write() {
  cat <<'EOF'
## 🎭 诗歌自由创作

请AI根据以下信息创作诗歌：

### 输入信息
- **主题/关键词**: [用户提供]
- **情感基调**: [喜悦/忧伤/怀念/激昂/宁静/浪漫]
- **诗歌形式**: [自由诗/格律诗/散文诗/歌词]
- **长度**: [短(4-8行)/中(12-20行)/长(20+行)]
- **语言**: [中文/英文/中英双语]

### 创作指引

#### 构思阶段
1. 确定核心意象（1-2个）
2. 选择情感色调
3. 构建意象网络

#### 创作原则
- **具体>抽象**: 用"枯藤老树昏鸦"代替"很荒凉"
- **动词>形容词**: 用有力的动词带动画面
- **少即是多**: 删掉所有可删的字
- **节奏感**: 注意音节长短交替
- **结尾有力**: 末句是全诗最重要的句子

#### 输出格式
```
[诗歌标题]

[诗歌正文]

---
📝 创作说明：
- 核心意象: ___
- 手法: ___
- 适用场景: [朗诵/分享/收藏/赠送]
```
EOF
}

cmd_haiku() {
  cat <<'EOF'
## 🌸 俳句生成器

请AI创作俳句（Haiku）：

### 俳句规则
- **第一行**: 5个音节/字
- **第二行**: 7个音节/字
- **第三行**: 5个音节/字
- 包含**季语**（季节元素）
- 捕捉**瞬间感悟**

### 输入信息
- **季节**: [春/夏/秋/冬]
- **主题**: [用户提供]
- **语言**: [中文/日文/英文]

### 俳句模板

#### 中文俳句
```
[5字——景物描写]
[7字——动态/变化]
[5字——感悟/转折]
```

#### 英文 Haiku
```
[5 syllables — nature image]
[7 syllables — action/movement]
[5 syllables — insight/turn]
```

### 经典示例

🌸 春
```
古池塘边静
一只青蛙跳入水
水声清寂远
```
— 松尾芭蕉 (改编)

🍂 秋
```
秋风扫落叶
一人独坐阶前石
月上柳梢头
```

❄️ 冬
```
An old silent pond
A frog jumps into the pond
Splash! Silence again.
```
— Matsuo Bashō

### 请生成
请根据主题创作**5首俳句**，中英各半：
1. [中文俳句]
2. [英文 Haiku]
3. [中文俳句]
4. [英文 Haiku]
5. [中文俳句——最佳作品]
EOF
}

cmd_couplet() {
  cat <<'EOF'
## 🧧 对联生成器

请AI创作对联：

### 对联规则
1. **字数相等**: 上下联字数完全相同
2. **词性相对**: 名词对名词、动词对动词
3. **平仄相反**: 上联仄声收，下联平声收
4. **内容相关**: 主题统一或形成对比

### 对联类型

#### 🧧 春联
```
上联: [7-11字，仄声收]
下联: [7-11字，平声收]
横批: [4字]
```

#### 💒 婚联
```
上联: [祝福新人]
下联: [美好期望]
横批: [4字吉语]
```

#### 🏢 行业联
```
上联: [体现行业特色]
下联: [表达经营理念]
横批: [行业精神]
```

#### 😄 趣联
```
上联: [巧用谐音/双关/拆字]
下联: [对仗工整，妙趣横生]
```

### 输入信息
- **类型**: [春联/婚联/寿联/行业联/趣联/挽联]
- **主题/场景**: [用户提供]
- **字数**: [5/7/9/11字]
- **风格**: [典雅/通俗/幽默/庄重]

### 输出格式
为每组对联标注：
- 平仄标注
- 词性对照
- 创意说明
EOF
}

cmd_modern() {
  cat <<'EOF'
## 🌊 现代诗创作

请AI创作现代诗：

### 输入信息
- **主题**: [用户提供]
- **情感**: [用户提供]
- **风格**: [清新/深沉/浪漫/哲理/实验]
- **长度**: [短诗8行以内/中篇20行/长诗20+行]

### 现代诗风格参考

#### 清新自然派
```
特点: 简洁明快、意象清新
代表: 顾城、海子
示例风格:
  短句为主
  自然意象（草/风/花/天空）
  纯净感
```

#### 深沉思辨派
```
特点: 意蕴深厚、思想性强
代表: 北岛、食指
示例风格:
  隐喻丰富
  社会关怀
  铿锵有力
```

#### 浪漫抒情派
```
特点: 情感充沛、语言优美
代表: 舒婷、席慕蓉
示例风格:
  情感流动
  意象唯美
  韵律感强
```

#### 口语先锋派
```
特点: 日常语言、解构传统
代表: 余秀华、于坚
示例风格:
  口语化
  生活化意象
  出人意料的转折
```

### 创作技巧
1. **意象链**: 用3-5个核心意象串联全诗
2. **张力**: 在对立中创造诗意（生/死、远/近）
3. **留白**: 故意省略，让读者填补
4. **断句**: 在意外处断行，制造节奏冲击
5. **结尾**: 最后两行定成败

### 输出格式
```
[诗歌标题]

[诗歌正文——注意排版]

---
📝 赏析：
- 核心意象: ___
- 主要手法: ___
- 朗读建议: [节奏/重音/停顿]
```
EOF
}

cmd_acrostic() {
  cat <<'EOF'
## 🔤 藏头诗生成器

请AI创作藏头诗：

### 藏头诗规则
- 每行第一个字连起来组成指定文字
- 整首诗意义完整、通顺
- 藏头不影响诗意表达

### 输入信息
- **藏头文字**: [用户提供，如"生日快乐"]
- **风格**: [古风/现代/幽默/浪漫]
- **诗体**: [五言/七言/自由体]

### 藏头诗模板

#### 七言藏头（示例：生日快乐）
```
生逢盛世沐春风，
日照华堂喜气浓。
快意人生多美好，
乐享年华万事通。
```

#### 五言藏头（示例：我爱你）
```
我立山巅望，
爱看云卷舒。
你如春风至，
心暖满庭除。
```

#### 现代体藏头
```
[字1]——[一句现代诗]
[字2]——[一句现代诗]
[字3]——[一句现代诗]
...
```

### 输出格式
```
藏头文字: [XXXX]

[完整诗作]

💡 藏头标注:
[字1] → 第一行
[字2] → 第二行
...

📝 诗意说明: ___
```

### 高级玩法
- **藏尾诗**: 每行最后一个字组成文字
- **藏中诗**: 每行中间某字组成文字
- **回文诗**: 正读反读都成诗
EOF
}

cmd_translate() {
  cat <<'EOF'
## 📜 古诗翻译/赏析

请AI翻译并赏析古诗：

### 输入信息
- **诗歌原文**: [用户提供]
- **翻译方向**: [古文→白话文 / 古文→英文 / 英文→中文诗]

### 翻译模板

#### 完整赏析格式
```
## [诗题]
**[作者]** · [朝代]

### 原文
[诗歌原文——标注读音难字]

### 注释
1. [词1]: [解释]
2. [词2]: [解释]
...

### 白话翻译
[逐句对照翻译，保留诗意]

### 英文翻译
[English translation — 保持诗歌形式]

### 赏析
- **主题**: [诗歌主旨]
- **意象**: [核心意象分析]
- **手法**: [修辞/表现手法]
- **情感**: [情感脉络]
- **名句**: "[最著名的句子]" → [为什么经典]

### 背景故事
[创作背景、作者生平相关]

### 类似推荐
- 《[类似诗1]》— [作者]: [一句话说明]
- 《[类似诗2]》— [作者]: [一句话说明]
```

### 翻译原则
- **信**: 忠于原意
- **达**: 通顺易懂
- **雅**: 保留诗意和美感
- 英译注意韵律和节奏
EOF
}

case "$CMD" in
  write)     cmd_write ;;
  haiku)     cmd_haiku ;;
  couplet)   cmd_couplet ;;
  modern)    cmd_modern ;;
  acrostic)  cmd_acrostic ;;
  translate) cmd_translate ;;
  help|--help|-h) show_help ;;
  *)
    echo "❌ 未知命令: $CMD"
    echo ""
    show_help
    exit 1
    ;;
esac
