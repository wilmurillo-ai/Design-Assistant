#!/usr/bin/env bash
# proofreader — 校对助手
# Usage: bash proofread.sh <command> [text...]
# Powered by BytesAgain

set -euo pipefail

CMD="${1:-help}"
shift 2>/dev/null || true
INPUT="$*"

case "$CMD" in
  check)
    cat <<'PROMPT'
## 全面校对

请对以下文本进行全面校对，检查所有类型的错误：

### 检查清单
1. **错别字/拼写**：找出所有拼写错误和错别字
2. **语法错误**：主谓搭配、时态、语态、句式结构
3. **标点符号**：标点使用是否正确、是否遗漏
4. **用词不当**：是否有用词不准确或搭配不当
5. **重复冗余**：是否有不必要的重复表达
6. **格式问题**：数字、日期、单位的格式是否规范

### 中文特别检查
- 的/地/得 使用是否正确
- 在/再、做/作、即/既 等易混字
- 中文标点 vs 英文标点混用
- 全角/半角混用

### 英文特别检查
- Subject-verb agreement
- Tense consistency
- Article usage (a/an/the)
- Comma splices and run-on sentences

### 待校对文本
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    cat <<'FORMAT'
### 输出格式
1. **错误列表**：
   | # | 位置 | 原文 | 错误类型 | 修改建议 |
   |---|------|------|----------|----------|
   | 1 | ...  | ...  | 错别字   | ...      |

2. **修改后全文**：标注所有修改的完整文本
3. **错误统计**：各类错误数量汇总
FORMAT
    ;;

  grammar)
    cat <<'PROMPT'
## 语法纠错

请专注检查以下文本的语法错误：

### 中文语法检查点
1. **主谓搭配**：主语和谓语是否搭配
2. **成分残缺**：是否缺少必要的句子成分
3. **语序不当**：定语、状语位置是否正确
4. **搭配不当**：动宾搭配、主谓搭配是否合理
5. **逻辑错误**：前后是否矛盾、因果是否成立
6. **关联词**：关联词使用是否正确（虽然…但是、不仅…而且）

### 英文语法检查点
1. **Subject-verb agreement**
2. **Tense consistency**
3. **Pronoun reference**
4. **Parallel structure**
5. **Dangling modifiers**
6. **Comma splices / run-on sentences**

### 待检查文本
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    echo "### 输出格式"
    echo "1. **语法错误列表**：每个错误的位置、原文、错误类型、修改建议"
    echo "2. **修正后全文**：语法修正后的完整文本"
    echo "3. **语法知识点**：涉及的语法规则简要说明"
    ;;

  style)
    cat <<'PROMPT'
## 文风统一检查

请检查以下文本的文风是否统一，并提供调整建议：

### 文风检查维度
1. **语体一致**：是否混用了口语和书面语
2. **人称一致**：人称代词是否统一（我/我们/本文）
3. **时态一致**：（英文）时态是否前后一致
4. **语气一致**：是否忽正式忽随意
5. **术语一致**：同一概念是否用了不同的词
6. **句式风格**：长短句比例是否协调

### 文风参考
- **学术**：严谨、客观、被动语态多
- **商务**：专业、礼貌、结构清晰
- **新闻**：客观、简洁、倒金字塔
- **文学**：生动、富有表现力
- **技术**：精确、步骤化、术语规范

### 待检查文本
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    echo "### 输出格式"
    echo "1. **文风诊断**：当前文本的文风特征"
    echo "2. **不一致之处**：列出文风不统一的地方"
    echo "3. **统一建议**：按目标文风给出修改建议"
    echo "4. **修改后文本**：文风统一后的版本"
    ;;

  consistency)
    cat <<'PROMPT'
## 一致性检查

请检查以下文本（或文档）的一致性问题：

### 一致性检查清单
1. **术语一致**：
   - 同一概念是否使用了不同的词（如"用户/客户/使用者"混用）
   - 中英文术语是否统一
2. **格式一致**：
   - 数字格式（阿拉伯数字 vs 汉字数字）
   - 日期格式
   - 标点风格（中文标点 vs 英文标点）
   - 列表编号格式
3. **大小写一致**（英文）：
   - 专有名词大小写
   - 标题大小写规则
4. **标点一致**：
   - 顿号 vs 逗号的使用
   - 引号风格（""vs「」）
   - 省略号格式
5. **称谓一致**：
   - 人称是否统一
   - 敬语使用是否一致

### 待检查文本
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    cat <<'FORMAT'
### 输出格式
1. **不一致项列表**：
   | # | 类型 | 出现位置 | 变体1 | 变体2 | 建议统一为 |
   |---|------|----------|-------|-------|------------|
   | 1 | 术语 | 第2/5段  | 用户  | 客户  | 用户       |

2. **一致性评分**：_/100
3. **修正建议**：优先级排序的修正清单
FORMAT
    ;;

  readability)
    cat <<'PROMPT'
## 可读性评分与优化

请评估以下文本的可读性，并提供优化建议：

### 可读性评估维度

#### 中文可读性指标
1. **平均句长**：每句平均字数（建议 15-25 字）
2. **段落长度**：每段行数（建议 3-5 行）
3. **生僻字比例**：非常用汉字占比
4. **被动句比例**：被动语态占比（建议 < 20%）
5. **长句比例**：超过 40 字的句子占比
6. **连接词使用**：逻辑连接是否充分

#### 英文可读性指标
1. **Flesch Reading Ease**：0-100 分（60-70 为标准）
2. **Flesch-Kincaid Grade Level**：对应年级水平
3. **Average sentence length**
4. **Average word length (syllables)**
5. **Passive voice percentage**

#### 通用指标
1. **标题和小标题**：是否有清晰的结构层次
2. **列表使用**：是否善用列表提高可读性
3. **视觉节奏**：长短段落是否交错
4. **信息密度**：每段是否信息过载

### 待评估文本
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    cat <<'FORMAT'
### 输出格式
1. **可读性评分卡**：
   | 指标 | 当前值 | 建议值 | 评级 |
   |------|--------|--------|------|
   | 平均句长 | 32字 | 15-25字 | ⚠️ |
   | ...  | ...    | ...    | ...  |

2. **总体评分**：_/100（附目标读者定位）
3. **优化建议**：按优先级列出改进建议
4. **优化示例**：选择最需要改进的段落给出修改示例
FORMAT
    ;;

  report)
    cat <<'PROMPT'
## 完整校对报告

请为以下文本生成一份完整的校对报告，涵盖所有检查维度：

### 报告结构

#### 一、概览
- 文本基本信息（字数、段落数、句子数）
- 总体质量评分
- 关键问题摘要

#### 二、错别字与拼写
- 所有拼写错误列表
- 修改建议

#### 三、语法问题
- 语法错误列表
- 修改建议及语法规则说明

#### 四、标点与格式
- 标点错误
- 格式不规范之处

#### 五、文风与一致性
- 文风诊断
- 不一致之处

#### 六、可读性
- 可读性评分
- 优化建议

#### 七、修改后全文
- 所有修改已标注的完整文本

#### 八、总结
- 错误统计汇总
- 整体评价
- 优先改进建议

### 待校对文本
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    echo "### 输出格式"
    echo "按上述报告结构输出完整校对报告，使用 Markdown 格式"
    ;;

  help|--help|-h|"")
    cat <<'HELP'
╔══════════════════════════════════════════════╗
║        🔍 Proofreader — 校对助手             ║
╠══════════════════════════════════════════════╣
║                                              ║
║  Commands:                                   ║
║    check        全面校对（错字+语法+标点）    ║
║    grammar      专注语法纠错                  ║
║    style        文风统一检查                  ║
║    consistency  一致性检查（术语/格式）        ║
║    readability  可读性评分与优化              ║
║    report       生成完整校对报告              ║
║    help         显示此帮助菜单                ║
║                                              ║
║  Usage:                                      ║
║    bash proofread.sh <command> [text]         ║
║                                              ║
║  Examples:                                   ║
║    bash proofread.sh check 检查这段文字       ║
║    bash proofread.sh grammar He don't know    ║
║    bash proofread.sh readability 评估可读性   ║
║                                              ║
╚══════════════════════════════════════════════╝
  Powered by BytesAgain | bytesagain.com | hello@bytesagain.com
HELP
    ;;

  *)
    echo "❌ Unknown command: $CMD"
    echo "Run 'bash proofread.sh help' for usage."
    exit 1
    ;;
esac
