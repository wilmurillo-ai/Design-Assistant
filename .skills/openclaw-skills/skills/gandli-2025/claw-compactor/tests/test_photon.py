# 7. Pillow-unavailable fallback
# ===========================================================================

class TestPhotonPillowFallback(unittest.TestCase):
    """When Pillow is not available, only detail:low is applied."""

    def _apply_without_pillow(self, content: str):
        """Patch PILLOW_AVAILABLE to False and run apply()."""
        stage = PhotonStage()
        import lib.fusion.photon as photon_mod
        original = photon_mod.PILLOW_AVAILABLE
        photon_mod.PILLOW_AVAILABLE = False
        try:
            return stage.apply(_ctx(content))
        finally:
            photon_mod.PILLOW_AVAILABLE = original

    def test_fallback_emits_pillow_warning(self):
        b64 = _make_tiny_jpeg_b64()
        blocks = [_openai_image_block(b64)]
        result = self._apply_without_pillow(json.dumps(blocks))
        pillow_warnings = [w for w in result.warnings if "Pillow" in w]
        self.assertTrue(len(pillow_warnings) >= 1, f"Expected Pillow warning, got: {result.warnings}")

    def test_fallback_still_sets_detail_low(self):
        b64 = _make_tiny_jpeg_b64()
        blocks = [_openai_image_block(b64, detail="high")]
        result = self._apply_without_pillow(json.dumps(blocks))
        parsed = json.loads(result.content)
        self.assertEqual(parsed[0]["image_url"]["detail"], "low")

    def test_fallback_image_data_unchanged(self):
        b64 = _make_tiny_jpeg_b64()
        blocks = [_anthropic_image_block(b64, fmt="jpeg")]
        result = self._apply_without_pillow(json.dumps(blocks))
        parsed = json.loads(result.content)
        # Without Pillow, raw data should be unchanged
        self.assertEqual(parsed[0]["source"]["data"], b64)


