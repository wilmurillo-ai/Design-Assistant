# 1. Token estimation formula
# ===========================================================================

class TestEstimateImageTokens:
    """estimate_image_tokens uses formula: ceil(w/512)*ceil(h/512)*85+170."""

    def test_512x512_exactly_one_tile(self):
        # ceil(512/512) * ceil(512/512) * 85 + 170 = 1*1*85+170 = 255
        assert estimate_image_tokens(512, 512) == 255

    def test_1024x1024_four_tiles(self):
        # ceil(1024/512) * ceil(1024/512) * 85 + 170 = 2*2*85+170 = 510
        assert estimate_image_tokens(1024, 1024) == 510

    def test_small_image_minimum(self):
        # ceil(100/512)=1, ceil(100/512)=1 → 1*1*85+170 = 255
        assert estimate_image_tokens(100, 100) == 255

    def test_wide_image(self):
        # 2048 wide, 512 tall → ceil(2048/512)=4, ceil(512/512)=1 → 4*85+170=510
        assert estimate_image_tokens(2048, 512) == 510

    def test_odd_dimensions_rounds_up(self):
        # 513 wide → ceil(513/512) = 2
        assert estimate_image_tokens(513, 512) == 2 * 1 * 85 + 170


