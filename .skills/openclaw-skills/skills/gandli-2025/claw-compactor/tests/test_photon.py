            b"\x13\r\x12\x11\x0f\x12\x11\x10\x10\x18\x16\x10\x16\x10\x1d\x18\x1c\x18"
            b"\xff\xc0\x00\x0b\x08\x00\x01\x00\x01\x01\x01\x11\x00"
