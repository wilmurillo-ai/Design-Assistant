def _make_tiny_png_b64() -> str:
    """Return a minimal base64-encoded 4×4 red PNG generated by Pillow.

    Returns an empty string if Pillow is unavailable; callers that depend on
    this should skip with ``@unittest.skipUnless(PILLOW_AVAILABLE, ...)``.
    """
    try:
        from PIL import Image
        img = Image.new("RGB", (4, 4), color=(255, 0, 0))
        buf = io.BytesIO()
        img.save(buf, format="PNG")
        return base64.b64encode(buf.getvalue()).decode("ascii")
    except ImportError:
        return ""


def _openai_image_block(b64: str, fmt: str = "jpeg", detail: str = "auto") -> dict:
    return {
        "type": "image_url",
        "image_url": {
            "url": f"data:image/{fmt};base64,{b64}",
            "detail": detail,
        },
    }


def _anthropic_image_block(b64: str, fmt: str = "jpeg") -> dict:
    return {
        "type": "image",
        "source": {
            "type": "base64",
            "media_type": f"image/{fmt}",
            "data": b64,
        },
    }


def _google_image_part(b64: str, fmt: str = "jpeg") -> dict:
    return {
        "inlineData": {
            "mimeType": f"image/{fmt}",
            "data": b64,
        }
    }


def _ctx(content: str) -> FusionContext:
    return FusionContext(content=content)


