class TestPhotonOpenAIDetailLow(unittest.TestCase):
    def setUp(self):
        self.stage = PhotonStage()

    def test_sets_detail_low_for_auto(self):
        b64 = _make_tiny_jpeg_b64()
        blocks = [_openai_image_block(b64, detail="auto")]
        content = json.dumps(blocks)
        result = self.stage.apply(_ctx(content))
        parsed = json.loads(result.content)
        self.assertEqual(parsed[0]["image_url"]["detail"], "low")

    def test_sets_detail_low_for_high(self):
        b64 = _make_tiny_jpeg_b64()
        blocks = [_openai_image_block(b64, detail="high")]
        content = json.dumps(blocks)
        result = self.stage.apply(_ctx(content))
        parsed = json.loads(result.content)
        self.assertEqual(parsed[0]["image_url"]["detail"], "low")

    def test_external_url_still_gets_detail_low(self):
        block = {
            "type": "image_url",
            "image_url": {
                "url": "https://example.com/image.jpg",
                "detail": "high",
            },
        }
        content = json.dumps([block])
        result = self.stage.apply(_ctx(content))
        parsed = json.loads(result.content)
        self.assertEqual(parsed[0]["image_url"]["detail"], "low")

    def test_marker_emitted_for_openai_image(self):
        b64 = _make_tiny_jpeg_b64()
        blocks = [_openai_image_block(b64)]
        content = json.dumps(blocks)
        result = self.stage.apply(_ctx(content))
        self.assertTrue(
            any("photon:openai" in m for m in result.markers),
            f"Expected photon:openai marker, got: {result.markers}",
        )


