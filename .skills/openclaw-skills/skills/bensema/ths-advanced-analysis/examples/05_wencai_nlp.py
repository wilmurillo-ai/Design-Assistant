# ══════════════════════════════════════════════════════════════
# 场景5：融合 wencai_nlp + klines 做深度分析
# ══════════════════════════════════════════════════════════════

with THS() as ths:

    # Step 1：问财选出候选股
    resp = ths.wencai_nlp("连续3日主力净流入，换手率大于5%，涨幅大于2%，非ST，市值大于30亿")
    candidates = resp.df
    print(f"\n=== 候选股 {len(candidates)} 只 ===")

    # Step 2：从候选股中提取 ths_code
    # wencai_nlp 返回格式：'300033.SZ' / '600519.SH' / '835305.BJ'
    def to_ths_code(code_str):
        code_str = str(code_str)
        if '.' in code_str:
            num, market = code_str.split('.')
            mapping = {'SH': 'USHA', 'SZ': 'USZA', 'BJ': 'USTM'}
            prefix = mapping.get(market.upper())
            return f"{prefix}{num}" if prefix else None
        num = code_str.zfill(6)
        if num.startswith('6'):        return f"USHA{num}"
        if num.startswith(('0', '3')): return f"USZA{num}"
        if num.startswith('8'):        return f"USTM{num}"
        return None

    codes = []
    for _, row in candidates.iterrows():
        raw_code = str(row.get('股票代码') or row.get('代码') or '')
        if not raw_code:
            continue
        ths_code = to_ths_code(raw_code)
        if ths_code:
            codes.append({'name': row.get('股票简称', raw_code), 'code': ths_code})

    # Step 3：拉 K 线做技术验证（取前10只）
    qualified = []
    for s in codes[:10]:
        resp = ths.klines(s['code'], interval="day", count=20, adjust="forward")
        if not resp or resp.df.empty:
            continue
        df = resp.df
        df['ma5']  = df['收盘价'].rolling(5).mean()
        df['ma10'] = df['收盘价'].rolling(10).mean()
        df['ma20'] = df['收盘价'].rolling(20).mean()
        latest = df.iloc[-1]

        # 验证：收盘在5日均线上方，且5日>10日>20日（均线多头）
        if (latest['收盘价'] > latest['ma5'] > latest['ma10'] > latest['ma20']):
            qualified.append({
                'name': s['name'],
                'code': s['code'],
                '最新价': latest['收盘价'],
                '5日涨幅': f"{(latest['收盘价']/df['收盘价'].iloc[0]-1)*100:.1f}%",
            })

    print(f"\n=== 技术验证后剩余 {len(qualified)} 只 ===")
    for q in qualified:
        print(q)


