#!/usr/bin/env python3
"""
投标方案生成脚本
输入：招标分析结果 JSON、项目信息
输出：完整技术方案 Markdown 文本
"""

import json
import sys

def generate_proposal(bid_info, industry="智能化"):
    """
    根据招标信息生成技术方案初稿
    bid_info: 招标分析结果字典
    industry: 行业类型（智能化/消防/机电/装修/市政）
    """
    # 这里应调用 LLM 生成完整方案，此处返回占位符
    proposal = f"""# {bid_info.get('项目信息', {}).get('项目名称', 'XXX项目')} 施工组织设计

## 一、工程概况

### 1.1 项目基本信息
- 建设单位：{bid_info.get('项目信息', {}).get('招标人', '（待补充）')}
- 工程地点：{bid_info.get('项目信息', {}).get('工程地点', '（待补充）')}
- 合同工期：{bid_info.get('关键时间', {}).get('工期要求', '（待补充）')}
- 质量目标：符合招标文件要求（合格）

### 1.2 工程主要内容
本工程为【{industry}】工程，主要施工内容包括：
（此处应根据招标文件技术要求详细列出）

### 1.3 施工现场条件
（根据项目实际情况填写）

### 1.4 工程重点难点分析
（根据招标文件和技术要求分析）

## 二、施工部署

### 2.1 项目管理目标
- 质量目标：合格率100%，一次性通过验收
- 安全目标：零重大事故
- 工期目标：按期或提前完成
- 文明施工目标：符合当地要求

### 2.2 施工组织架构
（见附件项目组织架构图）

### 2.3 施工总体流程
（根据专业特点编排施工顺序）

## 三、主要施工方案

### 3.1 [核心分项工程]
施工流程、工艺标准、质量控制

### 3.2 质量保证措施
材料检验、隐蔽验收、分项验收流程

### 3.3 安全技术措施
危险源识别、防护措施、应急预案

## 四、资源投入计划

### 4.1 劳动力计划表
| 月份 | 工种 | 人数 | 备注 |
|-----|------|-----|------|
| 第1月 | XXX | XX | 进场准备 |

### 4.2 机械设备计划
主要设备清单（设备名称、型号、数量）

### 4.3 主要材料供应计划
关键材料（线缆、管道、设备）进场时间

## 五、施工进度计划

### 5.1 工期目标分解
总工期 XXX 日历天，计划：
- 准备阶段：X天
- 主体施工：X天
- 调试验收：X天

### 5.2 关键节点控制
- 开工日期：YYYY-MM-DD
- 完成日期：YYYY-MM-DD

## 六、售后服务与培训
（智能化/机电等专业需要）
- 质保期：X年
- 响应时间：X小时内
- 培训计划：X次，覆盖XX人次

---
注：本方案为AI生成的初稿，需根据实际招标文件和工程情况细化完善。
"""
    return proposal

def main():
    if len(sys.argv) < 2:
        print("Usage: generate_proposal.py <bid_info.json> [industry]")
        sys.exit(1)
    
    with open(sys.argv[1], 'r') as f:
        bid_info = json.load(f)
    
    industry = sys.argv[2] if len(sys.argv) > 2 else "智能化"
    
    proposal = generate_proposal(bid_info, industry)
    print(proposal)

if __name__ == "__main__":
    main()
