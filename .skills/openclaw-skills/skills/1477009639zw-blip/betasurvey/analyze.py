#!/usr/bin/env python3
"""Survey Response Analyzer"""
import argparse, csv, sys

SENTIMENT_KEYWORDS = {
    'positive': ['great','amazing','excellent','love','best','good','fast','awesome','perfect'],
    'negative': ['bad','slow','terrible','worst','poor','expensive','disappointed','awful']
}

def analyze(text):
    text = text.lower()
    pos = sum(1 for w in SENTIMENT_KEYWORDS['positive'] if w in text)
    neg = sum(1 for w in SENTIMENT_KEYWORDS['negative'] if w in text)
    if pos > neg: return 'positive'
    if neg > pos: return 'negative'
    return 'neutral'

def main():
    p = argparse.ArgumentParser()
    p.add_argument('--input', required=True)
    p.add_argument('--output', default='report.md')
    args = p.parse_args()

    rows = []
    with open(args.input) as f:
        reader = csv.DictReader(f)
        for row in reader:
            rows.append(row)

    sentiments = {'positive':0,'negative':0,'neutral':0}
    themes = {}
    for r in rows:
        sent = analyze(r.get('response',''))
        sentiments[sent] += 1

    n = len(rows) or 1
    report = f"""# Survey Analysis Report

Generated by Beta AI Survey Analyzer

## Overview
- Total Responses: {n}
- Positive: {sentiments['positive']} ({sentiments['positive']/n*100:.0f}%)
- Neutral: {sentiments['neutral']} ({sentiments['neutral']/n*100:.0f}%)
- Negative: {sentiments['negative']} ({sentiments['negative']/n*100:.0f}%)

## Sample Responses
"""
    for r in rows[:5]:
        report += f"- \"{r.get('response','')[:80]}\"\n"

    report += "\n## Recommendations\n"
    report += f"- {(sentiments['negative']/n*100):.0f}% negative feedback - investigate and address\n"
    report += f"- {(sentiments['positive']/n*100):.0f}% positive - identify what's working\n"

    with open(args.output, 'w') as f:
        f.write(report)
    print(f"Report saved → {args.output}")

if __name__ == '__main__':
    main()
