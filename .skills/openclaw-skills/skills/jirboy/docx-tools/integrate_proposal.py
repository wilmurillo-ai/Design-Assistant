#!/usr/bin/env python3
"""
整合基金申请书各部分内容为一个完整的Word文档
"""
import sys
from docx import Document
from docx.shared import Pt, Inches, RGBColor
from docx.enum.text import WD_ALIGN_PARAGRAPH

def create_integrated_proposal(output_path):
    """创建整合后的基金申请书立项依据部分"""
    doc = Document()
    
    # 设置默认字体
    style = doc.styles['Normal']
    style.font.name = '宋体'
    style.font.size = Pt(12)
    
    # 标题
    title = doc.add_heading('一、立项依据', 0)
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER
    
    # （一）研究意义
    doc.add_heading('（一）研究意义', 1)
    
    doc.add_heading('1. 研究背景与价值', 2)
    doc.add_paragraph(
        '结构抗震试验是保障工程结构地震安全性的关键手段。随着我国城镇化进程加速和"韧性城市"建设的推进，'
        '对结构抗震性能的精确评估需求日益迫切。振动台试验作为最直观、最可靠的抗震试验方法，'
        '能够真实再现结构在地震作用下的动力响应，为结构抗震设计提供直接依据。'
    )
    doc.add_paragraph(
        '然而，大型结构振动台试验面临试件规模受限与试验成本高昂的双重困境。'
        '一方面，地震模拟振动台的承载能力和台面尺寸有限，难以容纳超大型结构整体；'
        '另一方面，单次振动台试验成本约15-30万元，全参数扫描需要200+组试验，在经济上不可行。'
        '振动台子结构试验（Shake Table Sub-Structure Testing）技术应运而生，'
        '通过将结构划分为数值子结构和试验子结构，仅对关键构件进行物理试验，其余部分采用数值仿真，'
        '在保证试验可靠性的同时显著降低成本。'
    )
    
    doc.add_heading('2. 核心科学问题的紧迫性', 2)
    
    doc.add_heading('（1）小样本困境制约智能化发展', 3)
    doc.add_paragraph(
        '人工智能方法在结构控制中展现出巨大潜力，但深度神经网络通常需要大量标注数据进行训练。'
        '以本团队前期研究为例，基于LSTM的振动台子结构试验建模方法需要上千组历史试验数据才能达到满意精度。'
        '考虑到振动台试验的高成本特性（单次试验15-30万元），如此庞大的数据需求在实际工程中难以满足，'
        '形成"高成本试验"与"大数据需求"之间的结构性矛盾。'
    )
    
    doc.add_heading('（2）固定模型难以反映结构演化', 3)
    doc.add_paragraph(
        '现有振动台子结构试验通常采用固定数值模型进行实时计算，未考虑试验过程中结构参数的时变特性。'
        '然而，在强烈地震作用下，结构构件不可避免地进入非线性阶段，刚度退化、阻尼增大、损伤累积，'
        '固定模型难以准确反映这一演化过程，导致数值-物理界面失配，影响试验结果的可靠性。'
    )
    
    doc.add_heading('（3）安全性与效率的权衡难题', 3)
    doc.add_paragraph(
        '传统控制方法虽能保证安全性，但难以适应结构参数的不确定性变化；'
        '强化学习方法虽具备自适应能力，但试错探索过程可能导致试件损坏或设备故障。'
        '如何在样本受限条件下实现安全、高效的自适应控制，是振动台子结构试验智能化发展必须突破的瓶颈。'
    )
    
    doc.add_heading('3. 本项目的研究价值', 2)
    
    doc.add_heading('（1）理论价值', 3)
    doc.add_paragraph(
        '本项目将物理信息神经网络、贝叶斯不确定性量化、强化学习等前沿方法有机融合，'
        '建立面向振动台子结构试验的小样本模型更新控制理论体系。'
        '通过将结构动力学方程、材料本构关系等物理约束嵌入数据驱动模型，突破传统方法对大量数据的依赖；'
        '通过双时间尺度架构实现模型辨识与控制的协同优化；通过物理信息强化学习实现安全高效的自适应控制。'
        '这一研究将丰富和发展智能结构试验的理论基础。'
    )
    
    doc.add_heading('（2）方法价值', 3)
    doc.add_paragraph(
        '本项目发展的R-PINN建模方法可在50-100组样本条件下达到传统方法1000组样本的精度水平，'
        '显著降低数据需求；双时间尺度架构在不影响实时控制的前提下实现模型在线更新；'
        '物理信息强化学习在保障安全性的同时提高样本效率10-100倍。'
        '这些方法的突破将直接提升振动台子结构试验的技术水平。'
    )
    
    doc.add_heading('（3）应用价值', 3)
    doc.add_paragraph(
        '本项目研究成果可直接应用于桥梁、建筑、隔震装置等结构的振动台子结构试验。'
        '通过降低样本需求、实现模型在线更新、保障试验安全性，'
        '可显著节约试验成本（预计降低50%以上试验次数）、缩短试验周期、提高试验结果的可靠性，'
        '为结构抗震性能评估提供更加高效、经济的技术手段。'
    )
    
    # 保存
    doc.save(output_path)
    return f"已创建整合版申请书：{output_path}"

if __name__ == "__main__":
    output = sys.argv[1] if len(sys.argv) > 1 else "立项依据_整合版.docx"
    print(create_integrated_proposal(output))
