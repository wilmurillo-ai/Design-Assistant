# -*- coding: utf-8 -*-
import sys
import datetime

def today():
    return datetime.date.today().strftime("%Y年%m月%d日")

def weekday_cn():
    days = ["一","二","三","四","五","六","日"]
    return "星期" + days[datetime.date.today().weekday()]

