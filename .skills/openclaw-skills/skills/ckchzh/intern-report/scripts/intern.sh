def reflect(experience):
    items = [e.strip() for e in experience.split(",") if e.strip()]
    if len(items) <= 1:
        items = [experience.strip()]
    exp_list = "\n".join(["    {}. {}".format(i+1, item) for i, item in enumerate(items)])

    # 可迁移能力映射
    skill_map = {
        "开发": ["编程能力", "逻辑思维", "问题拆解", "技术学习能力", "代码审查"],
        "设计": ["审美能力", "用户思维", "创意表达", "视觉传达", "设计工具"],
        "测试": ["细致严谨", "质量意识", "测试思维", "文档能力", "异常处理"],
        "产品": ["需求分析", "用户研究", "项目推进", "跨部门沟通", "数据分析"],
        "运营": ["数据分析", "内容创作", "用户理解", "活动策划", "渠道管理"],
        "销售": ["客户沟通", "谈判能力", "抗压能力", "目标导向", "人际关系"],
        "行政": ["组织协调", "多任务处理", "细节管理", "服务意识", "流程优化"],
    }

    # 尝试从经历中提取相关技能
    matched_skills = []
    for key, skills in skill_map.items():
        for item in items:
            if key in item:
                matched_skills.extend(skills)
                break

    if not matched_skills:
        matched_skills = ["沟通表达", "团队协作", "时间管理", "学习能力", "问题解决", "适应能力"]

    skills_display = "\n".join(["    {}. {}".format(i+1, s) for i, s in enumerate(matched_skills[:6])])

    print("""
