## 💵 经济补偿金计算

### 经济补偿 (N) vs 赔偿金 (2N)
| 类型 | 金额 | 适用情形 |
|------|------|----------|
| 经济补偿 (N) | 工作年限×月工资 | 合法解除/不续签 |
| 代通知金 (+1) | 1个月工资 | 未提前30天通知 |
| N+1 | N个月+1个月 | 合法解除但未提前通知 |
| 赔偿金 (2N) | 2×N | 违法解除 |

### 补偿金计算方法
```
经济补偿 = 工作年限 × 月平均工资

工作年限计算：
- 每满1年 → 算1个月
- 6个月以上不满1年 → 算1个月
- 不满6个月 → 算0.5个月

月平均工资：
- 离职前12个月的平均工资
- 包含奖金、补贴、加班费等
- 上限：当地社平工资3倍 (超过按3倍算，且最多12年)
```

### 计算示例
```
情景：工作5年3个月，月薪2万，公司提出协商解除

工作年限：5年3个月 → 5.5个月
月平均工资：¥20,000

经济补偿 = 5.5 × ¥20,000 = ¥110,000

如未提前30天通知 (N+1):
补偿总额 = ¥110,000 + ¥20,000 = ¥130,000

如属违法解除 (2N):
赔偿金 = 2 × ¥110,000 = ¥220,000
```

