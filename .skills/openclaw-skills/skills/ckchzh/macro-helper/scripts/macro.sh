    result = result + ws.Cells(i, 1).Value
Next i

' ✅ FAST: 1 read + memory processing
Dim data As Variant
data = ws.Range("A1:A10000").Value
For i = 1 To 10000
    result = result + data(i, 1)
Next i
\`\`\`

### 3. Avoid Select/Activate ⭐⭐⭐
\`\`\`vba
' ❌ SLOW
Sheets("Data").Select
Range("A1").Select
Selection.Value = "Hello"

' ✅ FAST
Sheets("Data").Range("A1").Value = "Hello"
\`\`\`

### 4. Use With Blocks ⭐⭐
\`\`\`vba
With ws.Range("A1:D1")
    .Font.Bold = True
    .Font.Size = 12
    .Interior.Color = RGB(0, 120, 215)
