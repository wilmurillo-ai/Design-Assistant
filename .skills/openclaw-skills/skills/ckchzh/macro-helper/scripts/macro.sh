End With
\`\`\`

### 5. Declare Specific Types ⭐⭐
\`\`\`vba
' ❌ Variant is 2-10x slower
Dim x As Variant

' ✅ Use specific type
Dim x As Long
\`\`\`

### 6. Use Long Instead of Integer ⭐
\`\`\`vba
' ❌ Integer is actually slower on 32/64-bit systems
