def risk(brand):
    name_len = len(brand)
    has_common = False
    common_words = ["中国","国际","环球","天下","第一","最好","金","银","龙","凤",
                    "华","国","盛","大","新","美","好","优","佳"]
    found_common = [w for w in common_words if w in brand]

    # 简单风险评估逻辑
    risk_level = "低"
    risk_score = 0
    risks = []

    if name_len <= 2:
        risk_score += 3
        risks.append("名称过短（{}个字），短名称被注册的概率极高".format(name_len))
    elif name_len == 3:
        risk_score += 1
        risks.append("3字名称较常见，需仔细排查近似")

    if found_common:
        risk_score += 2
        risks.append("包含常用字词「{}」，近似概率较高".format("、".join(found_common)))

    # 检查是否纯通用词
    generic_words = ["科技","网络","信息","电子","数码","智能","云","大数据","互联网"]
    found_generic = [w for w in generic_words if w in brand]
    if found_generic:
        risk_score += 2
        risks.append("包含行业通用词「{}」，显著性较弱".format("、".join(found_generic)))

    if risk_score >= 4:
        risk_level = "高"
        risk_emoji = "🔴"
    elif risk_score >= 2:
        risk_level = "中"
        risk_emoji = "🟡"
    else:
        risk_level = "低"
        risk_emoji = "🟢"

    if not risks:
        risks.append("名称具有一定独创性，初步判断近似风险较低")

    risk_list = "\n".join(["  {}. {}".format(i+1, r) for i, r in enumerate(risks)])

    print("""
================================================================================
              近似风险评估 — "{brand}"
================================================================================
日期：{date}

━━━ {emoji} 风险等级：{level} ━━━

  品牌名称：{brand}
  字符数：{length}
  风险评分：{score}/10

━━━ 📋 风险因素分析 ━━━

{risk_list}

━━━ 🔍 近似判定规则（商标局标准）━━━

  文字商标近似判定：
    1. 中文商标的汉字构成相同，仅字体/排列不同 → 近似
    2. 3字商标中2字相同且无显著区别 → 近似
    3. 含义相同或近似（如"皇冠"vs"王冠"）→ 近似
    4. 仅加减不影响含义的字（如"老""新""小"）→ 近似
    5. 读音相同或近似且无法区分 → 近似

  ⚠️ 以上为简化规则，实际判定更复杂

━━━ 💡 降低风险建议 ━━━

  1. 创造独创性强的名称
     - 臆造词（如"柯达""海尔"）→ 通过率最高
     - 任意词（把无关词用于某行业）→ 通过率较高
     - 暗示性词（暗示产品特点但不直接描述）→ 通过率中等

  2. 命名技巧
     - 组合两个不相关的字/词
     - 使用谐音创造新词
     - 加入独特的修饰字
     - 避免行业通用词

  3. 排查方法
     - 中国商标网查询：https://sbj.cnipa.gov.cn
     - 委托代理机构做专业检索
     - 查询同音、近形、近义商标

━━━ ✅ 建议操作 ━━━

  {suggest_header}
  1. 立即在中国商标网做全面检索
  2. 委托专业商标代理进行近似分析
  3. {suggest_backup}
  4. 确认无近似后尽快提交申请

================================================================================
⚠️ 本评估为初步AI分析，仅供参考，不构成法律意见
   正式注册前请务必在中国商标网做全面检索
================================================================================
""".format(
        brand=brand,
        date=today(),
        emoji=risk_emoji,
        level=risk_level,
        length=name_len,
        score=min(risk_score, 10),
        risk_list=risk_list,
        suggest_header="⚠️ 风险较高，强烈建议：" if risk_score >= 4 else "建议：",
        suggest_backup="准备2-3个备选名称" if risk_score >= 3 else "可同步准备1个备选名称"
    ))

