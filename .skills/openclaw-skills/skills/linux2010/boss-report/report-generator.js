#!/usr/bin/env node
/**
 * Boss Report — HTML Report Generator
 * Generates professional HTML work reports from markdown data
 */

const fs = require('fs');
const path = require('path');

function generateHTMLReport(reportData, outputPath) {
  const { title, date, type, sections, metrics } = reportData;

  const metricsHTML = metrics
    ? `<div style="margin: 24px 0;">
        ${metrics.map(m => `
          <div class="metric">
            <div class="number">${m.value}</div>
            <div class="label">${m.label}</div>
          </div>
        `).join('')}
      </div>`
    : '';

  const sectionsHTML = sections
    ? sections.map(s => `
        <h2>${s.title}</h2>
        ${s.content}
      `).join('')
    : '';

  const html = `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>${title} — ${date}</title>
  <style>
    * { box-sizing: border-box; margin: 0; padding: 0; }
    body {
      font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', Roboto, 'Helvetica Neue', Arial, sans-serif;
      background: linear-gradient(135deg, #667eea 0%, #764ba2 100%);
      min-height: 100vh;
      padding: 40px 20px;
    }
    .container {
      max-width: 900px;
      margin: 0 auto;
      background: white;
      border-radius: 16px;
      padding: 48px;
      box-shadow: 0 10px 40px rgba(0,0,0,0.15);
    }
    .header {
      text-align: center;
      margin-bottom: 40px;
      padding-bottom: 24px;
      border-bottom: 3px solid #667eea;
    }
    .header h1 {
      font-size: 2em;
      color: #1a1a2e;
      margin-bottom: 8px;
    }
    .header .date {
      color: #666;
      font-size: 1.1em;
    }
    .header .badge {
      display: inline-block;
      background: #667eea;
      color: white;
      padding: 4px 12px;
      border-radius: 20px;
      font-size: 0.85em;
      margin-top: 8px;
    }
    h2 {
      color: #16213e;
      margin: 32px 0 16px;
      padding-bottom: 8px;
      border-bottom: 2px solid #eee;
    }
    h2::before {
      content: '▸ ';
      color: #667eea;
    }
    p, li {
      color: #333;
      line-height: 1.7;
      margin: 8px 0;
    }
    ul { padding-left: 24px; }
    li { margin: 6px 0; }
    table {
      width: 100%;
      border-collapse: collapse;
      margin: 16px 0;
      font-size: 0.95em;
    }
    th, td {
      padding: 12px 16px;
      text-align: left;
      border-bottom: 1px solid #eee;
    }
    th {
      background: #f8f9fa;
      font-weight: 600;
      color: #16213e;
    }
    tr:hover { background: #f8f9fa; }
    .metric {
      display: inline-block;
      background: linear-gradient(135deg, #e8f4fd 0%, #f0e6ff 100%);
      padding: 16px 24px;
      border-radius: 12px;
      margin: 8px;
      text-align: center;
      min-width: 120px;
    }
    .metric .number {
      font-size: 2.2em;
      font-weight: bold;
      color: #667eea;
    }
    .metric .label {
      font-size: 0.85em;
      color: #666;
      margin-top: 4px;
    }
    .status-green { color: #28a745; font-weight: 600; }
    .status-yellow { color: #ffc107; font-weight: 600; }
    .status-red { color: #dc3545; font-weight: 600; }
    .footer {
      margin-top: 48px;
      padding-top: 24px;
      border-top: 2px solid #eee;
      text-align: center;
      color: #999;
      font-size: 0.9em;
    }
    .footer .emoji { font-size: 1.5em; }
    @media print {
      body { background: white; padding: 0; }
      .container { box-shadow: none; padding: 24px; }
    }
    @media (max-width: 600px) {
      .container { padding: 24px; }
      .header h1 { font-size: 1.5em; }
      .metric { min-width: 100px; padding: 12px 16px; }
      .metric .number { font-size: 1.8em; }
    }
  </style>
</head>
<body>
  <div class="container">
    <div class="header">
      <h1>${title}</h1>
      <div class="date">${date}</div>
      <div class="badge">${type} Report</div>
    </div>

    ${metricsHTML}

    ${sectionsHTML}

    <div class="footer">
      <div class="emoji">🦞</div>
      <p>Boss, every task has a response.</p>
      <p>Generated by OpenClaw AI Employee</p>
    </div>
  </div>
</body>
</html>`;

  fs.writeFileSync(outputPath, html, 'utf-8');
  console.log(`Report saved to: ${outputPath}`);
  return outputPath;
}

// CLI usage
if (require.main === module) {
  const args = process.argv.slice(2);
  if (args.length < 2) {
    console.log('Usage: node report-generator.js <input.json> <output.html>');
    process.exit(1);
  }
  const data = JSON.parse(fs.readFileSync(args[0], 'utf-8'));
  generateHTMLReport(data, args[1]);
}

module.exports = { generateHTMLReport };
