#!/usr/bin/env python3
"""
Автоматическая проверка качества дизайна.

Анализирует PNG-изображение и выдаёт оценку 1-10 с рекомендациями.
Проверяет: контраст, баланс цветов, сложность, пустое пространство.

Использование:
    python3 check_design.py путь/к/изображению.png
    python3 check_design.py баннер.png --verbose
"""

import sys
import math
import argparse
from collections import Counter
from PIL import Image


def относительная_яркость(r, g, b):
    """Вычисляет относительную яркость по формуле WCAG 2.0."""

    def линеаризация(c):
        c = c / 255.0
        return c / 12.92 if c <= 0.03928 else ((c + 0.055) / 1.055) ** 2.4

    return 0.2126 * линеаризация(r) + 0.7152 * линеаризация(g) + 0.0722 * линеаризация(b)


def коэффициент_контраста(цвет1, цвет2):
    """Вычисляет коэффициент контраста между двумя цветами (WCAG)."""
    l1 = относительная_яркость(*цвет1)
    l2 = относительная_яркость(*цвет2)
    lighter = max(l1, l2)
    darker = min(l1, l2)
    return (lighter + 0.05) / (darker + 0.05)


def анализ_цветов(image, sample_size=10000):
    """Анализирует цветовое распределение изображения."""
    img = image.convert("RGB")
    # Уменьшаем для скорости
    ratio = max(1, int(math.sqrt(img.width * img.height / sample_size)))
    small = img.resize((img.width // ratio, img.height // ratio), Image.NEAREST)

    pixels = list(small.getdata())
    total = len(pixels)

    # Квантизация до 32 уровней для группировки
    quantized = []
    for r, g, b in pixels:
        quantized.append((r // 8 * 8, g // 8 * 8, b // 8 * 8))

    counter = Counter(quantized)
    уникальных_цветов = len(counter)

    # Топ-20 цветов (для надёжного определения контраста текста)
    топ_цвета = counter.most_common(20)
    доли_топ = [(c, count / total) for c, count in топ_цвета]

    # Доминантный цвет (фон)
    фон = топ_цвета[0][0] if топ_цвета else (255, 255, 255)
    доля_фона = топ_цвета[0][1] / total if топ_цвета else 0

    # Средняя яркость
    яркости = [относительная_яркость(r, g, b) for r, g, b in pixels]
    средняя_яркость = sum(яркости) / len(яркости)

    return {
        "уникальных_цветов": уникальных_цветов,
        "топ_цвета": доли_топ,
        "фон": фон,
        "доля_фона": доля_фона,
        "средняя_яркость": средняя_яркость,
    }


def анализ_пустого_пространства(image, порог=0.05):
    """Оценивает долю пустого пространства (однородных областей)."""
    img = image.convert("RGB")
    small = img.resize((100, 100), Image.LANCZOS)
    pixels = list(small.getdata())

    # Определяем фоновый цвет (самый частый)
    counter = Counter([(r // 16 * 16, g // 16 * 16, b // 16 * 16) for r, g, b in pixels])
    фон = counter.most_common(1)[0][0]

    # Считаем пиксели близкие к фону
    близких = 0
    for r, g, b in pixels:
        dr = abs(r - фон[0])
        dg = abs(g - фон[1])
        db = abs(b - фон[2])
        if (dr + dg + db) / 3 < 30:
            близких += 1

    return близких / len(pixels)


def анализ_сложности(image):
    """Оценивает визуальную сложность через вариацию яркости соседних пикселей."""
    img = image.convert("L")  # Градации серого
    small = img.resize((200, 200), Image.LANCZOS)
    pixels = list(small.getdata())
    w, h = small.size

    различия = []
    for y in range(h - 1):
        for x in range(w - 1):
            i = y * w + x
            dx = abs(pixels[i] - pixels[i + 1])
            dy = abs(pixels[i] - pixels[i + w])
            различия.append(dx + dy)

    средняя_разница = sum(различия) / len(различия) if различия else 0
    return средняя_разница / 255.0  # Нормализуем 0-1


def анализ_баланса(image):
    """Проверяет визуальный баланс (распределение яркости по квадрантам)."""
    img = image.convert("L")
    w, h = img.size
    mid_x, mid_y = w // 2, h // 2

    квадранты = []
    for (x1, y1, x2, y2) in [
        (0, 0, mid_x, mid_y),
        (mid_x, 0, w, mid_y),
        (0, mid_y, mid_x, h),
        (mid_x, mid_y, w, h),
    ]:
        crop = img.crop((x1, y1, x2, y2))
        pixels = list(crop.getdata())
        средняя = sum(pixels) / len(pixels) if pixels else 128
        квадранты.append(средняя)

    # Разброс яркости между квадрантами
    средний = sum(квадранты) / 4
    отклонение = sum(abs(q - средний) for q in квадранты) / 4
    return отклонение / 255.0  # Нормализуем 0-1


def проверить_дизайн(путь, verbose=False):
    """Основная функция проверки."""
    try:
        img = Image.open(путь)
    except Exception as e:
        print(f"Ошибка: не удалось открыть {путь}: {e}")
        sys.exit(1)

    print(f"Файл: {путь}")
    print(f"Размер: {img.width}×{img.height} px")
    print(f"Формат: {img.format or 'N/A'}")
    print("-" * 50)

    оценки = []
    рекомендации = []

    # 1. Анализ цветов
    цвета = анализ_цветов(img)

    # Оценка количества цветов (5-15 квантизированных = хорошо)
    n_цветов = цвета["уникальных_цветов"]
    if n_цветов < 3:
        оценка_цветов = 5
        рекомендации.append("Слишком мало цветов — дизайн может выглядеть скудно")
    elif n_цветов <= 20:
        оценка_цветов = 10
    elif n_цветов <= 50:
        оценка_цветов = 8
    elif n_цветов <= 100:
        оценка_цветов = 6
    else:
        оценка_цветов = 4
        рекомендации.append("Слишком много цветов — ограничь палитру до 3-5 основных")
    оценки.append(("Палитра", оценка_цветов))

    if verbose:
        print(f"Уникальных цветовых групп: {n_цветов}")
        print("Топ-5 цветов:")
        for (r, g, b), доля in цвета["топ_цвета"]:
            hex_цвет = f"#{r:02X}{g:02X}{b:02X}"
            print(f"  {hex_цвет} — {доля:.1%}")

    # 2. Контраст фона и предполагаемого текста
    фон = цвета["фон"]
    # Предполагаем текст = самый контрастный цвет из топа
    макс_контраст = 1.0
    текст_цвет = (0, 0, 0)
    for (r, g, b), доля in цвета["топ_цвета"][1:]:
        к = коэффициент_контраста(фон, (r, g, b))
        if к > макс_контраст:
            макс_контраст = к
            текст_цвет = (r, g, b)

    if макс_контраст >= 7.0:
        оценка_контраста = 10
    elif макс_контраст >= 4.5:
        оценка_контраста = 8
    elif макс_контраст >= 3.0:
        оценка_контраста = 5
        рекомендации.append(f"Контраст {макс_контраст:.1f}:1 — ниже WCAG AA (нужно ≥4.5:1)")
    else:
        оценка_контраста = 3
        рекомендации.append(f"Критически низкий контраст {макс_контраст:.1f}:1 — текст будет нечитаем")
    оценки.append(("Контраст", оценка_контраста))

    if verbose:
        фон_hex = f"#{фон[0]:02X}{фон[1]:02X}{фон[2]:02X}"
        текст_hex = f"#{текст_цвет[0]:02X}{текст_цвет[1]:02X}{текст_цвет[2]:02X}"
        print(f"\nФон: {фон_hex}, Текст: {текст_hex}")
        print(f"Контраст: {макс_контраст:.1f}:1")

    # 3. Пустое пространство
    доля_пустого = анализ_пустого_пространства(img)

    if 0.15 <= доля_пустого <= 0.60:
        оценка_воздуха = 10
    elif 0.10 <= доля_пустого <= 0.70:
        оценка_воздуха = 7
    elif доля_пустого < 0.10:
        оценка_воздуха = 4
        рекомендации.append("Мало воздуха — добавь отступы вокруг элементов")
    else:
        оценка_воздуха = 5
        рекомендации.append("Слишком много пустого пространства — дизайн может казаться пустым")
    оценки.append(("Воздух", оценка_воздуха))

    if verbose:
        print(f"\nПустое пространство: {доля_пустого:.1%}")

    # 4. Визуальная сложность
    сложность = анализ_сложности(img)

    if 0.03 <= сложность <= 0.25:
        оценка_сложности = 10
    elif 0.02 <= сложность <= 0.35:
        оценка_сложности = 7
    elif сложность < 0.02:
        оценка_сложности = 5
        рекомендации.append("Дизайн слишком простой — добавь визуальных элементов")
    else:
        оценка_сложности = 4
        рекомендации.append("Слишком визуально загруженный — упрости композицию")
    оценки.append(("Сложность", оценка_сложности))

    if verbose:
        print(f"Визуальная сложность: {сложность:.3f}")

    # 5. Баланс
    дисбаланс = анализ_баланса(img)

    if дисбаланс <= 0.05:
        оценка_баланса = 10
    elif дисбаланс <= 0.10:
        оценка_баланса = 8
    elif дисбаланс <= 0.15:
        оценка_баланса = 6
    else:
        оценка_баланса = 4
        рекомендации.append("Сильный дисбаланс — перераспредели визуальный вес элементов")
    оценки.append(("Баланс", оценка_баланса))

    if verbose:
        print(f"Дисбаланс: {дисбаланс:.3f}")

    # 6. Размер (соответствие стандартным форматам)
    стандартные = [
        (1584, 396), (2560, 1440), (1500, 500), (1280, 769),
        (920, 520), (1080, 1080), (1280, 720), (1920, 600),
        (1024, 1024), (512, 512), (800, 800), (1920, 1080),
    ]
    подходит = any(
        abs(img.width - w) < 10 and abs(img.height - h) < 10
        for w, h in стандартные
    )
    if подходит:
        оценка_размера = 10
    elif img.width >= 800 and img.height >= 400:
        оценка_размера = 8
    elif img.width >= 400 and img.height >= 200:
        оценка_размера = 6
        рекомендации.append("Размер маловат — рекомендуется минимум 800×400 для баннеров")
    else:
        оценка_размера = 4
        рекомендации.append("Слишком маленький размер — увеличь разрешение")
    оценки.append(("Размер", оценка_размера))

    # Итоговая оценка
    print("\n" + "=" * 50)
    print("ОЦЕНКА ДИЗАЙНА")
    print("=" * 50)

    итого = 0
    for название, оценка in оценки:
        бар = "█" * оценка + "░" * (10 - оценка)
        print(f"  {название:12s} [{бар}] {оценка}/10")
        итого += оценка

    финал = round(итого / len(оценки), 1)
    print("-" * 50)

    уровень = "ОТЛИЧНО" if финал >= 8 else "ХОРОШО" if финал >= 6 else "НУЖНА ДОРАБОТКА" if финал >= 4 else "ПЕРЕДЕЛАТЬ"
    print(f"  {'ИТОГО':12s}                {финал}/10 — {уровень}")

    if рекомендации:
        print("\nРЕКОМЕНДАЦИИ:")
        for i, р in enumerate(рекомендации, 1):
            print(f"  {i}. {р}")
    else:
        print("\nРекомендаций нет — дизайн соответствует стандартам!")

    return финал


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Проверка качества дизайна PNG-изображения")
    parser.add_argument("path", help="Путь к PNG-файлу")
    parser.add_argument("--verbose", "-v", action="store_true", help="Подробный вывод")
    args = parser.parse_args()
    проверить_дизайн(args.path, verbose=args.verbose)
