def chinese_to_pinyin(text):
    """将中文转换为拼音"""
    if HAS_PYPINYIN:
        return ''.join(lazy_pinyin(text))
    else:
        # 简单的中文检测和提示
        result = []
        for char in text:
            if '\u4e00' <= char <= '\u9fff':
                print(f"提示: 检测到中文字符 '{char}'，请安装 pypinyin 库以支持中文转拼音: pip install pypinyin", file=sys.stderr)
                result.append('?')
            else:
                result.append(char)
        return ''.join(result)


def generate_dotline(text):
    """生成点线艺术"""
    # 处理中文
    text = chinese_to_pinyin(text)

    # 转换为大写，只保留字母、数字和空格
    text = text.upper()
    text = ''.join(c for c in text if c.isalnum() or c == ' ')

    if not text.strip():
        return "错误: 没有可显示的字符"

    # 获取每个字符的点线表示
    lines = ['' for _ in range(6)]

    for char in text:
        if char in LETTERS:
            char_lines = LETTERS[char]
            for i in range(6):
                lines[i] += char_lines[i]
        else:
            # 未知字符用空格代替
            for i in range(6):
                lines[i] += '    '

    return lines


def print_dotline(text):
    """打印点线艺术"""
    lines = generate_dotline(text)

    if isinstance(lines, str):
        print(lines)
        return

    # 打印空行
    print()

    # 打印每一行
    for line in lines:
        print(line)

    # 打印空行
    print()


def main():
    if len(sys.argv) < 2:
        print("用法: python dotline_art.py <文字>")
        print("示例: python dotline_art.py spring")
        print("      python dotline_art.py 你好")
        sys.exit(1)

    text = ' '.join(sys.argv[1:])
    print_dotline(text)


if __name__ == '__main__':
    main()
