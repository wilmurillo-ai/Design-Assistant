print("=" * 60)
print("测试：长文章多张配图功能")
print("=" * 60)

publisher = WechatPublisher()

print(f"\n文章长度：{len(LONG_ARTICLE)}字")
print(f"预计配图数量：{len(LONG_ARTICLE) // 400} 张正文配图 + 1 张封面图")
print()

article = publisher.create_article(
    title="AI 成长史 - 完整版",
    content=LONG_ARTICLE,
    multi_images=True
)

# 统计图片
import re
img_tags = re.findall(r'<img[^>]*>', article['content'])

print("\n" + "=" * 60)
print("测试结果")
print("=" * 60)
print(f"✅ 文章创建成功")
print(f"🖼️ 图片总数：{len(img_tags)} 张")
print(f"   - 封面图：1 张")
print(f"   - 正文配图：{len(img_tags) - 1} 张")

if len(img_tags) >= 3:
    print(f"\n🎉 多张配图功能完美工作！")
else:
    print(f"\n⚠️ 配图数量偏少，可能需要调整参数")
