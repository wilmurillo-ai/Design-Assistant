/**
 * 视频脚本模板生成器
 */

async function generate(topic) {
  const lines = [
    `# ${topic}`,
    '',
    '## 开场（0-10秒）',
    '',
    `"${topic}，今天带你一次搞懂！"`,
    '',
    '## 第一部分（10-60秒）',
    '',
    '背景介绍，为什么要关注这个话题...',
    '',
    '## 第二部分（60-180秒）',
    '',
    '核心内容展开...',
    '',
    '- 要点一',
    '- 要点二', 
    '- 要点三',
    '',
    '## 总结（最后20秒）',
    '',
    '总结要点 + 引导关注点赞...',
    '',
    '## 画面建议',
    '',
    '- 开场：特写镜头',
    '- 中段：演示画面',
    '- 结尾：真人出镜'
  ];
  
  return lines.join('\n');
}

module.exports = { generate };
