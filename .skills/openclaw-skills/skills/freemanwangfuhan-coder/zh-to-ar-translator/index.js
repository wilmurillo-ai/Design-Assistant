// 翻译函数
function translate(word) {
  if (wordDict[word]) {
    return wordDict[word];
  }
  return word + ' [未翻译]';
}

// 批量翻译
function translateBatch(words) {
  return words.map(w => ({
    chinese: w,
    arabic: translate(w)
  }));
}

// 命令行运行
const args = process.argv.slice(2);
if (args.length === 0) {
  console.error('请输入要翻译的中文词');
  console.error('用法: node index.js "中文词"');
  process.exit(1);
}

const results = translateBatch(args);
results.forEach(r => {
  console.log(`${r.chinese} -> ${r.arabic}`);
});
