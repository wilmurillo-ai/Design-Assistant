#!/usr/bin/env python3
from __future__ import annotations

from pathlib import Path
import sys


def prompt(label: str, default: str = "") -> str:
    suffix = f" [{default}]" if default else ""
    value = input(f"{label}{suffix}: ").strip()
    return value or default


def yes_no(label: str, default: bool = True) -> bool:
    hint = "Y/n" if default else "y/N"
    value = input(f"{label} ({hint}): ").strip().lower()
    if not value:
        return default
    return value in {"y", "yes"}


def yaml_quote(value: str) -> str:
    escaped = value.replace('"', '\\"')
    return f'"{escaped}"'


def main() -> int:
    print("=== Plane Flow Init Wizard ===\n")
    print("This wizard helps you create a local Plane config file.")
    print("It does NOT store your API token in the YAML file. Token stays in env.\n")

    config_path = Path(prompt("Config file path", "~/.plane_config.yaml")).expanduser()
    default_project_alias = prompt("Default project alias", "main")
    project_name = prompt("Actual Plane project name", "Your Project")

    print("\nState alias mapping (press Enter to keep defaults):")
    state_todo = prompt("  todo ->", "Todo")
    state_doing = prompt("  doing ->", "In Progress")
    state_done = prompt("  done ->", "Done")
    state_blocked = prompt("  blocked ->", "Blocked")

    print("\nPriority mapping (press Enter to keep defaults):")
    prio_urgent = prompt("  urgent ->", "urgent")
    prio_high = prompt("  high ->", "high")
    prio_medium = prompt("  medium ->", "medium")
    prio_low = prompt("  low ->", "low")

    content = f"""# Generated by plane-flow init wizard\ndefault_project: {default_project_alias}\n\nprojects:\n  {default_project_alias}: {yaml_quote(project_name)}\n\nstates:\n  todo: {yaml_quote(state_todo)}\n  doing: {yaml_quote(state_doing)}\n  done: {yaml_quote(state_done)}\n  blocked: {yaml_quote(state_blocked)}\n\npriorities:\n  urgent: {yaml_quote(prio_urgent)}\n  high: {yaml_quote(prio_high)}\n  medium: {yaml_quote(prio_medium)}\n  low: {yaml_quote(prio_low)}\n"""

    config_path.parent.mkdir(parents=True, exist_ok=True)
    if config_path.exists() and not yes_no(f"Overwrite existing file at {config_path}?", default=False):
        print("Aborted. Existing config left unchanged.")
        return 1

    config_path.write_text(content, encoding="utf-8")

    print("\nOK: wrote config file:")
    print(f"  {config_path}")

    print("\nNext: export these environment variables in your shell:")
    print("  export PLANE_BASE_URL='https://your-plane.example.com'")
    print("  export PLANE_API_TOKEN='your_api_token'")
    print("  export PLANE_WORKSPACE_ID='your-workspace-id'")
    print(f"  export PLANE_CONFIG_FILE='{config_path}'")

    print("\nThen test the connection:")
    print("  python3 scripts/test_connection.py")

    print("\nThen try:")
    print(f"  python3 scripts/plane_cli.py projects summary --project {yaml_quote(project_name)}")

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
