#!/usr/bin/env python3
# 窗口位置
window_x = 304
window_y = 176
window_w = 2138
window_h = 1266

# 門先生提供的绝对坐标 (x1, y1; x2, y2; x3, y3; x4, y4)
regions = {
    'player_icon': [(340, 247), (417, 247), (340, 314), (417, 314)],
    'player_hp_top': [(448, 247), (607, 247), (448, 314), (607, 314)],
    'gold': [(671, 247), (777, 247), (671, 314), (777, 314)],
    'floor': [(1184, 247), (1259, 247), (1184, 314), (1259, 314)],
    'total_cards': [(2240, 247), (2331, 247), (2240, 314), (2331, 314)],
    'energy': [(449, 1176), (535, 1176), (449, 1258), (535, 1258)],
    'player_hp_bottom': [(710, 1043), (976, 1043), (707, 1095), (976, 1095)],
    'enemy1_hp': [(1547, 1043), (1708, 1043), (1547, 1095), (1708, 1095)],
    'enemy2_hp': [(1770, 1043), (2005, 1043), (1770, 1095), (2005, 1095)],
    'enemy3_hp': [(2063, 1043), (2265, 1043), (2063, 1095), (2265, 1095)],
    'draw_pile': [(373, 1346), (451, 1346), (373, 1412), (451, 1412)],
    'discard_pile': [(2304, 1346), (2371, 1346), (2304, 1412), (2371, 1412)],
    'hand_cards': [(592, 1155), (2104, 1155), (592, 1416), (2104, 1416)],
}

print('=' * 60)
print('杀戮尖塔2 相对坐标配置')
print('=' * 60)
print(f'窗口基准: {window_w} x {window_h}')
print()

for name, coords in regions.items():
    # 计算相对坐标 (减去窗口偏移)
    rel = [(x - window_x, y - window_y) for x, y in coords]
    
    # 计算ROI: x, y, w, h
    x = min(rel[0][0], rel[2][0])
    y = min(rel[0][1], rel[1][1])
    w = max(rel[1][0], rel[3][0]) - x
    h = max(rel[2][1], rel[3][1]) - y
    
    print(f'"{name}": {{"x": {x}, "y": {y}, "w": {w}, "h": {h}}},')
