"""E2EE state persistence using per-credential directories.

[INPUT]: Dict generated by E2eeClient.export_state() (HPKE scheme, contains
         version="hpke_v1"), credential_layout helpers
[OUTPUT]: save_e2ee_state(), load_e2ee_state(), delete_e2ee_state()
[POS]: E2EE session state persistence module supporting cross-process HPKE
       E2EE communication in the indexed multi-credential layout

[PROTOCOL]:
1. Update this header when logic changes
2. Check the folder's CLAUDE.md after updating
"""

from __future__ import annotations

import json
import logging
from pathlib import Path
from typing import Any

from credential_layout import (
    has_legacy_layout,
    legacy_layout_hint,
    resolve_credential_paths,
    write_secure_json,
)

logger = logging.getLogger(__name__)


def _e2ee_state_path(credential_name: str) -> Path | None:
    """Resolve the E2EE state path for a credential."""
    paths = resolve_credential_paths(credential_name)
    if paths is None:
        return None
    return paths.e2ee_state_path


def save_e2ee_state(
    state: dict[str, Any],
    credential_name: str = "default",
) -> Path:
    """Save E2EE client state into the credential directory."""
    path = _e2ee_state_path(credential_name)
    if path is None:
        raise FileNotFoundError(
            f"Credential '{credential_name}' not found; cannot save E2EE state"
        )
    write_secure_json(path, state)
    logger.debug("Saved E2EE state credential=%s path=%s", credential_name, path)
    return path


def load_e2ee_state(credential_name: str = "default") -> dict[str, Any] | None:
    """Load E2EE client state from the credential directory."""
    path = _e2ee_state_path(credential_name)
    if path is None:
        if has_legacy_layout():
            logger.error("%s", legacy_layout_hint())
        return None
    if path.exists():
        logger.debug("Loaded E2EE state credential=%s path=%s", credential_name, path)
        return json.loads(path.read_text(encoding="utf-8"))
    return None


def delete_e2ee_state(credential_name: str = "default") -> bool:
    """Delete a saved E2EE state file from the credential directory."""
    path = _e2ee_state_path(credential_name)
    if path is None:
        if has_legacy_layout():
            logger.error("%s", legacy_layout_hint())
        return False
    if path.exists():
        path.unlink()
        logger.info("Deleted E2EE state credential=%s path=%s", credential_name, path)
        return True
    return False


__all__ = ["delete_e2ee_state", "load_e2ee_state", "save_e2ee_state"]
