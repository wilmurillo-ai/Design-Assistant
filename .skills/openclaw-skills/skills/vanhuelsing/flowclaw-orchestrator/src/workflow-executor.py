def _check_duplicate(task_id: str) -> Optional[Dict]:
    cutoff = int(time.time()) - DEDUP_WINDOW_HOURS * 3600
    with _db() as conn:
        row = conn.execute(
            "SELECT run_id, processed_at, workflow_name, status FROM processed_tasks "
            "WHERE task_id = ? AND processed_at > ?",
            (task_id, cutoff),
        ).fetchone()
    if row:
        return {
            "run_id": row["run_id"],
            "processed_at": row["processed_at"],
            "workflow_name": row["workflow_name"],
            "status": row["status"],
        }
    return None


