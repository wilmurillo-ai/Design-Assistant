def _track_run_start(run_id: str, workflow_name: str) -> None:
    _ACTIVE_RUNS[run_id] = {
        "run_id": run_id,
        "workflow_name": workflow_name,
        "started_at": time.time(),
    }
    active_workflows.inc()


def _track_run_end(run_id: str, workflow_name: str, status: str, duration_s: float, error: Optional[str] = None) -> None:
    _ACTIVE_RUNS.pop(run_id, None)
    active_workflows.dec()
    record = {
        "run_id": run_id,
        "workflow_name": workflow_name,
        "status": status,
        "duration_s": round(duration_s, 2),
        "finished_at": time.time(),
        "error": error,
    }
    _RUN_HISTORY.append(record)
    if error:
        _ERROR_LOG.append({**record, "error": error})


