def _safe_path(base: Path, relative: str) -> Path:
    """Resolve *relative* under *base*, raising ValueError on path traversal."""
    resolved = (base / relative).resolve()
    try:
        resolved.relative_to(base.resolve())
    except ValueError:
        raise ValueError(f"Path traversal detected: {relative!r} must stay under {base}")
    return resolved


