class StructuredLogger:
    """JSON-structured logger — every record is a single JSON line to stderr."""

    def __init__(self, name: str) -> None:
        self.name = name

    def _log(self, level: str, msg: str, extra: Dict) -> None:
        record = {
            "timestamp": datetime.now(timezone.utc).isoformat().replace("+00:00", "Z"),
            "level": level,
            "logger": self.name,
            "message": msg,
            **extra,
        }
        print(json.dumps(record, default=str), file=sys.stderr, flush=True)

    def info(self, msg: str, **kwargs) -> None:
        self._log("INFO", msg, kwargs)

    def warning(self, msg: str, **kwargs) -> None:
        self._log("WARNING", msg, kwargs)

    def error(self, msg: str, **kwargs) -> None:
        self._log("ERROR", msg, kwargs)

    def debug(self, msg: str, **kwargs) -> None:
        self._log("DEBUG", msg, kwargs)

    def exception(self, msg: str, **kwargs) -> None:
        import traceback
        kwargs["traceback"] = traceback.format_exc()
        self._log("ERROR", msg, kwargs)

    # --- Compatibility shims for old %-style format calls ---
    def _format_msg(self, msg: str, args) -> str:
        try:
            return msg % args if args else msg
        except Exception as exc:  # noqa: BLE001
            import sys
            print(f"WARNING: _format_msg failed: {exc}", file=sys.stderr)
            return f"{msg} {args}"

    def _compat(self, level: str, msg: str, *args, **kwargs) -> None:
        formatted = self._format_msg(msg, args)
        extra = {k: v for k, v in kwargs.items() if k not in ("exc_info", "stack_info")}
        self._log(level, formatted, extra)


