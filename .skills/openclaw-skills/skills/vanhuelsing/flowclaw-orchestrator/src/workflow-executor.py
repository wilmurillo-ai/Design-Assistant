@contextmanager
def _db():
    """Context manager for SQLite connections (thread-safe WAL mode)."""
    conn = sqlite3.connect(str(DB_PATH), check_same_thread=False)
    conn.execute("PRAGMA journal_mode=WAL")
    conn.row_factory = sqlite3.Row
    try:
        yield conn
        conn.commit()
    except Exception as exc:  # noqa: BLE001
        log.warning("Database transaction rolled back", error=str(exc))
        conn.rollback()
        raise
    finally:
        conn.close()


def _init_db() -> None:
    """Create SQLite schema if it doesn't exist."""
    DATA_DIR.mkdir(parents=True, exist_ok=True)
    PENDING_APPROVALS_DIR.mkdir(parents=True, exist_ok=True)

    with _db() as conn:
        conn.executescript("""
            CREATE TABLE IF NOT EXISTS processed_tasks (
                task_id        TEXT PRIMARY KEY,
                run_id         TEXT NOT NULL,
                processed_at   INTEGER NOT NULL,
                workflow_name  TEXT NOT NULL,
                status         TEXT NOT NULL DEFAULT 'running'
            );

            CREATE TABLE IF NOT EXISTS approvals (
                run_id       TEXT PRIMARY KEY,
                approved_by  TEXT NOT NULL,
                approved_at  INTEGER NOT NULL,
                notes        TEXT
            );

            CREATE TABLE IF NOT EXISTS workflow_states (
                run_id        TEXT PRIMARY KEY,
                task_id       TEXT,
                workflow_name TEXT NOT NULL,
                vars_json     TEXT NOT NULL,
                resume_step   INTEGER NOT NULL DEFAULT 0,
                created_at    INTEGER NOT NULL,
                expires_at    INTEGER NOT NULL
            );

            CREATE INDEX IF NOT EXISTS idx_processed_at
                ON processed_tasks (processed_at);

            CREATE INDEX IF NOT EXISTS idx_workflow_states_task
                ON workflow_states (task_id);
        """)

        try:
            conn.execute("ALTER TABLE processed_tasks ADD COLUMN status TEXT NOT NULL DEFAULT 'running'")
            conn.commit()
            log.info("DB migration: added 'status' column to processed_tasks")
        except sqlite3.OperationalError:
            pass

    log.info("DB initialized", path=str(DB_PATH))


def _record_task(task_id: str, run_id: str, workflow_name: str) -> bool:
    """Atomically record a new task. Returns True if recorded, False if duplicate."""
    try:
        with _db() as conn:
            cursor = conn.execute(
                "INSERT INTO processed_tasks (task_id, run_id, processed_at, workflow_name, status) "
                "VALUES (?, ?, ?, ?, 'running')",
                (task_id, run_id, int(time.time()), workflow_name),
            )
            conn.commit()
            return cursor.rowcount > 0
    except sqlite3.IntegrityError:
        return False


def _update_task_status(task_id: str, status: str) -> None:
    with _db() as conn:
        conn.execute("UPDATE processed_tasks SET status = ? WHERE task_id = ?", (status, task_id))


