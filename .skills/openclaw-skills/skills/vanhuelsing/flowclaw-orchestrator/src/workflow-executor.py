@app.route("/notion/create-subtask", methods=["POST"])
@require_auth
def notion_create_subtask():
    if not request.is_json:
        return jsonify({"status": "error", "error": "Content-Type must be application/json"}), 415
    data = request.get_json(silent=True)
    if not isinstance(data, dict):
        return jsonify({"status": "error", "error": "Request body must be a JSON object"}), 400

    name = data.get("name", "")
    if not isinstance(name, str):
        return jsonify({"status": "error", "error": "name must be a string"}), 400
    name = name.strip()
    if not name:
        return jsonify({"status": "skipped", "reason": "empty name"}), 200
    if len(name) > _MAX_NAME_LEN:
        return jsonify({"status": "error", "error": f"name too long (max {_MAX_NAME_LEN} chars)"}), 400

    executor = WorkflowExecutor()
    if not executor.notion:
        return jsonify({"status": "error", "error": "NOTION_API_KEY not configured"}), 500

    phase_map = {
        "Requirements": "P1", "Conception": "P2", "Development": "P3",
        "QA": "P4", "Staging": "P5", "Deploy": "P6",
        "Concept": "P2", "Architecture Review": "P2", "Design": "P2",
        "Frontend": "P3", "Backend": "P3",
    }
    role_map = {
        "frontend": ["Dev"], "backend": ["Dev"], "creative": ["Design"],
        "quality": ["QA"], "devops": ["Ops"], "product": ["PM"],
        "legal": ["Legal"], "finance": ["Finance"], "growth": ["Strategy"], "main": ["PM"],
    }
    # Allowlisted assigned_to values — only known agent IDs are accepted
    allowed_assigned_to = set(role_map.keys()) | {""}

    assigned_to = data.get("assignedTo", "")
    if not isinstance(assigned_to, str):
        assigned_to = ""
    assigned_to = assigned_to.strip()
    if assigned_to and assigned_to not in allowed_assigned_to:
        return jsonify({"status": "error",
                        "error": f"Invalid assignedTo value. Allowed: {sorted(allowed_assigned_to - {''})}"}), 400

    properties = {
        "Name": {"title": [{"text": {"content": name}}]},
        "Status": {"select": {"name": "Not Started"}},
        "Assigned To": {"rich_text": [{"text": {"content": assigned_to or "Unassigned"}}]},
    }

    roles = role_map.get(assigned_to, [])
    if roles:
        properties["Role"] = {"multi_select": [{"name": r} for r in roles]}

    phase_raw = data.get("phase", "")
    if not isinstance(phase_raw, str):
        phase_raw = ""
    phase = phase_map.get(phase_raw.strip())
    if phase:
        properties["Phase"] = {"select": {"name": phase}}

    due_date = data.get("dueDate")
    if due_date is not None:
        if not isinstance(due_date, str) or not _VALID_ISO_DATE_RE.match(due_date.strip()):
            return jsonify({"status": "error", "error": "dueDate must be a valid ISO 8601 date (YYYY-MM-DD)"}), 400
        properties["Due Date"] = {"date": {"start": due_date.strip()}}

    if data.get("isFastlane"):
        properties["Type"] = {"select": {"name": "Fastlane"}}
        properties["Pre-Approved"] = {"checkbox": True}

    db_id = os.environ.get("NOTION_TASKS_DATABASE_ID", "").strip()
    if not db_id:
        return jsonify({
            "status": "error",
            "error": "NOTION_TASKS_DATABASE_ID environment variable is not configured",
        }), 500
    if not _VALID_NOTION_ID_RE.match(db_id):
        return jsonify({
            "status": "error",
            "error": "NOTION_TASKS_DATABASE_ID is not a valid Notion UUID",
        }), 500

    try:
        result = executor.notion._request("POST", "pages",
                                          json={"parent": {"database_id": db_id}, "properties": properties})
        return jsonify({"status": "success", "pageId": result["id"], "url": result.get("url", "")})
    except Exception as exc:
        return jsonify({"status": "error", "error": str(exc)}), 500


