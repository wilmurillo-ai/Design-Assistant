@app.route("/workflow/execute", methods=["POST"])
@require_auth
def execute():
    if not request.is_json:
        return jsonify({"status": "error", "error": "Content-Type must be application/json"}), 415
    data = request.get_json(silent=True)
    if not isinstance(data, dict):
        return jsonify({"status": "error", "error": "Request body must be a JSON object"}), 400

    wf_name = data.get("workflowName", "")
    if not isinstance(wf_name, str):
        return jsonify({"status": "error", "error": "workflowName must be a string"}), 400
    wf_name = wf_name.strip()

    context = data.get("context", {})
    if not isinstance(context, dict):
        return jsonify({"status": "error", "error": "context must be a JSON object"}), 400

    if "task_id" in data and "taskId" not in context:
        context["taskId"] = data["task_id"]
    if "taskId" in data and "taskId" not in context:
        context["taskId"] = data["taskId"]

    if not wf_name:
        return jsonify({"status": "error", "error": "Missing workflowName"}), 400
    if not _VALID_NAME_RE.match(wf_name):
        return jsonify({"status": "error", "error": "Invalid workflowName format (alphanumeric, hyphens, underscores, 1-64 chars)"}), 400

    task_id = context.get("taskId") or data.get("task_id")
    if task_id is not None:
        if not isinstance(task_id, str):
            return jsonify({"status": "error", "error": "taskId must be a string"}), 400
        task_id = task_id.strip()
        if len(task_id) > _MAX_TASK_ID_LEN:
            return jsonify({"status": "error", "error": f"taskId too long (max {_MAX_TASK_ID_LEN} chars)"}), 400
        if not task_id:
            task_id = None

    if task_id:
        run_id = datetime.now(timezone.utc).strftime("%Y%m%d-%H%M%S") + "-" + uuid.uuid4().hex[:6]
        recorded = _record_task(task_id, run_id, wf_name)
        if not recorded:
            existing = _check_duplicate(task_id)
            if existing:
                log.info("Duplicate task rejected", task_id=task_id, original_run_id=existing["run_id"])
                return jsonify({
                    "status": "duplicate",
                    "task_id": task_id,
                    "original_run_id": existing["run_id"],
                    "original_workflow": existing["workflow_name"],
                    "original_status": existing.get("status", "unknown"),
                    "processed_at": existing["processed_at"],
                    "message": "Task already processed within 24h. Use /workflow/history to query.",
                }), 200
            else:
                log.warning("IntegrityError but no recent record — allowing through", task_id=task_id)
    else:
        run_id = None

    executor = WorkflowExecutor()
    if run_id:
        executor.run_id = run_id

    context["_workflow_name"] = wf_name
    result = executor.execute(wf_name, context)

    if task_id:
        final_status = result.get("status", "failed")
        if final_status not in ("pending_approval",):
            db_status = "completed" if final_status == "success" else "failed"
            try:
                _update_task_status(task_id, db_status)
            except Exception as exc:
                log.warning("Failed to update task status", error=str(exc))

    try:
        LOGS_DIR.mkdir(parents=True, exist_ok=True)
        log_file = LOGS_DIR / f"{executor.run_id}-{wf_name}.json"
        log_file.write_text(json.dumps(result, indent=2, default=str))
    except Exception as exc:
        log.warning("Failed to write log file", error=str(exc))

    result_status = result.get("status")
    status_code = 200 if result_status == "success" else (202 if result_status == "pending_approval" else 500)
    return jsonify(result), status_code


@app.route("/workflow/approve", methods=["POST"])
@require_auth
def approve_workflow():
    """Approve a pending workflow run and trigger resumption."""
    if not request.is_json:
        return jsonify({"status": "error", "error": "Content-Type must be application/json"}), 415
    data = request.get_json(silent=True)
    if not isinstance(data, dict):
        return jsonify({"status": "error", "error": "Request body must be a JSON object"}), 400

    run_id = data.get("run_id", "")
    approved_by = data.get("approved_by", "")

    if not isinstance(run_id, str):
        return jsonify({"status": "error", "error": "run_id must be a string"}), 400
    if not isinstance(approved_by, str):
        return jsonify({"status": "error", "error": "approved_by must be a string"}), 400

    run_id = run_id.strip()
    approved_by = approved_by.strip()

    if not run_id:
        return jsonify({"status": "error", "error": "Missing run_id"}), 400
    if not _VALID_RUN_ID_RE.match(run_id):
        return jsonify({"status": "error", "error": "Invalid run_id format"}), 400
    if not approved_by:
        return jsonify({"status": "error", "error": "Missing approved_by"}), 400
    if len(approved_by) > _MAX_APPROVED_BY_LEN:
        return jsonify({"status": "error", "error": f"approved_by too long (max {_MAX_APPROVED_BY_LEN} chars)"}), 400

    notes = data.get("notes", "")
    if not isinstance(notes, str):
        notes = ""
    notes = notes[:_MAX_NOTES_LEN]  # Truncate silently — it's a non-critical annotation

    try:
        _record_approval(run_id, approved_by, notes)
    except Exception as exc:
        return jsonify({"status": "error", "error": f"DB error: {exc}"}), 500

    log.info("Workflow approved", run_id=run_id, approved_by=approved_by)

