        if phase:
            phase_lower = phase.lower()
            if phase_lower in {"concept", "requirements", "architecture"}:
                return {"agentId": "backend", "runtime": "subagent", "reason": f"Phase: {phase}"}
            if phase_lower == "design":
                return {"agentId": "creative", "runtime": "subagent", "reason": f"Phase: {phase}"}
            if phase_lower == "qa":
                return {"agentId": "quality", "runtime": "subagent", "reason": f"Phase: {phase}"}
            if phase_lower == "deploy":
                return {"agentId": "devops", "runtime": "subagent", "reason": f"Phase: {phase}"}

        scores = {
            "architect": len(words & cls._ARCHITECT),
            "designer": len(words & cls._DESIGNER),
            "frontend": len(words & cls._FRONTEND),
            "backend": len(words & cls._BACKEND),
            "qa": len(words & cls._QA),
            "devops": len(words & cls._DEVOPS),
        }
        if tech_stack:
            stack = {s.lower() for s in tech_stack}
            if stack & {"typescript", "react", "nextjs", "tailwind"}:
                scores["frontend"] += 5
            if stack & {"python", "flask", "django", "fastapi"}:
                scores["backend"] += 5

        winner = max(scores, key=scores.get)
        mapping = {
            "architect": ("backend", "Architect keywords"),
            "designer": ("creative", "Designer keywords"),
            "qa": ("quality", "QA keywords"),
            "devops": ("devops", "DevOps keywords"),
            "frontend": ("frontend", "Frontend keywords"),
            "backend": ("backend", "Backend keywords"),
        }
        if scores[winner] > 0 and winner in mapping:
            agent_id, reason = mapping[winner]
            return {"agentId": agent_id, "runtime": "subagent", "reason": f"{reason} ({scores[winner]})"}

        return {"agentId": "frontend", "runtime": "subagent", "reason": "Default (frontend)"}


