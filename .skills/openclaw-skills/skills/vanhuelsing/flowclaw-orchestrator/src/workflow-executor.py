class NotionClient:
    def __init__(self, api_key: str) -> None:
        if not api_key:
            raise ValueError("NOTION_API_KEY environment variable is required")
        self._headers = {
            "Authorization": f"Bearer {api_key}",
            "Content-Type": "application/json",
            "Notion-Version": NOTION_VERSION,
        }

    def _request(self, method: str, path: str, **kwargs: Any) -> Dict:
        url = f"{NOTION_API}/{path}"
        last_error: Optional[Exception] = None

        for attempt in range(1, MAX_RETRIES + 1):
            try:
                r = requests.request(method, url, headers=self._headers, timeout=NOTION_TIMEOUT, **kwargs)
                if r.status_code == 429:
                    wait = min(float(r.headers.get("Retry-After", "1")), 60.0)
                    log.warning("Notion 429 rate limit", path=path, retry_after=wait, attempt=attempt)
                    time.sleep(wait)
                    continue
                r.raise_for_status()
                return r.json()
            except (requests.exceptions.Timeout, requests.exceptions.ConnectionError) as exc:
                last_error = exc
                if attempt < MAX_RETRIES:
                    time.sleep(0.5 * attempt)
                continue
            except requests.exceptions.HTTPError:
                log.error("Notion HTTP error", path=path, status=r.status_code)
                raise

        raise last_error or RuntimeError(f"Request to {path} failed after {MAX_RETRIES} attempts")

    def get_task(self, page_id: str) -> Dict:
        page = self._request("GET", f"pages/{page_id}")
        props = page.get("properties", {})

        def _title(p): return (p.get("title") or [{}])[0].get("text", {}).get("content", "")
        def _rich_text(p): return (p.get("rich_text") or [{}])[0].get("text", {}).get("content", "")
        def _select(p): s = p.get("select"); return s["name"] if s else None
        def _number(p): return p.get("number")
        def _checkbox(p): return p.get("checkbox", False)

        return {
            "id": page["id"], "url": page.get("url", ""),
            "name": _title(props.get("Name", {})),
            "type": _select(props.get("Type", {})),
            "status": _select(props.get("Status", {})),
            "phase": _select(props.get("Phase", {})),
            "budget_estimated": _number(props.get("Budget Estimated", {})),
            "pages_affected": _number(props.get("Pages Affected", {})),
            "gdpr_impact": _checkbox(props.get("GDPR Impact", {})),
            "pre_approved": _checkbox(props.get("Pre-Approved", {})),
            "assigned_to": _rich_text(props.get("Assigned To", {})),
            "description": _rich_text(props.get("Description", {})) if "Description" in props else "",
        }

    def get_page_raw(self, page_id: str) -> Dict:
        return self._request("GET", f"pages/{page_id}")

    def set_status(self, page_id: str, status: str) -> Dict:
        return self._request("PATCH", f"pages/{page_id}",
                             json={"properties": {"Status": {"select": {"name": status}}}})

    def set_health_check(self, page_id: str, result: str) -> Dict:
        return self._request("PATCH", f"pages/{page_id}",
                             json={"properties": {"Health Check": {"select": {"name": result}}}})


