def require_auth(f):
    """Decorator: enforce Bearer or X-API-Key authentication."""
    @wraps(f)
    def decorated(*args, **kwargs):
        expected = os.environ.get("WORKFLOW_EXECUTOR_API_KEY")
        if not expected:
            log.error("WORKFLOW_EXECUTOR_API_KEY not set — rejecting request")
            return jsonify({"error": "service_misconfigured", "message": "Authentication not configured"}), 503

        auth_header = request.headers.get("Authorization", "")
        api_key_header = request.headers.get("X-API-Key", "")
        provided = api_key_header or auth_header.removeprefix("Bearer ").strip()

        if provided and hmac.compare_digest(provided, expected):
            return f(*args, **kwargs)

        log.warning("Unauthorized request", path=request.path, remote_addr=request.remote_addr)
        return jsonify({"error": "unauthorized"}), 401

    return decorated


