class WorkflowExecutor:
    """Load and execute YAML workflows with proper step orchestration."""

    def __init__(self) -> None:
        self.notion = NotionClient(NOTION_API_KEY) if NOTION_API_KEY else None
        self.run_id = (
            datetime.now(timezone.utc).strftime("%Y%m%d-%H%M%S") + "-" + uuid.uuid4().hex[:6]
        )
        self.run_log: List[Dict] = []
        self._vars: Dict[str, str] = {}
        self._spawned_agents: Dict[str, Dict] = {}
        self._workflow_name: str = "unknown"

    # -- structured logging per run ----------------------------------------

    def _log(self, step: str, status: str, data: Any = None) -> None:
        entry: Dict[str, Any] = {
            "ts": datetime.now(timezone.utc).isoformat() + "Z",
            "run_id": self.run_id,
            "workflow": self._workflow_name,
            "step": step,
            "status": status,
        }
        if data is not None:
            entry["data"] = data
        self.run_log.append(entry)
        log.info("Step event", run_id=self.run_id, workflow=self._workflow_name,
                 step=step, status=status)

    # -- main entry --------------------------------------------------------

    def execute(self, workflow_name: str, context: Dict, resume_from_step: int = 0) -> Dict:
        """Execute all steps in order; stop on first failure."""
        self._workflow_name = workflow_name
        self._vars.update({k: str(v) for k, v in context.items() if v is not None})
        self._log("workflow", "started", {"context_keys": list(context.keys()), "resume_from": resume_from_step})

        _track_run_start(self.run_id, workflow_name)
        wf_start = time.time()

        try:
            wf = self._load(workflow_name)
        except (FileNotFoundError, ValueError, yaml.YAMLError) as exc:
            duration = time.time() - wf_start
            self._log("workflow", "load_failed", {"error": str(exc)})
            workflow_executions.labels(workflow_name=workflow_name, status="failed").inc()
            workflow_errors.labels(workflow_name=workflow_name, error_type="load_error").inc()
            _track_run_end(self.run_id, workflow_name, "failed", duration, error=str(exc))
            return self._result("failed", error=str(exc))

        steps = wf.get("steps", [])
        if not steps:
            self._log("workflow", "no_steps", {})
            workflow_executions.labels(workflow_name=workflow_name, status="failed").inc()
            _track_run_end(self.run_id, workflow_name, "failed", time.time() - wf_start, error="No steps")
            return self._result("failed", error="No steps defined in workflow")

        self._log("workflow", "loaded", {"steps": len(steps)})

        # Check for DAG mode (any step has depends_on)
        has_dag = any(
            (list(s.values())[0] or {}).get("depends_on") is not None
            for s in steps if isinstance(s, dict) and len(s) == 1
        )

        if has_dag and resume_from_step == 0:
            result = self._execute_dag(steps, workflow_name)
        else:
            result = self._execute_sequential(steps, workflow_name, resume_from_step)

        duration = time.time() - wf_start
        workflow_duration.labels(workflow_name=workflow_name).observe(duration)

        final_status = result.get("status", "failed")
        if final_status not in ("pending_approval",):
            metric_status = "success" if final_status == "success" else "failed"
            workflow_executions.labels(workflow_name=workflow_name, status=metric_status).inc()
            _track_run_end(self.run_id, workflow_name, final_status, duration,
                           error=result.get("error") if final_status != "success" else None)
        else:
            _track_run_end(self.run_id, workflow_name, "pending_approval", duration)

        return result

    def _execute_sequential(self, steps: List[Dict], workflow_name: str, resume_from_step: int = 0) -> Dict:
        """Classic sequential step execution (original behaviour)."""
        for step_idx, step_def in enumerate(steps):
            if step_idx < resume_from_step:
                self._log(f"step_{step_idx}", "skipped_resume", {})
                continue

            if not isinstance(step_def, dict) or len(step_def) != 1:
                log.warning("Invalid step definition", step_idx=step_idx, step=step_def)
                continue

            name, config = next(iter(step_def.items()))
            config = config or {}

            self._log(name, "started")
            step_start = time.time()
            result = self._run_step(name, config, step_idx=step_idx)
            step_dur = time.time() - step_start
            workflow_step_duration.labels(workflow_name=workflow_name, step=name).observe(step_dur)

            if result.get("status") == "pending_approval":
                self._log("workflow", "pending_approval", {"paused_at_step": step_idx})
                return self._result("pending_approval", run_id=self.run_id, paused_at_step=step_idx,
                                    message="Workflow paused — awaiting human approval.")

            if result.get("status") != "failed":
                for k, v in result.items():
                    if k != "status" and isinstance(v, (str, int, float, bool)):
                        self._vars[k] = str(v)

            self._log(name, "done", {"duration_s": round(step_dur, 2), "status": result.get("status")})

            if result.get("status") == "failed":
                self._log("workflow", "failed", {"failed_step": name, "error": result.get("error")})
                workflow_errors.labels(workflow_name=workflow_name, error_type="step_failed").inc()
                return self._result("failed", error=result.get("error", ""), failed_step=name)

        self._log("workflow", "completed")
        return self._result("success")

    def _execute_dag(self, steps: List[Dict], workflow_name: str) -> Dict:
        """Execute steps using a DAG (dependency graph) for parallel execution.

        Steps with no depends_on default to depending on the previous step
        (preserving existing sequential semantics). Steps that declare
        depends_on: [] run as soon as their explicit deps complete.

        Parallelism: up to 4 concurrent threads for independent steps.
        Note: OpenClaw agent *spawning* still happens serially (CLI limitation),
        but wait/IO-bound steps benefit from true parallelism.
        """
        # Build dependency map: step_name → set of step names it depends on
        step_names = []
        step_configs: Dict[str, Dict] = {}
        step_indices: Dict[str, int] = {}

        for idx, step_def in enumerate(steps):
            if not isinstance(step_def, dict) or len(step_def) != 1:
                continue
            name, config = next(iter(step_def.items()))
            config = config or {}
            step_names.append(name)
            step_configs[name] = config
            step_indices[name] = idx

        dag: Dict[str, Set[str]] = {}
        for i, name in enumerate(step_names):
            config = step_configs[name]
            if "depends_on" in config:
                deps = config["depends_on"]
                if deps is None:
                    deps = []
                dag[name] = set(deps)
            else:
                # Default: depend on previous step
                dag[name] = {step_names[i - 1]} if i > 0 else set()

        completed: Set[str] = set()
        results: Dict[str, Dict] = {}

        log.info("DAG execution mode", workflow=workflow_name, steps=len(step_names),
                 run_id=self.run_id)

        while len(completed) < len(step_names):
            # Find all steps ready to run
            ready = [
                n for n in step_names
                if n not in completed and dag[n].issubset(completed)
                and n not in results  # not already in-flight
            ]

            if not ready:
                remaining = [n for n in step_names if n not in completed]
                log.error("DAG deadlock", remaining=remaining, completed=list(completed))
                return self._result("failed", error=f"DAG deadlock — circular dependency among: {remaining}")

            if len(ready) == 1:
                # Run single step inline (no thread overhead)
                name = ready[0]
                config = self._resolve(step_configs[name])
                self._log(name, "started")
                step_start = time.time()
                result = self._run_step(name, config, step_idx=step_indices[name])
                step_dur = time.time() - step_start
                workflow_step_duration.labels(workflow_name=workflow_name, step=name).observe(step_dur)

                results[name] = result
                completed.add(name)

                if result.get("status") == "pending_approval":
                    self._log("workflow", "pending_approval", {"paused_at_step": step_indices[name]})
                    return self._result("pending_approval", run_id=self.run_id,
                                        paused_at_step=step_indices[name],
                                        message="Workflow paused — awaiting human approval.")

                if result.get("status") != "failed":
                    for k, v in result.items():
                        if k != "status" and isinstance(v, (str, int, float, bool)):
                            self._vars[k] = str(v)

                self._log(name, "done", {"duration_s": round(step_dur, 2)})

                if result.get("status") == "failed":
                    self._log("workflow", "failed", {"failed_step": name, "error": result.get("error")})
                    workflow_errors.labels(workflow_name=workflow_name, error_type="step_failed").inc()
                    return self._result("failed", error=result.get("error", ""), failed_step=name)

            else:
                # Multiple independent steps — run in parallel
                log.info("Parallel step batch", steps=ready, run_id=self.run_id)
                self._log("dag_parallel", "started", {"steps": ready})

                def _run_parallel_step(step_name: str) -> tuple:
                    cfg = self._resolve(step_configs[step_name])
                    self._log(step_name, "started")
                    t0 = time.time()
                    res = self._run_step(step_name, cfg, step_idx=step_indices[step_name])
                    dur = time.time() - t0
                    workflow_step_duration.labels(workflow_name=workflow_name, step=step_name).observe(dur)
                    self._log(step_name, "done", {"duration_s": round(dur, 2)})
                    return step_name, res, dur

                with concurrent.futures.ThreadPoolExecutor(max_workers=4) as executor:
                    futures = {executor.submit(_run_parallel_step, n): n for n in ready}
                    for future in concurrent.futures.as_completed(futures):
                        try:
                            step_name, result, dur = future.result()
                        except Exception as exc:
                            step_name = futures[future]
                            log.error("Parallel step exception", step=step_name, error=str(exc))
                            return self._result("failed", error=str(exc), failed_step=step_name)

                        results[step_name] = result
                        completed.add(step_name)

                        if result.get("status") != "failed":
                            for k, v in result.items():
                                if k != "status" and isinstance(v, (str, int, float, bool)):
                                    self._vars[k] = str(v)

                        if result.get("status") == "failed":
                            workflow_errors.labels(workflow_name=workflow_name, error_type="step_failed").inc()
                            return self._result("failed", error=result.get("error", ""), failed_step=step_name)

        self._log("workflow", "completed")
        return self._result("success")

    # -- helpers -----------------------------------------------------------

    def _result(self, status: str, **extra: Any) -> Dict:
        return {"status": status, "run_id": self.run_id, **extra, "log": self.run_log}

    @staticmethod
    def _load(name: str) -> Dict:
        """Load and validate workflow YAML against JSON schema."""
        if not _VALID_NAME_RE.match(name):
            raise ValueError(f"Invalid workflow name: {name!r} (alphanumeric, hyphens, underscores only)")

        path = WORKFLOWS_DIR / f"{name}.yaml"
        if not path.exists():
            raise FileNotFoundError(f"Workflow not found: {path}")

        try:
            data = yaml.safe_load(path.read_text())
        except yaml.YAMLError as exc:
            raise yaml.YAMLError(f"YAML parse error in {name}.yaml: {exc}") from exc

        if not isinstance(data, dict):
            raise ValueError(f"Workflow {name!r} is empty or not a YAML mapping")

        # Schema validation (if jsonschema available and schema file exists)
        if JSONSCHEMA_AVAILABLE and WORKFLOW_SCHEMA_PATH.exists():
            try:
                schema = json.loads(WORKFLOW_SCHEMA_PATH.read_text())
                jsonschema_validate(instance=data, schema=schema)
            except JsonSchemaValidationError as e:
                schema_validation_failures.labels(workflow_name=name).inc()
                log.error("Workflow schema validation failed",
                          workflow=name, error=e.message, path=str(list(e.absolute_path)))
                raise ValueError(f"Invalid workflow schema: {e.message}") from e
            except Exception as exc:
                log.warning("Schema validation skipped due to error", error=str(exc))
        elif not JSONSCHEMA_AVAILABLE:
            log.debug("jsonschema not installed — schema validation skipped")

        return data

    def _resolve(self, obj: Any) -> Any:
        if isinstance(obj, str):
            result = obj
            for key, val in self._vars.items():
                result = result.replace(f"{{{key}}}", str(val))
            unresolved = _UNRESOLVED_RE.findall(result)
            if unresolved:
                log.warning("Unresolved template variables", variables=unresolved, run_id=self.run_id)
            return result
        if isinstance(obj, dict):
            return {k: self._resolve(v) for k, v in obj.items()}
        if isinstance(obj, list):
            return [self._resolve(i) for i in obj]
        return obj

    def _run_step(self, name: str, config: Dict, step_idx: int = 0) -> Dict:
        config = self._resolve(config)
        handler = getattr(self, f"_step_{name}", None)

        if handler is None:
            return {"status": "failed", "error": f"Unknown step: {name!r}"}

        try:
            if name == "wait_approval":
                result = handler(config, step_idx=step_idx)
            else:
                result = handler(config)
            if not isinstance(result, dict):
                return {"status": "failed", "error": f"Step {name!r} returned invalid result type"}
            return result
        except Exception as exc:
            log.exception("Step raised exception", step=name, run_id=self.run_id, error=str(exc))
            return {"status": "failed", "error": str(exc)}

