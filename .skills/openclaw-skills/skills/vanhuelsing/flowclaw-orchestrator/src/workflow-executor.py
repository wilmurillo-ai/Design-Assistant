    def _step_spawn_agent(self, cfg: Dict) -> Dict:
        agent_id = cfg.get("agentId") or self._vars.get("agentId", "frontend")
        task_text = cfg.get("task", "")
        # Safe int coercion — YAML config may supply a float or string
        try:
            timeout_s = int(cfg.get("timeout", 600))
        except (TypeError, ValueError):
            timeout_s = 600

        if task_text.startswith("file:"):
            try:
                p = _safe_path(FLOWCLAW_DIR, task_text[5:])
                task_text = p.read_text() if p.exists() else task_text
            except ValueError as exc:
                return {"status": "failed", "error": f"Invalid task file path: {exc}"}

        if not isinstance(task_text, str):
            task_text = str(task_text)
        if not task_text.strip():
            return {"status": "failed", "error": "No task text provided"}
        if len(task_text) > _MAX_TASK_TEXT_LEN:
            return {"status": "failed", "error": f"Task text exceeds maximum length ({_MAX_TASK_TEXT_LEN} chars)"}

        openclaw_bin = shutil.which("openclaw")
        if not openclaw_bin:
            # Candidate paths for common Unix/macOS install locations.
            # Windows is not supported (see Platform Support in README).
            for candidate in [
                Path.home() / ".local/share/fnm/node-versions/v24.14.0/installation/bin/openclaw",
                Path("/usr/local/bin/openclaw"),
                Path("/opt/homebrew/bin/openclaw"),
            ]:
                if candidate.exists():
                    openclaw_bin = str(candidate)
                    break

        if not openclaw_bin:
            return {"status": "failed", "error": "openclaw CLI not found in PATH"}

        ALLOWED_AGENTS = {"frontend", "backend", "creative", "quality", "devops", "main"}
        if agent_id not in ALLOWED_AGENTS:
            return {"status": "failed", "error": f"Unknown agent: {agent_id!r}. Allowed: {sorted(ALLOWED_AGENTS)}"}

        session_id = f"wf-{self.run_id}-{agent_id}"
        cmd = [openclaw_bin, "agent", "--agent", agent_id, "--message", task_text,
               "--session-id", session_id, "--timeout", str(timeout_s), "--json"]

        log.info("Spawning agent", agent_id=agent_id, session_id=session_id,
                 task_preview=task_text[:100], run_id=self.run_id)

        try:
            proc = subprocess.Popen(cmd, stdout=subprocess.PIPE, stderr=subprocess.PIPE, text=True)
        except Exception as exc:
            return {"status": "failed", "error": f"Failed to launch openclaw agent: {exc}"}

        self._spawned_agents[session_id] = {
            "proc": proc, "agent_id": agent_id, "session_id": session_id,
            "started_at": time.time(), "timeout_s": timeout_s,
        }
        return {"status": "success", "session_id": session_id, "agent_id": agent_id}

    def _step_wait_completion(self, cfg: Dict) -> Dict:
        session_id = cfg.get("sessionId") or self._vars.get("session_id")
        store_as = cfg.get("store_as", "agent_result")

        if not session_id:
            return {"status": "failed", "error": "No session_id to wait for"}

        entry = self._spawned_agents.get(session_id)
        if not entry:
            return {"status": "failed", "error": f"No spawned agent found for session_id={session_id!r}"}

        proc: subprocess.Popen = entry["proc"]
        try:
            timeout_s = int(cfg.get("timeout", entry.get("timeout_s", 600)))
        except (TypeError, ValueError):
            timeout_s = 600
        remaining = max(1, timeout_s - (time.time() - entry["started_at"]))

        log.info("Waiting for agent", agent_id=entry["agent_id"], session_id=session_id,
                 timeout=remaining, run_id=self.run_id)

        try:
            stdout, stderr = proc.communicate(timeout=remaining)
        except subprocess.TimeoutExpired:
            proc.kill()
            proc.communicate()
            return {"status": "failed", "error": f"Agent timed out after {timeout_s}s", "session_id": session_id}
        except Exception as exc:
            return {"status": "failed", "error": f"Error waiting for agent: {exc}"}
        finally:
            self._spawned_agents.pop(session_id, None)

        wall_time = round(time.time() - entry["started_at"], 1)

        if proc.returncode != 0:
            err_snippet = (stderr or "")[:500].strip()
            log.error("Agent exited with error", agent_id=entry["agent_id"],
                      returncode=proc.returncode, run_id=self.run_id)
            return {"status": "failed", "error": f"Agent exited with code {proc.returncode}",
                    "stderr": err_snippet, "session_id": session_id, "wall_time_s": wall_time}

        try:
            result_json = json.loads(stdout)
        except json.JSONDecodeError:
            result_text = stdout.strip()
            self._vars[store_as] = result_text
            return {"status": "success", "session_id": session_id, "agent_id": entry["agent_id"],
                    store_as: result_text, "wall_time_s": wall_time, "raw": True}

        api_status = result_json.get("status", "")
        if api_status not in ("ok", "completed"):
            err = result_json.get("error") or result_json.get("summary") or f"status={api_status}"
            return {"status": "failed", "error": f"Agent returned error: {err}", "session_id": session_id}

        payloads = result_json.get("result", {}).get("payloads", [])
        reply_text = " ".join(p["text"] for p in payloads if isinstance(p, dict) and p.get("text")).strip()
        meta = result_json.get("result", {}).get("meta", {})
        agent_meta = meta.get("agentMeta", {})
        run_id_openclaw = result_json.get("runId", "")

        log.info("Agent completed", agent_id=entry["agent_id"], wall_time=wall_time,
                 run_id_openclaw=run_id_openclaw, run_id=self.run_id)

        self._vars[store_as] = reply_text
        return {
            "status": "success", "session_id": session_id, "agent_id": entry["agent_id"],
            store_as: reply_text, "wall_time_s": wall_time, "run_id_openclaw": run_id_openclaw,
            "model": agent_meta.get("model", ""),
            "tokens_total": agent_meta.get("usage", {}).get("total", 0),
            "aborted": meta.get("aborted", False),
        }

    def _step_wait_approval(self, cfg: Dict, step_idx: int = 0) -> Dict:
        """C4 FIX: Non-blocking two-phase approval gate."""
        run_id = self.run_id
        try:
            timeout_s = int(cfg.get("timeout", APPROVAL_TIMEOUT_DEFAULT))
        except (TypeError, ValueError):
            timeout_s = APPROVAL_TIMEOUT_DEFAULT
        channel_key = cfg.get("channel", "project_updates")
        if not isinstance(channel_key, str):
            channel_key = "project_updates"
        task_name = self._vars.get("task_name", "Unknown Task")
