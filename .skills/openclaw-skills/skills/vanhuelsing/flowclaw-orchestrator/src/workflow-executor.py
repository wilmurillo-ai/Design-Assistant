@app.route("/notify/discord", methods=["POST"])
@require_auth
def notify_discord():
    """Send a Discord message with channel allowlist enforcement."""
    if not request.is_json:
        return jsonify({"status": "error", "error": "Content-Type must be application/json"}), 415
    data = request.get_json(silent=True) or {}
    if not isinstance(data, dict):
        return jsonify({"status": "error", "error": "Request body must be a JSON object"}), 400

    channel_key = data.get("channel")
    channel_id = data.get("channelId")
    message = data.get("message", "")

    if not isinstance(message, str) or not message.strip():
        return jsonify({"status": "error", "error": "Missing or empty message"}), 400

    if len(message) > DISCORD_MAX_LEN:
        message = message[:DISCORD_MAX_LEN - 3] + "..."

    channels = _build_discord_channels()
    # Allowlist: only non-empty channel IDs that are configured
    allowed_ids = {v for v in channels.values() if v}

    if channel_id:
        if not isinstance(channel_id, str):
            return jsonify({"status": "error", "error": "channelId must be a string"}), 400
        if channel_id not in allowed_ids:
            log.warning("Rejected unauthorized Discord channel", channel_id=channel_id,
                        remote_addr=request.remote_addr)
            return jsonify({"error": "unauthorized_channel",
                            "message": "Channel ID not in allowlist."}), 403
    elif channel_key:
        if not isinstance(channel_key, str):
            return jsonify({"status": "error", "error": "channel must be a string"}), 400
        channel_id = channels.get(channel_key)
        if not channel_id:
            known = [k for k, v in channels.items() if v]
            return jsonify({"status": "error",
                            "error": f"Unknown or unconfigured channel: {channel_key!r}. Configured: {known}"}), 400
    else:
        return jsonify({"status": "error", "error": "Missing channel or channelId"}), 400

    if not DISCORD_BOT_TOKEN:
        return jsonify({"status": "error", "error": "DISCORD_BOT_TOKEN not configured"}), 500

    if len(message) > DISCORD_MAX_LEN:
        message = message[:DISCORD_MAX_LEN - 3] + "..."

    try:
        r = requests.post(
            f"{DISCORD_API}/channels/{channel_id}/messages",
            headers={"Authorization": f"Bot {DISCORD_BOT_TOKEN}", "Content-Type": "application/json"},
            json={"content": message},
            timeout=HTTP_TIMEOUT,
        )
        r.raise_for_status()
        return jsonify({"status": "success", "message_id": r.json().get("id", "")})
    except Exception as exc:
        return jsonify({"status": "error", "error": str(exc)}), 500


