#!/bin/bash
# 企业年金调查报告生成脚本 v2.0
# 用法：./generate_report.sh [企业名称] [调查数据文件] [输出目录]

set -e

COMPANY_NAME="${1:-}"
DATA_FILE="${2:-}"
OUTPUT_DIR="${3:-./reports}"

# 颜色定义
GREEN='\033[0;32m'
BLUE='\033[0;34m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
NC='\033[0m'

log_info() {
    echo -e "${BLUE}[INFO]${NC} $1"
}

log_success() {
    echo -e "${GREEN}[SUCCESS]${NC} $1"
}

log_error() {
    echo -e "${RED}[ERROR]${NC} $1"
}

# 检查参数
if [ -z "$COMPANY_NAME" ]; then
    log_error "企业名称不能为空"
    echo "用法：$0 \"企业名称\" [数据文件] [输出目录]"
    exit 1
fi

# 创建输出目录
mkdir -p "$OUTPUT_DIR"

# 生成时间戳
TIMESTAMP=$(date +"%Y-%m-%d_%H-%M-%S")
REPORT_FILE="$OUTPUT_DIR/${COMPANY_NAME}_年金调查报告_${TIMESTAMP}.md"

log_info "生成报告：$REPORT_FILE"

