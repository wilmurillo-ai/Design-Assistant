#!/usr/bin/env python3
"""Add a WeChat group to weflow-groups.yaml."""

import argparse
import sys

try:
    import yaml
except ImportError:
    print("PyYAML is required: pip install pyyaml", file=sys.stderr)
    sys.exit(1)


def main():
    parser = argparse.ArgumentParser(description="Add a WeChat group to weflow-groups.yaml")
    parser.add_argument("yaml_file", help="Path to weflow-groups.yaml")
    parser.add_argument("--host", help="WeFlow API host (e.g. http://<windows-ip>:5032)")
    parser.add_argument("--talker", required=True, help="Chatroom ID (e.g. 12345678@chatroom)")
    parser.add_argument("--name", required=True, help="Group display name")
    parser.add_argument("--summary-prompt", required=True, help="Prompt for AI summarization")
    parser.add_argument("--members", help="Path to members.json (generated by convert_members.py)")
    parser.add_argument("--subscribe-members", nargs="+", help="wxid list of members to follow")
    parser.add_argument("--update", action="store_true", help="Update existing group instead of failing")
    args = parser.parse_args()

    try:
        with open(args.yaml_file, "r") as f:
            config = yaml.safe_load(f) or {}
    except FileNotFoundError:
        config = {}

    if "groups" not in config:
        config["groups"] = []

    if args.host:
        config["host"] = args.host

    existing = None
    for g in config["groups"]:
        if g.get("talker") == args.talker:
            existing = g
            break

    if existing and not args.update:
        print(f"Error: talker {args.talker} already exists in {args.yaml_file} (use --update to modify)", file=sys.stderr)
        sys.exit(1)

    if existing:
        existing["name"] = args.name
        existing["summary_prompt"] = args.summary_prompt
        if args.members:
            existing["members"] = args.members
        if args.subscribe_members:
            existing["subscribe_members"] = args.subscribe_members
        print(f"Updated group '{args.name}' [{args.talker}] in {args.yaml_file}")
    else:
        group = {
            "talker": args.talker,
            "name": args.name,
            "summary_prompt": args.summary_prompt,
            "last_local_id": 0,
        }
        if args.members:
            group["members"] = args.members
        if args.subscribe_members:
            group["subscribe_members"] = args.subscribe_members
        config["groups"].append(group)
        print(f"Added group '{args.name}' [{args.talker}] to {args.yaml_file}")

    with open(args.yaml_file, "w") as f:
        yaml.dump(config, f, allow_unicode=True, default_flow_style=False, sort_keys=False)


if __name__ == "__main__":
    main()
