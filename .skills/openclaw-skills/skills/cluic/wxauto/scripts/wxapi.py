# Defaults
DEFAULT_PORT = 8000
DEFAULT_HOST = "localhost"
DEFAULT_TOKEN = "token"

# Resolved values
BASE_URL = None
TOKEN = None
SERVICE_DIR = None
SERVICE_STATUS_FILE = Path.home() / ".wxautox" / "service_status.json"

# Possible service directories to search
SERVICE_SEARCH_PATHS = [
    Path(__file__).parent.parent.parent / "wxauto-restful-api",  # sibling of skill dir
    Path(__file__).parent.parent / "wxauto-restful-api",  # nested in skill dir
    Path.home() / "wxauto-restful-api",  # in home directory
]

def find_service_dir():
    """Find the service directory."""
    # Check environment variable first
    env_dir = os.environ.get('WXAPI_SERVICE_DIR')
    if env_dir:
        env_path = Path(env_dir)
        if env_path.exists() and (env_path / "run.py").exists():
            return env_path
    
    # Check default search paths
    for path in SERVICE_SEARCH_PATHS:
        if path.exists() and (path / "run.py").exists():
            return path
    return None

def check_service_alive(base_url):
    """Check if service is responding."""
    try:
        resp = requests.get(f"{base_url}/", timeout=3)
        return resp.status_code == 200
    except Exception:
        return False

def start_service(service_dir):
    """Start the wxauto-restful-api service."""
    if not service_dir or not service_dir.exists():
        print(f"Error: Service directory not found")
        print(f"  Clone from: https://github.com/cluic/wxauto-restful-api")
        return False
    
    run_py = service_dir / "run.py"
    if not run_py.exists():
        print(f"Error: run.py not found in {service_dir}")
        return False
    
    print(f"Starting service from {service_dir}...")
    try:
        # Set environment for proper encoding
        env = os.environ.copy()
        env['PYTHONIOENCODING'] = 'utf-8'
        
        # Start service in background
        if sys.platform == "win32":
            subprocess.Popen(
                [sys.executable, "run.py"],
                cwd=str(service_dir),
                env=env,
                creationflags=subprocess.CREATE_NO_WINDOW | subprocess.DETACHED_PROCESS
            )
        else:
            subprocess.Popen(
                [sys.executable, "run.py"],
                cwd=str(service_dir),
                env=env,
                stdout=subprocess.DEVNULL,
                stderr=subprocess.DEVNULL,
                start_new_session=True
            )
        
        # Wait for service to start (up to 15 seconds)
        import time
        for i in range(15):
            time.sleep(1)
            # Re-read service_status.json
            if SERVICE_STATUS_FILE.exists():
                with open(SERVICE_STATUS_FILE, 'r', encoding='utf-8') as f:
                    status = json.load(f)
                    if status.get('status') == 'running':
                        port = status.get('port', DEFAULT_PORT)
                        host = '127.0.0.1' if status.get('host') == '0.0.0.0' else status.get('host', DEFAULT_HOST)
                        test_url = f"http://{host}:{port}"
                        if check_service_alive(test_url):
                            print(f"[OK] Service started on {test_url}")
                            return True
        
        print("Error: Service failed to start within 15 seconds")
        return False
    except Exception as e:
        print(f"Error starting service: {e}")
        return False

def resolve_config():
    """Resolve configuration from multiple sources."""
    global BASE_URL, TOKEN, SERVICE_DIR
    
    # Find service directory first
    SERVICE_DIR = find_service_dir()
    
    # 1. Try environment variables first
    base_url = os.environ.get('WXAPI_BASE_URL')
    token = os.environ.get('WXAPI_TOKEN')
    
    # 2. If no base_url, try WXAPI_PORT
    if not base_url:
        port = os.environ.get('WXAPI_PORT')
        host = os.environ.get('WXAPI_HOST', 'localhost')
        if port:
            base_url = f"http://{host}:{port}"
    
    # 3. Try service_status.json (auto-generated by wxauto-restful-api)
    service_was_started = False
    if not base_url:
        try:
            if SERVICE_STATUS_FILE.exists():
                with open(SERVICE_STATUS_FILE, 'r', encoding='utf-8') as f:
                    status = json.load(f)
                    
                    # Check if status says running
                    if status.get('status') == 'running':
                        port = status.get('port', DEFAULT_PORT)
                        host = '127.0.0.1' if status.get('host') == '0.0.0.0' else status.get('host', DEFAULT_HOST)
                        candidate_url = f"http://{host}:{port}"
                        
                        # Verify service is actually responding
                        if check_service_alive(candidate_url):
                            base_url = candidate_url
                        else:
                            # Status file says running but service not responding
                            # Try to start service
                            print(f"Service status file exists but service not responding")
                            if start_service(SERVICE_DIR):
                                service_was_started = True
                                # Re-read status file
                                if SERVICE_STATUS_FILE.exists():
                                    with open(SERVICE_STATUS_FILE, 'r', encoding='utf-8') as f:
                                        status = json.load(f)
                                        if status.get('status') == 'running':
                                            port = status.get('port', DEFAULT_PORT)
                                            host = '127.0.0.1' if status.get('host') == '0.0.0.0' else status.get('host', DEFAULT_HOST)
                                            base_url = f"http://{host}:{port}"
        except Exception as e:
            pass
    
    # 4. If still no base_url, try to start service
    if not base_url and SERVICE_DIR:
        print("No running service found")
        if start_service(SERVICE_DIR):
            service_was_started = True
            # Re-read status file
            try:
                if SERVICE_STATUS_FILE.exists():
                    with open(SERVICE_STATUS_FILE, 'r', encoding='utf-8') as f:
                        status = json.load(f)
                        if status.get('status') == 'running':
                            port = status.get('port', DEFAULT_PORT)
                            host = '127.0.0.1' if status.get('host') == '0.0.0.0' else status.get('host', DEFAULT_HOST)
                            base_url = f"http://{host}:{port}"
            except Exception:
                pass
    
    # 5. Try to load from config.yaml
    if not base_url or not token:
        try:
            import yaml
            
            config_paths = [
                SERVICE_DIR / "config.yaml" if SERVICE_DIR else None,
                Path(__file__).parent.parent / "wxauto-restful-api" / "config.yaml",
                os.environ.get('WXAPI_CONFIG', ''),
            ]
            
            for config_path in config_paths:
                if config_path and Path(config_path).exists():
                    with open(config_path, 'r', encoding='utf-8') as f:
                        config = yaml.safe_load(f)
                        if config:
                            if not base_url:
                                port = config.get('server', {}).get('port', DEFAULT_PORT)
                                host = config.get('server', {}).get('host', DEFAULT_HOST)
                                host = '127.0.0.1' if host == '0.0.0.0' else host
                                base_url = f"http://{host}:{port}"
                            if not token:
                                token = config.get('auth', {}).get('token', DEFAULT_TOKEN)
                        break
        except Exception:
            pass
    
    # 6. Apply defaults
    if not base_url:
        base_url = f"http://{DEFAULT_HOST}:{DEFAULT_PORT}"
    if not token:
        token = DEFAULT_TOKEN
    
    BASE_URL = base_url
    TOKEN = token

resolve_config()

def get_headers():
    return {
        "Authorization": f"Bearer {TOKEN}",
        "Content-Type": "application/json"
    }

def call_api(endpoint, data=None, method="POST"):
    """Call API endpoint."""
    url = f"{BASE_URL}{endpoint}"
    try:
        if method == "GET":
            resp = requests.get(url, headers=get_headers(), timeout=10)
        else:
            resp = requests.post(url, headers=get_headers(), json=data or {}, timeout=10)
        return resp.json()
    except requests.exceptions.ConnectionError:
        print("Error: Cannot connect to service. Start with: python run.py")
        return None
    except Exception as e:
        print(f"Error: {e}")
        return None

