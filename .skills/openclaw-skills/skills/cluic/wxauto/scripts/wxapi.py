def cmd_send(args):
    """Send message via main window."""
    data = {"who": args.who, "msg": args.message}
    if args.exact:
        data["exact"] = True
    if args.at:
        data["at"] = args.at.split(",")
    
    result = call_api("/v1/wechat/send", data)
    if result and result.get('success'):
        print(f"[OK] Message sent to {args.who}")
    elif result:
        print(f"[FAIL] {result.get('message', 'Unknown error')}")
    else:
        print("[FAIL] API call failed")

