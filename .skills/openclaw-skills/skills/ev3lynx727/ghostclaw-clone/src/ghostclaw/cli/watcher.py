#!/usr/bin/env python3
"""
Ghostclaw Watcher — monitor multiple repositories for architectural decay.

Usage:
  ghostclaw-watcher --repos-file repos.txt [--dry-run] [--create-pr] [--notify]
"""

import argparse
import subprocess
import sys
from pathlib import Path
from typing import Dict, List

from dotenv import load_dotenv
from ghostclaw.core.analyzer import CodebaseAnalyzer
from ghostclaw.lib.cache import VibeCache
from ghostclaw.lib.github import GitHubClient
from ghostclaw.lib.notify import Notifier

# Load .env file if present (secrets management)
load_dotenv()


def clone_or_pull(repo_url: str, dest_dir: Path) -> bool:
    """Clone a repo if not exists, otherwise pull latest. Handles local paths for testing."""
    # If dest_dir already exists and is a git repo, assume it's up-to-date or we'll use it directly
    if dest_dir.exists() and (dest_dir / ".git").exists():
        try:
            subprocess.run(
                ["git", "-C", str(dest_dir), "pull", "--rebase", "--quiet"],
                check=True,
                capture_output=True
            )
            return True
        except subprocess.CalledProcessError as e:
            print(f"Failed to pull {repo_url} into {dest_dir}: {e}", file=sys.stderr)
            return False
    # If dest_dir doesn't exist, we need to clone
    elif not dest_dir.exists():
        try:
            # If repo_url is a local path (not a URL), copy it instead of git clone
            repo_path = Path(repo_url)
            if repo_path.exists() and repo_path.is_dir():
                import shutil
                shutil.copytree(repo_path, dest_dir)
                # Initialize as git repo if not already
                if not (dest_dir / ".git").exists():
                    subprocess.run(["git", "init"], cwd=dest_dir, check=True, capture_output=True)
                    subprocess.run(["git", "add", "-A"], cwd=dest_dir, check=True, capture_output=True)
                    subprocess.run(["git", "commit", "-m", "Initial"], cwd=dest_dir, check=True, capture_output=True)
                return True
            else:
                subprocess.run(
                    ["git", "clone", "--quiet", repo_url, str(dest_dir)],
                    check=True,
                    capture_output=True
                )
                return True
        except subprocess.CalledProcessError as e:
            print(f"Failed to prepare repository {repo_url}: {e}", file=sys.stderr)
            return False
    else:
        # dest_dir exists but isn't a git repo; weird state
        print(f"Destination exists but not a git repo: {dest_dir}", file=sys.stderr)
        return False


def analyze_repo(repo_path: Path) -> Dict:
    """Run ghostclaw analysis on a repository."""
    analyzer = CodebaseAnalyzer()
    return analyzer.analyze(str(repo_path))


def should_create_pr(delta: int, vibe_score: int, threshold_delta: int = -5, min_score: int = 50) -> bool:
    """
    Decide whether to open a PR based on score changes.
    - PR if vibe dropped by threshold_delta points (default -5)
    - PR if vibe below min_score regardless of delta (urgent)
    """
    if delta <= threshold_delta:
        return True
    if vibe_score < min_score:
        return True
    return False


def generate_pr_body(repo: str, old_score: int, new_score: int, report: Dict) -> str:
    """Generate a PR description with findings and suggestions."""
    delta = new_score - old_score
    direction = "improved" if delta > 0 else "declined" if delta < 0 else "unchanged"

    body = f"""## 🏰 Ghostclaw Architectural Health Report

**Repository:** {repo}
**Action:** Vibe score {direction} from {old_score} → {new_score} ({delta:+d})

### Current Issues

"""
    issues = report.get('issues', [])
    if issues:
        for issue in issues[:15]:
            body += f"- {issue}\n"
        if len(issues) > 15:
            body += f"- ...and {len(issues) - 15} more (see full report)\n"
    else:
        body += "No significant issues detected. Great! ✅\n"

    body += """
### Recommended Refactorings

1. **Focus on high-impact modules** — check coupling metrics for unstable modules
2. **Split large files** to improve cohesion
3. **Fix circular dependencies** (if any)
4. **Follow stack conventions** — see references/stack-patterns.yaml

---

🤖 *Generated by [Ghostclaw](https://github.com/Ev3lynx727/ghostclaw) — your architectural sentinel*
"""
    return body


def main():
    parser = argparse.ArgumentParser(description="Ghostclaw Watcher")
    parser.add_argument(
        "--repos-file",
        required=True,
        help="Path to file containing repository URLs (one per line)"
    )
    parser.add_argument(
        "--work-dir",
        default="/tmp/ghostclaw-watcher",
        help="Directory to clone repositories into"
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Analyze but don't create PRs or send notifications"
    )
    parser.add_argument(
        "--create-pr",
        action="store_true",
        help="Create GitHub PRs with suggestions (requires gh auth)"
    )
    parser.add_argument(
        "--notify",
        action="store_true",
        help="Send notifications (Telegram if configured)"
    )
    parser.add_argument(
        "--telegram-token",
        help="Telegram bot token"
    )
    parser.add_argument(
        "--telegram-chat-id",
        help="Telegram chat ID"
    )
    parser.add_argument(
        "--gh-token",
        help="GitHub token (or set GH_TOKEN env)"
    )

    args = parser.parse_args()

    # Initialize components
    repos_file = Path(args.repos_file)
    if not repos_file.exists():
        print(f"Error: repos file not found: {repos_file}", file=sys.stderr)
        sys.exit(1)

    work_dir = Path(args.work_dir)
    work_dir.mkdir(parents=True, exist_ok=True)

    cache = VibeCache()
    github = GitHubClient(token=args.gh_token) if args.gh_token else GitHubClient()
    notifier = Notifier(
        telegram_token=args.telegram_token,
        telegram_chat_id=args.telegram_chat_id
    )

    # Read repository list
    repos = [line.strip() for line in repos_file.read_text().splitlines() if line.strip() and not line.startswith('#')]
    print(f"👻 Ghostclaw watcher starting: {len(repos)} repositories")

    for repo_url in repos:
        print(f"\n--- Processing {repo_url} ---")

        # Determine repo name for cloning
        repo_name = repo_url.rstrip('/').split('/')[-1]
        repo_dir = work_dir / repo_name

        # Clone or pull
        if not clone_or_pull(repo_url, repo_dir):
            continue

        # Analyze
        report = analyze_repo(repo_dir)
        vibe_score = report['vibe_score']
        issues = report.get('issues', [])

        print(f"Vibe Score: {vibe_score}/100, Issues: {len(issues)}")

        # Cache old score
        old_score = cache.get_latest_score(repo_url)
        delta = vibe_score - old_score if old_score is not None else 0
        cache.record_score(repo_url, vibe_score, metadata={
            "stack": report.get('stack'),
            "files_analyzed": report.get('files_analyzed')
        })

        # Decision: create PR?
        if args.create_pr and github.can_open_prs():
            if should_create_pr(delta, vibe_score):
                print(f"🔨 Would create PR: {repo_url} (score changed by {delta:+d})")
                if not args.dry_run:
                    branch_name = f"ghostclaw-improve-{vibe_score}"
                    pr_body = generate_pr_body(repo_url, old_score or 0, vibe_score, report)
                    pr_url = github.create_pr(
                        repo=repo_url,
                        title=f"🏰 Architectural improvements (vibe {vibe_score}/100)",
                        body=pr_body,
                        head=branch_name,
                        base="main",
                        draft=True
                    )
                    if pr_url:
                        print(f"✅ PR created: {pr_url}")
                    else:
                        print("❌ PR creation failed")
            else:
                print("No PR needed (score stable or improved)")

        # Notification
        if args.notify:
            notifier.notify(
                repo=repo_url,
                vibe_score=vibe_score,
                delta=delta,
                issues=issues[:10],
                pr_url=pr_url if 'pr_url' in locals() else None
            )

    print("\n✅ Watcher cycle complete")


if __name__ == "__main__":
    main()
