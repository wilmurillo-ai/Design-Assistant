echo "╚══════════════════════════════════════════════════════════╝"
echo ""

cat <<'EOF'
📋 测评说明
────────────────────────────────────────
以下列出了有些人可能会有的问题，请仔细阅读每一条，
然后根据最近一星期以内下述情况影响您的实际感觉，
在5个答案中选择一个。

评分标准:
0 = 没有 - 自觉无该项症状
1 = 很轻 - 自觉有该项症状，但影响轻微
2 = 中等 - 自觉有该项症状，有一定影响
3 = 偏重 - 自觉常有该项症状，有相当程度影响
4 = 严重 - 自觉该症状十分严重，影响严重

注意事项:
• 请根据您的真实感觉作答
• 没有所谓"正确"答案
• 完成大约需要10-15分钟

EOF

read -p "按 Enter 开始测评 (或输入 q 退出)... " start_input
if [[ "$start_input" == "q" || "$start_input" == "Q" ]]; then
echo "已退出测评。"
return
fi

echo ""
echo "开始测评..."
echo ""

local answers=()
local total=0

for i in {1..90}; do
local question="题目 $i"
echo "────────────────────────────────────────"
echo "题目 $i / 90"
echo ""
echo " $question"
echo ""
echo " 0=没有 1=很轻 2=中等 3=偏重 4=严重"
echo ""

while true; do
read -p " 您的选择 (0-4): " answer
if [[ "$answer" =~ ^[0-4]$ ]]; then
answers+=("$answer")
total=$((total + answer))
break
else
echo " 请输入 0-4 之间的数字"
fi
done
done

echo ""
echo "────────────────────────────────────────"
echo "测评完成！正在计算结果..."
echo ""
sleep 1

show_results "${answers[@]}"
}

show_results() {
local answers=("$@")

echo "╔══════════════════════════════════════════════════════════╗"
echo "║ 📊 SCL-90 测评结果报告 ║"
echo "╚══════════════════════════════════════════════════════════╝"
echo ""

local total_score=0
for score in "${answers[@]}"; do
total_score=$((total_score + score))
done

local positive_items=0
for score in "${answers[@]}"; do
if [ "$score" -ge 2 ]; then
positive_items=$((positive_items + 1))
fi
done

local avg_score=$(echo "scale=2; $total_score / 90" | bc)
local positive_avg=0
if [ "$positive_items" -gt 0 ]; then
positive_avg=$(echo "scale=2; $total_score / $positive_items" | bc)
fi

cat <<EOF
📋 总体指标
────────────────────────────────────────
总分: $total_score / 360
总均分: $avg_score
阳性项目数: $positive_items / 90
阳性症状均分: $positive_avg

EOF

local crisis_flag=0
if [ "$total_score" -ge 160 ]; then
crisis_flag=1
echo " ⚠️ 总分超过分界值 (≥160)"
fi
if [ "$positive_items" -ge 43 ]; then
crisis_flag=1
echo " ⚠️ 阳性项目数超过分界值 (≥43)"
fi

echo ""
echo "📊 因子分析"
echo "────────────────────────────────────────"

echo " [由于这是演示版本，详细因子分析功能需要完整实现]"
echo ""
echo " 完整版本将包含:"
echo " • 10个因子的详细得分"
echo " • 与中国常模的对比"
echo " • 可视化表示 (🟢正常/🟡边缘/🔴异常)"
echo " • 针对性的专业建议"

echo ""
echo "────────────────────────────────────────"

if [ "$crisis_flag" -eq 1 ]; then
echo "⚠️ 测评结果显示可能需要关注"
echo " 建议寻求专业心理咨询帮助"
else
echo "✅ 总体指标在正常范围内"
echo " 如有困扰，仍可寻求专业支持"
fi

echo ""
disclaimer

local timestamp=$(date +%Y%m%d_%H%M%S)
local result_file="$HISTORY_DIR/result_$timestamp.txt"

echo "保存结果到: $result_file"
{
echo "SCL-90 测评结果 - $(date)"
echo "总分: $total_score"
echo "阳性项目数: $positive_items"
echo "-------------------"
echo "答案: ${answers[*]}"
} > "$result_file"
}

