class CodeGenerator:
    """Generate production-ready boilerplate code."""
    
    def __init__(self, output_dir: str = "."):
        self.output_dir = Path(output_dir)
    
    def scaffold(self, language: str, name: str, template: str = "api",
                 features: List[str] = None, description: str = "") -> Path:
        """Create project scaffold."""
        
        if language != "python":
            print(f"Warning: Only Python scaffolding is implemented. Creating Python project.")
        
        project_dir = self.output_dir / name
        project_dir.mkdir(parents=True, exist_ok=True)
        
        # Create directories
        for dir_name in ["src", "tests", "docs"]:
            (project_dir / dir_name).mkdir(exist_ok=True)
        
        # Create files
        (project_dir / "README.md").write_text(f"# {name}\n\n{description or 'A Python project'}\n")
        (project_dir / "pyproject.toml").write_text(f'''[project]
name = "{name}"
version = "0.1.0"
description = "{description or 'A Python project'}"

[build-system]
requires = ["setuptools"]
build-backend = "setuptools.build_meta"
''')
        (project_dir / "src" / "__init__.py").write_text(f'"""{name} - {description or 'A Python project'}"""\n')
        (project_dir / "tests" / "__init__.py").write_text("")
        (project_dir / ".gitignore").write_text("__pycache__/\n*.py[cod]\n.env\n.venv/\n")
        
        print(f"✓ Created {language} project: {name}")
        print(f"  Directory: {project_dir}")
        
        return project_dir
    
    def crud(self, model_name: str, fields: Dict[str, str],
             language: str = "python", database: str = "sql") -> str:
        """Generate CRUD operations."""
        
        if language == "python":
            return python_crud_template(model_name, fields, database)
        else:
            return f"# CRUD for {language} not yet implemented"
    
    def model(self, model_name: str, fields: Dict[str, str],
              orm: str = "sqlalchemy") -> str:
        """Generate database model."""
        
        if orm == "sqlalchemy":
            return python_model_template(model_name, fields)
        else:
            return f"# Model for {orm} not yet implemented"
    
    def test(self, source_dir: str, test_type: str = "unit",
             framework: str = "pytest") -> str:
        """Generate test templates."""
        
        source_path = Path(source_dir)
        if not source_path.exists():
            return f"Source directory not found: {source_dir}"
        
        # Find Python files
        py_files = list(source_path.glob("**/*.py"))
        test_content = []
        
        for py_file in py_files[:10]:
            module_name = str(py_file.with_suffix("")).replace("/", ".")
            test_content.append(python_test_template(module_name))
        
        return "\n\n".join(test_content)
    
    def config(self, config_type: str, language: str = "python") -> str:
        """Generate configuration files."""
        
        if config_type == "docker":
            return f'''FROM python:3.11-slim

WORKDIR /app

COPY requirements.txt .
RUN pip install --no-cache-dir -r requirements.txt

COPY . .

CMD ["python", "main.py"]
'''
        elif config_type == "ci":
            return f'''name: CI

on: [push, pull_request]

jobs:
  test:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4
      - uses: actions/setup-python@v5
        with:
          python-version: '3.11'
      - run: pip install -r requirements.txt
      - run: pytest
'''
        else:
            return f"# Config for {config_type} not yet implemented"


def parse_fields(fields_str: str) -> Dict[str, str]:
    """Parse fields string into dictionary."""
    
    fields = {}
    if not fields_str:
        return fields
    
    for field in fields_str.split(","):
        field = field.strip()
        if ":" in field:
            name, ftype = field.split(":", 1)
            fields[name.strip()] = ftype.strip()
        else:
            fields[field] = "str"
    
    return fields


def main():
    """CLI entry point."""
    
    import argparse
    
    parser = argparse.ArgumentParser(description="Code Generator")
    subparsers = parser.add_subparsers(dest='command', help='Command')
    
    # scaffold command
    scaffold_parser = subparsers.add_parser('scaffold', help='Create project scaffold')
    scaffold_parser.add_argument('language', help='Language (python, typescript, go)')
    scaffold_parser.add_argument('name', help='Project name')
    scaffold_parser.add_argument('--template', default='api', help='Template variant')
    scaffold_parser.add_argument('--features', help='Comma-separated features')
    scaffold_parser.add_argument('--output', default='.', help='Output directory')
    
    # crud command
    crud_parser = subparsers.add_parser('crud', help='Generate CRUD operations')
    crud_parser.add_argument('model', help='Model name')
    crud_parser.add_argument('--fields', help='Comma-separated fields (name:type)')
    crud_parser.add_argument('--language', default='python', help='Target language')
    crud_parser.add_argument('--database', default='sql', help='Database type')
    crud_parser.add_argument('--output', default='.', help='Output directory')
    
    # model command
    model_parser = subparsers.add_parser('model', help='Generate database model')
    model_parser.add_argument('name', help='Model name')
    model_parser.add_argument('--fields', help='Comma-separated fields (name:type)')
    model_parser.add_argument('--orm', default='sqlalchemy', help='ORM (sqlalchemy, prisma)')
    model_parser.add_argument('--output', default='.', help='Output directory')
    
    # test command
    test_parser = subparsers.add_parser('test', help='Generate tests')
    test_parser.add_argument('--source', default='./src', help='Source directory')
    test_parser.add_argument('--type', default='unit', help='Test type')
    test_parser.add_argument('--framework', default='pytest', help='Test framework')
    
    # config command
    config_parser = subparsers.add_parser('config', help='Generate configuration')
    config_parser.add_argument('type', choices=['docker', 'ci', 'lint'], help='Config type')
    config_parser.add_argument('--language', default='python', help='Language')
    
    args = parser.parse_args()
    
    generator = CodeGenerator(output_dir=getattr(args, 'output', '.'))
    
    if args.command == 'scaffold':
        features = args.features.split(',') if args.features else []
        generator.scaffold(args.language, args.name, template=args.template, features=features)
    
    elif args.command == 'crud':
        fields = parse_fields(args.fields or "name:str")
        code = generator.crud(args.model, fields, args.language, args.database)
        print(code)
    
    elif args.command == 'model':
        fields = parse_fields(args.fields or "name:str")
        code = generator.model(args.name, fields, args.orm)
        print(code)
    
    elif args.command == 'test':
        code = generator.test(args.source, args.type, args.framework)
        print(code)
    
    elif args.command == 'config':
        code = generator.config(args.type, args.language)
        print(code)
    
    else:
        parser.print_help()
        sys.exit(1)


if __name__ == "__main__":
    main()