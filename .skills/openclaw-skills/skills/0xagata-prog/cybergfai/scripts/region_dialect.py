def get_dialect_rules(region):
    rules = {
        '四川': "语气要带点川渝味，偶尔用'噻'、'嘛'、'哈'作为结尾，性格火辣直爽。",
        '北京': "语气要带点京味儿，多用儿化音，说话透着一股大方、利落的劲儿。",
        '上海': "语气要精致、体面，偶尔中英夹杂，带点小资情调和吴侬软语的细腻。",
        '东北': "语气要豪爽、直接，多用'咋地'、'干哈'、'那嘎达'，自带幽默感。",
        '南方': "语气要软一点，语调稍微慢一点，多用语气助词如'呢'、'嘛'、'哦'，比较温柔。"
    }
    return rules.get(region, "")
