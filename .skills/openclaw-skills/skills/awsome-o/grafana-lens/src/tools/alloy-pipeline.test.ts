import { describe, expect, test } from "vitest";
import {
  extractComponentIds,
  inferSignalFromConfig,
  redactSecrets,
  rawConfigSuggestedWorkflow,
  scanForCredentials,
} from "../alloy/pipeline-helpers.js";

describe("extractComponentIds", () => {
  test("extracts simple two-part component IDs", () => {
    const config = `
prometheus.scrape "my_scrape" {
  targets = []
}

prometheus.remote_write "my_write" {
  endpoint { url = "http://mimir:9009" }
}
`;
    const ids = extractComponentIds(config);
    expect(ids).toEqual([
      "prometheus.scrape.my_scrape",
      "prometheus.remote_write.my_write",
    ]);
  });

  test("extracts three-part component IDs (e.g., otelcol.receiver.otlp)", () => {
    const config = `
otelcol.receiver.otlp "default" {
  grpc { endpoint = "0.0.0.0:4317" }
}

otelcol.processor.batch "default" {
  output { traces = [] }
}

otelcol.exporter.otlphttp "tempo" {
  client { endpoint = "http://tempo:4318" }
}
`;
    const ids = extractComponentIds(config);
    expect(ids).toEqual([
      "otelcol.receiver.otlp.default",
      "otelcol.processor.batch.default",
      "otelcol.exporter.otlphttp.tempo",
    ]);
  });

  test("extracts four-part component IDs (e.g., prometheus.exporter.postgres)", () => {
    const config = `
prometheus.exporter.postgres "my_pg" {
  data_source_names = []
}
`;
    const ids = extractComponentIds(config);
    expect(ids).toEqual(["prometheus.exporter.postgres.my_pg"]);
  });

  test("handles mixed component types", () => {
    const config = `
discovery.docker "discover" {
  host = "unix:///var/run/docker.sock"
}

loki.source.docker "src" {
  host = "unix:///var/run/docker.sock"
  targets = discovery.docker.discover.targets
}

loki.write "writer" {
  endpoint { url = "http://loki:3100" }
}
`;
    const ids = extractComponentIds(config);
    expect(ids).toEqual([
      "discovery.docker.discover",
      "loki.source.docker.src",
      "loki.write.writer",
    ]);
  });

  test("returns empty array for config with no components", () => {
    const config = `// just a comment\n// no components here`;
    expect(extractComponentIds(config)).toEqual([]);
  });

  test("returns empty array for empty string", () => {
    expect(extractComponentIds("")).toEqual([]);
  });

  test("ignores inline references (not at start of line)", () => {
    const config = `
prometheus.scrape "main" {
  forward_to = [prometheus.remote_write.main.receiver]
}
`;
    const ids = extractComponentIds(config);
    // Should only find the block definition, not the inline reference
    expect(ids).toEqual(["prometheus.scrape.main"]);
  });

  test("handles labels with underscores and numbers", () => {
    const config = `loki.source.syslog "lens_abc123_syslog" {\n  listener {}\n}`;
    const ids = extractComponentIds(config);
    expect(ids).toEqual(["loki.source.syslog.lens_abc123_syslog"]);
  });

  test("handles config with header comments (generated by AlloyConfigBuilder)", () => {
    const config = `// Pipeline: my-pipeline (recipe: scrape-endpoint)
// Managed by openclaw-grafana-lens — do not edit manually.

prometheus.scrape "lens_a1b2_scrape" {
  targets = [{ "__address__" = "localhost:9090" }]
}

prometheus.remote_write "lens_a1b2_write" {
  endpoint { url = "http://mimir:9009/api/prom/push" }
}
`;
    const ids = extractComponentIds(config);
    expect(ids).toEqual([
      "prometheus.scrape.lens_a1b2_scrape",
      "prometheus.remote_write.lens_a1b2_write",
    ]);
  });

  test("handles tail_sampling config with nested blocks", () => {
    const config = `
otelcol.processor.tail_sampling "sampler" {
  decision_wait = "10s"
  policy {
    name = "probabilistic"
    type = "probabilistic"
    probabilistic {
      sampling_percentage = 50
    }
  }
  output {
    traces = [otelcol.processor.batch.default.input]
  }
}
`;
    const ids = extractComponentIds(config);
    expect(ids).toEqual(["otelcol.processor.tail_sampling.sampler"]);
  });
});

// ── inferSignalFromConfig ─────────────────────────────────────────

describe("inferSignalFromConfig", () => {
  test("detects logs from loki.source.file", () => {
    expect(inferSignalFromConfig('loki.source.file "my_logs" { }')).toBe("logs");
  });

  test("detects logs from loki.write", () => {
    expect(inferSignalFromConfig('loki.write "default" { endpoint { url = "..." } }')).toBe("logs");
  });

  test("detects logs from faro.receiver", () => {
    expect(inferSignalFromConfig('faro.receiver "frontend" { server { listen_port = 12347 } }')).toBe("logs");
  });

  test("detects traces from otelcol with traces output", () => {
    const config = `
otelcol.receiver.otlp "default" { }
otelcol.exporter.otlphttp "tempo" {
  client { endpoint = "http://tempo:4318" }
}
// wiring
traces = [otelcol.exporter.otlphttp.tempo.input]
`;
    expect(inferSignalFromConfig(config)).toBe("traces");
  });

  test("detects traces from tempo keyword", () => {
    expect(inferSignalFromConfig('// sends to tempo backend')).toBe("traces");
  });

  test("detects profiles from pyroscope.scrape", () => {
    expect(inferSignalFromConfig('pyroscope.scrape "golang_app" { targets = [] }')).toBe("profiles");
  });

  test("detects profiles from pyroscope.write", () => {
    expect(inferSignalFromConfig('pyroscope.write "default" { endpoint { url = "..." } }')).toBe("profiles");
  });

  test("defaults to metrics for prometheus components", () => {
    expect(inferSignalFromConfig('prometheus.scrape "main" { targets = [] }')).toBe("metrics");
  });

  test("defaults to metrics for empty config", () => {
    expect(inferSignalFromConfig("")).toBe("metrics");
  });

  test("loki wins over prometheus in mixed config", () => {
    const config = `
prometheus.scrape "scraper" { }
loki.source.file "logs" { }
`;
    expect(inferSignalFromConfig(config)).toBe("logs");
  });

  test("faro wins over prometheus in mixed config", () => {
    const config = `
prometheus.remote_write "default" { }
faro.receiver "frontend" { }
`;
    expect(inferSignalFromConfig(config)).toBe("logs");
  });
});

// ── redactSecrets ─────────────────────────────────────────────────

describe("redactSecrets", () => {
  test("redacts listed credential params", () => {
    const params = { connectionString: "postgres://user:pass@host/db", scrapeInterval: "15s" };
    const recipe = { credentialParams: ["connectionString"] };
    const result = redactSecrets(params, recipe);
    expect(result.connectionString).toBe("***REDACTED***");
    expect(result.scrapeInterval).toBe("15s");
  });

  test("preserves all params when no credentials listed", () => {
    const params = { url: "http://localhost:9090", interval: "30s" };
    const recipe = { credentialParams: [] as string[] };
    const result = redactSecrets(params, recipe);
    expect(result).toEqual(params);
  });

  test("skips credential param if value is falsy", () => {
    const params = { token: "", other: "kept" };
    const recipe = { credentialParams: ["token"] };
    const result = redactSecrets(params, recipe);
    expect(result.token).toBe("");
    expect(result.other).toBe("kept");
  });

  test("does not mutate original params object", () => {
    const params = { secret: "my-secret" };
    const recipe = { credentialParams: ["secret"] };
    redactSecrets(params, recipe);
    expect(params.secret).toBe("my-secret");
  });
});

// ── rawConfigSuggestedWorkflow ────────────────────────────────────

describe("rawConfigSuggestedWorkflow", () => {
  test("metrics signal suggests grafana_query", () => {
    const workflow = rawConfigSuggestedWorkflow("my-pipeline", "metrics");
    expect(workflow).toHaveLength(2);
    expect(workflow[0].tool).toBe("alloy_pipeline");
    expect(workflow[1].tool).toBe("grafana_query");
  });

  test("logs signal suggests grafana_query_logs", () => {
    const workflow = rawConfigSuggestedWorkflow("my-pipeline", "logs");
    expect(workflow[1].tool).toBe("grafana_query_logs");
  });

  test("traces signal suggests grafana_query_traces", () => {
    const workflow = rawConfigSuggestedWorkflow("my-pipeline", "traces");
    expect(workflow[1].tool).toBe("grafana_query_traces");
  });

  test("profiles signal suggests grafana_explore_datasources", () => {
    const workflow = rawConfigSuggestedWorkflow("my-pipeline", "profiles");
    expect(workflow[1].tool).toBe("grafana_explore_datasources");
  });

  test("uses first sample query when provided", () => {
    const queries = { upCheck: 'up{job="test"}', scrapeRate: "scrape_samples_scraped" };
    const workflow = rawConfigSuggestedWorkflow("my-pipeline", "metrics", queries);
    expect(workflow[1].example).toEqual({ expr: 'up{job="test"}' });
  });

  test("uses placeholder when no sample queries", () => {
    const workflow = rawConfigSuggestedWorkflow("my-pipeline", "metrics");
    expect(workflow[1].example).toEqual({ expr: 'up{job="..."}' });
  });

  test("first step is always alloy_pipeline status check", () => {
    const workflow = rawConfigSuggestedWorkflow("test-pipe", "logs");
    expect(workflow[0]).toEqual({
      tool: "alloy_pipeline",
      action: "Verify data is flowing",
      example: { action: "status", name: "test-pipe" },
    });
  });
});

// ── scanForCredentials ──────────────────────────────────────────────

describe("scanForCredentials", () => {
  test("detects postgres URI with embedded credentials", () => {
    const config = `prometheus.exporter.postgres "db" {
  data_source_names = ["postgres://admin:secret123@db:5432/mydb"]
}`;
    const warnings = scanForCredentials(config);
    expect(warnings).toHaveLength(1);
    expect(warnings[0]).toContain("postgres://");
    expect(warnings[0]).toContain("sys.env");
  });

  test("detects mysql URI with embedded credentials", () => {
    const warnings = scanForCredentials('connection_string = "mysql://root:pass@db:3306/app"');
    expect(warnings).toHaveLength(1);
    expect(warnings[0]).toContain("mysql://");
  });

  test("detects redis URI with embedded credentials", () => {
    const warnings = scanForCredentials('address = "redis://user:pw@redis:6379"');
    expect(warnings).toHaveLength(1);
    expect(warnings[0]).toContain("redis://");
  });

  test("does NOT flag sys.env() wrapped credentials", () => {
    const config = `prometheus.exporter.postgres "db" {
  data_source_names = [sys.env("MY_DSN")]
}`;
    const warnings = scanForCredentials(config);
    expect(warnings).toHaveLength(0);
  });

  test("detects bare password string value", () => {
    const config = `password = "my-secret-password"`;
    const warnings = scanForCredentials(config);
    expect(warnings).toHaveLength(1);
    expect(warnings[0]).toContain("password");
  });

  test("does NOT flag password using sys.env()", () => {
    const config = `password = sys.env("MY_PASSWORD")`;
    const warnings = scanForCredentials(config);
    expect(warnings).toHaveLength(0);
  });

  test("returns empty array for clean config", () => {
    const config = `prometheus.scrape "main" {
  targets = [{ "__address__" = "localhost:9090" }]
  scrape_interval = "15s"
}`;
    const warnings = scanForCredentials(config);
    expect(warnings).toHaveLength(0);
  });

  test("detects both URI and keyword credentials", () => {
    const config = `
connection = "postgres://admin:pass@db:5432/app"
token = "sk-1234567890"
`;
    const warnings = scanForCredentials(config);
    expect(warnings).toHaveLength(2);
    expect(warnings[0]).toContain("postgres://");
    expect(warnings[1]).toContain("token");
  });
});
