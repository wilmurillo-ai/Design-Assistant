#!/usr/bin/env python3
"""Claude Code statusline installer: install, configure, and manage the statusline.

Subcommands:
  install     - Install statusline script and configure Claude Code settings
  uninstall   - Remove statusline script and revert settings
  status      - Show current installation state
  configure   - Update statusline config (thresholds, colors, display mode)
"""

import argparse
import json
import os
import shutil
import subprocess
import sys
from pathlib import Path

CLAUDE_DIR = Path.home() / ".claude"
CONFIG_FILE = CLAUDE_DIR / "statusline.config"
SCRIPT_DIR = CLAUDE_DIR / "scripts"
STATUSLINE_SCRIPT = SCRIPT_DIR / "statusline.py"
SETTINGS_LOCAL = CLAUDE_DIR / "settings.local.json"
SETTINGS_JSON = CLAUDE_DIR / "settings.json"

# Bundled statusline.py location (same directory as this script)
BUNDLED_SCRIPT = Path(__file__).resolve().parent / "statusline.py"

VALID_COLORS = {
    "green", "yellow", "orange", "red", "blue", "cyan",
    "magenta", "purple", "white", "pink",
    "bright-green", "bright-yellow", "bright-red",
    "bright-blue", "bright-cyan", "bright-magenta",
}

COLOR_TO_ANSI = {
    "green": "32", "yellow": "33", "orange": "38;5;208", "red": "31",
    "blue": "34", "cyan": "36", "magenta": "35", "purple": "35",
    "white": "37", "pink": "38;5;213",
    "bright-green": "92", "bright-yellow": "93", "bright-red": "91",
    "bright-blue": "94", "bright-cyan": "96", "bright-magenta": "95",
}

DEFAULT_CONFIG = {
    "TOKEN_DISPLAY": "separate",
    "THRESHOLD_YELLOW": "40",
    "THRESHOLD_ORANGE": "50",
    "THRESHOLD_RED": "70",
    "COLOR_GREEN": "\\033[32m",
    "COLOR_YELLOW": "\\033[33m",
    "COLOR_ORANGE": "\\033[38;5;208m",
    "COLOR_RED": "\\033[31m",
}


def check_jq():
    """Check if jq is installed. Returns True if available."""
    return shutil.which("jq") is not None


def validate_threshold(value):
    """Validate threshold is an integer 0-100. Returns int or raises ValueError."""
    n = int(value)
    if not 0 <= n <= 100:
        raise ValueError(f"Threshold must be 0-100, got {n}")
    return n


def validate_color(value):
    """Validate color is a known name or numeric ANSI code. Returns ANSI escape string."""
    if value in COLOR_TO_ANSI:
        return f"\\033[{COLOR_TO_ANSI[value]}m"
    # Accept raw numeric codes like "32" or "38;5;208"
    parts = value.split(";")
    if all(p.isdigit() for p in parts):
        return f"\\033[{value}m"
    raise ValueError(
        f"Unknown color '{value}'. Use a name ({', '.join(sorted(VALID_COLORS))}) "
        f"or ANSI code (e.g. 32, 38;5;208)"
    )


def load_settings(path):
    """Load a JSON settings file. Returns dict (empty if missing/invalid)."""
    if not path.exists():
        return {}
    try:
        return json.loads(path.read_text())
    except (json.JSONDecodeError, OSError):
        return {}


def save_settings(path, data):
    """Write JSON settings file with secure permissions."""
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(data, indent=2) + "\n")
    os.chmod(str(path), 0o600)


def write_config(values, path=None):
    """Write statusline config file with secure permissions."""
    path = path or CONFIG_FILE
    lines = [
        "# Claude Code Status Line Configuration",
        f"# Generated by statusline_installer.py",
        "",
        "# Display format: separate or combined",
        f"TOKEN_DISPLAY={values.get('TOKEN_DISPLAY', DEFAULT_CONFIG['TOKEN_DISPLAY'])}",
        "",
        "# Warning thresholds (percentage of context window)",
        f"THRESHOLD_YELLOW={values.get('THRESHOLD_YELLOW', DEFAULT_CONFIG['THRESHOLD_YELLOW'])}",
        f"THRESHOLD_ORANGE={values.get('THRESHOLD_ORANGE', DEFAULT_CONFIG['THRESHOLD_ORANGE'])}",
        f"THRESHOLD_RED={values.get('THRESHOLD_RED', DEFAULT_CONFIG['THRESHOLD_RED'])}",
        "",
        "# Custom colors (ANSI escape codes)",
        f"COLOR_GREEN={values.get('COLOR_GREEN', DEFAULT_CONFIG['COLOR_GREEN'])}",
        f"COLOR_YELLOW={values.get('COLOR_YELLOW', DEFAULT_CONFIG['COLOR_YELLOW'])}",
        f"COLOR_ORANGE={values.get('COLOR_ORANGE', DEFAULT_CONFIG['COLOR_ORANGE'])}",
        f"COLOR_RED={values.get('COLOR_RED', DEFAULT_CONFIG['COLOR_RED'])}",
        "",
    ]
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text("\n".join(lines))
    os.chmod(str(path), 0o600)


def read_config(path=None):
    """Read existing statusline config. Returns dict of key=value pairs."""
    path = path or CONFIG_FILE
    if not path.exists():
        return dict(DEFAULT_CONFIG)
    values = dict(DEFAULT_CONFIG)
    for line in path.read_text().splitlines():
        line = line.strip()
        if not line or line.startswith("#"):
            continue
        if "=" in line:
            key, _, val = line.partition("=")
            key = key.strip()
            val = val.strip().strip("'\"")
            if key in DEFAULT_CONFIG:
                values[key] = val
    return values


def get_statusline_command():
    """Return the command string for settings.json (python3 path/to/statusline.py)."""
    return f"python3 {STATUSLINE_SCRIPT}"


def update_settings_file(settings_path):
    """Add statusLine config to a Claude Code settings file. Returns True if updated."""
    data = load_settings(settings_path)
    cmd = get_statusline_command()
    current = data.get("statusLine", {}).get("command")
    if current == cmd:
        return False  # Already configured
    data["statusLine"] = {
        "type": "command",
        "command": cmd,
    }
    save_settings(settings_path, data)
    return True


def remove_from_settings(settings_path):
    """Remove statusLine config from a settings file. Returns True if removed."""
    if not settings_path.exists():
        return False
    data = load_settings(settings_path)
    if "statusLine" not in data:
        return False
    del data["statusLine"]
    save_settings(settings_path, data)
    return True


def cmd_install(args):
    """Install statusline script and configure Claude Code."""
    # 1. Check bundled script exists
    if not BUNDLED_SCRIPT.exists():
        print(f"Error: statusline.py not found at {BUNDLED_SCRIPT}", file=sys.stderr)
        sys.exit(1)

    # 2. Remove old artifacts
    old_bash_script = SCRIPT_DIR / "statusline.sh"
    if old_bash_script.exists():
        old_bash_script.unlink()
        print(f"Removed old bash script: {old_bash_script}")

    # Clean up settings.local.json (remove stale entries)
    if SETTINGS_LOCAL.exists():
        local_data = load_settings(SETTINGS_LOCAL)
        cleaned = False
        if "statusline_script" in local_data:
            del local_data["statusline_script"]
            cleaned = True
        if "statusLine" in local_data:
            del local_data["statusLine"]
            cleaned = True
        if cleaned:
            save_settings(SETTINGS_LOCAL, local_data)
            print(f"Cleaned old entries from: {SETTINGS_LOCAL.name}")

    # 3. Create scripts directory
    SCRIPT_DIR.mkdir(parents=True, exist_ok=True)
    print(f"Scripts directory: {SCRIPT_DIR}")

    # 4. Copy statusline.py
    shutil.copy2(str(BUNDLED_SCRIPT), str(STATUSLINE_SCRIPT))
    os.chmod(str(STATUSLINE_SCRIPT), 0o755)
    print(f"Installed: {STATUSLINE_SCRIPT}")

    # 5. Create config with defaults (or provided values)
    config = dict(DEFAULT_CONFIG)
    if args.token_display:
        config["TOKEN_DISPLAY"] = args.token_display
    if args.threshold_yellow is not None:
        config["THRESHOLD_YELLOW"] = str(args.threshold_yellow)
    if args.threshold_orange is not None:
        config["THRESHOLD_ORANGE"] = str(args.threshold_orange)
    if args.threshold_red is not None:
        config["THRESHOLD_RED"] = str(args.threshold_red)

    # Validate threshold ordering
    y = int(config["THRESHOLD_YELLOW"])
    o = int(config["THRESHOLD_ORANGE"])
    r = int(config["THRESHOLD_RED"])
    if not (y <= o <= r):
        print(f"Error: thresholds must be ascending: yellow({y}) <= orange({o}) <= red({r})",
              file=sys.stderr)
        sys.exit(1)

    write_config(config)
    print(f"Config: {CONFIG_FILE}")

    # 6. Update settings.json (not settings.local.json)
    if update_settings_file(SETTINGS_JSON):
        print(f"Updated: {SETTINGS_JSON.name}")
    else:
        print(f"Already configured: {SETTINGS_JSON.name}")

    print("\nInstall complete. Restart Claude Code to see the status line.")


def cmd_uninstall(args):
    """Remove statusline script and revert settings."""
    removed = []

    # Remove script
    if STATUSLINE_SCRIPT.exists():
        STATUSLINE_SCRIPT.unlink()
        removed.append(str(STATUSLINE_SCRIPT))
        print(f"Removed: {STATUSLINE_SCRIPT}")
    else:
        print("Script not found (already removed)")

    # Remove old bash script if present
    old_bash_script = SCRIPT_DIR / "statusline.sh"
    if old_bash_script.exists():
        old_bash_script.unlink()
        removed.append(str(old_bash_script))
        print(f"Removed old bash script: {old_bash_script}")

    # Remove config if requested
    if args.remove_config and CONFIG_FILE.exists():
        CONFIG_FILE.unlink()
        removed.append(str(CONFIG_FILE))
        print(f"Removed: {CONFIG_FILE}")

    # Remove from settings.json (primary location)
    if remove_from_settings(SETTINGS_JSON):
        print(f"Reverted: {SETTINGS_JSON.name}")

    # Clean up settings.local.json (remove stale entries if present)
    if SETTINGS_LOCAL.exists():
        local_data = load_settings(SETTINGS_LOCAL)
        cleaned = False
        if "statusline_script" in local_data:
            del local_data["statusline_script"]
            cleaned = True
        if "statusLine" in local_data:
            del local_data["statusLine"]
            cleaned = True
        if cleaned:
            save_settings(SETTINGS_LOCAL, local_data)
            print(f"Cleaned old entries from: {SETTINGS_LOCAL.name}")

    if removed:
        print(f"\nUninstall complete. Removed {len(removed)} file(s).")
    else:
        print("\nNothing to remove.")


def cmd_status(args):
    """Show current installation state as JSON."""
    info = {}

    # Script installed?
    info["script_installed"] = STATUSLINE_SCRIPT.exists()
    info["script_path"] = str(STATUSLINE_SCRIPT)

    # Config exists?
    info["config_exists"] = CONFIG_FILE.exists()
    if CONFIG_FILE.exists():
        info["config"] = read_config()

    # Settings configured? Check settings.json (primary location)
    expected_cmd = get_statusline_command()
    if SETTINGS_JSON.exists():
        data = load_settings(SETTINGS_JSON)
        cmd = data.get("statusLine", {}).get("command", "")
        info["settings.json_configured"] = cmd == expected_cmd
        info["settings.json_command"] = cmd

    # Also check settings.local.json for stale entries
    if SETTINGS_LOCAL.exists():
        local_data = load_settings(SETTINGS_LOCAL)
        has_stale = "statusLine" in local_data or "statusline_script" in local_data
        if has_stale:
            info["settings.local.json_has_stale_entries"] = True
            if "statusLine" in local_data:
                info["settings.local.json_statusLine"] = local_data["statusLine"]
            if "statusline_script" in local_data:
                info["settings.local.json_statusline_script"] = local_data["statusline_script"]

    # git available?
    info["git_installed"] = shutil.which("git") is not None

    print(json.dumps(info, indent=2))


def cmd_configure(args):
    """Update statusline configuration."""
    config = read_config()
    changed = False

    if args.token_display:
        config["TOKEN_DISPLAY"] = args.token_display
        changed = True

    if args.threshold_yellow is not None:
        config["THRESHOLD_YELLOW"] = str(args.threshold_yellow)
        changed = True
    if args.threshold_orange is not None:
        config["THRESHOLD_ORANGE"] = str(args.threshold_orange)
        changed = True
    if args.threshold_red is not None:
        config["THRESHOLD_RED"] = str(args.threshold_red)
        changed = True

    if args.color_green:
        config["COLOR_GREEN"] = validate_color(args.color_green)
        changed = True
    if args.color_yellow:
        config["COLOR_YELLOW"] = validate_color(args.color_yellow)
        changed = True
    if args.color_orange:
        config["COLOR_ORANGE"] = validate_color(args.color_orange)
        changed = True
    if args.color_red:
        config["COLOR_RED"] = validate_color(args.color_red)
        changed = True

    if not changed:
        print("No changes specified. Current config:")
        for k, v in config.items():
            print(f"  {k}={v}")
        return

    # Validate threshold ordering
    y = int(config["THRESHOLD_YELLOW"])
    o = int(config["THRESHOLD_ORANGE"])
    r = int(config["THRESHOLD_RED"])
    if not (y <= o <= r):
        print(f"Error: thresholds must be ascending: yellow({y}) <= orange({o}) <= red({r})",
              file=sys.stderr)
        sys.exit(1)

    write_config(config)
    print(f"Config updated: {CONFIG_FILE}")
    for k, v in config.items():
        print(f"  {k}={v}")


def add_threshold_args(parser):
    """Add common threshold arguments to a parser."""
    parser.add_argument(
        "--token-display", choices=["separate", "combined"],
        help="Token display mode (separate shows In/Out/Cache, combined shows total)"
    )
    parser.add_argument(
        "--threshold-yellow", type=validate_threshold, metavar="N",
        help="Yellow warning threshold (0-100)"
    )
    parser.add_argument(
        "--threshold-orange", type=validate_threshold, metavar="N",
        help="Orange warning threshold (0-100)"
    )
    parser.add_argument(
        "--threshold-red", type=validate_threshold, metavar="N",
        help="Red warning threshold (0-100)"
    )


def main():
    parser = argparse.ArgumentParser(
        description="Claude Code statusline installer and configurator"
    )
    sub = parser.add_subparsers(dest="command", required=True)

    # install
    p_install = sub.add_parser("install", help="Install statusline and configure Claude Code")
    add_threshold_args(p_install)

    # uninstall
    p_uninstall = sub.add_parser("uninstall", help="Remove statusline")
    p_uninstall.add_argument(
        "--remove-config", action="store_true",
        help="Also remove the configuration file"
    )

    # status
    sub.add_parser("status", help="Show installation status")

    # configure
    p_config = sub.add_parser("configure", help="Update statusline configuration")
    add_threshold_args(p_config)
    p_config.add_argument("--color-green", metavar="COLOR", help="Color for low usage")
    p_config.add_argument("--color-yellow", metavar="COLOR", help="Color for medium usage")
    p_config.add_argument("--color-orange", metavar="COLOR", help="Color for high usage")
    p_config.add_argument("--color-red", metavar="COLOR", help="Color for critical usage")

    args = parser.parse_args()
    {
        "install": cmd_install,
        "uninstall": cmd_uninstall,
        "status": cmd_status,
        "configure": cmd_configure,
    }[args.command](args)


if __name__ == "__main__":
    main()
