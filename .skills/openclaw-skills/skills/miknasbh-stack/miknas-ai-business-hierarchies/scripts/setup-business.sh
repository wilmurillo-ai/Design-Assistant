#!/bin/bash

# AI Business Hierarchies - Setup Script
# Creates folder structure and spawns initial agents for AI business

set -e

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

echo -e "${BLUE}=== AI Business Hierarchies Setup ===${NC}\n"

# Prompt for business name
read -p "Business name (e.g., my-company): " BUSINESS_NAME

# Validate business name
if [ -z "$BUSINESS_NAME" ]; then
    echo -e "${RED}Error: Business name cannot be empty${NC}"
    exit 1
fi

# Create business directory structure
BUSINESS_DIR="$HOME/business/$BUSINESS_NAME"
echo -e "${YELLOW}Creating business structure at: $BUSINESS_DIR${NC}\n"

mkdir -p "$BUSINESS_DIR/departments"
mkdir -p "$BUSINESS_DIR/departments/sales/supervisor/workers"
mkdir -p "$BUSINESS_DIR/departments/marketing/supervisor/workers"
mkdir -p "$BUSINESS_DIR/departments/operations/supervisor/workers"
mkdir -p "$BUSINESS_DIR/departments/hr/supervisor/workers"
mkdir -p "$BUSINESS_DIR/departments/accounting/supervisor/workers"
mkdir -p "$BUSINESS_DIR/reports/daily"
mkdir -p "$BUSINESS_DIR/reports/weekly"
mkdir -p "$BUSINESS_DIR/reports/monthly"

# Copy business template
echo -e "${YELLOW}Copying strategic layer template...${NC}"
cp "$(dirname "$0")/../assets/business-template.md" "$BUSINESS_DIR/departments/strategy.md"

echo -e "${GREEN}✓ Folder structure created${NC}\n"

# Prompt for BOG
echo -e "${BLUE}=== Strategic Layer Setup ===${NC}\n"
read -p "What is your Big Obsessional Goal (BOG)? " BOG
read -p "What is your current #1 bottleneck? " BOTTLENECK
read -p "Who is your target audience? " AUDIENCE
read -p "How are you uniquely positioned? " POSITIONING

# Update strategy file with user input
cat > "$BUSINESS_DIR/departments/strategy.md" << EOF
# Strategic Layer - $BUSINESS_NAME

---

## Big Obsessional Goal (BOG)

$BOG

---

## Current Bottleneck

$BOTTLENECK

---

## Target Audience

$AUDIENCE

---

## Positioning

$POSITIONING

---

*Generated by AI Business Hierarchies skill*
EOF

echo -e "${GREEN}✓ Strategic layer defined${NC}\n"

# Prompt for initial departments
echo -e "${BLUE}=== Department Selection ===${NC}\n"
echo "Select departments to start with (comma-separated):"
echo "  1. sales"
echo "  2. marketing"
echo "  3. operations"
echo "  4. hr"
echo "  5. accounting"
read -p "Initial departments (default: sales): " DEPARTMENTS_INPUT

if [ -z "$DEPARTMENTS_INPUT" ]; then
    DEPARTMENTS_INPUT="sales"
fi

echo -e "\n${YELLOW}Setting up initial departments...${NC}\n"

# Create agent spawn commands for each department
DEPARTMENTS=(${DEPARTMENTS_INPUT//,/ })

for DEPT in "${DEPARTMENTS[@]}"; do
    DEPT=$(echo "$DEPT" | tr '[:upper:]' '[:lower:]' | xargs)

    case $DEPT in
        sales)
            echo -e "${GREEN}✓ Sales department configured${NC}"
            ;;
        marketing)
            echo -e "${GREEN}✓ Marketing department configured${NC}"
            ;;
        operations)
            echo -e "${GREEN}✓ Operations department configured${NC}"
            ;;
        hr)
            echo -e "${GREEN}✓ HR department configured${NC}"
            ;;
        accounting)
            echo -e "${GREEN}✓ Accounting department configured${NC}"
            ;;
        *)
            echo -e "${RED}✗ Unknown department: $DEPT (skipping)${NC}"
            ;;
    esac
done

echo -e "\n${BLUE}=== Next Steps ===${NC}\n"
echo "1. Review and complete the strategic layer:"
echo "   $BUSINESS_DIR/departments/strategy.md"
echo ""
echo "2. Spawn CEO agent:"
echo "   sessions_spawn \\"
echo "     --agent-id $BUSINESS_NAME-ceo \\"
echo "     --task \"Manage complete business operations with strategy from $BUSINESS_DIR/departments/strategy.md\" \\"
echo "     --mode session \\"
echo "     --thread true \\"
echo "     --label \"CEO Agent - $BUSINESS_NAME\""
echo ""
echo "3. Spawn department managers for: $DEPARTMENTS_INPUT"
echo ""
echo "4. Run daily reporting setup:"
echo "   ./scripts/setup-daily-reporting.sh $BUSINESS_NAME"
echo ""
echo -e "${GREEN}=== Setup Complete! ===${NC}\n"
echo "Business directory: $BUSINESS_DIR"
echo "Strategic layer: $BUSINESS_DIR/departments/strategy.md"
echo ""
echo "🚀 Ready to build your AI business!"
