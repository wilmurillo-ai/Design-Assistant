#!/usr/bin/env node

import { readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const sourcePath = path.join(root, 'src', 'ui', 'dashboard.html');
const outPath = path.join(root, 'src', 'ui', 'dashboard.generated.ts');

const html = await readFile(sourcePath, 'utf8');
const output = `// Auto-generated by scripts/generate-dashboard-module.mjs\n// Do not edit by hand; edit src/ui/dashboard.html and regenerate.\n\nexport const DASHBOARD_HTML = ${JSON.stringify(html)};\n`;

await writeFile(outPath, output, 'utf8');
console.log(`Wrote ${path.relative(root, outPath)}`);
