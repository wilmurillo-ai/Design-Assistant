def get_review_type_name(type_code):
    """获取复盘类型中文名"""
    mapping = {
        "情绪": "情绪流",
        "龙头": "龙头战法",
        "趋势": "趋势交易",
        "数据": "量化数据",
        "个人": "个人交易",
        "游资": "顶级游资",
        "完整": "完整复盘"
    }
    return mapping.get(type_code, "复盘")


def generate_review(review_type="完整", output_path=None):
    """生成复盘模板"""
    if review_type not in TEMPLATES:
        print(f"❌ 不支持的复盘类型: {review_type}")
        print(f"支持的类型: {', '.join(TEMPLATES.keys())}")
        return
    
    # 获取当前日期时间
    now = datetime.now()
    date_str = now.strftime("%Y-%m-%d")
    time_str = now.strftime("%H:%M")
    
    # 填充模板
    template = TEMPLATES[review_type]
    content = template.format(date=date_str, time=time_str)
    
    # 确定输出路径
    if output_path is None:
        reviews_dir = os.path.expanduser("~/a-share-reviews")
        os.makedirs(reviews_dir, exist_ok=True)
        output_path = os.path.join(reviews_dir, f"{date_str}-{review_type}-复盘.md")
    
    # 写入文件
    with open(output_path, "w", encoding="utf-8") as f:
        f.write(content)
    
    type_name = get_review_type_name(review_type)
    print(f"✅ 已生成 {type_name}复盘模板")
    print(f"📄 文件路径: {output_path}")
    print(f"\n📝 模板预览 (前20行):")
    print("-" * 40)
    for line in content.split("\n")[:20]:
        print(line)
    print("...")
    
    return output_path


def main():
    parser = argparse.ArgumentParser(
        description="A股每日复盘模板生成器",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
示例:
  python review.py                    # 生成完整复盘模板
  python review.py --type 情绪         # 生成短线情绪流复盘
  python review.py --type 龙头         # 生成龙头战法复盘
  python review.py --type 趋势         # 生成趋势交易复盘
  python review.py --type 数据         # 生成量化数据复盘
  python review.py --type 游资         # 生成顶级游资复盘（完整版）
  python review.py --type 完整         # 生成职业交易员复盘
        """
    )
    
    parser.add_argument(
        "--type",
        choices=["情绪", "龙头", "趋势", "数据", "个人", "游资", "完整"],
        default="完整",
        help="复盘类型 (默认: 完整)"
    )
    
    parser.add_argument(
        "--output",
        help="输出文件路径 (默认: ~/a-share-reviews/YYYY-MM-DD-{type}.md)"
    )
    
    args = parser.parse_args()
    
    generate_review(args.type, args.output)


if __name__ == "__main__":
    main()
