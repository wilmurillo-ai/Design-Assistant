def request_json(url):
    headers = {'User-Agent': 'tjweather_clawhub/1.0 (weather_skill)'}
    req = urllib.request.Request(url, headers=headers)
    try:
        with urllib.request.urlopen(req, timeout=5) as response:
            return json.loads(response.read().decode('utf-8'))
    except Exception: return None

def _has_keyword_overlap(query, display_name):
    """
    置信度校验: 检查查询词中的关键词是否出现在返回地名中
    截取输入中长度>=2 的子串，至少有一个出现在结果中
    """
    # 生成所有长度 >= 2 的子串作为关键词
    keywords = set()
    for i in range(len(query)):
        for j in range(i + 2, min(i + 6, len(query) + 1)):
            keywords.add(query[i:j])
    return any(kw in display_name for kw in keywords)

