#!/usr/bin/env python3
"""
Subtitle translation script.
- Read source-language SRT files generated by transcribe.py
- Translate them into a target language with an LLM
- Generate bilingual or target-language-only subtitles
"""

import os
import sys
import re
import time
import argparse
from pathlib import Path
from openai import OpenAI

LANGUAGE_NAMES = {
    'zh': 'Chinese', 'en': 'English', 'ja': 'Japanese',
    'ko': 'Korean', 'fr': 'French', 'de': 'German',
    'es': 'Spanish', 'pt': 'Portuguese', 'ru': 'Russian',
    'ar': 'Arabic', 'hi': 'Hindi', 'it': 'Italian',
    'nl': 'Dutch', 'pl': 'Polish', 'tr': 'Turkish',
    'vi': 'Vietnamese', 'th': 'Thai', 'id': 'Indonesian',
    'sv': 'Swedish', 'da': 'Danish', 'no': 'Norwegian',
    'fi': 'Finnish', 'cs': 'Czech', 'uk': 'Ukrainian',
    'el': 'Greek', 'he': 'Hebrew', 'hu': 'Hungarian',
    'ro': 'Romanian', 'bg': 'Bulgarian', 'ms': 'Malay',
}


def get_language_name(code):
    """Resolve a language code to its English name for use in prompts."""
    return LANGUAGE_NAMES.get(code, code)


def parse_srt(srt_path):
    """Parse an SRT file into a list of subtitle entries."""
    with open(srt_path, 'r', encoding='utf-8-sig') as f:
        content = f.read()

    blocks = re.split(r'\n\s*\n', content.strip())
    entries = []

    for block in blocks:
        lines = block.strip().split('\n')
        if len(lines) < 3:
            continue
        time_match = re.match(
            r'(\d{2}:\d{2}:\d{2}[,.]\d{3})\s*-->\s*(\d{2}:\d{2}:\d{2}[,.]\d{3})',
            lines[1].strip(),
        )
        if not time_match:
            continue
        start, end = time_match.groups()
        start = start.replace('.', ',')
        end = end.replace('.', ',')
        text = ' '.join(l.strip() for l in lines[2:]).strip()
        entries.append({'start': start, 'end': end, 'text': text})

    return entries


def api_call_with_retry(call_fn, max_retries=3, base_delay=2.0):
    """Execute an API call with exponential backoff retry."""
    for attempt in range(max_retries):
        try:
            return call_fn()
        except Exception as e:
            if attempt == max_retries - 1:
                raise
            delay = base_delay * (2 ** attempt)
            print(f"  API call failed ({e}), retrying in {delay:.0f}s...")
            time.sleep(delay)


def translate_text(text, client, model, target_lang_name):
    """Translate a single text with an LLM."""
    prompt = (
        f"Translate the following text into natural, fluent {target_lang_name}. "
        "Preserve technical terminology accurately.\n\n"
        f"Source text: {text}\n\n"
        f"Return only the translated {target_lang_name} text with no extra explanation:"
    )

    try:
        response = api_call_with_retry(lambda: client.chat.completions.create(
            model=model,
            messages=[{"role": "user", "content": prompt}],
            temperature=0.3,
            max_tokens=4096,
        ))
        return response.choices[0].message.content.strip()
    except Exception as e:
        print(f"  Translation failed: {e}")
        return text


def parse_batch_response(result_text, expected_count):
    """Parse batch translation response, handling various numbering formats."""
    lines = result_text.strip().split('\n')

    translations = []
    for line in lines:
        line = line.strip()
        if not line:
            continue
        cleaned = re.sub(r'^\d+\s*[.。、):）：\-]\s*', '', line).strip()
        if cleaned:
            translations.append(cleaned)

    if len(translations) == expected_count:
        return translations

    non_empty = [l.strip() for l in lines if l.strip()]
    if len(non_empty) == expected_count:
        return [re.sub(r'^\d+\s*[.。、):）：\-]\s*', '', l).strip() for l in non_empty]

    return None


def translate_batch(entries, client, model, target_lang_name, batch_size=10):
    """Translate subtitle entries in batches for better efficiency."""
    translated = []
    total = len(entries)

    for i in range(0, total, batch_size):
        batch = entries[i:i+batch_size]
        batch_texts = [e['text'] for e in batch]

        prompt = (
            f"Translate the following {len(batch_texts)} sentences into natural, "
            f"fluent {target_lang_name}. Keep one translation per line and preserve the "
            "original order.\nPreserve technical terminology accurately.\n\n"
            "Source text:\n"
        )
        for j, text in enumerate(batch_texts, 1):
            prompt += f"{j}. {text}\n"

        prompt += (
            "\nReturn the translations in this format, with one translation per line:\n"
            f"1. [{target_lang_name} translation of sentence 1]\n"
            f"2. [{target_lang_name} translation of sentence 2]\n"
            "..."
        )

        print(f"  Translating batch {i//batch_size + 1}/{(total-1)//batch_size + 1} "
              f"({i+1}-{min(i+batch_size, total)}/{total})")

        try:
            response = api_call_with_retry(lambda: client.chat.completions.create(
                model=model,
                messages=[{"role": "user", "content": prompt}],
                temperature=0.3,
                max_tokens=max(4096, batch_size * 512),
            ))

            result = response.choices[0].message.content.strip()
            translations = parse_batch_response(result, len(batch))

            if translations is None:
                print("  Batch output mismatch, falling back to single-item translation")
                translations = [translate_text(t, client, model, target_lang_name) for t in batch_texts]

            for entry, trans in zip(batch, translations):
                translated.append({
                    'start': entry['start'],
                    'end': entry['end'],
                    'source': entry['text'],
                    'target': trans,
                })

        except Exception as e:
            print(f"  Batch translation failed: {e}. Falling back to single-item translation")
            for entry in batch:
                trans = translate_text(entry['text'], client, model, target_lang_name)
                translated.append({
                    'start': entry['start'],
                    'end': entry['end'],
                    'source': entry['text'],
                    'target': trans,
                })

    return translated


def save_srt_bilingual(entries, output_path):
    """Save bilingual subtitles with source and target text on separate lines."""
    with open(output_path, 'w', encoding='utf-8') as f:
        for i, entry in enumerate(entries, 1):
            f.write(f"{i}\n")
            f.write(f"{entry['start']} --> {entry['end']}\n")
            f.write(f"{entry['source']}\n")
            f.write(f"{entry['target']}\n\n")

    print(f"  Saved bilingual subtitles: {output_path}")


def save_srt_target_only(entries, output_path):
    """Save target-language-only subtitles."""
    with open(output_path, 'w', encoding='utf-8') as f:
        for i, entry in enumerate(entries, 1):
            f.write(f"{i}\n")
            f.write(f"{entry['start']} --> {entry['end']}\n")
            f.write(f"{entry['target']}\n\n")

    print(f"  Saved target-language subtitles: {output_path}")


def find_source_srts(directory, target_lang):
    """Find source SRT files in a directory, excluding bilingual and target-lang files."""
    all_srts = list(directory.glob('*.srt'))
    target_suffix = f'.{target_lang}.srt'
    files = [
        f for f in all_srts
        if not f.name.endswith('.bilingual.srt')
        and not f.name.endswith(target_suffix)
        and re.search(r'\.[a-z]{2,3}\.srt$', f.name)
    ]
    return files


def extract_base_name(srt_file):
    """Extract the base name by stripping the language code suffix from an SRT filename."""
    stem = srt_file.stem
    parts = stem.rsplit('.', 1)
    if len(parts) == 2 and re.match(r'^[a-z]{2,3}$', parts[1]):
        return parts[0]
    return stem


def main():
    parser = argparse.ArgumentParser(description='Subtitle translation')
    parser.add_argument('srt', help='SRT file or directory')
    parser.add_argument('-o', '--output', default='./translated', help='Output directory')
    parser.add_argument('-t', '--target-lang', default='zh',
                        help='Target language code (default: zh)')
    parser.add_argument('--bilingual', action='store_true', help='Generate bilingual subtitles')
    parser.add_argument('--target-only', action='store_true',
                        help='Generate target-language-only subtitles')
    parser.add_argument('--api-key', default=os.getenv('OPENAI_API_KEY'), help='OpenAI API key')
    parser.add_argument('--base-url',
                        default=os.getenv('OPENAI_BASE_URL', 'https://openrouter.ai/api/v1'),
                        help='API base URL')
    parser.add_argument('--model', default='google/gemini-3-flash-preview', help='Model name')
    parser.add_argument('--batch-size', type=int, default=10, help='Batch size')

    args = parser.parse_args()

    if not args.api_key:
        print("Please set the OPENAI_API_KEY environment variable or pass --api-key")
        sys.exit(1)

    if not args.bilingual and not args.target_only:
        args.bilingual = True
        args.target_only = True

    target_lang_name = get_language_name(args.target_lang)
    print(f"Target language: {target_lang_name} ({args.target_lang})\n")

    client = OpenAI(api_key=args.api_key, base_url=args.base_url)

    srt_path = Path(args.srt)
    output_dir = Path(args.output)
    output_dir.mkdir(parents=True, exist_ok=True)

    if srt_path.is_file():
        files = [srt_path]
    elif srt_path.is_dir():
        files = find_source_srts(srt_path, args.target_lang)
        print(f"Found {len(files)} source subtitle files\n")
    else:
        print(f"Path does not exist: {srt_path}")
        sys.exit(1)

    for file in sorted(files):
        print(f"Processing: {file.name}")

        entries = parse_srt(file)
        print(f"  Subtitle entries: {len(entries)}")

        translated = translate_batch(entries, client, args.model, target_lang_name, args.batch_size)

        base_name = extract_base_name(file)

        if args.bilingual:
            bilingual_path = output_dir / f"{base_name}.bilingual.srt"
            save_srt_bilingual(translated, bilingual_path)

        if args.target_only:
            target_path = output_dir / f"{base_name}.{args.target_lang}.srt"
            save_srt_target_only(translated, target_path)

        print()


if __name__ == "__main__":
    main()
