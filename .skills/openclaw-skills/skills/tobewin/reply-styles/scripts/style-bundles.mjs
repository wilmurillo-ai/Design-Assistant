export function listStyles() {
  return Object.keys(STYLES).sort();
}

export function getStyle(style) {
  return STYLES[style] ?? null;
}

export function getIntensity(intensity = "") {
  return INTENSITIES[intensity] ?? null;
}

export function getChannel(channel = "") {
  return CHANNELS[channel] ?? null;
}

export function getPersona(persona = "") {
  return PERSONAS[persona] ?? null;
}

export function getMode(mode = "") {
  return MODES[mode] ?? null;
}

export function recommendStyle({ scene = "", goal = "" }) {
  if (scene && SCENE_TO_STYLE[scene]) return SCENE_TO_STYLE[scene];
  if (goal && GOAL_TO_STYLE[goal]) return GOAL_TO_STYLE[goal];
  return "warm-professional";
}

export function buildReplyBundle({
  style = "",
  scene = "",
  goal = "",
  intensity = "",
  channel = "wechat",
  persona = "",
  mode = "",
}) {
  const styleName = style || recommendStyle({ scene, goal });
  const styleBundle = getStyle(styleName);
  if (!styleBundle) return null;

  const resolvedIntensity = intensity || styleBundle.defaultIntensity || "balanced";
  const intensityBundle = getIntensity(resolvedIntensity) ?? INTENSITIES.balanced;
  const channelBundle = getChannel(channel) ?? CHANNELS.wechat;
  const personaBundle = getPersona(persona);
  const modeBundle = getMode(mode);

  const unique = (values) => [...new Set(values.filter(Boolean))];
  const preferredWords = unique([
    ...(styleBundle.preferredWords ?? []),
    ...(personaBundle?.preferredWords ?? []),
  ]);
  const avoidWords = unique([
    ...(styleBundle.avoidWords ?? []),
    ...(personaBundle?.avoidWords ?? []),
  ]);
  const compositeSignature = [
    styleBundle.signature ?? "",
    personaBundle?.signature ?? "",
    modeBundle?.signature ?? "",
  ].filter(Boolean).join("；");

  return {
    style: styleName,
    styleCn: styleBundle.cn,
    intensity: resolvedIntensity,
    intensityCn: intensityBundle.cn,
    channel,
    channelCn: channelBundle.cn,
    tone: styleBundle.tone,
    signature: styleBundle.signature ?? "",
    compositeSignature,
    preferredWords,
    avoidWords,
    sampleOpening: styleBundle.sampleOpening?.[channel] ?? styleBundle.sampleOpening?.wechat ?? "",
    sampleClosing: styleBundle.sampleClosing?.[channel] ?? styleBundle.sampleClosing?.wechat ?? "",
    opening: styleBundle.opening,
    body: styleBundle.body,
    closing: styleBundle.closing,
    do: styleBundle.do,
    avoid: styleBundle.avoid,
    defaultIntensity: styleBundle.defaultIntensity,
    bestChannels: styleBundle.bestChannels,
    channelRules: channelBundle,
    intensityRules: intensityBundle,
    persona,
    personaCn: personaBundle?.cn ?? "",
    personaSignature: personaBundle?.signature ?? "",
    personaBias: personaBundle
      ? {
          opening: personaBundle.openingBias,
          body: personaBundle.bodyBias,
          closing: personaBundle.closingBias,
        }
      : null,
    mode,
    modeCn: modeBundle?.cn ?? "",
    modeSignature: modeBundle?.signature ?? "",
    modeStructure: modeBundle?.structure ?? [],
  };
}
