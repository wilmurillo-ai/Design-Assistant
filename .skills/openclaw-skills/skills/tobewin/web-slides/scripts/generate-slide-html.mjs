function parseArgs(argv) {
  const args = {
    title: "Premium HTML Deck",
    subtitle: "Single-file presentation generated by web-slides",
    scene: "",
    density: "medium",
    mobile: false,
    out: "dist/deck.html",
    content: "",
    theme: "",
  };

  for (let i = 2; i < argv.length; i += 1) {
    const part = argv[i];
    if (part === "--title") args.title = argv[i + 1] ?? args.title;
    if (part === "--subtitle") args.subtitle = argv[i + 1] ?? args.subtitle;
    if (part === "--scene") args.scene = argv[i + 1] ?? args.scene;
    if (part === "--density") args.density = argv[i + 1] ?? args.density;
    if (part === "--mobile") args.mobile = true;
    if (part === "--out") args.out = argv[i + 1] ?? args.out;
    if (part === "--content") args.content = argv[i + 1] ?? args.content;
    if (part === "--theme") args.theme = argv[i + 1] ?? args.theme;
  }

  return args;
}

function escapeHtml(input) {
  return String(input)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#39;");
}

