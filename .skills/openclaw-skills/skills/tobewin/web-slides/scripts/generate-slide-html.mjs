function loadContent(args) {
  if (!args.content) return null;

  const filePath = path.resolve(process.cwd(), args.content);
  const raw = fs.readFileSync(filePath, "utf8");
  const parsed = JSON.parse(raw);
  const errors = validateDeckContent(parsed);

  if (errors.length) {
    throw new Error(`Invalid content schema:\n- ${errors.join("\n- ")}`);
  }

  return {
    title: parsed.title || args.title,
    subtitle: parsed.subtitle || args.subtitle,
    scene: parsed.scene || args.scene,
    density: parsed.density || args.density,
    mobile: parsed.mobile ?? args.mobile,
    slides: Array.isArray(parsed.slides) ? parsed.slides : [],
  };
}

function normalizeSlides(slides, fallbackScene, fallbackTheme, fallbackCount) {
  if (!slides?.length) {
    return buildDeckContent(fallbackScene, fallbackTheme, fallbackCount);
  }

  return slides.map((slide) => ({
    ...slide,
    layout: slide.layout || "insight",
  }));
}

