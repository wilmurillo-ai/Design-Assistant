// arrServiceConfig holds what we need to generate a recyclarr config entry.
type arrServiceConfig struct {
	Name    string
	BaseURL string
	APIKey  string
	Type    string // "radarr", "sonarr", "lidarr"
}

// generateRecyclarrConfig builds a recyclarr.yml from the current setup state.
func generateRecyclarrConfig(state *SetupState) string {
	var b strings.Builder

	b.WriteString("# yaml-language-server: $schema=https://schemas.recyclarr.dev/latest/config-schema.json\n")
	b.WriteString("# Generated by Admirarr setup — https://github.com/maxtechera/admirarr\n")
	b.WriteString("# Re-run `admirarr setup` to regenerate.\n\n")

	profile := config.QualityProfile()
	if profile == "" {
		profile = "HD-1080p"
	}

	// Collect services
	var radarrServices []arrServiceConfig
	var sonarrServices []arrServiceConfig

	for _, name := range []string{"radarr"} {
		svc := state.Services[name]
		if svc == nil || !svc.Reachable || state.Keys[name] == "" {
			continue
		}
		radarrServices = append(radarrServices, arrServiceConfig{
			Name:    name,
			BaseURL: state.InternalURL(name),
			APIKey:  state.Keys[name],
			Type:    "radarr",
		})
	}
	for _, name := range []string{"sonarr"} {
		svc := state.Services[name]
		if svc == nil || !svc.Reachable || state.Keys[name] == "" {
			continue
		}
		sonarrServices = append(sonarrServices, arrServiceConfig{
			Name:    name,
			BaseURL: state.InternalURL(name),
			APIKey:  state.Keys[name],
			Type:    "sonarr",
		})
	}

	// Radarr section
	if len(radarrServices) > 0 {
		b.WriteString("radarr:\n")
		for _, svc := range radarrServices {
			b.WriteString(fmt.Sprintf("  %s:\n", svc.Name))
			b.WriteString(fmt.Sprintf("    base_url: %s\n", svc.BaseURL))
			b.WriteString(fmt.Sprintf("    api_key: %s\n", svc.APIKey))
			b.WriteString("\n")
			b.WriteString("    quality_definition:\n")
			b.WriteString("      type: movie\n")
			b.WriteString("\n")

			b.WriteString("    quality_profiles:\n")
			b.WriteString(fmt.Sprintf("      - name: %s\n", profile))
			b.WriteString("        reset_unmatched_scores:\n")
			b.WriteString("          enabled: true\n")
			b.WriteString("\n")

			// Custom formats: always include Repack/Proper + common unwanted
			b.WriteString("    custom_formats:\n")
			b.WriteString("      - trash_ids:\n")
			writeRadarrCustomFormats(&b)
			b.WriteString("        assign_scores_to:\n")
			b.WriteString(fmt.Sprintf("          - name: %s\n", profile))
			b.WriteString("\n")
		}
	}

	// Sonarr section
	if len(sonarrServices) > 0 {
		b.WriteString("sonarr:\n")
		for _, svc := range sonarrServices {
			b.WriteString(fmt.Sprintf("  %s:\n", svc.Name))
			b.WriteString(fmt.Sprintf("    base_url: %s\n", svc.BaseURL))
			b.WriteString(fmt.Sprintf("    api_key: %s\n", svc.APIKey))
			b.WriteString("\n")
			b.WriteString("    quality_definition:\n")
			b.WriteString("      type: series\n")
			b.WriteString("\n")

			b.WriteString("    quality_profiles:\n")
			b.WriteString(fmt.Sprintf("      - name: %s\n", profile))
			b.WriteString("        reset_unmatched_scores:\n")
			b.WriteString("          enabled: true\n")
			b.WriteString("\n")

			b.WriteString("    custom_formats:\n")
			b.WriteString("      - trash_ids:\n")
			writeSonarrCustomFormats(&b)
			b.WriteString("        assign_scores_to:\n")
			b.WriteString(fmt.Sprintf("          - name: %s\n", profile))
			b.WriteString("\n")
		}
	}

	return b.String()
}

