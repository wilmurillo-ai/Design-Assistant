// writeRecyclarrConfig writes the generated recyclarr config to the appropriate location.
// For Docker: writes into the container's config volume.
// For native: writes to ~/.config/recyclarr/recyclarr.yml.
func writeRecyclarrConfig(state *SetupState, content string) error {
	rt := recyclarr.Detect()

	switch rt.Method {
	case "docker":
		// Write into the Docker container
		ctx, cancel := context.WithTimeout(context.Background(), 10*time.Second)
		defer cancel()
		cmd := exec.CommandContext(ctx, "docker", "exec", "-i", rt.Path, "sh", "-c",
			"cat > /config/recyclarr.yml")
		cmd.Stdin = strings.NewReader(content)
		return cmd.Run()

	case "native":
		// Write to native config path
		dir := filepath.Join(os.Getenv("HOME"), ".config", "recyclarr")
		os.MkdirAll(dir, 0755)
		return os.WriteFile(filepath.Join(dir, "recyclarr.yml"), []byte(content), 0644)

	default:
		// Try to write to compose config dir for when recyclarr gets deployed later
		composeDir := state.ComposeDir
		if composeDir == "" {
			composeDir = filepath.Join(os.Getenv("HOME"), "docker")
		}
		configDir := filepath.Join(composeDir, "config", "recyclarr")
		os.MkdirAll(configDir, 0755)
		return os.WriteFile(filepath.Join(configDir, "recyclarr.yml"), []byte(content), 0644)
	}
}

// ensureRecyclarr deploys recyclarr if it's not available and the user selected it.
func ensureRecyclarr(state *SetupState) recyclarr.Runtime {
	rt := recyclarr.Detect()
	if rt.Method != "none" {
		return rt
	}

	// Check if recyclarr was selected
	if !hasService(state, "recyclarr") {
		return rt
	}

	fmt.Printf("  %s Deploying Recyclarr…\n", ui.GoldText("⟳"))
	result := deploy.Service("recyclarr")
	if result.Error != nil {
		fmt.Printf("  %s Deploy failed: %v\n", ui.Err("✗"), result.Error)
		return rt
	}

	fmt.Printf("  %s Recyclarr deployed (container: %s)\n", ui.Ok("✓"), result.Container)

	// Wait for container to be ready
	time.Sleep(3 * time.Second)

	return recyclarr.Detect()
}
