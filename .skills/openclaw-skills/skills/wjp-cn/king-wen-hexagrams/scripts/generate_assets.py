def caution_clause(text: str) -> str:
    if text.startswith("注意"):
        return "要防" + text[2:]
    return text


def parse_source_commentary() -> dict[int, dict]:
    text = SOURCE_COMMENTARY_PATH.read_text(encoding="utf-8")
    blocks = re.split(r"(?m)^##\s+", text)
    results: dict[int, dict] = {}
    section_index = 0
    for block in blocks[1:]:
        lines = block.splitlines()
        if not lines:
            continue
        header = lines[0].strip()
        if header.startswith("附："):
            break
        match = re.match(r"([^、]+)、(.+?)（([01]{6})）$", header)
        if not match:
            continue
        section_index += 1
        body = "\n".join(lines[1:])

        def extract(label: str) -> str | None:
            field_match = re.search(rf"\*\*{label}：\*\*\s*(.+)", body)
            if not field_match:
                return None
            return field_match.group(1).strip()

        line_entries = []
        for row in re.finditer(r"(?m)^\|\s*([^|\n]+?)\s*\|\s*([^|\n]+?)\s*\|\s*([^|\n]+?)\s*\|?\s*$", body):
            line_name = row.group(1).strip()
            if line_name == "爻" or set(line_name) <= {"-"}:
                continue
            line_entries.append(
                {
                    "name": line_name,
                    "text": row.group(2).strip(),
                    "gist": row.group(3).strip(),
                }
            )

        results[section_index] = {
            "title": match.group(2).strip(),
            "binary": match.group(3),
            "gua_ci": extract("卦辞"),
            "tuan_yue": extract("彖曰"),
            "xiang_yue": extract("象曰"),
            "line_entries": line_entries[:6],
            "extra_line_entries": line_entries[6:],
        }
    return results


