def build_classic_excerpt_lines(entry: dict) -> list[str]:
    lines = [f"- `卦辞`：{entry['source_gua_ci']}"]
    if entry["source_tuan_yue"]:
        lines.append(f"- `彖曰`：{entry['source_tuan_yue']}")
    if entry["source_xiang_yue"]:
        lines.append(f"- `象曰`：{entry['source_xiang_yue']}")
    if entry["extra_line_entries"]:
        extras = [f"{item['name']}：{item['text']}。{item['gist']}" for item in entry["extra_line_entries"]]
        lines.append(f"- `补充用爻`：{'；'.join(extras)}")
    return lines


def build_line_position_lines(entry: dict) -> list[str]:
    lines = []
    for index, (name, title, detail) in enumerate(LINE_POSITION_GUIDANCE):
        if index < len(entry["line_entries"]):
            line_entry = entry["line_entries"][index]
            lines.append(
                f"- `{name}动`：重点看`{title}`。{detail}更贴近本卦原典语义时，可读成：`{line_entry['name']}`曰“{line_entry['text']}”；可把要点抓成：{line_entry['gist']}。"
            )
        else:
            lines.append(
                f"- `{name}动`：重点看`{title}`。{detail}若原典表格暂未完整解析，可先按当前技能摘要理解为：{entry['line_texts'][index]}"
            )
    return lines


