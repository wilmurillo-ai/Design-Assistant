#!/usr/bin/env bash
set -euo pipefail

# Usage:
#   ./setup_local_pull.sh <vps-host-or-ip> <ssh-key-path> <destination-path> [interval-minutes] [vps-user]
# Example:
#   ./setup_local_pull.sh 134.199.174.28 ~/.ssh/do-reolink-relay /mnt/nas/reolink 10 reolinkftp
#
# Security notes:
#   - vps-user should be the restricted FTP ingest user (NOT root).
#   - That user needs read access to /srv/reolink/incoming on the VPS.
#   - All input is validated before being written to the generated script.

# ── Input validation ─────────────────────────────────────────────────────────

if [[ $# -lt 3 ]]; then
  echo "Usage: $0 <vps-host-or-ip> <ssh-key-path> <destination-path> [interval-minutes] [vps-user]"
  exit 1
fi

VPS_HOST="$1"
SSH_KEY="$2"
DEST_PATH="$3"
INTERVAL_MIN="${4:-10}"
VPS_USER="${5:-reolinkftp}"   # Default: restricted ingest user, never root

# Validate host: alphanumeric, dots, hyphens only (hostnames + IPv4)
if [[ ! "$VPS_HOST" =~ ^[a-zA-Z0-9._-]+$ ]]; then
  echo "ERROR: VPS_HOST contains invalid characters: $VPS_HOST" >&2
  exit 1
fi

# Validate VPS_USER: alphanumeric + underscore/hyphen only
if [[ ! "$VPS_USER" =~ ^[a-zA-Z0-9_-]+$ ]]; then
  echo "ERROR: VPS_USER contains invalid characters: $VPS_USER" >&2
  exit 1
fi

if [[ "$VPS_USER" == "root" ]]; then
  echo "ERROR: Do not run rsync as root. Provide a restricted user (e.g. reolinkftp)." >&2
  echo "       Grant that user read access to /srv/reolink/incoming on the VPS." >&2
  exit 1
fi

# Validate interval: integer 1-1440
if [[ ! "$INTERVAL_MIN" =~ ^[0-9]+$ ]] || (( INTERVAL_MIN < 1 || INTERVAL_MIN > 1440 )); then
  echo "ERROR: interval-minutes must be an integer between 1 and 1440" >&2
  exit 1
fi

# Resolve and validate SSH key path (no path traversal)
SSH_KEY_REAL="$(realpath -m -- "$SSH_KEY")"
if [[ "$SSH_KEY_REAL" != "$HOME/"* && "$SSH_KEY_REAL" != /home/* && "$SSH_KEY_REAL" != /etc/ssh/* ]]; then
  echo "ERROR: SSH key path looks unusual: $SSH_KEY_REAL" >&2
  exit 1
fi

# Resolve and validate destination path
DEST_REAL="$(realpath -m -- "$DEST_PATH")"
if [[ "$DEST_REAL" == / || "$DEST_REAL" == /etc* || "$DEST_REAL" == /usr* ]]; then
  echo "ERROR: Destination path is a system directory: $DEST_REAL" >&2
  exit 1
fi

# ── Generate pull script ──────────────────────────────────────────────────────
# Use printf to write values as data, not code. Each variable is written
# as a quoted assignment with special characters escaped.

mkdir -p "$HOME/bin" "$HOME/.config/systemd/user"
mkdir -p "$DEST_REAL"

PULL_SCRIPT="$HOME/bin/reolink_pull.sh"

# Write header and static config using printf (values quoted, never eval'd)
{
  printf '#!/usr/bin/env bash\n'
  printf 'set -euo pipefail\n\n'
  printf '# Auto-generated by setup_local_pull.sh — do not edit by hand.\n\n'

  # Write config as quoted variables — printf %q ensures shell-safe quoting
  printf 'VPS_HOST=%q\n'  "$VPS_HOST"
  printf 'VPS_USER=%q\n'  "$VPS_USER"
  printf 'VPS_SRC=%q\n'   "incoming/"
  printf 'SSH_KEY=%q\n'   "$SSH_KEY_REAL"
  printf 'DST=%q\n'       "${DEST_REAL}/"
  printf 'LOG=%q\n'       "$HOME/reolink-sync.log"
  printf 'LOCK=%q\n\n'    "/tmp/reolink_pull.lock"

  # Static runtime logic — no user-supplied values embedded as code
  cat <<'STATIC'
exec 9>"$LOCK"
flock -n 9 || exit 0

log(){ echo "$(date -Is) $*" >> "$LOG"; }

# Verify destination mount is present before syncing
dest_parent="$(dirname "$DST")"
if ! mountpoint -q "$dest_parent" 2>/dev/null && ! mountpoint -q "$DST" 2>/dev/null; then
  # Destination may be a local path (not a mount) — only fail if the dir is missing
  if [[ ! -d "$DST" ]]; then
    log "ERROR destination missing and not a mount: $DST"
    exit 1
  fi
fi

mkdir -p "$DST"

for i in 1 2 3; do
  if rsync -az --remove-source-files --partial --append-verify \
      -e "ssh -i ${SSH_KEY} -o BatchMode=yes -o ConnectTimeout=20 -o StrictHostKeyChecking=accept-new" \
      "${VPS_USER}@${VPS_HOST}:${VPS_SRC}" "$DST" >> "$LOG" 2>&1; then
    log "OK rsync pass $i"
    break
  fi
  log "WARN rsync failed pass $i"
  sleep $((i * 20))
done

# Clean up empty dirs on VPS (restricted to the ingest path only)
ssh -i "$SSH_KEY" \
    -o BatchMode=yes \
    -o ConnectTimeout=20 \
    -o StrictHostKeyChecking=accept-new \
    "${VPS_USER}@${VPS_HOST}" \
    'find incoming -mindepth 1 -type d -empty -delete 2>/dev/null || true' \
  >> "$LOG" 2>&1 || log "WARN cleanup skipped"

log "DONE"
STATIC
} > "$PULL_SCRIPT"

chmod 700 "$PULL_SCRIPT"

# ── Systemd user units ────────────────────────────────────────────────────────

cat >"$HOME/.config/systemd/user/reolink-pull.service" <<'UNIT'
[Unit]
Description=Pull Reolink files from VPS to destination storage

[Service]
Type=oneshot
ExecStart=%h/bin/reolink_pull.sh
UNIT

# interval written as validated integer — safe to interpolate
cat >"$HOME/.config/systemd/user/reolink-pull.timer" <<UNIT
[Unit]
Description=Run Reolink pull every ${INTERVAL_MIN} minutes (persistent)

[Timer]
OnCalendar=*:0/${INTERVAL_MIN}
Persistent=true
RandomizedDelaySec=30

[Install]
WantedBy=timers.target
UNIT

systemctl --user daemon-reload
systemctl --user enable --now reolink-pull.timer

echo
systemctl --user list-timers | grep reolink-pull || true

echo
cat <<EOM

Done.

Next steps:
  1) Ensure the VPS user '${VPS_USER}' can read /srv/reolink/incoming:
       sudo usermod -aG reolinkftp ${VPS_USER}   # if needed
  2) Test manually: $PULL_SCRIPT
  3) Ensure linger is enabled (required for user timers without login):
       sudo loginctl enable-linger $USER
  4) Check logs: tail -f $HOME/reolink-sync.log

Security reminder:
  - SSH key at ${SSH_KEY_REAL} should be 0600 and dedicated to this relay only.
  - VPS user '${VPS_USER}' should have no sudo and no shell login if possible.
EOM
