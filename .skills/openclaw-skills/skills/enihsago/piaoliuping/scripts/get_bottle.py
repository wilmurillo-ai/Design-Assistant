    output += f"—— {bottle['author']}\n"
    output += f"类型：{bottle['type']}"

    print(output)

    # 同时输出 JSON 格式以便程序使用
    if "--json" in sys.argv:
        print("\n---\n")
        print(json.dumps(bottle, ensure_ascii=False, indent=2))

