cmd_practice() {
  cat <<'EOF'
## 💪 语法练习题生成器

请AI生成语法练习题：

### 输入
- **语法点**: [用户提供]
- **难度**: [初级/中级/高级]
- **题目数量**: [5/10/20]

### 题型模板

#### 一、选择题 (Multiple Choice)
```
1. She ___ to school every day.
   A. go    B. goes    C. going    D. gone

   ✅ 答案: B
   💡 解析: 主语She是第三人称单数，
            一般现在时动词加-es。
```

#### 二、填空题 (Fill in the Blank)
```
1. I ___ (read) this book since last week.
   
   ✅ 答案: have been reading
   💡 解析: since表示从过去某时到现在，
            用现在完成进行时。
```

#### 三、改错题 (Error Correction)
```
1. ❌ "He don't like coffee."
   
   ✅ 修正: "He doesn't like coffee."
   💡 解析: 第三人称单数否定用doesn't。
```

#### 四、句型转换 (Transformation)
```
1. 原句: "Tom wrote the letter."
   要求: 改为被动语态
   
   ✅ 答案: "The letter was written by Tom."
   💡 解析: 被动语态 = be + 过去分词
```

#### 五、翻译题 (Translation)
```
1. 中译英: "如果我是你，我就不会这样做。"
   
   ✅ 答案: "If I were you, I wouldn't do this."
   💡 解析: 与现在事实相反的虚拟语气，
            从句用were，主句用would+动词原形。
```

### 输出格式
```
📝 语法练习: [语法点]
📊 难度: [等级] | 题数: [X]
⏰ 建议用时: [X]分钟

[题目区域]

---
📋 答案与解析
[详细答案和解释]

📊 评分标准:
- 90-100%: 🌟 掌握excellent
- 70-89%: 👍 基本掌握
- 50-69%: 📖 需要复习
- <50%: 🔄 建议重学该语法点
```
EOF
}

cmd_compare() {
  cat <<'EOF'
## 🔄 中英语法对比

请AI分析中英语法差异：

### 输入
- **语法点**: [用户提供，如"时态"/"语序"/"从句"]
- **或句子**: [用户提供一句中文，分析英文表达差异]

### 对比模板

```markdown
## 🔄 中英对比: [语法主题]

### 核心差异

| 维度 | 中文 🇨🇳 | 英文 🇬🇧 |
|------|----------|----------|
| [维度1] | [中文特点] | [英文特点] |
| [维度2] | [中文特点] | [英文特点] |
| [维度3] | [中文特点] | [英文特点] |

### 例句对比

**场景1: [场景描述]**
🇨🇳 中文: "[中文句子]"
🇬🇧 英文: "[英文句子]"
📝 差异: [逐词对比，标注不同之处]
⚠️ 常见错误: "[中式英语错误]"
✅ 正确: "[地道表达]"

**场景2: [场景描述]**
🇨🇳 中文: "[中文句子]"
🇬🇧 英文: "[英文句子]"
📝 差异: [差异分析]
```

### 常见中英差异专题

#### 1. 时态系统
```
🇨🇳 中文: 没有时态变化，靠"了/过/着/将"
   "我昨天去了商店" / "我明天去商店"
   
🇬🇧 英文: 12种时态，动词形式变化
   "I went to the store" / "I will go to the store"
   
⚠️ 中国学生常犯: 忘记变动词形式
```

#### 2. 语序差异
```
🇨🇳 中文: 时间/地点在动词前
   "我昨天在图书馆看了一本书"
   
🇬🇧 英文: 时间/地点通常在句尾
   "I read a book in the library yesterday"
```

#### 3. 冠词
```
🇨🇳 中文: 没有冠词系统
   "我是学生" / "给我一本书"
   
🇬🇧 英文: a/an/the 必须使用
   "I am a student" / "Give me a book"
```

#### 4. 单复数
```
🇨🇳 中文: 名词没有形态变化
   "一本书" / "三本书" (书不变)
   
🇬🇧 英文: 名词有单复数变化
   "a book" / "three books" (加s)
```

### 记忆技巧
[针对每个差异点，给出实用的记忆方法]

### 练习
[3-5道针对性练习题，训练中英思维转换]
EOF
}

case "$CMD" in
  check)    cmd_check ;;
  correct)  cmd_correct ;;
  explain)  cmd_explain ;;
  level)    cmd_level ;;
  practice) cmd_practice ;;
  compare)  cmd_compare ;;
  help|--help|-h) show_help ;;
  *)
    echo "❌ 未知命令: $CMD"
    echo ""
    show_help
    exit 1
    ;;
esac
