cmd_gitignore() {
    local lang="${1:-python}"
    case "$lang" in
        python|py)
            echo "__pycache__/"; echo "*.pyc"; echo ".venv/"; echo "dist/"; echo "*.egg-info/"
            echo ".env"; echo ".pytest_cache/"; echo ".mypy_cache/"
            ;;
        node|js|ts)
            echo "node_modules/"; echo "dist/"; echo ".env"; echo "*.log"; echo "coverage/"
            ;;
        go|golang)
            echo "bin/"; echo "vendor/"; echo "*.exe"; echo ".env"
            ;;
        *) echo "Languages: python, node, go" ;;
    esac
    _log "gitignore" "$lang"
}

cmd_config_gen() {
    local fmt="${1:-yaml}"
    case "$fmt" in
        yaml|yml)
            echo "app:"; echo "  name: myapp"; echo "  port: 8080"; echo "  debug: false"
            echo "database:"; echo "  host: localhost"; echo "  port: 5432"; echo "  name: mydb"
            ;;
        toml)
            echo "[app]"; echo 'name = "myapp"'; echo "port = 8080"; echo "debug = false"
            echo ""; echo "[database]"; echo 'host = "localhost"'; echo "port = 5432"
            ;;
        json)
            echo '{"app":{"name":"myapp","port":8080},"database":{"host":"localhost","port":5432}}'
            ;;
        ini)
            echo "[app]"; echo "name = myapp"; echo "port = 8080"
            echo ""; echo "[database]"; echo "host = localhost"; echo "port = 5432"
            ;;
        *) echo "Formats: yaml, toml, json, ini" ;;
    esac
    _log "config" "$fmt"
}

cmd_license() {
    local type="${1:-mit}"
    local year=$(date +%Y)
    case "$type" in
        mit) echo "MIT License"; echo "Copyright (c) $year"; echo ""; echo "Permission is hereby granted..." ;;
        apache) echo "Apache License 2.0"; echo "Copyright $year" ;;
        gpl) echo "GNU General Public License v3.0" ;;
        *) echo "Types: mit, apache, gpl" ;;
    esac
    _log "license" "$type"
}

cmd_history() {
    local log="$DATA_DIR/history.log"
    [ -f "$log" ] && tail -20 "$log" || echo "No history yet."
}

_log() {
    echo "$(date '+%Y-%m-%d %H:%M') | $1 | $2" >> "$DATA_DIR/history.log"
}

case "${1:-help}" in
    class)      shift; cmd_class "$@" ;;
    api)        shift; cmd_api "$@" ;;
    cli)        shift; cmd_cli "$@" ;;
    model)      shift; cmd_model "$@" ;;
    test)       shift; echo "TODO: test generator" ;;
    crud)       shift; echo "TODO: crud generator" ;;
    config)     shift; cmd_config_gen "$@" ;;
    docker)     shift; cmd_docker "$@" ;;
    readme)     shift; cmd_readme "$@" ;;
    gitignore)  shift; cmd_gitignore "$@" ;;
    snippet)    shift; echo "TODO: snippet" ;;
    license)    shift; cmd_license "$@" ;;
    history)    cmd_history ;;
    help|-h)    show_help ;;
    version|-v) echo "code-generator v$VERSION" ;;
    *)          echo "Unknown: $1"; show_help; exit 1 ;;
esac
