# Beautify GitHub Profile — GitHub profile README generator (12K+ stars)
set -euo pipefail
CMD="${1:-help}"; shift 2>/dev/null || true
case "$CMD" in
help) echo "Beautify GitHub Profile — README generator
Commands:
  generate <username>    Generate profile README
  badges <topics>        Generate badge collection
  stats <username>       GitHub stats card markdown
  streak <username>      Streak stats markdown
  languages <username>   Top languages card
  social <platform> <u>  Social badge
  header <text>          Animated header
  template <style>       Full template (minimal/developer/creative)
  info                   Version info
Powered by BytesAgain | bytesagain.com";;
generate)
    user="${1:-developer}"
    cat << EOF
# Hi there 👋 I'm $user

## About Me
- 🔭 I'm currently working on **awesome projects**
- 🌱 I'm learning **new technologies**
- 💬 Ask me about **anything**
- 📫 Reach me at: **your@email.com**

## Tech Stack
![Python](https://img.shields.io/badge/-Python-3776AB?style=flat&logo=python&logoColor=white)
![JavaScript](https://img.shields.io/badge/-JavaScript-F7DF1E?style=flat&logo=javascript&logoColor=black)
![Docker](https://img.shields.io/badge/-Docker-2496ED?style=flat&logo=docker&logoColor=white)

## GitHub Stats
![Stats](https://github-readme-stats.vercel.app/api?username=$user&show_icons=true&theme=tokyonight)

![Top Langs](https://github-readme-stats.vercel.app/api/top-langs/?username=$user&layout=compact&theme=tokyonight)

## Streak
![Streak](https://github-readme-streak-stats.herokuapp.com/?user=$user&theme=tokyonight)

---
*Generated by BytesAgain*
EOF
;;
badges)
    topics="${1:-python,javascript,docker}"
    echo "## Badges:"
    IFS=',' read -ra tags <<< "$topics"
    for tag in "${tags[@]}"; do
        t=$(echo "$tag" | xargs)
        echo "![${t}](https://img.shields.io/badge/-${t}-blue?style=flat&logo=${t}&logoColor=white)"
    done;;
stats)
    user="${1:-developer}"
    echo "![Stats](https://github-readme-stats.vercel.app/api?username=$user&show_icons=true&theme=tokyonight)";;
streak)
    user="${1:-developer}"
    echo "![Streak](https://github-readme-streak-stats.herokuapp.com/?user=$user&theme=tokyonight)";;
languages)
    user="${1:-developer}"
    echo "![Top Langs](https://github-readme-stats.vercel.app/api/top-langs/?username=$user&layout=compact&theme=tokyonight)";;
social)
    platform="${1:-twitter}"; user="${2:-username}"
    case "$platform" in
        twitter|x) echo "[![Twitter](https://img.shields.io/badge/-@$user-1DA1F2?style=flat&logo=twitter&logoColor=white)](https://twitter.com/$user)";;
        linkedin) echo "[![LinkedIn](https://img.shields.io/badge/-$user-0A66C2?style=flat&logo=linkedin)](https://linkedin.com/in/$user)";;
        youtube) echo "[![YouTube](https://img.shields.io/badge/-$user-FF0000?style=flat&logo=youtube)](https://youtube.com/@$user)";;
        *) echo "Platforms: twitter, linkedin, youtube, github";;
    esac;;
header)
    text="${1:-Hello World}"
    echo "![Header](https://capsule-render.vercel.app/api?type=waving&color=gradient&height=200&text=$text&fontSize=50&animation=fadeIn)";;
template)
    style="${1:-minimal}"
    case "$style" in
        minimal) bash "$0" generate "developer";;
        developer) echo "# 💻 Developer Profile"; bash "$0" generate "developer"; bash "$0" badges "python,go,rust,docker,kubernetes";;
        creative) bash "$0" header "Creative Dev"; bash "$0" generate "creative-dev";;
        *) echo "Templates: minimal, developer, creative";;
    esac;;
info) echo "Beautify GitHub Profile v1.0.0"; echo "Inspired by: beautify-github-profile (12,000+ stars)"; echo "Powered by BytesAgain | bytesagain.com";;
*) echo "Unknown: $CMD"; exit 1;;
esac
