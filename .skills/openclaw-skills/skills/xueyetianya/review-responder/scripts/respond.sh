- **H**ear — 倾听，理解客户不满
- **E**mpathize — 共情，表达理解
- **A**pologize — 道歉，承认问题
- **R**esolve — 解决，提供方案
- **D**iagnose — 诊断，防止再发

### 差评回复模板 (按问题类型)

