> 亲，非常抱歉给您带来不好的体验！看到您反映[具体问题]，
> 我们深感歉意。这不是我们的品质标准，已经反馈给质检团队排查原因。
> 我们为您提供以下解决方案：
> 1. 免费换货 (承担来回运费)
> 2. 全额退款
> 3. 部分退款补偿 (¥XX)
> 请您联系客服处理，我们一定让您满意。再次抱歉！🙏

**物流问题**
> 亲，让您久等了，非常抱歉！[物流问题具体原因]，
> 我们已经和快递公司反馈此问题。
> 为表歉意，已为您发放[XX元优惠券]，下次下单可使用。
> 包裹有任何问题请随时联系我们，第一时间为您解决！

**与描述不符**
> 亲，感谢您的反馈！关于[具体不符之处]，
> 可能是[解释原因：光线/显示屏色差/个人感受差异]。
> 但无论如何，您的感受最重要。我们可以：
> 1. 安排退换货
> 2. 为您做部分补偿
> 请联系客服沟通处理方案，我们一定让您满意！

**服务态度问题**
> 亲，看到您的评价我们非常自责！服务不周是我们的失误，
> 已经对相关客服进行培训和提醒。
> 您的体验对我们至关重要，希望给我们一次改正的机会。
> 店长已为您准备了[补偿方案]，期待您的联系。

### 差评回复禁忌
❌ 不要争论对错
❌ 不要推卸责任
❌ 不要复制粘贴万能回复
❌ 不要暴露客户隐私
❌ 不要承诺做不到的事

### 差评挽回流程
```
1. 24h内公开回复 (态度诚恳)
2. 私信联系客户 (了解详情)
3. 提供解决方案 (2-3个选项)
4. 执行方案 (快速处理)
5. 跟进确认 (是否满意)
6. 邀请修改评价 (不强迫)
```
PROMPT
