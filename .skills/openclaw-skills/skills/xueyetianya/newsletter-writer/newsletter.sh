cmd_analyze() {
  cat <<'EOF'
## 📊 Newsletter效果分析框架

请AI分析邮件数据并给出优化建议：

### 核心指标仪表盘

#### 送达指标
| 指标 | 计算方式 | 基准值 | 优秀值 |
|------|----------|--------|--------|
| 送达率 | 送达数/发送数 | >95% | >99% |
| 弹回率 | 弹回数/发送数 | <5% | <1% |
| 硬弹回 | 无效地址 | <2% | <0.5% |
| 软弹回 | 临时失败 | <3% | <1% |

#### 互动指标
| 指标 | 计算方式 | 基准值 | 优秀值 |
|------|----------|--------|--------|
| 打开率 | 打开数/送达数 | 20-25% | >30% |
| 点击率(CTR) | 点击数/送达数 | 2-5% | >7% |
| 点击打开率(CTOR) | 点击数/打开数 | 10-15% | >20% |
| 退订率 | 退订数/送达数 | <0.5% | <0.2% |
| 垃圾投诉率 | 投诉数/送达数 | <0.1% | <0.05% |

#### 转化指标
| 指标 | 说明 |
|------|------|
| 转化率 | 完成目标行动的比例 |
| RPE | 每封邮件收入 |
| ROI | 邮件营销投资回报率 |
| LTV | 邮件用户生命周期价值 |

### 诊断清单

**打开率低？检查：**
- [ ] 标题是否有吸引力
- [ ] 发件人名称是否可信
- [ ] 发送时间是否合适
- [ ] 是否进入垃圾箱
- [ ] 列表质量（僵尸用户比例）

**点击率低？检查：**
- [ ] CTA是否清晰突出
- [ ] 内容与标题是否一致
- [ ] 链接数量是否过多
- [ ] 移动端排版是否友好
- [ ] 价值主张是否明确

**退订率高？检查：**
- [ ] 发送频率是否过高
- [ ] 内容是否与订阅预期一致
- [ ] 是否提供频率选项
- [ ] 个性化程度是否足够

### 优化行动计划模板
```
问题: ___________
当前值: ___%
目标值: ___%
假设原因: ___________
优化方案:
  1. ___________
  2. ___________
  3. ___________
验证方式: A/B测试 / 前后对比
时间周期: ___周
```
EOF
}

case "$CMD" in
  write)      cmd_write ;;
  subject)    cmd_subject ;;
  segment)    cmd_segment ;;
  ab-test)    cmd_ab_test ;;
  schedule)   cmd_schedule ;;
  analyze)    cmd_analyze ;;
  help|--help|-h) show_help ;;
  *)
    echo "❌ 未知命令: $CMD"
    echo ""
    show_help
    exit 1
    ;;
esac
