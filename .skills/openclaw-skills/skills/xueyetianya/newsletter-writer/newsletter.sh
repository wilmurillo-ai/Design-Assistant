cmd_write() {
  cat <<'EOF'
## 📧 Newsletter邮件内容模板

请AI根据以下框架生成完整Newsletter：

### 输入信息
- **主题/话题**: [用户提供]
- **目标受众**: [用户提供]
- **品牌调性**: [专业/轻松/幽默/权威]
- **期望CTA**: [注册/购买/阅读/分享]

### 邮件结构

#### 1️⃣ 预览文本 (Preview Text)
> 40-90字符，补充标题信息，激发好奇心

#### 2️⃣ 开头Hook (2-3句)
策略选择：
- 🎯 **痛点切入**: "你是否经常遇到..."
- 📊 **数据震撼**: "90%的人都不知道..."
- 📖 **故事开头**: "上周，我们的客户小王..."
- ❓ **问题引导**: "如果你能在10分钟内..."

#### 3️⃣ 正文内容
- **核心价值点1**: 标题 + 2-3句解释 + 具体例子
- **核心价值点2**: 标题 + 2-3句解释 + 具体例子
- **核心价值点3**: 标题 + 2-3句解释 + 具体例子

#### 4️⃣ 社会证明
- 客户评价/数据/案例（1-2个）

#### 5️⃣ CTA区域
- **主CTA按钮**: [动词+好处] 如"立即获取免费指南"
- **次CTA链接**: 备选行动

#### 6️⃣ 签名区
- 发件人姓名+职位
- PS附言（制造紧迫感或额外价值）

### 写作原则
- 段落不超过3句话
- 使用"你"而非"您"（除非品牌要求）
- 每段一个核心信息
- 移动端友好：单列布局
- 图片需有ALT文本描述
EOF
}

cmd_subject() {
  cat <<'EOF'
## ✉️ 高打开率邮件标题生成器

请AI为Newsletter生成**10个标题变体**，使用以下公式：

### 标题公式库

**数字型**:
1. [数字] + 个 + [方法/技巧] + 让你 + [好处]
2. [数字] + 个 + [错误/陷阱] + 正在毁掉你的 + [目标]

**好奇型**:
3. 我们发现了 + [领域] + 的一个秘密...
4. 为什么 + [反直觉观点] + ？
5. [权威人物] + 不会告诉你的 + [话题]

**紧迫型**:
6. 最后 + [时间] + : + [行动] + 的最佳时机
7. [限时/限量] + [优惠/机会] + 即将结束

**个性化型**:
8. [姓名]，+ 你的 + [专属内容] + 已准备好
9. 基于你的 + [行为] + ，我们推荐...

**问题型**:
10. 你还在为 + [痛点] + 烦恼吗？

### 评估标准
| 维度 | 检查项 |
|------|--------|
| 长度 | 6-10个中文词 / 30-50英文字符 |
| Emoji | 最多1个，放在开头 |
| 个性化 | 是否包含动态变量 |
| 紧迫感 | 是否有时间限制 |
| 好处 | 是否明确价值 |
| 避雷 | 避免垃圾邮件触发词(免费/赚钱/点击) |

### 输出格式
为每个标题标注：
- 预计打开率评分 (1-10)
- 适用受众
- 最佳配对预览文本
EOF
}

