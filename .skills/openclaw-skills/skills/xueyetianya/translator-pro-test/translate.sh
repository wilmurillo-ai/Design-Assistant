#!/usr/bin/env bash
# translator-pro — 专业翻译助手
# Usage: bash translate.sh <command> [text...]
# Powered by BytesAgain

set -euo pipefail

CMD="${1:-help}"
shift 2>/dev/null || true
INPUT="$*"

case "$CMD" in
  translate)
    cat <<'PROMPT'
## 专业翻译

请翻译以下文本，要求：
1. **自动检测**源语言
2. **目标语言**：如文本中有 `[xx]` 标记则翻译为该语言，否则中↔英自动互译
3. **翻译质量**：准确、流畅、自然，符合目标语言表达习惯
4. **专业术语**：保持专业术语的准确性
5. **语境理解**：根据上下文选择最恰当的词义

### 支持语言代码
- `en` 英文 | `zh` 中文简体 | `zh-TW` 中文繁体
- `ja` 日文 | `ko` 韩文 | `fr` 法文 | `de` 德文 | `es` 西班牙文

### 翻译原则
- **信**：忠实原文，准确传达信息
- **达**：通顺流畅，符合目标语言习惯
- **雅**：表达得体，文风与原文一致

### 待翻译文本
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    echo "### 输出格式"
    echo "1. **源语言**：检测到的源语言"
    echo "2. **目标语言**：翻译目标语言"
    echo "3. **翻译结果**：完整翻译"
    echo "4. **翻译说明**：关键翻译选择的说明（如有歧义）"
    ;;

  compare)
    cat <<'PROMPT'
## 多版本翻译对比

请提供以下文本的多个翻译版本，便于对比选择：

### 翻译版本要求
1. **直译版**：尽量贴近原文结构和用词
2. **意译版**：重新组织语言，追求自然地道
3. **文学版**：追求文采和韵律，适合正式出版

### 对比维度
- 准确度：信息传达的完整性
- 流畅度：读起来的自然程度
- 风格：是否保持了原文的语气和风格
- 适用场景：各版本适合什么场合

### 待翻译文本
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    cat <<'FORMAT'
### 输出格式
| 维度 | 直译版 | 意译版 | 文学版 |
|------|--------|--------|--------|
| 译文 | ...    | ...    | ...    |
| 准确度 | ★★★ | ★★★ | ★★☆ |
| 流畅度 | ★★☆ | ★★★ | ★★★ |
| 推荐场景 | 技术文档 | 日常沟通 | 正式出版 |

**最终推荐**：根据使用场景给出推荐版本
FORMAT
    ;;

  glossary)
    cat <<'PROMPT'
## 专业术语表生成

请为以下术语/领域生成专业术语对照表：

### 术语表要求
1. **多语言对照**：中/英（可选日/韩）
2. **释义说明**：每个术语的简要解释
3. **使用语境**：术语的典型使用场景
4. **易混淆区分**：标注容易混淆的近义术语
5. **缩写标注**：常见缩写形式

### 输入格式
- 直接输入术语列表（逗号分隔）
- 或指定领域名称，自动生成常用术语表

### 待处理输入
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    cat <<'FORMAT'
### 输出格式
| 英文 | 中文 | 缩写 | 释义 | 易混淆 |
|------|------|------|------|--------|
| ...  | ...  | ...  | ...  | ...    |

注意事项：标注该领域必须统一的译法
FORMAT
    ;;

  localize)
    cat <<'PROMPT'
## 本地化适配

请对以下文本进行本地化处理，不仅仅是翻译，还需要文化适配：

### 本地化检查清单
1. **日期格式**：适配目标地区格式（MM/DD vs DD/MM vs YYYY年MM月DD日）
2. **货币单位**：转换为目标地区货币（汇率参考）
3. **度量衡**：公制↔英制互转
4. **文化适配**：
   - 颜色含义（红色在中国=喜庆，在西方=危险）
   - 数字忌讳（4在中日韩=不吉利，13在西方=不吉利）
   - 节日引用替换
   - 习语和俚语适配
5. **UI/UX本地化**：文本长度适配、排版方向、字体建议

### 待本地化文本
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    echo "### 输出格式"
    echo "1. **本地化结果**：完整的本地化文本"
    echo "2. **适配清单**：列出所有本地化调整"
    echo "3. **文化注意事项**：需要特别注意的文化差异"
    echo "4. **建议**：进一步本地化的建议"
    ;;

  proofread)
    cat <<'PROMPT'
## 翻译校对

请校对以下翻译文本，找出错误并提供改进建议：

### 校对维度
1. **准确性**：是否有误译、漏译、多译
2. **流畅度**：是否有翻译腔、不自然的表达
3. **术语一致性**：同一术语是否翻译一致
4. **语法正确**：目标语言的语法是否正确
5. **风格匹配**：翻译风格是否与原文匹配
6. **格式保持**：标点、大小写、数字格式是否正确

### 待校对文本
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    cat <<'FORMAT'
### 输出格式
1. **错误列表**：
   | # | 原译 | 问题类型 | 修改建议 | 严重程度 |
   |---|------|----------|----------|----------|
   | 1 | ...  | 误译     | ...      | 🔴高     |

2. **修改后全文**：校对修改后的完整译文

3. **质量评分**（满分100）：
   - 准确性：_/30
   - 流畅度：_/25
   - 术语一致：_/20
   - 语法：_/15
   - 风格：_/10
   - 总分：_/100
FORMAT
    ;;

  batch)
    cat <<'PROMPT'
## 批量翻译

请批量翻译以下多条文本：

### 批量翻译规则
1. **统一语言方向**：所有文本使用相同的源→目标语言方向
2. **术语一致**：确保批量文本中术语翻译一致
3. **编号保持**：保持原文编号/序号
4. **格式**：用 `|` 分隔多条文本，或每行一条

### 目标语言
如文本中有 `[xx]` 标记则翻译为该语言，否则中↔英自动互译

### 待翻译文本
PROMPT
    if [ -n "$INPUT" ]; then
      echo ""
      echo "$INPUT"
    fi
    echo ""
    cat <<'FORMAT'
### 输出格式
| # | 原文 | 翻译 |
|---|------|------|
| 1 | ...  | ...  |
| 2 | ...  | ...  |
| 3 | ...  | ...  |

**术语一致性检查**：列出跨条目统一使用的术语翻译
FORMAT
    ;;

  help|--help|-h|"")
    cat <<'HELP'
