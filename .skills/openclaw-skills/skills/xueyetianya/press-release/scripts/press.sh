def show_help():
    print("""
📰 新闻稿 & 公关文案生成器
==============================

用法：press.sh <命令> [参数...]

命令：
  launch  <产品名> <亮点1,亮点2,...>     产品发布新闻稿
  funding <公司名> <轮次> <金额>         融资公告新闻稿
  event   <活动名> <时间> <地点>         活动宣传新闻稿
  crisis  <事件> <立场>                  危机公关声明
  help                                   显示此帮助

示例：
  press.sh launch "AI助手Pro" "多模态,实时对话,本地部署"
  press.sh funding "星辰科技" "A轮" "5000万美元"
  press.sh event "AI开发者大会" "2026年5月15日" "上海世博中心"
  press.sh crisis "数据泄露事件" "积极整改，全面排查"
""")

if __name__ == "__main__":
    action = sys.argv[1] if len(sys.argv) > 1 else "help"
    args = sys.argv[2:]

    if action == "launch":
        if len(args) < 2:
            print("❌ 用法: press.sh launch <产品名> <亮点1,亮点2,...>")
            sys.exit(1)
        launch(args[0], args[1])
    elif action == "funding":
        if len(args) < 3:
            print("❌ 用法: press.sh funding <公司名> <轮次> <金额>")
            sys.exit(1)
        funding(args[0], args[1], args[2])
    elif action == "event":
        if len(args) < 3:
            print("❌ 用法: press.sh event <活动名> <时间> <地点>")
            sys.exit(1)
        event(args[0], args[1], args[2])
    elif action == "crisis":
        if len(args) < 2:
            print("❌ 用法: press.sh crisis <事件> <立场>")
            sys.exit(1)
        crisis(args[0], args[1])
    elif action == "help":
        show_help()
    else:
        print("❌ 未知命令: {}".format(action))
        show_help()
        sys.exit(1)
