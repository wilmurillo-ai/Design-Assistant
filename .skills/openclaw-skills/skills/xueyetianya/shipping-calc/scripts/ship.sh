## 📦 运费计算

### 运费计算公式
```
实际重量费 = 实际重量(kg) × 单价
体积重量 = 长(cm) × 宽(cm) × 高(cm) ÷ 体积系数
计费重量 = max(实际重量, 体积重量)
运费 = 首重价 + (计费重量 - 首重) × 续重单价
```

### 体积系数
| 物流方式 | 体积系数 | 说明 |
|----------|----------|------|
| 国内快递 | ÷6000 | 顺丰/通达系 |
