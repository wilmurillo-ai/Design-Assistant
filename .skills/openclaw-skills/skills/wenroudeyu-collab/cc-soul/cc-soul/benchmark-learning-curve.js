function evaluateRecall(results, query) {
  const checkHit = (mem) => {
    const c = mem.content;
    return query.expectedKeywords.some((kw) => c.includes(kw));
  };
  const top3 = results.slice(0, 3);
  return {
    hit3: top3.some((r) => checkHit(r)),
    top1: results.length > 0 && checkHit(results[0])
  };
}
