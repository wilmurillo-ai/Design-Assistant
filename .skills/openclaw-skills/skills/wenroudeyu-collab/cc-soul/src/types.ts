export interface UserRhythm {
  activeHours: number[]           // histogram of message counts per hour (24 slots)
  weekdayTopics: string[]         // frequent weekday topics
  weekendTopics: string[]         // frequent weekend topics
  avgResponseDelay: number        // avg seconds between user messages
  lastMessageTs: number           // timestamp of last message
}

export interface UserProfile {
  userId: string
  displayName: string
  tier: 'owner' | 'known' | 'new' // auto-detected
  messageCount: number
  corrections: number
  lastSeen: number
  firstSeen: number
  topics: string[] // this user's frequent topics
  style: 'technical' | 'casual' | 'mixed' // auto-detected from message patterns
  rhythm?: UserRhythm
  // Relationship dynamics
  trust: number             // 0-1, how much user trusts cc's answers
  familiarity: number       // 0-1, how well cc knows this user
  relationshipTrend: 'improving' | 'stable' | 'declining'
  lastConflict: number      // timestamp of last correction/disagreement
  sharedEpisodes: number    // count of completed conversations together
  personaHistory?: { persona: string; count: number }[]  // persona usage tracking
  language?: string  // auto-detected: 'zh', 'en', 'ja', 'ko', 'es', 'fr', 'de', 'ru', etc.
  bio?: string       // user self-description: occupation, expertise, interests — extracted from conversation
  languageDna?: { topWords: Record<string, number>; avgLength: number; samples: number }
}

