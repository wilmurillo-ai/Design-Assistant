// ═══════════════════════════════════════════════════════════════════════════════
// MEMORY
// ═══════════════════════════════════════════════════════════════════════════════

export interface Memory {
  content: string
  scope: string // preference | fact | event | opinion | topic | correction | dream | curiosity | reflection | proactive | discovery | task | reflexion | gratitude | visual
  ts: number
  relevance?: number // computed on recall
  emotion?: string // neutral | warm | important | painful | funny
  userId?: string // who created this memory
  tags?: string[] // semantic tags generated by CLI for tag-based recall
  visibility?: 'global' | 'channel' | 'private' // memory visibility scope
  channelId?: string // channel where this memory was created
  confidence?: number    // 0-1, starts at 0.7, boosted on access, degraded on contradiction
  lastAccessed?: number  // timestamp of last recall
  validFrom?: number     // when this fact became true (timestamp)
  validUntil?: number    // when this fact stopped being true (0 = still valid)
  // ── Time-decay tiered memory fields ──
  tier?: 'short_term' | 'mid_term' | 'long_term' | 'fading' | 'gist' | 'absorbed'  // lifecycle stage (default: short_term)
  recallCount?: number   // how many times this memory has been recalled
  lastRecalled?: number  // timestamp of last recall
  recallContexts?: string[]  // reconsolidation: contexts in which this memory was recalled
  // ── Semantic versioning: previous versions of this memory ──
  history?: { content: string; ts: number }[]
  // ── Chain-of-Thought reasoning trace ──
  reasoning?: {
    context: string      // 触发这条记忆的上下文
    conclusion: string   // 结论
    confidence: number   // 推理置信度 0-1
  }
  // ── Situational context at creation time ──
  situationCtx?: {
    attention?: string   // technical/emotional/casual/correction
    intent?: string      // wants_opinion/wants_action/wants_answer
    mood?: number        // body.mood at creation (-1 to 1)
    energy?: number      // body.energy at creation (0-1)
  }
  // ── Source tracking (防幻觉：区分用户亲口说的 vs AI 自己推断的) ──
  source?: 'user_said' | 'ai_inferred' | 'ai_observed' | 'system'
  // ── Emotional intensity at creation (高情绪记忆衰减更慢) ──
  emotionIntensity?: number  // 0-1, used by smart-forget to slow decay
  // ── 闪光灯记忆（Flashbulb Memory）：高情绪事件深度编码 ──
  flashbulb?: {
    surroundingContext: string  // 前后对话摘要
    bodyState: { mood: number; energy: number }  // 创建时的身体状态
    mentionedPeople: string[]  // 当时提到的人
    detailLevel: 'full'        // 标记：不压缩，永久保留原文
  }
  importance?: number    // 1-10, 预期违背评分 (surprise score)
  surprise?: number      // 同 importance，别名
  because?: string           // causal reason: WHY this decision/preference was made
  // ── Bayesian confidence: Beta distribution parameters ──
  bayesAlpha?: number        // Beta α (default 2), incremented on positive evidence
  bayesBeta?: number         // Beta β (default 1), incremented on negative evidence
  // ── FSRS-4.5 spaced repetition state (replaces Weibull for new memories) ──
  fsrs?: { stability: number; difficulty: number; reps: number; lapses: number }
  // ── Living Memory Network: micro-links accumulated from recall co-occurrences ──
  microLinks?: { query: string; memoryRef: string; sharedKeywords: string[]; ts: number }[]
  // ── Prospective Tags: AAM 预测的未来查询词（写入时生成，零 LLM doc2query）──
  prospectiveTags?: string[]
  // ── P1a: Memory-level injection competition ──
  injectionEngagement?: number  // 被注入后用户 engaged 的次数（trigram overlap > 0.3）
  injectionMiss?: number        // 被注入后用户 ignored 的次数（trigram overlap < 0.1）
  // ── P2a: 日期双写（懒生成）──
  contentNormalized?: string    // 日期归一化版本（"昨天" → "2026-03-30"）。召回/注入用此字段
  // ── 记忆唯一 ID ──
  memoryId?: string  // 创建时生成，永不变

  // ── 记忆溯源链（Memory Lineage）──
  lineage?: Array<{
    action: 'created' | 'reshaped' | 'compressed' | 'gisted' | 'promoted' | 'demoted' | 'merged' | 'superseded'
    ts: number
    trigger?: string
    delta?: string
    from?: number
    to?: number
  }>

  // ── 事实版本链（Fact Supersession Chain）──
  supersedes?: string      // 本记忆取代了哪条旧记忆的 memoryId
  supersededBy?: string    // 本记忆被哪条新记忆取代的 memoryId

  // ── Hindsight 认知网络分类 ──
  network?: 'world' | 'experience' | 'opinion' | 'entity'
  // ── 前瞻锚定（Prospective Anchoring）──
  prospective?: {
    trigger: string
    expiresAt: number
    action: string
  }
  // ── P0a: Graveyard metadata ──
  _graveyardOriginalScope?: string
  _graveyardTs?: number
  _needsVerification?: boolean
  _complementOf?: string[]  // 互补候选的 memoryId 列表（写入时标记，heartbeat 消费）
  _preheated?: boolean  // 预测性预热标记（heartbeat 设置，recall 后清除）
  _segmentId?: number   // 话题河流：同一段对话中的记忆共享 segmentId
  _entityIds?: string[] // 写入时由 findMentionedEntities 提取的实体名列表（slotization 用）
  _eventDate?: number   // 事件发生的原始时间戳（区别于 ts 的归一化时间）
  utility?: number  // -5 to +5, default 0. MemRL: "did this memory help?"
  _matchScore?: number  // activation-field 计算的匹配分数（运行时临时字段）
  _gist?: { polarity: number; abstractTags: string[] }  // benchmark gist 摘要（运行时临时字段）
}

// ═══════════════════════════════════════════════════════════════════════════════
// STRUCTURED FACTS — key-value triples extracted from natural language memories
// Mem0-style fact store for precise queries ("用户喜欢什么语言？" → Python)
// ═══════════════════════════════════════════════════════════════════════════════

export interface StructuredFact {
  subject: string       // "user" | entity name
  predicate: string     // "likes" | "works_at" | "uses" | "dislikes" | "has" | "prefers" | "lives_in"
  object: string        // the value
  confidence: number    // 0-1
  source: 'user_said' | 'ai_inferred' | 'ai_observed'
  ts: number            // when extracted
  validUntil: number    // 0 = still valid, >0 = expired at this time
  memoryRef?: string    // link to original Memory content (first 60 chars)
  segmentId?: number    // 话题河流：关联到对应的对话段（P1 fix）
  supersedes?: string   // 事实版本链：本事实取代的旧 object 值
}

