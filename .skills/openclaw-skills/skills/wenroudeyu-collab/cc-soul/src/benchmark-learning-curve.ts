function generateMessages(): { content: string; domain: string; phase: number }[] {
  const messages: { content: string; domain: string; phase: number }[] = []
  for (const domain of DOMAINS) {
    const all = [
      ...domain.phase1.map(c => ({ content: c, domain: domain.name, phase: 1 })),
      ...domain.phase2.map(c => ({ content: c, domain: domain.name, phase: 2 })),
      ...domain.phase3.map(c => ({ content: c, domain: domain.name, phase: 3 })),
      ...domain.phase4.map(c => ({ content: c, domain: domain.name, phase: 4 })),
    ]
    messages.push(...all)
  }
  return messages
}

function toMemory(msg: { content: string; domain: string; phase: number }, idx: number): Memory {
  const scopeMap: Record<number, string> = { 1: 'fact', 2: 'preference', 3: 'event', 4: 'opinion' }
  const emotionMap: Record<number, string> = { 1: 'neutral', 2: 'neutral', 3: 'important', 4: 'neutral' }
  return {
    content: msg.content,
    scope: scopeMap[msg.phase] || 'fact',
    ts: Date.now() - (1200 - idx) * 60000, // spread over ~20 hours
    confidence: msg.phase === 1 ? 0.9 : msg.phase === 2 ? 0.8 : msg.phase === 3 ? 0.85 : 0.75,
    emotion: emotionMap[msg.phase],
    source: 'user_said',
    tier: 'long_term',
    recallCount: 0,
    memoryId: `bench_${idx}`,
  }
}

