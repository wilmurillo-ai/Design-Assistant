// ═══════════════════════════════════════════════════════════════════════════════
// EVALUATION
// ═══════════════════════════════════════════════════════════════════════════════

function evaluateRecall(results: Memory[], query: TestQuery): { hit3: boolean; top1: boolean } {
  const checkHit = (mem: Memory) => {
    const c = mem.content
    return query.expectedKeywords.some(kw => c.includes(kw))
  }
  const top3 = results.slice(0, 3)
  return {
    hit3: top3.some(r => checkHit(r)),
    top1: results.length > 0 && checkHit(results[0]),
  }
}

// ═══════════════════════════════════════════════════════════════════════════════
// MAIN BENCHMARK
// ═══════════════════════════════════════════════════════════════════════════════

const CHECKPOINTS = [50, 100, 200, 300, 400, 500, 600, 800, 1000, 1200]

async function main() {
  console.log('=== AAM Learning Curve Benchmark ===')
  console.log(`Domains: ${DOMAINS.length}, Messages per domain: 100, Total: 1200`)
  console.log(`Test queries: ${TEST_QUERIES.length} (${TEST_QUERIES.filter(q => q.type === 'direct').length} direct + ${TEST_QUERIES.filter(q => q.type === 'semantic').length} semantic)`)
  console.log(`Checkpoints: ${CHECKPOINTS.join(', ')}`)
  console.log()

  const allMessages = generateMessages()

  // Shuffle messages so domains are interleaved (more realistic)
  const shuffled = [...allMessages]
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]]
  }

  console.log(`Generated ${shuffled.length} messages across ${DOMAINS.length} domains`)
  console.log()

  // In-memory store (not written to disk)
  const memoryPool: Memory[] = []

  type Row = {
    messages: number
    hit3: string
    hit3Direct: string
    hit3Semantic: string
    top1: string
    aamVocab: number
    aamDocs: number
    avgLatencyMs: number
  }
  const results: Row[] = []

  let nextCheckpoint = 0

  for (let i = 0; i < shuffled.length; i++) {
    const msg = shuffled[i]
    const mem = toMemory(msg, i)
    memoryPool.push(mem)

    // Feed into AAM's association network
    const emotionIntensity = msg.phase === 3 ? 0.7 : msg.phase === 4 ? 0.5 : 0.2
    learnAssociation(msg.content, emotionIntensity)

    const count = i + 1

    // Check if we hit a checkpoint
    if (nextCheckpoint < CHECKPOINTS.length && count === CHECKPOINTS[nextCheckpoint]) {
      nextCheckpoint++

      // Run all 40 test queries
      let hit3Count = 0, top1Count = 0
      let hit3Direct = 0, hit3Semantic = 0
      let directCount = 0, semanticCount = 0
      let totalLatency = 0

      for (const tq of TEST_QUERIES) {
        const start = performance.now()
        const recalled = activationRecall(memoryPool, tq.query, 3, 0, 0.5)
        const elapsed = performance.now() - start
        totalLatency += elapsed

        const eval_ = evaluateRecall(recalled, tq)
        if (eval_.hit3) hit3Count++
        if (eval_.top1) top1Count++

        if (tq.type === 'direct') {
          directCount++
          if (eval_.hit3) hit3Direct++
        } else {
          semanticCount++
          if (eval_.hit3) hit3Semantic++
        }
      }

      const stats = getAAMStats()
      const row: Row = {
        messages: count,
        hit3: (hit3Count / TEST_QUERIES.length * 100).toFixed(1) + '%',
        hit3Direct: (hit3Direct / directCount * 100).toFixed(1) + '%',
        hit3Semantic: (hit3Semantic / semanticCount * 100).toFixed(1) + '%',
        top1: (top1Count / TEST_QUERIES.length * 100).toFixed(1) + '%',
        aamVocab: stats.vocabularySize,
        aamDocs: stats.totalDocs,
        avgLatencyMs: Math.round(totalLatency / TEST_QUERIES.length * 100) / 100,
      }
      results.push(row)

      console.log(`[Checkpoint ${count}] Hit@3=${row.hit3} (D:${row.hit3Direct} S:${row.hit3Semantic}) Top-1=${row.top1} Vocab=${row.aamVocab} Latency=${row.avgLatencyMs}ms`)
    }
  }

  // ── Final report ──
  console.log()
  console.log('=== RESULTS ===')
  console.log()

  // Table header
  const hdr = '| Messages | Hit@3  | Direct | Semantic | Top-1  | AAM Vocab | AAM Docs | Avg Latency |'
  const sep = '|----------|--------|--------|----------|--------|-----------|----------|-------------|'
  console.log(hdr)
  console.log(sep)
  for (const r of results) {
    console.log(
      `| ${String(r.messages).padStart(8)} | ${r.hit3.padStart(6)} | ${r.hit3Direct.padStart(6)} | ${r.hit3Semantic.padStart(8)} | ${r.top1.padStart(6)} | ${String(r.aamVocab).padStart(9)} | ${String(r.aamDocs).padStart(8)} | ${String(r.avgLatencyMs + 'ms').padStart(11)} |`
    )
  }
  console.log()

  // Summary
  const first = results[0]
  const last = results[results.length - 1]
  console.log(`Hit@3 improvement: ${first.hit3} → ${last.hit3}`)
  console.log(`Top-1 improvement: ${first.top1} → ${last.top1}`)
  console.log(`AAM vocabulary growth: ${first.aamVocab} → ${last.aamVocab}`)
  console.log(`Average latency range: ${results.map(r => r.avgLatencyMs).reduce((a, b) => Math.min(a, b))}ms - ${results.map(r => r.avgLatencyMs).reduce((a, b) => Math.max(a, b))}ms`)
}

main().catch(e => {
  console.error('Benchmark failed:', e)
  process.exit(1)
})
