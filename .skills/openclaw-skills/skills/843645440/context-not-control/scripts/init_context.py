#!/usr/bin/env python3
"""
Initialize project context and permission configuration.
Creates PROJECT.md and PERMISSION_CONFIG.yaml in the workspace.
"""

import os
import sys
import argparse
from pathlib import Path

def get_workspace_root():
    """Find workspace root (current directory or parent with .git)"""
    current = Path.cwd()
    if (current / '.git').exists():
        return current
    for parent in current.parents:
        if (parent / '.git').exists():
            return parent
    return current

def create_project_template(project_name, permission_level):
    """Generate PROJECT.md template"""
    return f"""# PROJECT.md - Project Context

> Auto-generated by context-not-control skill
> Last updated: {__import__('datetime').datetime.now().strftime('%Y-%m-%d %H:%M')}

---

## 🎯 Project: {project_name}

### Goals
<!-- What do you want to achieve? -->
- [ ] TODO: Define primary goal
- [ ] TODO: Define success criteria

### Target Users
<!-- Who will use this? -->
- TODO: Describe target users

### Core Use Cases
<!-- What are the main scenarios? -->
1. TODO: Primary use case
2. TODO: Secondary use case

### Technical Constraints
<!-- Any limitations or requirements? -->
- **Tech Stack**: TODO (e.g., Python, Node.js, React)
- **Platform**: TODO (e.g., Web, Mobile, Desktop)
- **Deployment**: TODO (e.g., Cloud, On-premise)
- **Budget**: TODO
- **Timeline**: TODO

### Success Criteria
<!-- What does "done" look like? -->
- [ ] TODO: Define measurable success criteria

---

## 🔧 Permission Level

**Current Level**: {permission_level} ({['Master Mode', 'Collaborative Mode', 'Assistant Mode'][permission_level-1]})

See PERMISSION_CONFIG.yaml for detailed permission rules.

---

## 📝 Iteration History

### Initial Setup
- Created project context
- Set permission level to {permission_level}

<!-- AI will append updates here -->
"""

def create_permission_config(permission_level):
    """Generate PERMISSION_CONFIG.yaml"""
    return f"""# Permission Configuration
# Generated by context-not-control skill

# Permission level: 1=Master, 2=Collaborative (default), 3=Assistant
permission_level: {permission_level}

# Red lines: Operations that ALWAYS require confirmation
red_lines:
  - spend_money
  - send_public_message
  - delete_database
  - restart_production_service
  - modify_production_config

# Yellow lines: Operations that require confirmation at level 2+
yellow_lines:
  - delete_file
  - modify_system_config
  - install_system_package
  - restart_service
  - send_email

# Green lines: Everything else (no confirmation needed at level 1-2)
# At level 3, all operations require confirmation

# Custom rules (optional)
custom_red_lines: []
custom_yellow_lines: []

# Notes:
# - Level 1 (Master): Only red lines require confirmation
# - Level 2 (Collaborative): Red + yellow lines require confirmation
# - Level 3 (Assistant): All operations require confirmation
"""

def main():
    parser = argparse.ArgumentParser(
        description='Initialize project context and permission configuration'
    )
    parser.add_argument(
        '--project-name',
        default='My Project',
        help='Project name (default: My Project)'
    )
    parser.add_argument(
        '--permission-level',
        type=int,
        choices=[1, 2, 3],
        default=2,
        help='Permission level: 1=Master, 2=Collaborative (default), 3=Assistant'
    )
    parser.add_argument(
        '--output-dir',
        help='Output directory (default: workspace root)'
    )
    
    args = parser.parse_args()
    
    # Determine output directory
    if args.output_dir:
        output_dir = Path(args.output_dir)
    else:
        output_dir = get_workspace_root()
    
    output_dir.mkdir(parents=True, exist_ok=True)
    
    # Create PROJECT.md
    project_file = output_dir / 'PROJECT.md'
    if project_file.exists():
        print(f"[WARNING] {project_file} already exists")
        response = input("Overwrite? (y/N): ")
        if response.lower() != 'y':
            print("[SKIP] PROJECT.md not modified")
        else:
            project_file.write_text(
                create_project_template(args.project_name, args.permission_level)
            )
            print(f"[OK] Created {project_file}")
    else:
        project_file.write_text(
            create_project_template(args.project_name, args.permission_level)
        )
        print(f"[OK] Created {project_file}")
    
    # Create PERMISSION_CONFIG.yaml
    config_file = output_dir / 'PERMISSION_CONFIG.yaml'
    if config_file.exists():
        print(f"[WARNING] {config_file} already exists")
        response = input("Overwrite? (y/N): ")
        if response.lower() != 'y':
            print("[SKIP] PERMISSION_CONFIG.yaml not modified")
        else:
            config_file.write_text(create_permission_config(args.permission_level))
            print(f"[OK] Created {config_file}")
    else:
        config_file.write_text(create_permission_config(args.permission_level))
        print(f"[OK] Created {config_file}")
    
    print("\n" + "="*60)
    print("✅ Initialization complete!")
    print("="*60)
    print(f"\nNext steps:")
    print(f"1. Edit {project_file} to fill in your project details")
    print(f"2. Review {config_file} to customize permission rules")
    print(f"3. Start working with AI using 'Context, not Control' approach")
    print(f"\nPermission level: {args.permission_level} ({['Master', 'Collaborative', 'Assistant'][args.permission_level-1]} Mode)")

if __name__ == '__main__':
    try:
        main()
    except KeyboardInterrupt:
        print("\n[CANCELLED] Initialization cancelled by user")
        sys.exit(1)
    except Exception as e:
        print(f"\n[ERROR] {e}", file=sys.stderr)
        sys.exit(1)
