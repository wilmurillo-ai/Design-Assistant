def explain_error_code(code: Any, msg: str) -> str:
    try:
        code_int = int(code)
    except Exception:
        code_int = None

    hint = ERROR_HINTS.get(code_int, "请检查参数、权限、签名、时间戳或网络状态。")
    return f"错误码: {code}\n错误信息: {msg}\n建议: {hint}"