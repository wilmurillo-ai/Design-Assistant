AQI_LEVELS = [
    (0, 50, '优', '😀'),
    (51, 100, '良', '🙂'),
    (101, 150, '轻度污染', '😐'),
    (151, 200, '中度污染', '😷'),
    (201, 300, '重度污染', '😱'),
    (301, 999, '严重污染', '💀'),
]


def get_aqi_level(aqi):
    for lo, hi, name, emoji in AQI_LEVELS:
        if lo <= aqi <= hi:
            return f'{emoji} {name} (AQI {aqi})'
    return f'? (AQI {aqi})'


