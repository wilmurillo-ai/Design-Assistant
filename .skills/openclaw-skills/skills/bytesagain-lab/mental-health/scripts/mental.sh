    *)       level_cn="$level" level_icon="🟡" ;;
  esac

  echo "╔══════════════════════════════════════════════════════════╗"
  echo "║           😤 压力管理: ${level_icon} ${level_cn}"
  echo "╚══════════════════════════════════════════════════════════╝"
  echo ""

  case "$level" in
    low)
      cat <<'EOF'
