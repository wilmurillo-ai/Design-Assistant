cmd_breathe() {
  local technique="${1:-deep}"

  echo "╔══════════════════════════════════════════════════════════╗"
  echo "║           🫁 呼吸练习"
  echo "╚══════════════════════════════════════════════════════════╝"
  echo ""

  case "$technique" in
    478)
      cat <<'EOF'
🧘 4-7-8 呼吸法 (快速放松)
────────────────────────────────────────
  这是最有效的快速放松技巧之一。

  准备: 找一个舒适的姿势坐好
  舌尖轻抵上颚，门牙后方

  开始练习 (重复4次):

  ┌─────────────────────────────────┐
  │  👃 吸气 ── 4秒               │
  │  ⏸️ 屏息 ── 7秒               │
  │  👄 呼气 ── 8秒               │
  └─────────────────────────────────┘

  第1次:  吸 1..2..3..4
          屏 1..2..3..4..5..6..7
          呼 1..2..3..4..5..6..7..8

  第2次:  吸 1..2..3..4
          屏 1..2..3..4..5..6..7
          呼 1..2..3..4..5..6..7..8

  第3次:  吸 1..2..3..4
          屏 1..2..3..4..5..6..7
          呼 1..2..3..4..5..6..7..8

  第4次:  吸 1..2..3..4
          屏 1..2..3..4..5..6..7
          呼 1..2..3..4..5..6..7..8

  ✅ 做完了吗？感受一下身体的变化。
  适用: 焦虑、入睡困难、紧张时刻
EOF
      ;;
    box)
      cat <<'EOF'
🧘 方块呼吸法 (Box Breathing)
────────────────────────────────────────
  美国海豹突击队使用的压力管理技巧。

  ┌──── 吸气4秒 ────┐
  │                   │
  屏     ┌───┐      屏
  息     │   │      息
  4秒    │   │      4秒
  │      └───┘      │
  └──── 呼气4秒 ────┘

  练习 (重复4-6次):

  ┌─────────────────────────────────┐
  │  👃 吸气 ── 4秒               │
  │  ⏸️ 屏息 ── 4秒               │
  │  👄 呼气 ── 4秒               │
  │  ⏸️ 屏息 ── 4秒               │
  └─────────────────────────────────┘

  适用: 压力大、需要集中注意力、情绪激动时
EOF
      ;;
    deep|*)
      cat <<'EOF'
🧘 腹式深呼吸 (基础放松)
────────────────────────────────────────
  最简单也最有效的放松方式。

  准备:
  • 舒适地坐着或躺下
  • 一只手放在胸口，一只手放在腹部

  练习 (重复5-10次):

  ┌─────────────────────────────────┐
  │  👃 鼻子缓慢吸气 ── 5秒      │
  │     (感觉腹部鼓起)             │
  │                                 │
  │  👄 嘴巴缓慢呼气 ── 5秒      │
  │     (感觉腹部收缩)             │
  └─────────────────────────────────┘

  要点:
  ✅ 腹部的手要有起伏
  ✅ 胸口的手基本不动
  ✅ 呼气比吸气略长
  ✅ 不要憋气

  适用: 日常放松、初学者、随时随地
EOF
      ;;
  esac
}

cmd_journal() {
  local prompt="${1:-}"
  local date
  date=$(date +%Y-%m-%d)

  local prompts=(
    "今天发生了什么让你有感触的事？"
    "现在你最大的感受是什么？试着给它命名。"
    "今天有什么让你开心/难过/生气/焦虑的事？"
    "如果你能对今天的自己说一句话，你会说什么？"
    "最近有什么一直在你心里的事情？写出来看看。"
    "今天你对自己做了哪些好事？"
    "什么事情在消耗你的能量？什么在补充？"
    "如果压力是0-10分，现在是几分？是什么导致的？"
  )

  # Pick a random prompt if none given
  if [ -z "$prompt" ]; then
    local idx=$((RANDOM % ${#prompts[@]}))
    prompt="${prompts[$idx]}"
  fi

  cat <<EOF
╔══════════════════════════════════════════════════════════╗
║           📓 情绪日记 — ${date}
╚══════════════════════════════════════════════════════════╝

💭 今日引导问题:
────────────────────────────────────────

  "${prompt}"

────────────────────────────────────────

📝 写作建议:
  • 不用在意文笔，想到什么写什么
  • 诚实面对自己的感受
  • 不评判，只记录
  • 写完后看看是否好受一些

💡 写作框架:
  1. 今天的情绪: (开心/平静/焦虑/难过/愤怒...)
  2. 发生了什么: (事件描述)
  3. 我的感受:   (身体感受+心理感受)
  4. 我的想法:   (对这件事的看法)
  5. 我能做什么: (下一步行动)

📋 更多引导问题:
EOF

  for p in "${prompts[@]}"; do
    echo "  • ${p}"
  done

  echo ""
  echo "💡 坚持写情绪日记可以帮助:"
  echo "   ✅ 提高情绪觉察能力"
  echo "   ✅ 释放负面情绪"
  echo "   ✅ 发现情绪模式"
  echo "   ✅ 辅助心理咨询"
}

