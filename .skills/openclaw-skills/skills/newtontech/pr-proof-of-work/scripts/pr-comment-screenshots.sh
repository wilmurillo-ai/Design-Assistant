#!/usr/bin/env bash
# Post before/after screenshots as a PR comment.
#
# Usage: pr-comment-screenshots.sh <owner/repo> <pr-number> <screenshot-dir>
#
# Reads manifest.json from each test subdirectory, uploads images to a
# temporary branch, and posts a markdown comment with embedded images.
#
# No Python dependency — uses only grep/cut for JSON parsing.
#
set -euo pipefail

REPO="${1:?Usage: pr-comment-screenshots.sh <owner/repo> <pr-number> <screenshot-dir>}"
PR_NUM="${2:?Missing PR number}"
SCREENSHOT_DIR="${3:?Missing screenshot dir}"

if [ ! -d "$SCREENSHOT_DIR" ]; then
  echo "ERROR: screenshot dir not found: $SCREENSHOT_DIR" >&2
  exit 1
fi

cd "$(git rev-parse --show-toplevel)"

# ── 1. Push screenshots to a temp branch for raw URLs ──
BRANCH="e2e-screenshots-${PR_NUM}"
CURRENT=$(git branch --show-current)

# Save screenshots to temp location
TMP=$(mktemp -d)
cp -r "$SCREENSHOT_DIR"/* "$TMP/" 2>/dev/null || true

git checkout -b "$BRANCH" 2>/dev/null || git checkout "$BRANCH" 2>/dev/null
mkdir -p e2e-screenshots
cp -r "$TMP"/* e2e-screenshots/ 2>/dev/null || true
git add e2e-screenshots/
git commit -m "chore: add E2E screenshots for PR #${PR_NUM}" 2>/dev/null || true

REMOTE=$(git remote -v | grep -m1 "(push)" | awk '{print $1}')
git push "$REMOTE" "$BRANCH" --force 2>&1 || true
git checkout "$CURRENT" 2>/dev/null

# ── 2. Build comment from manifests ──
BODY="## E2E Test Screenshots\n\n"
BODY+="| Test | Before (Bug) | After (Fixed) |\n|---|:---:|:---:|\n"

HAS_PAIRS=false
for manifest in "$SCREENSHOT_DIR"/*/manifest.json; do
  [ -f "$manifest" ] || continue

  # Parse simple JSON with grep (no jq/python dependency needed)
  TEST_NAME=$(grep -o '"testName":"[^"]*"' "$manifest" | cut -d'"' -f4 || echo "test")
  BEFORE_PATH=$(grep -o '"before":"[^"]*"' "$manifest" | cut -d'"' -f4 || echo "")
  AFTER_PATH=$(grep -o '"after":"[^"]*"' "$manifest" | cut -d'"' -f4 || echo "")

  BEFORE_FILENAME=$(basename "$BEFORE_PATH" 2>/dev/null || echo "")
  AFTER_FILENAME=$(basename "$AFTER_PATH" 2>/dev/null || echo "")
  TEST_DIR=$(basename "$(dirname "$manifest")" 2>/dev/null || echo "")

  if [ -n "$BEFORE_FILENAME" ] && [ -n "$AFTER_FILENAME" ]; then
    HAS_PAIRS=true
    BODY+="| **${TEST_NAME}** | "
    BODY+="![Before](https://github.com/${REPO}/raw/${BRANCH}/e2e-screenshots/${TEST_DIR}/${BEFORE_FILENAME}) | "
    BODY+="![After](https://github.com/${REPO}/raw/${BRANCH}/e2e-screenshots/${TEST_DIR}/${AFTER_FILENAME}) |\n"
  fi
done

if [ "$HAS_PAIRS" = false ]; then
  BODY+="_No before/after pairs found in ${SCREENSHOT_DIR}_\n"
fi

BODY+="\n---\n_Generated by tdd-e2e-pr-workflow skill_"

echo -e "$BODY" > /tmp/pr-comment-body.md

# ── 3. Post comment ──
gh pr comment "$PR_NUM" --repo "$REPO" --body-file /tmp/pr-comment-body.md
echo "Comment posted to PR #$PR_NUM"

# Cleanup
rm -rf "$TMP" /tmp/pr-comment-body.md
