#!/bin/bash
# Daily cost report with HTML email delivery
# Usage: daily-cost-report-email.sh [date]

set -euo pipefail

DATE="${1:-yesterday}"

# Handle date format for macOS/Linux compatibility
if date -v-1d +%Y-%m-%d >/dev/null 2>&1; then
  # macOS
  DATE_STR=$(date -j -f "%Y-%m-%d" "$DATE" "+%Y-%m-%d" 2>/dev/null || date -j -v-1d "+%Y-%m-%d")
else
  # Linux
  DATE_STR=$(date -d "$DATE" "+%Y-%m-%d" 2>/dev/null || date -d "yesterday" "+%Y-%m-%d")
fi

REPORT_FILE="/tmp/cost-report-${DATE_STR}.md"
HTML_FILE="/tmp/cost-report-${DATE_STR}.html"

# Generate the markdown report
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
bash "$SCRIPT_DIR/daily-cost-report.sh" "$DATE" > /dev/null 2>&1 || true

# If report exists, create HTML email
if [ -f "$REPORT_FILE" ]; then
  # Extract summary stats
  TOTAL_COST=$(grep "Total Cost" "$REPORT_FILE" | head -1 | awk -F'|' '{print $3}' | xargs)
  TOTAL_TOKENS=$(grep "Total Tokens" "$REPORT_FILE" | head -1 | awk -F'|' '{print $3}' | xargs)
  REPORT_DATE=$(grep "Date:" "$REPORT_FILE" | head -1 | awk -F: '{print $2}' | xargs)
  
  # Create HTML with embedded markdown (formatted as readable HTML)
  cat > "$HTML_FILE" << EOF
<!DOCTYPE html>
<html>
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <style>
    * { margin: 0; padding: 0; box-sizing: border-box; }
    body { 
      font-family: -apple-system, BlinkMacSystemFont, "Segoe UI", Roboto, "Helvetica Neue", sans-serif;
      color: #333;
      background: #f5f5f5;
      padding: 20px;
    }
    .container { 
      max-width: 600px; 
      margin: 0 auto; 
      background: white; 
      padding: 40px; 
      border-radius: 8px; 
      box-shadow: 0 2px 8px rgba(0,0,0,0.1);
    }
    .header { 
      text-align: center; 
      margin-bottom: 30px; 
      border-bottom: 2px solid #f0f0f0; 
      padding-bottom: 20px;
    }
    .header h1 { 
      font-size: 28px; 
      margin-bottom: 5px; 
      color: #000;
    }
    .header .date { 
      color: #666; 
      font-size: 14px;
    }
    .metrics { 
      background: #f9f9f9; 
      padding: 20px; 
      border-radius: 6px; 
      margin-bottom: 30px;
    }
    .metric-row { 
      display: flex; 
      justify-content: space-between; 
      padding: 10px 0;
      border-bottom: 1px solid #eee;
    }
    .metric-row:last-child { 
      border-bottom: none;
    }
    .metric-label { 
      color: #666; 
      font-weight: 500;
    }
    .metric-value { 
      color: #0066cc; 
      font-weight: 600;
    }
    h2 { 
      font-size: 18px; 
      margin-top: 30px; 
      margin-bottom: 15px; 
      color: #222;
      border-bottom: 1px solid #e0e0e0;
      padding-bottom: 10px;
    }
    table { 
      width: 100%; 
      border-collapse: collapse;
      margin-bottom: 20px;
      font-size: 14px;
    }
    th { 
      background: #f0f0f0; 
      padding: 12px; 
      text-align: left; 
      font-weight: 600; 
      border: 1px solid #ddd;
    }
    td { 
      padding: 10px 12px; 
