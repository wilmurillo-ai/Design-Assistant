def get_stock_name(company_name):
    """获取企业对应的股票名称"""
    # 精确匹配
    for key, stock_name in STOCK_NAMES.items():
        if key in company_name:
            return stock_name
    
    # 模糊匹配（取前3个字）
    company_short = company_name[:3]
    for key, stock_name in STOCK_NAMES.items():
        if company_short in key or key in company_short:
            return stock_name
    
    return None
