def extract_keywords(material: str) -> list:
    """从材料中提取关键词"""
    # 提取数字、百分比、公司名等
    patterns = [
        r'\d+[.%]',  # 数字和百分比
        r'[A-Z][a-z]+[A-Z][a-z]+',  # 驼峰命名
        r'[\u4e00-\u9fff]{2,}',  # 中文词汇
    ]
    keywords = []
    for pattern in patterns:
        keywords.extend(re.findall(pattern, material))
    return list(set(keywords))[:20]

def generate_title(material: str) -> str:
    """根据材料生成报告标题"""
    # 尝试从材料中提取主题
    lines = material.strip().split('\n')
    for line in lines[:10]:
        if len(line) > 10 and len(line) < 50:
            # 清理特殊字符
            title = re.sub(r'[#*·•\-\d:.%]', '', line).strip()
            if 5 < len(title) < 40:
                return title
    return "深度研究报告"

def analyze_material(material: str) -> dict:
    """分析材料，提取结构化信息"""
    result = {
        'facts': [],      # 事实
        'inferences': [], # 推论
        'judgments': [],  # 判断
        'data': [],       # 数据
        'companies': [],  # 公司
        'trends': [],     # 趋势
    }
    
    lines = material.strip().split('\n')
    for line in lines:
        line = line.strip()
        if not line:
            continue
        
        # 识别数据类型
        if any(kw in line for kw in ['%', '亿元', '亿美元', '倍', '增速']):
            result['data'].append(line)
        
        # 识别公司
        if any(kw in line for kw in ['科技', '股份', '有限公司', '集团']):
            result['companies'].append(line)
        
        # 识别趋势
        if any(kw in line for kw in ['增长', '爆发', '下滑', '反转', '拐点']):
            result['trends'].append(line)
    
    return result

