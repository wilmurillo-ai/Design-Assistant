from __future__ import annotations

import os
import sys

import pytest
from hypothesis import given, settings, strategies as st

# Ensure the skill package is importable
sys.path.insert(0, os.path.join(os.path.dirname(__file__), ".."))

from step_executor import RetryHandler, StepExecutor
from stream_handler import StreamingOutputHandler

# ---------------------------------------------------------------------------
# Strategies
# ---------------------------------------------------------------------------

# Generate random lists of token strings (1-50 tokens, each 0-20 chars)
token_lists = st.lists(
    st.text(min_size=0, max_size=20),
    min_size=1,
    max_size=50,
)

# ---------------------------------------------------------------------------
# Test helpers
# ---------------------------------------------------------------------------


class TokenStreamingMockLLM:
    """Mock LLM that streams a predetermined list of tokens."""

    def __init__(self, tokens: list[str]) -> None:
        self._tokens = tokens

    async def generate(self, prompt: str):
        for token in self._tokens:
            yield token


class RecordingStreamHandler(StreamingOutputHandler):
    """Stream handler that records all on_token calls."""

    def __init__(self) -> None:
        self.recorded_tokens: list[str] = []
        self.step_start_calls: list[tuple[int, int, str]] = []
        self.step_complete_calls: list[tuple[str, float]] = []

    def on_step_start(self, step_number: int, total_steps: int, step_name: str) -> None:
        self.step_start_calls.append((step_number, total_steps, step_name))

    def on_token(self, token: str) -> None:
        self.recorded_tokens.append(token)

    def on_step_complete(self, step_name: str, time_taken_seconds: float) -> None:
        self.step_complete_calls.append((step_name, time_taken_seconds))

    def on_pipeline_complete(self, total_time_seconds: float, steps_executed: int) -> None:
        pass


# ---------------------------------------------------------------------------
# Property 12: Token streaming 正確轉發
# ---------------------------------------------------------------------------


class TestTokenStreamingProperty:
    """**Validates: Requirements 6.2**"""

    @settings(max_examples=100)
    @given(tokens=token_lists)
    @pytest.mark.asyncio
    async def test_all_tokens_forwarded_to_stream_handler(
        self, tokens: list[str]
    ) -> None:
        """For any sequence of tokens generated by the LLM during a step
        execution, each token should be forwarded to the streaming output
        handler via on_token."""

        recorder = RecordingStreamHandler()
        llm = TokenStreamingMockLLM(tokens)
        retry_handler = RetryHandler(max_retries=0)
        executor = StepExecutor(llm, retry_handler, recorder)

        await executor.execute_step(
            step_name="draft",
            prompt_template="Write about: {{context}}",
            context="test topic",
            step_number=1,
            total_steps=1,
        )

        # All tokens from LLM must be forwarded to stream handler
        assert recorder.recorded_tokens == tokens, (
            f"Tokens forwarded to stream handler do not match LLM tokens.\n"
            f"  LLM tokens: {tokens!r}\n"
            f"  Recorded:   {recorder.recorded_tokens!r}"
        )

    @settings(max_examples=100)
    @given(tokens=token_lists)
    @pytest.mark.asyncio
    async def test_token_concatenation_equals_step_output(
        self, tokens: list[str]
    ) -> None:
        """The concatenation of all forwarded tokens should equal the step's
        complete output."""

        recorder = RecordingStreamHandler()
        llm = TokenStreamingMockLLM(tokens)
        retry_handler = RetryHandler(max_retries=0)
        executor = StepExecutor(llm, retry_handler, recorder)

        result = await executor.execute_step(
            step_name="plan",
            prompt_template="Plan: {{context}}",
            context="some task",
            step_number=1,
            total_steps=1,
        )

        expected_output = "".join(tokens)

        # Concatenation of forwarded tokens == step output
        assert "".join(recorder.recorded_tokens) == expected_output, (
            f"Concatenation of recorded tokens does not match expected.\n"
            f"  Concatenated: {''.join(recorder.recorded_tokens)!r}\n"
            f"  Expected:     {expected_output!r}"
        )

        # Step output must also equal the concatenation
        assert result.output == expected_output, (
            f"Step output does not match token concatenation.\n"
            f"  Step output: {result.output!r}\n"
            f"  Expected:    {expected_output!r}"
        )
