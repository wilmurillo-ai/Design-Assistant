#!/usr/bin/env python3
"""
metadata.py - 元数据处理模块
提供元数据提取、格式转换、BibTeX 导出等功能

命令行参数:
  输入方式 (选择一个):
    --id                  单个 arXiv ID
    --input, -i           输入元数据 JSON 文件
    --merge               合并多个元数据文件

  输出选项:
    --format, -f          输出格式: json, bibtex, csv, ris (默认: json)
    --output, -o          输出文件路径

  日志 (可选):
    --log-level           日志级别: DEBUG, INFO, WARNING, ERROR (默认: INFO)
    --log-file            日志文件路径
"""

import argparse
import sys
import csv
from datetime import datetime, timezone
from pathlib import Path
from typing import Dict, Any, List, Optional, TextIO

# 支持作为模块导入和独立脚本运行
try:
    from .utils import (
        setup_logging,
        get_logger,
        load_json,
        save_json,
        parse_arxiv_id,
    )
    from .search import ArxivSearch
except ImportError:
    from utils import (
        setup_logging,
        get_logger,
        load_json,
        save_json,
        parse_arxiv_id,
    )
    from search import ArxivSearch


class MetadataExtractor:
    """元数据提取器"""

    def __init__(
        self,
        log_level: str = "INFO",
        log_file: str = None,
    ):
        """
        初始化提取器

        Args:
            log_level: 日志级别
            log_file: 日志文件路径
        """
        setup_logging(level=log_level, log_file=log_file)
        self.logger = get_logger()
        self.searcher = ArxivSearch(log_level=log_level, log_file=log_file)

    def get_metadata(self, arxiv_id: str) -> Optional[Dict[str, Any]]:
        """
        获取单个论文的元数据

        Args:
            arxiv_id: arXiv ID

        Returns:
            论文元数据
        """
        results = self.searcher.search_by_id([arxiv_id])
        if results["papers"]:
            return results["papers"][0]
        return None

    def to_bibtex(self, paper: Dict[str, Any]) -> str:
        """
        将论文元数据转换为 BibTeX 格式

        Args:
            paper: 论文元数据

        Returns:
            BibTeX 字符串
        """
        arxiv_id = paper.get("arxiv_id", "")
        title = paper.get("title", "").replace("{", "\\{").replace("}", "\\}")
        authors = paper.get("authors", [])

        author_strs = []
        for author in authors:
            name = author.get("name", "")
            if "," in name:
                author_strs.append(name)
            else:
                parts = name.split()
                if len(parts) > 1:
                    author_strs.append(f"{parts[-1]}, {' '.join(parts[:-1])}")
                else:
                    author_strs.append(name)

        author_field = " and ".join(author_strs)

        first_author = authors[0].get("name", "").split(",")[0].lower() if authors else "unknown"
        year = paper.get("published_date", "")[:4] if paper.get("published_date") else "0000"
        first_word = paper.get("title", "").split()[0].lower() if paper.get("title") else "paper"
        citation_key = f"{first_author}:{year}:{first_word}"

        bibtex = f"@article{{{citation_key},\n"
        bibtex += f"  title = {{{title}}},\n"
        bibtex += f"  author = {{{author_field}}},\n"

        if paper.get("published_date"):
            bibtex += f"  year = {{{paper['published_date'][:4]}}},\n"
            bibtex += f"  month = {{{paper['published_date'][5:7]}}},\n"

        if paper.get("primary_category"):
            bibtex += f"  journal = {{arXiv preprint {paper['arxiv_id']}}},\n"

        bibtex += f"  eprint = {{{paper['arxiv_id']}}},\n"
        bibtex += f"  archivePrefix = {{arXiv}},\n"

        if paper.get("primary_category"):
            bibtex += f"  primaryClass = {{{paper['primary_category']}}},\n"

        bibtex += f"  url = {{{paper.get('arxiv_url', '')}}},\n"
        abstract_short = paper.get('abstract', '')[:500]
        if len(paper.get('abstract', '')) > 500:
            abstract_short += "..."
        bibtex += f"  abstract = {{{abstract_short}}}\n"
        bibtex += "}\n"

        return bibtex

    def export_bibtex(self, papers: List[Dict[str, Any]], output_path: str) -> None:
        """批量导出为 BibTeX"""
        output_path = Path(output_path)
        output_path.parent.mkdir(parents=True, exist_ok=True)

        with open(output_path, "w", encoding="utf-8") as f:
            f.write(f"% BibTeX export generated by arXiv Search Skill\n")
            f.write(f"% Generated on: {datetime.now(timezone.utc).isoformat()}Z\n")
            f.write(f"% Total papers: {len(papers)}\n\n")

            for paper in papers:
                bibtex = self.to_bibtex(paper)
                f.write(bibtex)
                f.write("\n")

        self.logger.info(f"BibTeX 已导出到: {output_path}")

    def to_ris(self, paper: Dict[str, Any]) -> str:
        """转换为 RIS 格式"""
        ris = "TY  - JOUR\n"

        if paper.get("title"):
            ris += f"T1  - {paper['title']}\n"

        for author in paper.get("authors", []):
            ris += f"AU  - {author.get('name', '')}\n"

        if paper.get("published_date"):
            date_parts = paper["published_date"].split("-")
            py_line = f"PY  - {date_parts[0]}"
            if len(date_parts) > 1:
                py_line += f"/{date_parts[1]}"
            if len(date_parts) > 2:
                py_line += f"/{date_parts[2]}"
            ris += py_line + "\n"

        if paper.get("abstract"):
            ris += f"AB  - {paper['abstract']}\n"

        ris += f"AN  - {paper.get('arxiv_id', '')}\n"
        ris += f"UR  - {paper.get('arxiv_url', '')}\n"

        if paper.get("categories"):
            ris += f"KW  - {', '.join(paper['categories'])}\n"

        ris += "ER  - \n\n"
        return ris

    def export_ris(self, papers: List[Dict[str, Any]], output_path: str) -> None:
        """导出 RIS 格式"""
        output_path = Path(output_path)
        output_path.parent.mkdir(parents=True, exist_ok=True)

        with open(output_path, "w", encoding="utf-8") as f:
            for paper in papers:
                f.write(self.to_ris(paper))

        self.logger.info(f"RIS 已导出到: {output_path}")

    def export_csv(self, papers: List[Dict[str, Any]], output_path: str) -> None:
        """导出 CSV 格式"""
        output_path = Path(output_path)
        output_path.parent.mkdir(parents=True, exist_ok=True)

        fieldnames = [
            "arxiv_id",
            "title",
            "authors",
            "published_date",
            "updated_date",
            "primary_category",
            "categories",
            "arxiv_url",
            "pdf_url",
            "doi",
            "abstract",
        ]

        with open(output_path, "w", encoding="utf-8", newline="") as f:
            writer = csv.DictWriter(f, fieldnames=fieldnames)
            writer.writeheader()

            for paper in papers:
                row = {
                    "arxiv_id": paper.get("arxiv_id", ""),
                    "title": paper.get("title", ""),
                    "authors": "; ".join([a.get("name", "") for a in paper.get("authors", [])]),
                    "published_date": paper.get("published_date", ""),
                    "updated_date": paper.get("updated_date", ""),
                    "primary_category": paper.get("primary_category", ""),
                    "categories": ", ".join(paper.get("categories", [])),
                    "arxiv_url": paper.get("arxiv_url", ""),
                    "pdf_url": paper.get("pdf_url", ""),
                    "doi": paper.get("doi", ""),
                    "abstract": paper.get("abstract", "")[:1000],
                }
                writer.writerow(row)

        self.logger.info(f"CSV 已导出到: {output_path}")

    def merge_metadata(self, metadata_files: List[str], dedup_by: str = "arxiv_id") -> Dict[str, Any]:
        """合并多个元数据文件"""
        all_papers = []
        seen_ids = set()

        for filepath in metadata_files:
            try:
                data = load_json(filepath)
                papers = data.get("papers", []) if isinstance(data, dict) else data

                for paper in papers:
                    paper_id = paper.get(dedup_by)
                    if paper_id and paper_id not in seen_ids:
                        seen_ids.add(paper_id)
                        all_papers.append(paper)

            except Exception as e:
                self.logger.warning(f"无法加载文件 {filepath}: {e}")

        self.logger.info(f"合并完成，共 {len(all_papers)} 篇论文（去重后）")

        return {
            "search_metadata": {
                "merged_from": [str(Path(f).name) for f in metadata_files],
                "timestamp": datetime.now(timezone.utc).isoformat() + "Z",
                "returned_results": len(all_papers),
            },
            "papers": all_papers,
        }


def main():
    """命令行入口"""
    parser = argparse.ArgumentParser(
        description="arXiv 元数据处理工具",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
示例:
  # 获取单个论文元数据
  python metadata.py --id 2301.01234

  # 从元数据文件导出 BibTeX
  python metadata.py --input results.json --format bibtex --output refs.bib

  # 导出 CSV
  python metadata.py --input results.json --format csv --output papers.csv

  # 合并多个元数据文件
  python metadata.py --merge result1.json result2.json --output merged.json
        """
    )

    # 输入方式
    input_group = parser.add_mutually_exclusive_group(required=True)
    input_group.add_argument("--id", help="单个 arXiv ID")
    input_group.add_argument("--input", "-i", help="输入元数据 JSON 文件")
    input_group.add_argument("--merge", nargs="+", help="合并多个元数据文件")

    # 输出格式
    parser.add_argument(
        "--format", "-f",
        choices=["json", "bibtex", "csv", "ris"],
        default="json",
        help="输出格式 (默认: json)",
    )
    parser.add_argument("--output", "-o", help="输出文件路径")

    # 日志
    parser.add_argument("--log-level", choices=["DEBUG", "INFO", "WARNING", "ERROR"], default="INFO", help="日志级别 (默认: INFO)")
    parser.add_argument("--log-file", help="日志文件路径")

    args = parser.parse_args()

    extractor = MetadataExtractor(
        log_level=args.log_level,
        log_file=args.log_file,
    )

    logger = get_logger()

    try:
        papers = []

        if args.id:
            paper = extractor.get_metadata(args.id)
            if paper:
                papers = [paper]
            else:
                logger.error(f"无法获取论文: {args.id}")
                return 1

        elif args.input:
            data = load_json(args.input)
            if isinstance(data, dict) and "papers" in data:
                papers = data["papers"]
            elif isinstance(data, list):
                papers = data
            else:
                logger.error("无法识别的输入格式")
                return 1

        elif args.merge:
            merged = extractor.merge_metadata(args.merge)
            papers = merged["papers"]

            if args.format == "json" and args.output:
                save_json(merged, args.output)
                logger.info(f"合并结果已保存到: {args.output}")

        if not papers:
            logger.warning("没有处理的论文")
            return 0

        if args.format == "bibtex":
            if args.output:
                extractor.export_bibtex(papers, args.output)
            else:
                for paper in papers:
                    print(extractor.to_bibtex(paper))

        elif args.format == "ris":
            if args.output:
                extractor.export_ris(papers, args.output)
            else:
                for paper in papers:
                    print(extractor.to_ris(paper))

        elif args.format == "csv":
            if args.output:
                extractor.export_csv(papers, args.output)
            else:
                logger.error("CSV 格式需要指定 --output 文件")
                return 1

        else:  # json
            output_data = {
                "metadata": {
                    "generated_at": datetime.now(timezone.utc).isoformat() + "Z",
                    "count": len(papers),
                },
                "papers": papers,
            }
            if args.output:
                save_json(output_data, args.output)
                logger.info(f"已保存到: {args.output}")
            else:
                import json
                print(json.dumps(output_data, indent=2, ensure_ascii=False))

        print(f"\n处理完成: {len(papers)} 篇论文")
        return 0

    except Exception as e:
        logger.error(f"执行失败: {e}")
        import traceback
        traceback.print_exc()
        return 1


if __name__ == "__main__":
    sys.exit(main())
