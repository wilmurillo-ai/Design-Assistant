const fs = require("fs");
const path = require("path");
const logger = require("./logger");
const securityScrubber = require("./securityScrubber");

/**
 * Manifest Generator Logic (v1.0.0)
 * Generates JSON and Markdown inventory reports with backup support.
 */

const manifestGenerator = {
  /**
   * Detects duplicate skills across different sources.
   */
  _detectDuplicates: (skills) => {
    const skillMap = new Map();
    
    skills.forEach((skill) => {
      const key = skill.name.toLowerCase();
      if (!skillMap.has(key)) {
        skillMap.set(key, []);
      }
      skillMap.get(key).push(skill);
    });

    const duplicates = [];
    const recommendedOrder = ["clawhub", "skillsmp", "git", "npm", "manual"];

    skillMap.forEach((instances, name) => {
      if (instances.length > 1) {
        const sorted = instances.sort((a, b) => {
          const idxA = recommendedOrder.indexOf(a.method);
          const idxB = recommendedOrder.indexOf(b.method);
          if (idxA !== idxB) return idxA - idxB;
          
          const verA = a.version || "0.0.0";
          const verB = b.version || "0.0.0";
          return manifestGenerator._compareVersions(verB, verA);
        });

        duplicates.push({
          name: instances[0].name,
          count: instances.length,
          instances: instances.map((i) => ({
            method: i.method,
            source: i.source,
            version: i.version || "unknown",
            path: i.path
          })),
          recommended: sorted[0]
        });
      }
    });

    return duplicates;
  },

  _compareVersions: (v1, v2) => {
    const parts1 = v1.split(".").map((p) => isNaN(Number(p)) ? 0 : Number(p));
    const parts2 = v2.split(".").map((p) => isNaN(Number(p)) ? 0 : Number(p));
    for (let i = 0; i < Math.max(parts1.length, parts2.length); i++) {
      const p1 = parts1[i] || 0;
      const p2 = parts2[i] || 0;
      if (p1 !== p2) return p1 - p2;
    }
    return 0;
  },

  /**
   * Generates the dual manifest (JSON + MD).
   */
  generate: (skills, outputPath, templatePath) => {
    const jsonPath = outputPath.endsWith(".json") ? outputPath : `${outputPath}.json`;
    const mdPath = jsonPath.replace(".json", ".md");

    const duplicates = manifestGenerator._detectDuplicates(skills);

    // 1. Prepare JSON Data
    const manifestData = {
      manifestVersion: 2,
      lastUpdated: new Date().toISOString(),
      totalSkills: skills.length,
      duplicates: duplicates,
      skills: skills.map((s) => ({
        name: s.name,
        version: s.version || "0.0.1",
        source: s.source,
        method: s.method,
        isPrivate: s.isPrivate,
        path: s.path,
        realPath: s.realPath,
        description: s.description || "",
        status: s.status || "active",
        lastChecked: new Date().toISOString()
      }))
    };

    // 2. Backup Existing Manifest
    if (fs.existsSync(jsonPath)) {
      try {
        fs.copyFileSync(jsonPath, `${jsonPath}.bak`);
        logger.info(`Backup created: ${path.basename(jsonPath)}.bak`);
      } catch (e) {
        logger.warn(`Failed to create backup: ${e.message}`);
      }
    }

    // 3. Write JSON Manifest
    try {
      fs.writeFileSync(jsonPath, JSON.stringify(manifestData, null, 2));
      logger.success(`Manifest JSON updated: ${jsonPath}`);
    } catch (e) {
      logger.error(`Failed to write JSON manifest: ${e.message}`);
    }

    // 4. Generate Markdown Report
    manifestGenerator._generateMarkdown(manifestData, mdPath, templatePath);
  },

  _generateMarkdown: (data, mdPath, templatePath) => {
    try {
      let content = "";
      if (fs.existsSync(templatePath)) {
        content = fs.readFileSync(templatePath, "utf8");
      } else {
        content = `# SKILLS MANIFEST\n\nGenerated by OpenClaw Inventory Manager\n\nLast Updated: {{lastUpdated}}\n\nTotal Skills: {{totalSkills}}\n\n{{duplicatesSection}}\n\n| Icon | Name | Version | Description | Source | Method | Private | Status |\n| :--- | :--- | :---: | :--- | :--- | :---: | :---: | :---: |\n{{skillRows}}`;
      }

      const rows = data.skills
        .map((s) => {
          const icon = manifestGenerator._getIcon(s.method);
          const sourceLink = s.source.startsWith("http") ? `[Link](${s.source})` : s.source;
          return `| ${icon} | **${s.name}** | ${s.version} | ${s.description} | ${sourceLink} | ${s.method} | ${s.isPrivate ? "🔒 Yes" : "🔓 No"} | ${s.status} |`;
        })
        .join("\n");

      let duplicatesSection = "";
      if (data.duplicates && data.duplicates.length > 0) {
        duplicatesSection = `## ⚠️ Duplicate Skills Detected\n\nThe following skills are installed from multiple sources:\n\n`;
        data.duplicates.forEach((dup) => {
          duplicatesSection += `### ${dup.name}\n`;
          duplicatesSection += `| Source | Method | Version |\n| :--- | :--- | :--- |\n`;
          dup.instances.forEach((inst) => {
            duplicatesSection += `| ${inst.source} | ${inst.method} | ${inst.version} |\n`;
          });
          duplicatesSection += `\n**Recommended:** ${dup.recommended.method} (${dup.recommended.version})\n\n`;
        });
      }

      let md = content
        .replace("{{lastUpdated}}", data.lastUpdated)
        .replace("{{totalSkills}}", data.totalSkills)
        .replace("{{duplicatesSection}}", duplicatesSection)
        .replace("{{skillRows}}", rows);

      fs.writeFileSync(mdPath, md);
      logger.success(`Manifest Markdown updated: ${mdPath}`);
    } catch (e) {
      logger.error(`Failed to generate markdown report: ${e.message}`);
    }
  },

  _getIcon: (method) => {
    switch (method) {
      case "git": return "📁";
      case "npm": return "📦";
      case "clawhub": return "🦀";
      case "skillsmp": return "🧩";
      case "manual": return "👤";
      default: return "🔹";
    }
  }
};

module.exports = manifestGenerator;
