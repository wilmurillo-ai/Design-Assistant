# 三才配置吉凶（简化版）
SANCAI_WUXING = {
    "金": {"生": "水", "克": "木"},
    "木": {"生": "火", "克": "土"},
    "水": {"生": "木", "克": "火"},
    "火": {"生": "土", "克": "金"},
    "土": {"生": "金", "克": "水"},
}


def get_stroke(char):
    """获取汉字笔画数"""
    return STROKE_COUNT.get(char, 0)


def get_number_meaning(num):
    """获取数理含义"""
    if num <= 0:
        return {"吉凶": "凶", "含义": "数理不明"}
    if num > 81:
        num = (num - 1) % 81 + 1  # 超过81则取余
    return NUMBER_MEANING.get(num, {"吉凶": "中", "含义": "普通之数"})


def calculate_wuge(xing, ming):
    """
    计算五格
    
    Args:
        xing: 姓氏
        ming: 名字
    
    Returns:
        dict: 五格信息
    """
    # 计算笔画
    xing_strokes = [get_stroke(c) for c in xing]
    ming_strokes = [get_stroke(c) for c in ming]
    
    xing_total = sum(xing_strokes)
    ming_total = sum(ming_strokes)
    
    # 天格（姓氏笔画 + 1，单姓）
    tiange = xing_total + 1 if len(xing) == 1 else sum(xing_strokes[:-1]) + xing_strokes[-1]
    
    # 人格（姓最后一字 + 名第一字）
    if len(xing) == 1:
        renge = xing_strokes[0] + ming_strokes[0] if ming_strokes else xing_strokes[0]
    else:
        renge = xing_strokes[-1] + ming_strokes[0] if ming_strokes else sum(xing_strokes)
    
    # 地格（名字笔画总和）
    dige = ming_total
    
    # 外格（总格 - 人格 + 1）
    waige = tiange + renge + dige - renge + 1
    
    # 总格（姓名全部笔画）
    zongge = xing_total + ming_total
    
    return {
        "天格": {"数理": tiange, **get_number_meaning(tiange)},
        "人格": {"数理": renge, **get_number_meaning(renge)},
        "地格": {"数理": dige, **get_number_meaning(dige)},
        "外格": {"数理": waige, **get_number_meaning(waige)},
        "总格": {"数理": zongge, **get_number_meaning(zongge)},
    }


def analyze_sancai(wuge):
    """分析三才配置"""
    # 简化版：根据数理尾数判断五行
    def get_wuxing(num):
        tail = num % 10
        if tail in [1, 2]:
            return "木"
        elif tail in [3, 4]:
            return "火"
        elif tail in [5, 6]:
            return "土"
        elif tail in [7, 8]:
            return "金"
        else:
            return "水"
    
    tiange_wx = get_wuxing(wuge["天格"]["数理"])
    renge_wx = get_wuxing(wuge["人格"]["数理"])
    dige_wx = get_wuxing(wuge["地格"]["数理"])
    
    # 三才配置
    sancai = f"{tiange_wx}{renge_wx}{dige_wx}"
    
    # 简单判断
    if tiange_wx == renge_wx == dige_wx:
        result = "大吉"
    elif SANCAI_WUXING.get(tiange_wx, {}).get("生") == renge_wx and \
         SANCAI_WUXING.get(renge_wx, {}).get("生") == dige_wx:
        result = "吉"
    elif SANCAI_WUXING.get(tiange_wx, {}).get("克") == renge_wx or \
         SANCAI_WUXING.get(renge_wx, {}).get("克") == dige_wx:
        result = "凶"
    else:
        result = "中"
    
    return {
        "三才配置": sancai,
        "天格五行": tiange_wx,
        "人格五行": renge_wx,
        "地格五行": dige_wx,
        "吉凶": result
    }


def analyze_name(xing, ming):
    """
    分析姓名
    
    Args:
        xing: 姓氏
        ming: 名字
    
    Returns:
        dict: 分析结果
    """
    wuge = calculate_wuge(xing, ming)
    sancai = analyze_sancai(wuge)
    
    # 综合评价
    ji_count = sum(1 for v in wuge.values() if v["吉凶"] == "吉")
    xiong_count = sum(1 for v in wuge.values() if v["吉凶"] == "凶")
    
    if ji_count >= 4 and sancai["吉凶"] in ["大吉", "吉"]:
        overall = "优秀"
    elif ji_count >= 3 and sancai["吉凶"] in ["大吉", "吉", "中"]:
        overall = "良好"
    elif xiong_count >= 3 or sancai["吉凶"] == "凶":
        overall = "需改进"
    else:
        overall = "一般"
    
    return {
        "姓名": f"{xing}{ming}",
        "姓氏": xing,
        "名字": ming,
        "五格": wuge,
        "三才": sancai,
        "综合评价": overall
    }


def main():
    """主函数"""
    if len(sys.argv) < 3:
        print("用法: python name_analyzer.py <姓> <名>")
        print("示例: python name_analyzer.py 张 伟")
        print("      python name_analyzer.py 李 明华")
        sys.exit(1)
    
    try:
        xing = sys.argv[1]
        ming = sys.argv[2]
        
        result = analyze_name(xing, ming)
        print(json.dumps(result, ensure_ascii=False, indent=2))
        
    except Exception as e:
        print(f"错误: {e}")
        sys.exit(1)


