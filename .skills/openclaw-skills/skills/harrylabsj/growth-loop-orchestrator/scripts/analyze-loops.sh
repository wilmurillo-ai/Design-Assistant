#!/bin/bash
#
# analyze-loops.sh - Analyze existing growth loops
# Usage: ./analyze-loops.sh [--skill <name>] [--portfolio]
#

set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
SKILL_DIR="$(dirname "$SCRIPT_DIR")"
DATA_DIR="${GROWTH_DATA_DIR:-$SKILL_DIR/data}"

# Colors
GREEN='\033[0;32m'
BLUE='\033[0;34m'
YELLOW='\033[1;33m'
NC='\033[0m'

# Parse arguments
SKILL_NAME=""
PORTFOLIO=false

while [[ $# -gt 0 ]]; do
    case $1 in
        --skill)
            SKILL_NAME="$2"
            shift 2
            ;;
        --portfolio)
            PORTFOLIO=true
            shift
            ;;
        *)
            echo "Unknown option: $1"
            exit 1
            ;;
    esac
done

if [[ -z "$SKILL_NAME" && "$PORTFOLIO" == false ]]; then
    echo "Usage: $0 [--skill <name>] [--portfolio]"
    exit 1
fi

# Create data directory
