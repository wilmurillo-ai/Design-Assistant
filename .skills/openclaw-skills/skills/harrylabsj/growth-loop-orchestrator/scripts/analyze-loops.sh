REPORT_FILE="$DATA_DIR/LOOP-ANALYSIS-$(date +%Y%m%d).md"

echo -e "${BLUE}📊 Analyzing growth loops${NC}"
echo "================================"

if [[ "$PORTFOLIO" == true ]]; then
    SCOPE="Portfolio"
else
    SCOPE="Skill: $SKILL_NAME"
