import requests
import time
import uuid
import sys
import argparse
import json
import os
import threading
import hashlib
from urllib.parse import urlparse
from datetime import datetime, timedelta

# ========== Rate Limiting ==========
_rate_limit_lock = threading.Lock()
_rate_limit_store = {}  # {client_key: [(timestamp, count), ...]}

RATE_LIMIT_MAX = 20          # 最多 20 次
RATE_LIMIT_WINDOW = 60       # 滑动窗口 60 秒

def _check_rate_limit(client_id: str) -> bool:
    """检查是否超过频率限制，返回 True 表示允许调用"""
    now = datetime.now()
    window_start = now - timedelta(seconds=RATE_LIMIT_WINDOW)
    key = hashlib.sha256(client_id.encode()).hexdigest()[:16]
    with _rate_limit_lock:
        # 清理过期记录
        _rate_limit_store[key] = [
            ts for ts in _rate_limit_store.get(key, [])
            if ts > window_start
        ]
        count = len(_rate_limit_store[key])
        if count >= RATE_LIMIT_MAX:
            return False
        _rate_limit_store[key].append(now)
        return True

def _get_rate_limit_remaining(client_id: str) -> int:
    """获取剩余可调用次数"""
    now = datetime.now()
    window_start = now - timedelta(seconds=RATE_LIMIT_WINDOW)
    key = hashlib.sha256(client_id.encode()).hexdigest()[:16]
    with _rate_limit_lock:
        valid = [ts for ts in _rate_limit_store.get(key, []) if ts > window_start]
        return max(0, RATE_LIMIT_MAX - len(valid))
