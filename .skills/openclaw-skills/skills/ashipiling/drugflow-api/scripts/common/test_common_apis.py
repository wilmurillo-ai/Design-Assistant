#!/usr/bin/env python3
import argparse
import json
import time
from typing import Any, Dict

import requests

from drugflow_api import DrugFlowAPIClient


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Smoke test common DrugFlow APIs with session auth.")
    parser.add_argument("--base-url", required=True, help="Supports domain-only input, e.g. new.drugflow.com")
    parser.add_argument("--email", required=True)
    parser.add_argument("--password", required=True)
    parser.add_argument("--account", default="person", choices=["person", "team"])

    parser.add_argument("--ws-id", default=None)
    parser.add_argument("--ws-name", default="codex-common-api-workspace")

    parser.add_argument("--test-signup", action="store_true", help="Also validate /signup API")
    parser.add_argument("--signup-email", default=None, help="If omitted, use autogenerated email")
    parser.add_argument("--signup-password", default="CodexSkill_12345")
    parser.add_argument("--signup-name", default="codex-user")
    parser.add_argument("--signup-organization", default="codex")
    return parser.parse_args()


def auto_signup_email(signin_email: str) -> str:
    _, sep, domain = signin_email.partition("@")
    picked_domain = domain if sep else "example.com"
    suffix = int(time.time())
    return f"codex-skill-{suffix}@{picked_domain}"


def main() -> None:
    args = parse_args()
    output: Dict[str, Any] = {
        "base_url": args.base_url,
        "signin_email": args.email,
        "account": args.account,
        "signup_test": {"enabled": bool(args.test_signup)},
    }

    if args.test_signup:
        signup_email = args.signup_email or auto_signup_email(args.email)
        with requests.Session() as signup_session:
            signup_client = DrugFlowAPIClient(args.base_url, session=signup_session)
            signup_payload = signup_client.signup(
                email=signup_email,
                password=args.signup_password,
                name=args.signup_name,
                organization=args.signup_organization,
                allow_exists=True,
            )
            output["signup_test"] = {
                "enabled": True,
                "email": signup_email,
                "payload": signup_payload,
            }

    with requests.Session() as session:
        client = DrugFlowAPIClient(args.base_url, session=session)
        signin_payload = client.signin(args.email, args.password)
        workspace_list = client.list_workspaces(page=1, page_size=20)
        ws_id = client.ensure_workspace(args.ws_id, args.ws_name)
        balance_tokens = client.get_balance(args.account)
        jobs_payload = client.list_jobs(ws_id=ws_id, page=1, page_size=20)

    output["signin"] = signin_payload
    output["workspace"] = {
        "resolved_ws_id": ws_id,
        "count": workspace_list.get("count"),
        "available_count": workspace_list.get("available_count"),
    }
    output["balance"] = {"tokens": balance_tokens}
    output["jobs"] = {
        "count": jobs_payload.get("count"),
        "preview_ids": [item.get("id") for item in jobs_payload.get("results", [])[:5]],
    }

    print(json.dumps(output, ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()

