@server.call_tool()
async def call_tool(
    name: str, arguments: dict
) -> list[types.TextContent]:
    result: object

    if name == "search":
        result = _tool_search(
            arguments["query"],
            int(arguments.get("n_results", 5)),
        )
    elif name == "get_concept":
        result = _tool_get_concept(arguments["name"])
    elif name == "get_related":
        result = _tool_get_related(
            arguments["concept"],
            int(arguments.get("n", 5)),
        )
    elif name == "get_learning_path":
        result = _tool_get_learning_path(arguments["start"], arguments["goal"])
    elif name == "get_prerequisites":
        result = _tool_get_prerequisites(arguments["concept"])
    elif name == "get_examples":
        result = _tool_get_examples(arguments["concept"])
    elif name == "list_concepts":
        result = _tool_list_concepts(
            arguments.get("tag", ""),
            int(arguments.get("limit", 20)),
        )
    elif name == "get_page":
        result = _tool_get_page(arguments["url"])
    else:
        result = {{"error": f"Unknown tool: {{name}}"}}

    return [types.TextContent(type="text", text=json.dumps(result, indent=2))]


