def generate(input_dir: Path, model: str, force: bool = False):
    server_path = input_dir / "server.py"
    config_path = input_dir / "mcp_config.json"

    if server_path.exists() and not force:
        print(f"server.py already exists. Use --force to overwrite.")
        sys.exit(0)

    cfg = _infer_config(input_dir, model)

    # Fill template
    server_code = _SERVER_TEMPLATE.format(
        topic=cfg["topic"],
        slug=cfg["slug"],
        embedding_model=cfg["embedding_model"],
    )
    server_path.write_text(server_code)

    # Write config snippet — use sys.executable so the venv Python is baked in
    config_json = _CONFIG_TEMPLATE.format(
        slug=cfg["slug"],
        server_path=server_path.resolve(),
        python_cmd=sys.executable,
    )
    config_path.write_text(config_json)

    stats = cfg["stats"]
    print()
    print("━" * 62)
    print("  MCP server generated!")
    print(f"  Topic:   {cfg['topic']}")
    print(f"  Server:  {server_path}")
    print(f"  Config:  {config_path}")
    print()
    print(f"  Graph:   {stats.get('pages', '?')} pages, "
          f"{stats.get('chunks', '?')} chunks, "
          f"{stats.get('concepts', '?')} concepts")
    print()
    print("  Next steps:")
    print(f"  1. Add the contents of mcp_config.json to:")
    print(f"     ~/Library/Application Support/Claude/claude_desktop_config.json")
