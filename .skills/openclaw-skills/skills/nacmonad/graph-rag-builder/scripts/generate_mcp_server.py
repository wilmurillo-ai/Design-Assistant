def main():
    parser = argparse.ArgumentParser(
        description="Generate a self-contained MCP server from the graph + embeddings (M5)",
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument("--input", required=True, metavar="DIR",
                        help="MCP output dir (e.g. ./output/strudel-cc-mcp)")
    parser.add_argument("--model", default="all-MiniLM-L6-v2", metavar="MODEL",
                        help="Embedding model used in M4 (default: all-MiniLM-L6-v2)")
    parser.add_argument("--force", action="store_true",
                        help="Overwrite existing server.py")
    args = parser.parse_args()

    input_dir = Path(args.input)
    if not input_dir.exists():
        print(f"ERROR: Input directory not found: {input_dir}", file=sys.stderr)
        sys.exit(1)

    print(f"Generating MCP server for: {input_dir.name}")
    generate(input_dir, model=args.model, force=args.force)


