#!/usr/bin/env python3
"""
generate_mcp_server.py — MCP Server Generator (M5)

Reads graph.json + embeddings/ from the output directory and writes a
fully self-contained MCP server (server.py) to that same directory.

The generated server exposes 8 tools:
  search            Semantic search over chunk content
  get_concept       Look up a concept and its related chunks
  get_related       Find related concepts via graph edges
  get_learning_path Shortest path between two concepts in the knowledge graph
  get_prerequisites List prerequisites for a concept
  get_examples      Find code examples for a concept
  list_concepts     List all indexed concepts (optionally filtered by tag)
  get_page          Return all chunks for a given page URL

Usage:
    python scripts/generate_mcp_server.py --input ./output/strudel-cc-mcp
    python scripts/generate_mcp_server.py --input ./output/strudel-cc-mcp \\
        --model all-MiniLM-L6-v2 --force
"""

import argparse
