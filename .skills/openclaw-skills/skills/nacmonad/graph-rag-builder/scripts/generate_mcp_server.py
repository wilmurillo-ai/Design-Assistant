# ---------------------------------------------------------------------------
# Load resources at startup
# ---------------------------------------------------------------------------

def _load_graph() -> nx.MultiDiGraph:
    if not GRAPH_JSON.exists():
        print(f"WARNING: {{GRAPH_JSON}} not found — graph tools will be limited",
              file=sys.stderr)
        return nx.MultiDiGraph()
    data = json.loads(GRAPH_JSON.read_text())
    return nx.node_link_graph(data, edges="edges")


def _load_embeddings():
    """Load numpy embedding index. Returns (model, chunk_matrix, chunk_index,
    concept_matrix, concept_index) or (None, ...) if not available."""
    meta_path = EMBEDDINGS_DIR / "meta.json"
    if not EMBEDDINGS_DIR.exists() or not meta_path.exists():
        print(f"WARNING: {{EMBEDDINGS_DIR}} not found — run build_embeddings.py first",
              file=sys.stderr)
        return None, None, None, None, None
    try:
        model         = SentenceTransformer(EMBEDDING_MODEL)
        chunk_matrix  = np.load(EMBEDDINGS_DIR / "chunks.npy")
        chunk_index   = json.loads((EMBEDDINGS_DIR / "chunks.json").read_text())
        concept_matrix = np.load(EMBEDDINGS_DIR / "concepts.npy")
        concept_index  = json.loads((EMBEDDINGS_DIR / "concepts.json").read_text())
        return model, chunk_matrix, chunk_index, concept_matrix, concept_index
    except Exception as e:
        print(f"WARNING: Could not load embeddings: {{e}}", file=sys.stderr)
        return None, None, None, None, None


def _vec_search(query: str, model, matrix, index, n: int = 5) -> list[tuple]:
    """Cosine similarity search. Returns [(meta_dict, score), ...]."""
    qv = model.encode([query], convert_to_numpy=True).astype(np.float32)
    qv = qv / np.maximum(np.linalg.norm(qv), 1e-8)
    scores = matrix @ qv[0]
    top = np.argsort(scores)[::-1][:n]
    return [(index[i], float(scores[i])) for i in top]


print(f"Loading {{TOPIC}} knowledge graph...", file=sys.stderr)
G = _load_graph()
_emb_model, _chunk_mat, _chunk_idx, _concept_mat, _concept_idx = _load_embeddings()
print(
    f"Ready: {{G.number_of_nodes()}} nodes, {{G.number_of_edges()}} edges",
    file=sys.stderr,
)

# ---------------------------------------------------------------------------
# Concept node helpers
