import sys
from pathlib import Path

# ---------------------------------------------------------------------------
# Introspect the output directory to derive config values
# ---------------------------------------------------------------------------

def _infer_config(input_dir: Path, model: str) -> dict:
    """Return a config dict derived from graph.json + the --model flag."""
    graph_path = input_dir / "graph.json"
    if not graph_path.exists():
        print(f"WARNING: graph.json not found — run build_graph.py first", file=sys.stderr)
        stats = {"pages": 0, "chunks": 0, "concepts": 0}
    else:
        meta = json.loads(graph_path.read_text()).get("meta", {})
        stats = meta.get("stats", {})

    # Derive a human-readable topic from the folder name
    # e.g. "strudel-cc-mcp" → "strudel-cc"
    slug = input_dir.name
    if slug.endswith("-mcp"):
        slug = slug[:-4]
    topic = slug.replace("-", " ").replace("_", " ").title()

    return {
        "topic":           topic,
        "slug":            slug,
        "embedding_model": model,
        "stats":           stats,
    }


