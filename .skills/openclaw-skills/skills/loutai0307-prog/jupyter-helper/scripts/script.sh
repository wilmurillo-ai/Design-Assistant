#!/usr/bin/env bash
# jupyter-helper/scripts/script.sh
# Manage Jupyter notebooks: convert, extract, run, clean, and inspect.
# Powered by BytesAgain | bytesagain.com

set -euo pipefail

COMMAND="${1:-help}"
shift || true

# ─── colour helpers ────────────────────────────────────────────────────────────
RED='\033[0;31m'; GREEN='\033[0;32m'; YELLOW='\033[1;33m'
CYAN='\033[0;36m'; BOLD='\033[1m'; RESET='\033[0m'

info()  { echo -e "${CYAN}${*}${RESET}"; }
ok()    { echo -e "${GREEN}✔ ${*}${RESET}"; }
warn()  { echo -e "${YELLOW}⚠ ${*}${RESET}"; }
err()   { echo -e "${RED}✘ ${*}${RESET}"; exit 1; }
head_() { echo -e "\n${BOLD}${*}${RESET}"; }

# ─── require a .ipynb file ────────────────────────────────────────────────────
require_nb() {
  local f="${1:-}"
  [[ -z "$f" ]] && err "Usage: $0 $COMMAND <notebook.ipynb> [options]"
  [[ ! -f "$f" ]] && err "File not found: $f"
  [[ "${f##*.}" != "ipynb" ]] && warn "File does not have .ipynb extension — proceeding anyway."
  echo "$f"
}

# ─── check nbconvert availability ─────────────────────────────────────────────
check_nbconvert() {
  if ! python3 -m nbconvert --version &>/dev/null 2>&1; then
    err "nbconvert not found.\nInstall it with:\n  pip install nbconvert\n  pip install nbconvert[webpdf]   # optional PDF support"
  fi
}

# ─── check python3 ────────────────────────────────────────────────────────────
require_python() {
  command -v python3 &>/dev/null || err "python3 not found. Please install Python 3.8+."
}

# ─── COMMAND: convert ─────────────────────────────────────────────────────────
cmd_convert() {
  local nb; nb=$(require_nb "${1:-}")
  local fmt="${2:-script}"   # script | html | markdown | pdf | rst | latex
  shift 2 2>/dev/null || true

  check_nbconvert

  local base="${nb%.ipynb}"
  local out_flag=""
  local ext=""
  case "$fmt" in
    py|script)   fmt="script";   ext=".py"   ;;
    html)        fmt="html";     ext=".html" ;;
    md|markdown) fmt="markdown"; ext=".md"   ;;
    rst)         fmt="rst";      ext=".rst"  ;;
    latex|tex)   fmt="latex";    ext=".tex"  ;;
    pdf)         fmt="pdf";      ext=".pdf"  ;;
    *)
      err "Unknown format: $fmt\nSupported: py/script | html | md/markdown | rst | latex | pdf"
      ;;
  esac

  local outfile="${base}${ext}"
  info "Converting '$nb' → '$outfile'  (format: $fmt) …"

  if python3 -m nbconvert --to "$fmt" --output "$outfile" "$nb" 2>&1; then
    ok "Saved: $outfile"
  else
    err "Conversion failed. Check nbconvert is installed and notebook is valid."
  fi
}

# ─── COMMAND: extract ─────────────────────────────────────────────────────────
cmd_extract() {
  local nb; nb=$(require_nb "${1:-}")
  local outfile="${2:-}"
  require_python

  if [[ -z "$outfile" ]]; then
    outfile="${nb%.ipynb}_extracted.py"
  fi

  info "Extracting code cells from '$nb' → '$outfile' …"

  python3 - "$nb" "$outfile" <<'PYEOF'
import sys, json

nb_path  = sys.argv[1]
out_path = sys.argv[2]

with open(nb_path, encoding="utf-8") as f:
    nb = json.load(f)

cells   = nb.get("cells", [])
lang    = nb.get("metadata", {}).get("kernelspec", {}).get("language", "python")
extracted = []
code_cells = 0

for i, cell in enumerate(cells, 1):
    if cell.get("cell_type") != "code":
        continue
    src = "".join(cell.get("source", []))
    if not src.strip():
        continue
    code_cells += 1
    extracted.append(f"# ── Cell {code_cells} (input #{i}) ──────────────────\n")
    extracted.append(src)
    if not src.endswith("\n"):
        extracted.append("\n")
    extracted.append("\n")

header = (
    f"# Extracted from: {nb_path}\n"
    f"# Language: {lang}\n"
    f"# Code cells: {code_cells}\n"
    f"# Generated by jupyter-helper\n\n"
)

with open(out_path, "w", encoding="utf-8") as f:
    f.write(header)
    f.writelines(extracted)

print(f"  Code cells extracted : {code_cells}")
print(f"  Lines written        : {sum(1 for _ in open(out_path))}")
PYEOF
  ok "Saved: $outfile"
}

# ─── COMMAND: run ─────────────────────────────────────────────────────────────
cmd_run() {
  local nb; nb=$(require_nb "${1:-}")
  local timeout="${2:-120}"
  check_nbconvert

  info "Executing notebook '$nb' (timeout: ${timeout}s) …"
  info "Output will be saved to: ${nb%.ipynb}_executed.ipynb"

  local outfile="${nb%.ipynb}_executed.ipynb"

  if python3 -m nbconvert \
      --to notebook \
      --execute \
      --ExecutePreprocessor.timeout="$timeout" \
      --output "$outfile" \
      "$nb" 2>&1; then
    ok "Execution complete → $outfile"

    # brief summary of outputs
    python3 - "$outfile" <<'PYEOF'
import json, sys

with open(sys.argv[1]) as f:
    nb = json.load(f)

cells = nb.get("cells", [])
code_cells  = sum(1 for c in cells if c.get("cell_type") == "code")
output_cells = sum(1 for c in cells if c.get("cell_type") == "code" and c.get("outputs"))
errors = sum(
    1 for c in cells
    for o in c.get("outputs", [])
    if o.get("output_type") == "error"
)

print(f"\n  Code cells   : {code_cells}")
print(f"  With output  : {output_cells}")
if errors:
    print(f"  ⚠ Errors     : {errors} (see executed notebook for details)")
else:
    print(f"  Errors       : 0  ✔")
PYEOF
  else
    err "Execution failed. Check the notebook for errors or increase timeout."
  fi
}

# ─── COMMAND: clean ───────────────────────────────────────────────────────────
cmd_clean() {
  local nb; nb=$(require_nb "${1:-}")
  local inplace="${2:---inplace}"
  require_python

  local outfile
  if [[ "$inplace" == "--inplace" || "$inplace" == "-i" ]]; then
    outfile="$nb"
    info "Clearing all outputs in-place: '$nb' …"
  else
    outfile="${nb%.ipynb}_clean.ipynb"
    info "Clearing outputs → '$outfile' …"
  fi

  python3 - "$nb" "$outfile" <<'PYEOF'
import sys, json

nb_path  = sys.argv[1]
out_path = sys.argv[2]

with open(nb_path, encoding="utf-8") as f:
    nb = json.load(f)

cells = nb.get("cells", [])
cleared = 0
total_outputs = 0

for cell in cells:
    if cell.get("cell_type") == "code":
        n_out = len(cell.get("outputs", []))
        total_outputs += n_out
        if n_out or cell.get("execution_count") is not None:
            cell["outputs"] = []
            cell["execution_count"] = None
            cleared += 1

with open(out_path, "w", encoding="utf-8") as f:
    json.dump(nb, f, indent=1, ensure_ascii=False)
    f.write("\n")

print(f"  Code cells cleared   : {cleared}")
print(f"  Outputs removed      : {total_outputs}")
PYEOF
  ok "Done: $outfile"
}

# ─── COMMAND: info ────────────────────────────────────────────────────────────
cmd_info() {
  local nb; nb=$(require_nb "${1:-}")
  require_python

  python3 - "$nb" <<'PYEOF'
import sys, json, os

nb_path = sys.argv[1]

with open(nb_path, encoding="utf-8") as f:
    nb = json.load(f)

meta    = nb.get("metadata", {})
ks      = meta.get("kernelspec", {})
lang_info = meta.get("language_info", {})
cells   = nb.get("cells", [])
nbformat = f"{nb.get('nbformat', '?')}.{nb.get('nbformat_minor', '?')}"

code_cells     = [c for c in cells if c.get("cell_type") == "code"]
markdown_cells = [c for c in cells if c.get("cell_type") == "markdown"]
raw_cells      = [c for c in cells if c.get("cell_type") == "raw"]

total_code_lines = sum(
    len("".join(c.get("source", [])).splitlines())
    for c in code_cells
)
total_md_lines = sum(
    len("".join(c.get("source", [])).splitlines())
    for c in markdown_cells
)

cells_with_output = sum(1 for c in code_cells if c.get("outputs"))
errors = sum(
    1 for c in code_cells
    for o in c.get("outputs", [])
    if o.get("output_type") == "error"
)

file_size = os.path.getsize(nb_path)

print(f"\n📓 Notebook Info — {nb_path}")
print(f"{'─'*50}")
print(f"  Format          : nbformat {nbformat}")
print(f"  Kernel          : {ks.get('display_name', ks.get('name', 'unknown'))}")
print(f"  Language        : {lang_info.get('name', ks.get('language', 'unknown'))}")
print(f"  Python version  : {lang_info.get('version', 'n/a')}")
print(f"{'─'*50}")
print(f"  Total cells     : {len(cells)}")
print(f"    Code cells    : {len(code_cells)}")
print(f"    Markdown      : {len(markdown_cells)}")
print(f"    Raw           : {len(raw_cells)}")
print(f"{'─'*50}")
print(f"  Code lines      : {total_code_lines}")
print(f"  Markdown lines  : {total_md_lines}")
print(f"  Cells w/ output : {cells_with_output}")
if errors:
    print(f"  ⚠ Error outputs : {errors}")
print(f"{'─'*50}")
print(f"  File size       : {file_size:,} bytes ({file_size/1024:.1f} KB)")
print()
PYEOF
}

# ─── COMMAND: help ────────────────────────────────────────────────────────────
cmd_help() {
  echo ""
  echo -e "${BOLD}📓 Jupyter Helper${RESET} — manage Jupyter notebooks from the command line"
  echo ""
  echo -e "  ${CYAN}convert${RESET}  <nb.ipynb> [format]     Convert to py/html/md/rst/latex/pdf"
  echo -e "  ${CYAN}extract${RESET}  <nb.ipynb> [out.py]     Extract all code cells to a .py file"
  echo -e "  ${CYAN}run${RESET}      <nb.ipynb> [timeout]    Execute notebook headlessly"
  echo -e "  ${CYAN}clean${RESET}    <nb.ipynb> [--inplace]  Clear all outputs from notebook"
  echo -e "  ${CYAN}info${RESET}     <nb.ipynb>              Show notebook metadata & stats"
  echo -e "  ${CYAN}help${RESET}                            Show this help"
  echo ""
  echo -e "  Examples:"
  echo -e "    $0 convert analysis.ipynb html"
  echo -e "    $0 extract model.ipynb extracted.py"
  echo -e "    $0 run experiment.ipynb 300"
  echo -e "    $0 clean report.ipynb --inplace"
  echo -e "    $0 info notebook.ipynb"
  echo ""
  echo -e "${YELLOW}Powered by BytesAgain | bytesagain.com${RESET}"
  echo ""
}

# ─── dispatch ─────────────────────────────────────────────────────────────────
case "$COMMAND" in
  convert) cmd_convert "$@" ;;
  extract) cmd_extract "$@" ;;
  run)     cmd_run     "$@" ;;
  clean)   cmd_clean   "$@" ;;
  info)    cmd_info    "$@" ;;
  help|--help|-h) cmd_help ;;
  *)
    err "Unknown command: $COMMAND"
    cmd_help
    exit 1
    ;;
esac

