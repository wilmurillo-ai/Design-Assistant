#!/usr/bin/env python3
"""
Generate a Metasploit resource script (.rc) from structured CLI arguments.
"""

from __future__ import annotations

import argparse
import re
import sys
from pathlib import Path

KEY_RE = re.compile(r"^[A-Za-z0-9_]+$")


def parse_key_value(items: list[str], flag_name: str) -> list[tuple[str, str]]:
    pairs: list[tuple[str, str]] = []
    for item in items:
        if "=" not in item:
            raise ValueError(f"{flag_name} expects KEY=VALUE, got: {item}")
        key, value = item.split("=", 1)
        key = key.strip()
        value = value.strip()
        if not key or not KEY_RE.match(key):
            raise ValueError(
                f"{flag_name} key must match [A-Za-z0-9_]+, got: {key or '<empty>'}"
            )
        if value == "":
            raise ValueError(f"{flag_name} value cannot be empty for key: {key}")
        pairs.append((key, value))
    return pairs


def quote_value(value: str) -> str:
    if any(ch.isspace() for ch in value) or '"' in value:
        return '"' + value.replace("\\", "\\\\").replace('"', '\\"') + '"'
    return value


def build_lines(args: argparse.Namespace) -> list[str]:
    lines = [
        "# Auto-generated by scripts/build_rc.py",
        "# Review carefully before executing in msfconsole.",
    ]

    if args.spool:
        lines.append(f"spool {quote_value(args.spool)}")
    if args.workspace:
        lines.append(f"workspace {quote_value(args.workspace)}")

    lines.append(f"use {args.module}")

    if args.rhosts:
        lines.append(f"set RHOSTS {quote_value(args.rhosts)}")
    if args.rport is not None:
        lines.append(f"set RPORT {args.rport}")
    if args.payload:
        lines.append(f"set PAYLOAD {args.payload}")
    if args.lhost:
        lines.append(f"set LHOST {quote_value(args.lhost)}")
    if args.lport is not None:
        lines.append(f"set LPORT {args.lport}")
    if args.targeturi:
        lines.append(f"set TARGETURI {quote_value(args.targeturi)}")
    if args.ssl:
        lines.append("set SSL true")

    set_pairs = parse_key_value(args.set or [], "--set")
    setg_pairs = parse_key_value(args.setg or [], "--setg")

    for key, value in set_pairs:
        lines.append(f"set {key} {quote_value(value)}")
    for key, value in setg_pairs:
        lines.append(f"setg {key} {quote_value(value)}")

    if args.check:
        lines.append("check")

    command = args.run_mode
    if args.job:
        command += " -j"
    if args.background:
        command += " -z"
    lines.append(command)

    if args.list_sessions:
        lines.append("sessions -l")

    if args.auto_exit:
        if args.spool:
            lines.append("spool off")
        lines.append("exit -y")

    return lines


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Build a Metasploit .rc script for repeatable exploit execution."
    )
    parser.add_argument("--module", required=True, help="Metasploit module path")
    parser.add_argument("--rhosts", help="RHOSTS value")
    parser.add_argument("--rport", type=int, help="RPORT value")
    parser.add_argument("--payload", help="Payload module path")
    parser.add_argument("--lhost", help="LHOST value")
    parser.add_argument("--lport", type=int, help="LPORT value")
    parser.add_argument("--targeturi", help="TARGETURI value")
    parser.add_argument("--ssl", action="store_true", help="Set SSL true")
    parser.add_argument("--workspace", help="Optional Metasploit workspace name")
    parser.add_argument("--spool", help="Enable console logging to this file")
    parser.add_argument(
        "--set",
        action="append",
        default=[],
        metavar="KEY=VALUE",
        help="Additional module option (repeatable)",
    )
    parser.add_argument(
        "--setg",
        action="append",
        default=[],
        metavar="KEY=VALUE",
        help="Additional global option (repeatable)",
    )
    parser.add_argument("--check", action="store_true", help="Run check before execution")
    parser.add_argument(
        "--run-mode",
        choices=["run", "exploit"],
        default="run",
        help="Execution command",
    )
    parser.add_argument("--job", action="store_true", help="Run in job mode (-j)")
    parser.add_argument(
        "--background",
        action="store_true",
        help="Do not interact with session after exploit (-z)",
    )
    parser.add_argument(
        "--list-sessions",
        action="store_true",
        help="Append sessions -l after execution",
    )
    parser.add_argument(
        "--auto-exit",
        action="store_true",
        help="Append exit -y (and spool off when spool is enabled)",
    )
    parser.add_argument(
        "--output",
        type=Path,
        help="Write output to this .rc path. Defaults to stdout.",
    )
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    try:
        lines = build_lines(args)
    except ValueError as exc:
        print(f"[ERROR] {exc}", file=sys.stderr)
        return 1

    content = "\n".join(lines) + "\n"
    if args.output:
        args.output.parent.mkdir(parents=True, exist_ok=True)
        args.output.write_text(content, encoding="utf-8")
        print(f"[OK] Wrote {args.output}")
        return 0

    print(content, end="")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
