    def get_article(self, law_name: str, article_number: str) -> str:
        """
        获取法条内容
        
        Args:
            law_name: 法律名称
            article_number: 法条编号（如"第 87 条"）
            
        Returns:
            法条内容，如果找不到则返回 None
        """
        if law_name in self.articles:
            if article_number in self.articles[law_name]:
                return self.articles[law_name][article_number]
        
        return None
    
    def get_articles_batch(self, articles_list: list) -> dict:
        """
        批量获取法条内容
        
        Args:
            articles_list: 法条列表，格式如 ['劳动合同法第 87 条', '民法典第 577 条']
            
        Returns:
            法条内容字典
        """
        result = {}
        
        for article_ref in articles_list:
            # 解析法条引用
            for law_name in self.articles.keys():
                if law_name in article_ref:
                    # 提取法条编号
                    article_num = article_ref.replace(law_name, '').strip()
                    content = self.get_article(law_name, article_num)
                    if content:
                        result[article_ref] = {
                            'law': law_name,
                            'article': article_num,
                            'content': content
                        }
                    break
        
        return result
    
    def format_articles_for_report(self, articles_list: list) -> str:
        """
        格式化法条用于报告输出
        
        Args:
            articles_list: 法条引用列表
            
        Returns:
            格式化的法条内容
        """
        lines = []
        lines.append("【核心法条详解】")
        
        for article_ref in articles_list:
            # 解析法条引用
            for law_name in self.articles.keys():
                if law_name in article_ref:
                    article_num = article_ref.replace(law_name, '').strip()
                    content = self.get_article(law_name, article_num)
                    
                    if content:
                        lines.append(f"  📜 {law_name}{article_num}")
                        # 内容换行处理（每 60 字换行）
                        wrapped_content = self._wrap_text(content, 60)
                        lines.append(f"     {wrapped_content}")
                        lines.append("")
                    break
        
        return '\n'.join(lines)
    
    def _wrap_text(self, text: str, width: int) -> str:
        """文本换行"""
        if len(text) <= width:
            return text
        
        # 按句号、分号换行
        result = []
        current_line = ""
        
        for char in text:
            current_line += char
            if char in ['。', '；', '：'] and len(current_line) > width // 2:
                result.append(current_line)
                current_line = ""
        
        if current_line:
            result.append(current_line)
        
        return '\n     '.join(result)


