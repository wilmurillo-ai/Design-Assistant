    def search_cases(self, dispute_type, keywords=None, limit=5):
        """
        检索类似案例
        
        Args:
            dispute_type: 纠纷类型
            keywords: 关键词列表（可选）
            limit: 返回案例数量限制
            
        Returns:
            匹配的案例列表
        """
        # 纠纷类型映射（处理同义词）
        type_mapping = {
            '网络购物': '消费者权益',
            '网购': '消费者权益',
            '消费': '消费者权益',
            '离婚': '婚姻家庭',
            '婚姻': '婚姻家庭',
            '借款': '借款合同',
            '借贷': '借款合同',
            '劳动': '劳动合同',
            '辞退': '劳动合同',
            '车祸': '交通事故',
            '交通': '交通事故',
            '装修': '装修合同',
            '股权': '股权纠纷',
            '股东': '股权纠纷',
        }
        
        # 先映射纠纷类型
        mapped_type = type_mapping.get(dispute_type, dispute_type)
        
        # 获取对应类型的案例
        cases = self.cases.get(mapped_type, [])
        
        # 如果没有直接匹配，尝试模糊匹配
        if not cases:
            for key, value in self.cases.items():
                if key in dispute_type or dispute_type in key or key in mapped_type:
                    cases = value
                    break
        
        # 如果有关键词，进一步筛选
        if keywords and cases:
            filtered_cases = []
            for case in cases:
                # 检查关键词是否匹配争议焦点、裁判要旨或关键词
                match_score = 0
                text = case['争议焦点'] + case['裁判要旨'] + ' '.join(case['关键词'])
                for kw in keywords:
                    if kw in text:
                        match_score += 1
                if match_score > 0:
                    filtered_cases.append((match_score, case))
            
            # 按匹配度排序
            filtered_cases.sort(key=lambda x: x[0], reverse=True)
            cases = [case for score, case in filtered_cases]
        
        return cases[:limit]
    
    def get_similar_cases(self, case_info, limit=3):
        """
        根据案件信息获取类似案例
        
        Args:
            case_info: 案件信息字典
            limit: 返回案例数量
            
        Returns:
            类似案例列表
        """
        dispute_type = case_info.get('纠纷类型', '')
        cause = case_info.get('起因', '')
        
        # 提取关键词
        keywords = []
        if '借款' in dispute_type or '借贷' in cause:
            keywords = ['借条', '转账', '诉讼时效']
        elif '劳动' in dispute_type or '辞退' in cause:
            keywords = ['违法解除', '加班费', '双倍工资']
        elif '离婚' in dispute_type or '出轨' in cause:
            keywords = ['出轨', '抚养权', '财产分割']
        elif '消费' in dispute_type or '假货' in cause:
            keywords = ['假货', '欺诈', '三倍赔偿']
        elif '交通' in dispute_type or '车祸' in cause:
            keywords = ['误工费', '伤残', '交强险']
        elif '装修' in dispute_type:
            keywords = ['延期', '质量问题', '违约金']
        
        # 检索案例
        return self.search_cases(dispute_type, keywords, limit)
    
    def format_case_for_report(self, case):
        """
        格式化案例用于报告输出
        
        Args:
            case: 案例字典
            
        Returns:
            格式化的字符串
        """
        lines = []
        lines.append(f"  📖 {case['案号']}")
        lines.append(f"     法院：{case['法院']}")
        lines.append(f"     日期：{case['日期']}")
        lines.append(f"     争议焦点：{case['争议焦点']}")
        lines.append(f"     裁判要旨：{case['裁判要旨']}")
        lines.append(f"     判决结果：{case['判决结果']}")
        lines.append(f"     关键词：{', '.join(case['关键词'])}")
        return '\n'.join(lines)


