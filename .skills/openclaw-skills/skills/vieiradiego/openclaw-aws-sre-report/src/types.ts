/**
 * openclaw-aws-sre-report
 * TypeScript types for the AWS SRE Report skill
 */

/** Configuration for the SreReporter */
export interface SreConfig {
  /** AWS region (e.g. "us-east-1") */
  region: string;
  /** Telegram Bot API token */
  telegramBotToken: string;
  /** Telegram chat ID to receive the report */
  telegramChatId: string;
  /** Full SQS Dead Letter Queue URL */
  dlqUrl: string;
  /** Full SQS main queue URL (used as audios-processed proxy) */
  audioQueueUrl: string;
  /** Lambda function names to monitor for errors */
  lambdaFunctions: string[];
  /** Monthly AWS budget in USD for FinOps alerts (default: 50) */
  monthlyBudgetUsd?: number;
  /** SLO target percentage (default: 99.5) */
  sloTarget?: number;
  /** Bedrock model ID for incident diagnosis (default: claude-haiku-4-5) */
  bedrockModelId?: string;
  /** AWS region for Bedrock — Cost Explorer requires us-east-1 (default: same as region) */
  bedrockRegion?: string;
}

/** Cost data collected from AWS Cost Explorer */
export interface CostData {
  /** Yesterday's total cost in USD */
  yesterday: number;
  /** Cost breakdown by AWS service */
  byService: Record<string, number>;
  /** Month-to-date total cost in USD */
  mtd: number;
}

/** Structured incident generated by Bedrock when issues are detected */
export interface Incident {
  /** What is happening and why (2-3 specific sentences) */
  contexto: string;
  /** What the SRE agent found automatically */
  solucoes: string[];
  /** Specific actions with real AWS CLI commands for the operator */
  ctas: string[];
}

/** Full SRE report produced by SreReporter.run() */
export interface SreReport {
  /** Report generation timestamp */
  timestamp: Date;
  /** Number of messages in the DLQ */
  dlqCount: number;
  /** Total Lambda errors across all monitored functions (last 24h) */
  lambdaErrors: number;
  /** Messages sent to the main queue in 24h (proxy for processed items) */
  audiosProcessed: number;
  /** SLO value as a percentage (0–100) */
  sloValue: number;
  /** Error budget burned as a percentage (0–100+) */
  errorBudgetBurned: number;
  /** AWS cost data (null if Cost Explorer unavailable) */
  costs: CostData | null;
  /** Incident details (null when all systems healthy) */
  incident: Incident | null;
  /** Formatted MarkdownV2 Telegram message */
  text: string;
}

/** Per-function Lambda error count */
export interface LambdaErrorDetail {
  name: string;
  count: number;
}
