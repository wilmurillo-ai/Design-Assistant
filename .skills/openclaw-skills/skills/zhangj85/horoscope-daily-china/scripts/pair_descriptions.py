def get_pair_description(sign1, sign2):
    """获取星座配对的详细描述"""
    import random
    
    # 尝试查找直接匹配
    key = (sign1, sign2)
    if key not in PAIR_DESCRIPTIONS:
        # 尝试反向匹配
        key = (sign2, sign1)
    
    if key in PAIR_DESCRIPTIONS:
        data = PAIR_DESCRIPTIONS[key]
        score = random.randint(data["score_range"][0], data["score_range"][1])
        title = random.choice(data["titles"])
        desc = random.choice(data["descriptions"])
        pair_type = data["type"]
        
        return {
            "sign1": sign1,
            "sign2": sign2,
            "score": str(score),
            "title": title,
            "tag": pair_type,
            "desc": desc,
        }
    
    # 如果没有找到特定组合，使用通用描述
    return generate_generic_pair(sign1, sign2)


