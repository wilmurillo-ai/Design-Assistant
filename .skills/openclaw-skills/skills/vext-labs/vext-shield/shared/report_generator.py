"""Markdown report generator for VEXT Shield.

Generates security reports for scan results, audits, red team tests,
and dashboard summaries. All output is clean markdown.
"""

from __future__ import annotations

from collections import Counter
from datetime import datetime, timezone
from typing import Any

from .scanner_core import Finding, ScanResult
from .utils import score_to_grade, severity_to_score


class ReportGenerator:
    """Generates markdown security reports from scan/audit/redteam data."""

    # -------------------------------------------------------------------
    # Scan report
    # -------------------------------------------------------------------

    def generate_scan_report(self, results: list[ScanResult]) -> str:
        """Generate a comprehensive scan report for one or more skills."""
        now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
        lines: list[str] = []

        lines.append("# VEXT Shield — Skill Security Scan Report")
        lines.append("")
        lines.append(f"**Generated:** {now}")
        lines.append(f"**Skills scanned:** {len(results)}")
        lines.append("")

        # Executive summary
        lines.append("## Executive Summary")
        lines.append("")
        lines.append(self._executive_summary(results))
        lines.append("")

        # Results table
        lines.append("## Scan Results")
        lines.append("")
        lines.append("| Skill | Risk Level | Findings | Files Scanned | Duration |")
        lines.append("|-------|-----------|----------|---------------|----------|")
        for r in results:
            badge = self._severity_badge(r.risk_level)
            lines.append(
                f"| {r.skill_name} | {badge} | {len(r.findings)} | "
                f"{r.files_scanned} | {r.scan_duration_ms}ms |"
            )
        lines.append("")

        # Detailed findings per skill
        for r in results:
            if r.findings:
                lines.append(f"## {r.skill_name}")
                lines.append("")
                lines.append(f"**Path:** `{r.skill_path}`")
                lines.append(f"**Risk Level:** {self._severity_badge(r.risk_level)}")
                lines.append("")
                lines.append(self._findings_table(r.findings))
                lines.append("")

                # Detailed finding descriptions
                for f in r.findings:
                    lines.append(f"### {f.id}: {f.name}")
                    lines.append("")
                    lines.append(f"- **Severity:** {self._severity_badge(f.severity)}")
                    lines.append(f"- **Category:** {f.category} / {f.subcategory}")
                    lines.append(f"- **File:** `{f.file_path}`" + (f" (line {f.line_number})" if f.line_number else ""))
                    lines.append(f"- **Confidence:** {f.confidence:.0%}")
                    lines.append(f"- **Description:** {f.description}")
                    lines.append(f"- **Matched:** `{f.matched_text}`")
                    lines.append("")

        # Footer
        lines.append("---")
        lines.append("*Report generated by VEXT Shield. Built by Vext Labs.*")

        return "\n".join(lines)

    # -------------------------------------------------------------------
    # Audit report
    # -------------------------------------------------------------------

    def generate_audit_report(self, audit_results: dict[str, Any]) -> str:
        """Generate a comprehensive installation audit report."""
        now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
        lines: list[str] = []

        lines.append("# VEXT Shield — OpenClaw Installation Audit Report")
        lines.append("")
        lines.append(f"**Generated:** {now}")

        grade = audit_results.get("grade", "?")
        score = audit_results.get("score", 0)
        lines.append(f"**Security Grade:** {grade} ({score}/100)")
        lines.append("")

        # Configuration checks
        lines.append("## Configuration Security")
        lines.append("")
        config_checks = audit_results.get("config_checks", [])
        if config_checks:
            lines.append("| Check | Status | Details |")
            lines.append("|-------|--------|---------|")
            for check in config_checks:
                status = "PASS" if check.get("passed") else "FAIL"
                icon = "+" if check.get("passed") else "!"
                lines.append(f"| {check.get('name', '?')} | {icon} {status} | {check.get('details', '')} |")
            lines.append("")

        # File permission checks
        lines.append("## File Permissions")
        lines.append("")
        perm_checks = audit_results.get("permission_checks", [])
        if perm_checks:
            lines.append("| File | Mode | Status | Issues |")
            lines.append("|------|------|--------|--------|")
            for p in perm_checks:
                status = "OK" if p.get("secure") else "WARN"
                issues = ", ".join(p.get("issues", [])) or "None"
                lines.append(f"| `{p.get('path', '?')}` | {p.get('mode', '?')} | {status} | {issues} |")
            lines.append("")

        # Network exposure
        lines.append("## Network Exposure")
        lines.append("")
        network = audit_results.get("network_checks", [])
        if network:
            for check in network:
                status = "PASS" if check.get("passed") else "FAIL"
                lines.append(f"- **{check.get('name', '?')}**: {status} — {check.get('details', '')}")
            lines.append("")

        # Skill scan summary
        scan_results = audit_results.get("skill_scan_results")
        if scan_results:
            lines.append("## Installed Skills Security")
            lines.append("")
            lines.append("| Skill | Risk Level | Findings |")
            lines.append("|-------|-----------|----------|")
            for r in scan_results:
                if isinstance(r, ScanResult):
                    lines.append(f"| {r.skill_name} | {self._severity_badge(r.risk_level)} | {len(r.findings)} |")
                elif isinstance(r, dict):
                    lines.append(f"| {r.get('skill_name', '?')} | {r.get('risk_level', '?')} | {r.get('findings_count', 0)} |")
            lines.append("")

        # SOUL.md integrity
        soul_check = audit_results.get("soul_check")
        if soul_check:
            lines.append("## SOUL.md Integrity")
            lines.append("")
            if soul_check.get("suspicious"):
                lines.append("**WARNING: Suspicious content detected in SOUL.md**")
                lines.append("")
                for issue in soul_check.get("issues", []):
                    lines.append(f"- {issue}")
            else:
                lines.append("No suspicious content detected in SOUL.md.")
            lines.append("")

        # Remediation
        remediations = audit_results.get("remediations", [])
        if remediations:
            lines.append("## Recommended Remediations")
            lines.append("")
            for i, r in enumerate(remediations, 1):
                lines.append(f"{i}. {r}")
            lines.append("")

        lines.append("---")
        lines.append("*Report generated by VEXT Shield. Built by Vext Labs.*")

        return "\n".join(lines)

    # -------------------------------------------------------------------
    # Red team report
    # -------------------------------------------------------------------

    def generate_redteam_report(self, test_results: dict[str, Any]) -> str:
        """Generate a penetration test style report."""
        now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
        lines: list[str] = []

        skill_name = test_results.get("skill_name", "Unknown")
        verdict = test_results.get("verdict", "UNKNOWN")

        lines.append("# VEXT Shield — Adversarial Security Test Report")
        lines.append("")
        lines.append(f"**Target Skill:** {skill_name}")
        lines.append(f"**Generated:** {now}")
        lines.append(f"**Verdict:** {'FAIL' if verdict == 'FAIL' else 'PASS'}")
        lines.append("")

        # Executive summary
        lines.append("## Executive Summary")
        lines.append("")
        total_tests = test_results.get("total_tests", 0)
        passed = test_results.get("tests_passed", 0)
        failed = test_results.get("tests_failed", 0)
        lines.append(f"VEXT Shield ran **{total_tests}** adversarial tests against `{skill_name}`.")
        lines.append(f"**{passed}** tests passed, **{failed}** tests identified security issues.")
        lines.append("")

        # Test batteries
        batteries = test_results.get("batteries", [])
        for battery in batteries:
            name = battery.get("name", "Unknown Test")
            lines.append(f"## {name}")
            lines.append("")
            lines.append(f"**Result:** {battery.get('result', '?')}")
            lines.append("")

            tests = battery.get("tests", [])
            if tests:
                lines.append("| Test | Result | Details |")
                lines.append("|------|--------|---------|")
                for t in tests:
                    result = "PASS" if t.get("passed") else "FAIL"
                    icon = "+" if t.get("passed") else "!"
                    lines.append(f"| {t.get('name', '?')} | {icon} {result} | {t.get('details', '')} |")
                lines.append("")

            # Findings from this battery
            findings = battery.get("findings", [])
            for f in findings:
                lines.append(f"### Finding: {f.get('name', '?')}")
                lines.append("")
                lines.append(f"- **Severity:** {f.get('severity', '?')}")
                lines.append(f"- **Description:** {f.get('description', '')}")
                if f.get("proof"):
                    lines.append(f"- **Proof:** `{f['proof']}`")
                if f.get("remediation"):
                    lines.append(f"- **Remediation:** {f['remediation']}")
                lines.append("")

        # Remediation summary
        all_remediations = test_results.get("remediations", [])
        if all_remediations:
            lines.append("## Remediation Recommendations")
            lines.append("")
            for i, r in enumerate(all_remediations, 1):
                lines.append(f"{i}. {r}")
            lines.append("")

        lines.append("---")
        lines.append("*Report generated by VEXT Shield. Built by Vext Labs.*")

        return "\n".join(lines)

    # -------------------------------------------------------------------
    # Dashboard report
    # -------------------------------------------------------------------

    def generate_dashboard_report(self, all_data: dict[str, Any]) -> str:
        """Generate an aggregated security dashboard report."""
        now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
        lines: list[str] = []

        grade = all_data.get("grade", "?")
        score = all_data.get("score", 0)

        lines.append("# VEXT Shield — Security Dashboard")
        lines.append("")
        lines.append(f"**Generated:** {now}")
        lines.append(f"**Overall Security Grade:** {grade} ({score}/100)")
        lines.append("")

        # Component status
        lines.append("## Component Status")
        lines.append("")
        lines.append("| Component | Last Run | Status | Findings |")
        lines.append("|-----------|----------|--------|----------|")
        for component in all_data.get("components", []):
            lines.append(
                f"| {component.get('name', '?')} | {component.get('last_run', 'Never')} | "
                f"{component.get('status', '?')} | {component.get('findings_count', 0)} |"
            )
        lines.append("")

        # Recent alerts
        alerts = all_data.get("alerts", [])
        if alerts:
            lines.append("## Recent Alerts")
            lines.append("")
            for alert in alerts[:20]:
                lines.append(f"- [{alert.get('time', '?')}] **{alert.get('severity', '?')}** — {alert.get('message', '')}")
            lines.append("")

        # Scan summary
        scan_data = all_data.get("scan_summary")
        if scan_data:
            lines.append("## Skill Scan Summary")
            lines.append("")
            lines.append(f"- Skills scanned: {scan_data.get('total', 0)}")
            lines.append(f"- Clean: {scan_data.get('clean', 0)}")
            lines.append(f"- Low risk: {scan_data.get('low', 0)}")
            lines.append(f"- Medium risk: {scan_data.get('medium', 0)}")
            lines.append(f"- High risk: {scan_data.get('high', 0)}")
            lines.append(f"- Critical: {scan_data.get('critical', 0)}")
            lines.append("")

        # Firewall summary
        fw_data = all_data.get("firewall_summary")
        if fw_data:
            lines.append("## Firewall Summary")
            lines.append("")
            lines.append(f"- Active rules: {fw_data.get('rules', 0)}")
            lines.append(f"- Violations today: {fw_data.get('violations_today', 0)}")
            lines.append(f"- Blocked requests: {fw_data.get('blocked', 0)}")
            lines.append("")

        lines.append("---")
        lines.append("*Report generated by VEXT Shield. Built by Vext Labs.*")

        return "\n".join(lines)

    # -------------------------------------------------------------------
    # Helpers
    # -------------------------------------------------------------------

    @staticmethod
    def _severity_badge(severity: str) -> str:
        """Return a text badge for a severity level."""
        badges = {
            "CRITICAL": "[CRITICAL]",
            "HIGH": "[HIGH]",
            "MEDIUM": "[MEDIUM]",
            "LOW": "[LOW]",
            "INFO": "[INFO]",
            "CLEAN": "[CLEAN]",
        }
        return badges.get(severity.upper(), f"[{severity.upper()}]")

    @staticmethod
    def _findings_table(findings: list[Finding]) -> str:
        """Generate a summary findings table."""
        lines = [
            "| ID | Severity | Category | Name | File | Line |",
            "|----|----------|----------|------|------|------|",
        ]
        for f in findings:
            line_str = str(f.line_number) if f.line_number else "-"
            lines.append(
                f"| {f.id} | {f.severity} | {f.category} | {f.name} | "
                f"`{f.file_path}` | {line_str} |"
            )
        return "\n".join(lines)

    @staticmethod
    def _executive_summary(results: list[ScanResult]) -> str:
        """Generate an executive summary from scan results."""
        total = len(results)
        risk_counts = Counter(r.risk_level for r in results)
        total_findings = sum(len(r.findings) for r in results)

        parts: list[str] = []
        parts.append(f"Scanned **{total}** skill(s) and found **{total_findings}** total finding(s).")
        parts.append("")

        if risk_counts.get("CRITICAL", 0) > 0:
            parts.append(f"**{risk_counts['CRITICAL']}** skill(s) rated **CRITICAL** — immediate action required.")
        if risk_counts.get("HIGH", 0) > 0:
            parts.append(f"**{risk_counts['HIGH']}** skill(s) rated **HIGH** risk.")
        if risk_counts.get("MEDIUM", 0) > 0:
            parts.append(f"**{risk_counts['MEDIUM']}** skill(s) rated **MEDIUM** risk.")
        if risk_counts.get("LOW", 0) > 0:
            parts.append(f"**{risk_counts['LOW']}** skill(s) rated **LOW** risk.")
        if risk_counts.get("CLEAN", 0) > 0:
            parts.append(f"**{risk_counts['CLEAN']}** skill(s) rated **CLEAN** — no issues detected.")

        return "\n".join(parts)
