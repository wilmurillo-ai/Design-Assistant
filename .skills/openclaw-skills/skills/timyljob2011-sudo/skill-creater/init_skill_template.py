#!/usr/bin/env python3
"""
Skill Template Generator
Generates a complete skill structure based on user requirements.
"""

import os
import sys
import json
import re
from datetime import datetime
from pathlib import Path

def generate_skill_name(description: str) -> str:
    """Generate kebab-case skill name from description"""
    # Remove special chars, convert to lowercase
    cleaned = re.sub(r'[^\w\s-]', '', description.lower())
    # Replace spaces with hyphens
    kebab = re.sub(r'\s+', '-', cleaned.strip())
    # Remove consecutive hyphens
    kebab = re.sub(r'-+', '-', kebab)
    return kebab[:50]  # Limit length

def generate_skill_description(intent: str, triggers: list) -> str:
    """Generate proper skill description with triggers"""
    trigger_phrases = ", ".join(triggers[:5])  # Top 5 triggers
    return f"{intent}. Use when the user mentions: {trigger_phrases}."

def create_skill_md(name: str, description: str, instructions: str, complexity: str) -> str:
    """Generate SKILL.md content"""
    return f"""---
name: {name}
description: {description}
---

# {name.replace('-', ' ').title()}

{instructions}

## Usage

### Basic Usage
```
User: [典型触发语句]
→ Skill activates and [执行动作]
```

### Examples

**Example 1: [场景描述]**
```
User: [用户输入]
[Expected output]
```

**Example 2: [场景描述]**
```
User: [用户输入]
[Expected output]
```

## Notes

- [重要说明1]
- [重要说明2]
- [边界情况处理]

---

*Generated by Skill Craftsmen*
"""

def create_manifest(name: str, description: str, version: str = "1.0.0", 
                   author: str = "", tags: list = None) -> dict:
    """Generate Clawhub manifest.json"""
    if tags is None:
        tags = ["utility"]
    
    return {
        "name": name,
        "version": version,
        "description": description,
        "author": author or "Anonymous",
        "tags": tags,
        "minOpenclawVersion": "0.20.0",
        "entry": "SKILL.md",
        "created": datetime.now().isoformat(),
        "license": "MIT"
    }

def create_script_template(name: str, script_type: str = "python") -> str:
    """Generate starter script"""
    if script_type == "python":
        return f'''#!/usr/bin/env python3
"""
{name.replace('-', '_')} - Script description
"""

import argparse
import sys

def main():
    parser = argparse.ArgumentParser(description='Skill helper script')
    parser.add_argument('--input', '-i', help='Input file')
    parser.add_argument('--output', '-o', help='Output file')
    args = parser.parse_args()
    
    # Your logic here
    print(f"Processing: {{args.input}}")
    
    return 0

if __name__ == "__main__":
    sys.exit(main())
'''
    elif script_type == "node":
        return f'''#!/usr/bin/env node
/**
 * {name} - Script description
 */

const fs = require('fs');
const path = require('path');

function main() {{
    const args = process.argv.slice(2);
    
    // Your logic here
    console.log('Processing...');
}}

main();
'''
    else:
        return f'''#!/bin/bash
# {name} - Script description

set -e

INPUT="$1"
OUTPUT="$2"

# Your logic here
echo "Processing: $INPUT"
'''

def generate_skill(skill_name: str, intent: str, triggers: list, 
                  instructions: str, complexity: str = "standard",
                  author: str = "", has_scripts: bool = True) -> dict:
    """
    Generate complete skill structure
    
    Returns dict with file paths and contents
    """
    structure = {}
    
    # Generate description
    description = generate_skill_description(intent, triggers)
    
    # SKILL.md
    structure['SKILL.md'] = create_skill_md(skill_name, description, instructions, complexity)
    
    # manifest.json
    tags = [complexity, "utility"]
    if has_scripts:
        tags.append("scripted")
    structure['manifest.json'] = json.dumps(
        create_manifest(skill_name, description, author=author, tags=tags),
        indent=2
    )
    
    # Scripts (if needed)
    if has_scripts and complexity in ["standard", "advanced"]:
        structure['scripts/helper.py'] = create_script_template(skill_name, "python")
        structure['scripts/helper.js'] = create_script_template(skill_name, "node")
    
    # References (if advanced)
    if complexity == "advanced":
        structure['references/examples.md'] = """# Examples

## Example 1
...

## Example 2
...
"""
        structure['references/guide.md'] = """# Detailed Guide

...
"""
    
    # Optional README
    structure['README.md'] = f"""# {skill_name}

{description}

## Installation

```bash
openclaw skill install ./{skill_name}
```

## Usage

See SKILL.md for detailed usage instructions.

## License

MIT
"""
    
    return structure

def save_skill_to_disk(structure: dict, output_dir: str) -> str:
    """Save skill structure to disk"""
    base_path = Path(output_dir)
    base_path.mkdir(parents=True, exist_ok=True)
    
    for file_path, content in structure.items():
        full_path = base_path / file_path
        full_path.parent.mkdir(parents=True, exist_ok=True)
        full_path.write_text(content, encoding='utf-8')
    
    return str(base_path)

if __name__ == "__main__":
    # CLI usage
    if len(sys.argv) < 2:
        print("Usage: init_skill_template.py '<skill description>'")
        sys.exit(1)
    
    description = sys.argv[1]
    name = generate_skill_name(description)
    print(f"Generated skill name: {name}")
